<h3>1.1 GENERAL</h3>
<p>The Periyar Valley Irrigation Project envisages the utilization of controlled releases from the Ennakkal Dam, being constructed across the Edamalayar, one of the tributaries of Periyar River.  The dam was designed to have a live storage of 1017.8 mm³ and was utilized for various purposes such as irrigation, power production, salinity control, industrial and domestic uses and navigation.  The release pattern of the reservoir was based on irrigation and other allied requirements.  The controlled release would be interrupted by the Periyar barrage at Bhoothathankettu with suitable modification and diverted through the main canal to cater to the irrigation  needs to 32800 ha of lands lying on the left bank of the Periyar.  The project aimed in the raising of additional crops in an area of 20,000 ha.  The agricultural land is spread over the six Thaluks of Ernakulam district viz. Kothamangalam,. Moovattupuzha, Kunnathunadu, Aluva, Parur and Kanayannur.  This is one of the very few projects in the State which maintains full reservoir level and can supply water to a large extent of ayacut area throughout the year.  The main component of Periyar Valley Irrigation Project includes:</p>
<ul class="list10">
     <li><i class="fas fa-caret-right" aria-hidden="true"></i>Periyar barrage at Bhoothathankettu</li>
	 <li><i class="fas fa-caret-right" aria-hidden="true"></i>Main canal</li>
	 <li><i class="fas fa-caret-right" aria-hidden="true"></i>Law level canal</li>
	 <li><i class="fas fa-caret-right" aria-hidden="true"></i>High level canal</li>
