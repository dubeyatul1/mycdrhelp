<h3>1.8 Project area</h3>
<p>The main dam at Ennakal across the Idamalayar river is in Idukki district and is at latitude 10° 13’ 15” N and is at longitude 76º42’16”E. The periyar barrage is at latitude 10º8’16” N and longitude 76º40’00”E. The road bridge over the barrage connects the Ernakulam and Idukki districts. the nearest railway station is at Alwaye on the southern railway. The ayacut of the project is spread over the taluks of kothamangalam, muvattupuzha, kunnathanadu, alwaye, kanayannur and Parur taluks of Ernakulam district.the barrage is accessible by good motorable road from Alwaye railway station and important places like kothamangalam,muvattupuzha and perumbavoor .the head work is 52 km away from Alwaye railway station. the main canal ,distribution system and the ayacut area are all accessible by district and village roads. The national highway no. 47 runs through the ayacut area and crosses the Parur branch canal at Alwaye. The project area can be generally divided into 3 distinct groups as the mountainous forest areas, the thinly populated hill slopes, the heavily populated plains and the industrial belt around Cochin. Hills and valleys are the general features of the project area. There are vast stretches of low lying swamps in the area. The mountainous regions are made up of disintegrated rocks mixed with boulders and rock covered with a thin layer of loam. The hill slopes are all gravelly and contains a good percentage of laterite soil. The wet lands in the area are all composed of stiff clay. The climate in project area falls in line with the general climatic conditions of the state. There are two monsoon seasons in the area-the south west and north east, from June to September and October to December respectively. The summer is from January to May with few showers in April and May. The south west monsoon is very active in this area, when the area gets 75% of its annual rainfall. The temperature variation in the area is substantial.</p>
<h3>1.9 Historical development</h3>
<p>The settlement in this basin is believed to be very old. The pre-history of this region is shrouded in mystery. There is no clear evidence of the Paleolithic man having lived here. This region is rich in pre historic monuments which contain relics such as bones, stones and implements and pottery. The small size dolmens of megalithic monuments found at Vadattupara in Malayattur reserve forest deserves special attention.during1963, some relics of monolithic culture, believed to date back from 200 BC to first century AD were discovered from Kodanad in Kunnathunad taluk, while excavating the Kodanad branch canal under the scheme. the relics included a good number of terra cotta gigurines; pot sherds with engravings generally available in megalithic pottery were also unearthened at the site. All these tend to prove that early settlement in this basin took place along the sea coast and river banks. Project was sanctioned on 1956 for Rs 348 lakhs and construction was started during 1956 during 5 year plan. Construction of barrage started during 1958 and was completed in 1963.the project was declared of complete in 1992.total cost of project as on 31.03.200 was worked out to Rs100.5 Crores.  Ayacut achieved till March 2000 was 30489 hectares against the envisaged Ayacut of 32800 hectres of paddy.</p>
