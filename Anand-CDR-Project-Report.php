	 <li><i class="fas fa-caret-right" aria-hidden="true"></i>Minor distributaries</li>
</ul>
<p>The sanction for the project was accorded by the Government of India in 1956 and was partially commissioned in 1967 to irrigate 25616 ha..  The scheme was revised in 1965 in order to extent the benefit of the project to a total extent of 32800 ha. of lands lying on left bank of Periyar by raising FRL to +34.95 m from +32.20 m.  this was to be achieved by raising the height of shutters at Bhoothathankettu and then building a High Level Canal at a higher elevation so as to command ayacut areas at higher attitude.  Work on the reversed scheme was carried on till late 1980’s and nearly 750 k.m. lengths of canals were completed.  </p>

<h3>1.2 SCOPE</h3>
<p>The scheme is the projected remedy for the shortage of water in the lower reaches of the river for various purposes such as irrigation, salinity control, industrial and domestic uses especially during the lean months of the year. </p>
<p>The major scope of Periyar Valley Irrigation Project is incorporated with  the irrigation of agricultural lands of Kerala.  The major portion of the agriculture land is constituted by rice which is the staple food of Kerala.  The production of rice faces great scarcity in the waster requirements during summer.  This leads to a vide gap between the production and the demand of rice requirements.  The Periyar Valley Irrigation Project benefits an area of 32800 ha. of lands.</p>
<p>Apart from the irrigation benefits both flow as well as lift the scheme will catter to the domestic and industrial needs, in addition to checking the ingress of salinity in the lower reaches of the river.  Saline water intrusion in to the river, which have their in to the see, is a common phenomenon occurring.  Salinity affects domestic and industrial water supply as well as irrigation.  Paddy cannot resist a saline concentration of above 2000 ppm. If the salinity concentration of water exceeds a critical values, it becomes unsuitable both for industrial and domestic conception.  The Periyar at its lower reaches is under the influences of tidal action and hence the problem of salinity in the river in acute.  Salinity intrusion can take place in two different ways: at the river mouth due to density currents, tidal action and molecular diffusion and also across the coast as ground water flow.  With the development of industries, growth of population and trade in the area, demand for good water also is on increase.  </p>
<p>The major industries in the region benefited by Periyar valley Irrigation Project are the Aluminium Factory, The Fertilizers and Chemicals Travancore Ltd., the Travancore Cochin Chemicals, The Appolo Tyre Factory, The Indian Rare Earths, The Travancore Rayons, Cominco Binani Zinc Factory, The Kochi Refineries Ltd., The Cochin Fertilizer Factory, Tile Factories, Fisheries and Textile Mills.</p>
<p>Periyar valley Irrigation Project also serves domestic as well as drinking purposes.  When there is full flow in the canal, due to seepage the wells in the surrounding lands gains rising the existing level of water and as a result it serves the drinking purposes of population residing.  The domestic use includes bathing, washing, lavatories etc.</p>
<p>Necessity of barrage:   The Periyar valley Irrigation Project was found inefficient to serve the large ayacut area proposed in the summer or in the lean months of the year.  In order to meet the required amount of water in these lean months, a fair amount of water is stored during the raining season which would be utilized later.  Moreover the heavy intensity of rainfall during the four months may cause floods in the river and consequent damage to the law lying areas whereas the other other part of the year the rainfall is not sufficient to meet the crop needs.  The only remedy to this problem is to have sufficient storage to be controlled and utilized during the different crop periods according to necessity.</p>



