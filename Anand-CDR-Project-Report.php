<h3>1.4 TOPOGRAPHY	</h3>
<p>The general topography of the area presents no drainage difficulties.  The numerous streams emptying in to the main river, affords enough scopes for letting out the drainage to flow in to the rivers.  As an anti-water-logging measures, the drainage courses in the fields are to be cleared annually and encroachments of drainage channels should be prevented.</p>
<h3>1.5 GEOLOGY</h3>
<p>The Project area may be divided in to four belts according to the various geological formations occurring in the region.  The belts are oriented more or less north south and consist of the following:</p>
<ul class="list10">
     <li><i class="fas fa-caret-right" aria-hidden="true"></i>A belt of crystalline rocks of Archean groups.</li>
	 <li><i class="fas fa-caret-right" aria-hidden="true"></i>A belt of residual laterite.</li>
	 <li><i class="fas fa-caret-right" aria-hidden="true"></i>A narrow belt of Varkala beds of the tertiary groups.</li>
	 <li><i class="fas fa-caret-right" aria-hidden="true"></i>The Western most belts of recent deposits </li>
</ul>

<p>The crystalline rocks of the Archean formation are made up of pink granite gneisses, Grey gneisses, and charnockites.  The charnockites show intrusive relationship with gneisses.  The later may be considered to be equivalent to the Penisular suite.  The residian laterite occurs as a belt towards the west of archean crystalline rocks.  The is the resultant product of in situ alteration of the crystalline rock.  These types of formations are predominant in Kunnathunadu, Kothamangalam and Muvattupuzha Taluks.</p>
