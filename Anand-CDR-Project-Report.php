<h3>2.1 GENERAL</h3>
<p>Periyar Valley Irrigation Project in Ernakulam district is one of the largest Irrigation Project in the state.  The project was envisaged to meet the irrigation requirement of 32800 hectares of agricultural land spread over six thaluks of Ernakulam districts viz: Kothamangalam, Muvattupuzha, Kunnathunadu, Aluva, Parur and Kanayannur Taluks being supplemented with the tale race discharge of Edamalayar Hydro Electric Projects.  This is one of the very few projects in the state which maintains full reservoir level and supply water to a large extends of Ayacut area through out the year.  PVIP comprises of one barrage at Boothathankettu in Kothamangalam Taluk and main canal of length 8 KMtrs., low level canal 20.5 K.Mtrs., high level canal 23 K Mtrs. And a net work of branches and minors.  Irrigation was successfully carried out for about 20 years.  Due to constant rain and improper maintenance and upkeep of the distribution system, the conveyance efficiency has come down gradually.  Canal system unable to catch the demands of its beneficiaries.</p>
<p>Various studies were conducted in order to study the reasons of inefficiencies of the system</p>

<h4 style="font-size:20px;">2.2  STUDIES CONDUCTED</h3>
<h4 style="font-size:18px;">2.2.1 Public survey</h4>
<p>To study the response of the inhabitants of the surrounding area about the workability of the system and its usability, a public survey was conducted.  A data sheet was distributed among people involving details of schedules various uses of water, its portability and the maintenance and upkeep of the system.</p>
<p>This survey was conducted in selected places at Boothathankettu, Kothamangalam, Kodanad, Mulavoor, Perumbavoor, Kizhakkambalam, Alwaye, Kunjattukara, Keezhillam, Valakom, Irapuram, Kolencherry and Eruveli.</p>
<p>From the analysis of the filled up data sheet, it was found that the system was lacking proper maintenance and that there was inadequacy in the amount of water to meet the necessary requirement. </p>
