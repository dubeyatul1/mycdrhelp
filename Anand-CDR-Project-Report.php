<h3>Surface float method:</h3>
<p>Surface floats are made of light material such as Corks or hollow wooden blocks so that they may float on the surface.  They are painted on top and some times attached with a flag to easily distinguish it from any other floating object.  </p>
<p>Two wire ropes are stretched across the channel section one at the beginning and other at the end of the selected reach for conducting the test.  This distance is called test reach.  A third rope is stretched upstream of the test reach by 15 to 20 m.  All the three ropes are marked at various points so as to divide the channel with in various compartments, the centre line of which defines the part of track for the floats.  The floats are dropped in water along various paths near the third rope.  They acquire uniform velocity by the time they enter the test reach.  The time of travel of the test reach of only those floats which follow their correct track is recorded.</p>


   
