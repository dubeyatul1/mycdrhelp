                <p><strong>[CE 1.7]</strong></p>
                <p align="justify">I did all possible online &amp; offline research to have broader view about the analysis if landslide. In addition to this during the project lifespan I had consistent meetings with my peers &amp; project supervisor in order to debate about the snags &amp; hitches. I also had the task to concoct documents &amp; reports with diagrams for unfolding the entire process. Finally, Project report was offered for review of project in which all-inclusive process was described.</p>
