                <p align="justify">The project was carried out during the Bachelors Degree in Civil Engineering. I completed this project by teaming up with two other team members &amp; it was completed under the supervision and direction of our project supervisor.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 1.2]</strong></p>
                <p align="justify">During my course work studies, I have studied analysis of soil which wedged my attention and I decided to pursue a project on this subject. I was invigorated by my supervisor for undertaking this project as it addressed a practical problem &amp; was an extraordinary project amongst many other choices. I was able to complete this project efficaciously within the deadline. I was able to take in to account all the relative common &amp; uncommon issues accordingly. My competence as a project patron was even valued by the Project Supervisor.</p>
                <p><strong>[CE 1.3]</strong></p>
                <p align="justify">The length of Sukian dyke of Mangla dam is about 5.33 km .The core material used in Sukian dam is B type sandstone so Sukian dam is an earth fill type dam.. A- Type sandstone is applied on the shoulders. The peak height of embankment at intake above the core trench is 144 feet while the length is 16,900 feet. A complex active landslide that has progressed over the years has got worst due to the severe flood in 2014. WAPDA undertook repeated low cost measures to mitigate the damages due to flood. Currently, NESPAK has been contacted to redesign the dyke.</p>
                <p align="justify">The landslide area is located near Mangla Dam. Dyke slope has a relief of 1230 m. The landslide initiated in 2014 and gradually developed to a multiple landslide complex. IRSA and WAPDA undertook series of measures to reinstate the landslide affected reach, but the situation got worst when the flood hit the affected area. The chronological order of development of the landslide and previous remedial measures undertaken by the department of WAPDA were carried out during the site visit for the collection of samples.</p>
                <ul>
                  <li>Landslide phenomenon initiated in year 2002 about 180 m below the Mangla power House road. It progressed retrogressively and damaged nearly 100 m along stretch of the Dyke in 2004.</li>
                  <li>The department intended to reinstate the dyke by introducing the gravels/concretion to stabilize the soil by any means.</li>
                  <li>Unfortunately, these repairs also got damaged when the landslide reactivated in 2012.</li>
                  <li>The landslide is still active, especially along the toe of the embankment.</li>
                  <li>A site visit was carried out after the 2014 flood for collection of soil samples. The uphill slope has faced a major slide which is shown in Figure 1 and 2.</li>
                </ul>
