                <p align="justify">This career episode is related to my project titled “Failure Analysis of Sukian Dyke Landslide in Case Mangla Dam after 2014 Flood Season”. This project was carried out as part of my Bachelor of Science in Civil Engineering degree requirement from …12/1/2015…..to …10/06/2015…</p>
