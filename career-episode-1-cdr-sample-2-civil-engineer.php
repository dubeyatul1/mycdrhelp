                <p align="justify"><b>My roles &amp; responsibilities were -</b></p>
                <ul>
                  <li>Comprehensive visit of the site area in order to get adequate information concerning site geometry, assortment of disturbed and unobstructed soil samples for laboratory testing.</li>
                  <li>Performance of the laboratory test on the soil samples collected during site reconnaissance visit.</li>
                  <li>Index properties for the sorting of soil type and shear strength at diverse degrees of saturation &amp; evaluated the same to be used in stability analysis.</li>
                  <li>Performance of slope stability analysis using SLOPE/W.</li>
                  <li>Critical review of the research findings</li>
