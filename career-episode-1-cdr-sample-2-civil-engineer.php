                <h2 class="sample_heading">LABORATORY TESTING</h2>
                <p align="justify">I brought the soil samples collected from the site to laboratory in order to perform the laboratory tests for evaluation of desired soil properties to be used in the analysis. <br/><b>I prepared a laboratory testing program which included the following tests: </b></p>
                <ul>
                  <li>Grain Size Analysis</li>
                  <li>Hydrometer Analysis</li>
                  <li>Specific Gravity</li>
                  <li>Atterberg limits</li>
                  <li>Modified Compaction</li>
                  <li>Unconfined Compression</li>
                  <li>Shear Box (Direct Shear Test)</li>
