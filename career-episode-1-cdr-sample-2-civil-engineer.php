                <p><strong>[CE 1.9]</strong></p>
                <p align="justify">I conducted a detailed site reconnaissance visit in order to understand the landslide mass movement phenomenon and to frame the work methodology for the study and analysis of stability. I studied the information about the historical background of the landslide. I held a detailed meeting with the local management authority &amp; topographic section data of the landslide along with sub surface geological data information were collected which were of vital information for the analysis of the landslide. From all the information collected during the site visit, ‘SUKIAN DYKE’ landslide can be classified as flood induced landslide. I noticed wide open cracks at the top of the hill slope.</p>
                <p><strong>[CE 1.10]</strong></p>
                <p align="justify">I collected sufficient quantity of bulk samples from the slide material to find out the residual shear strength characteristics and in-situ shear strength parameters of the soil mass were collected.</p>
