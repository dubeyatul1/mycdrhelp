                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 1.3]</strong></p>
                <p>Al-Toukhi Company for Industry, Trading and Contracting are well known as Engineering, Procurement and Construction Company which is located in Riyadh, Saudi Arabia. The company has been giving its services for last 40 years, working on more than 80 electro-mechanical projects on turnkey basis. This company is the main EPC Contractor for Saudi Electricity Company (SEC) and Saudi Aramco. It is particularly famous for building power stations, gas turbine and diesel sub-stations, overhead and underground transmission lines, distribution and transmission substation networks, and turbine inlet air cooling projects. The company has more than 1,500 employees with an annual turnover of 1.5 billion SAR.</p>
                <p><strong>[CE 1.4]</strong></p>
                <p>The main objective of this project was to meet the growing demand on electricity in Saudi Arabia at that period of time and to be part of the electricity supply grid in the southern area of Saudi Arabia. The project was started to transmit the electricity by constructing a substation, in the southern part of Saudi Arabia. The substation was connected to Shuqaiq and Namera North Transmission Network. It aimed to step down the transmission voltage to sub-transmission voltage 380/132/13.8 KV.</p>
                <p><strong>[CE 1.5]</strong></p>
                <p><b>The responsibilities which I carried out as a Procurement Engineer during this project included: </b></p>
                <ul>
                  <li>Purchase the electrical equipment needed for constructing the substation. The main equipment was Gas Insulated Switchgear with their different ratings (380KV &amp; 132KV), up to 502 MVA Power Transformers, Shunt Reactors, Protection and Substation Automation Systems, Communications, MV Switchgear, HV/LV/MV &amp; Control Cables, Batteries, Chargers, LV Panels, Fire Fighting Systems, … etc.</li>
                  <li>Check the purchase requests to confirm the clarity of the specification and bill of quantities.</li>
                  <li>Prepare the Request for Quotation, send them with complete specifications to vendors/suppliers and follow up with vendors until receiving the offers.</li>
                  <li>Discuss the technical aspects of the offers with the engineering coordinator to get the offers technically evaluated.</li>
                  <li>Prepare the commercial evaluation for the technically accepted offers.</li>
                  <li>Negotiate the delivery, payment terms, prices and other commercial conditions with vendors.</li>
                  <li>Establish the draft Contract/Purchase Order based on the best offer recommended in coordination with the procurement coordinator.</li>
                  <li>Coordinate and communicate with engineering coordinator and project manager to get their comments/approvals to the draft Contract/Purchase Order.</li>
                  <li>Send the Contract/Purchase Order to the selected vendor/supplier and follow up with them to get their acknowledgment.</li>
                  <li>Introduce vendor’s/supplier’s staff to the project team for the kickoff.</li>
                </ul>
