                <p><strong>[CE 1.12]</strong></p>
                <p>In this project, I used a few standards of IEC, ANSI, and IEEE during my project.</p>
                <p><strong>[CE 1.13]</strong></p>
                <p>During the project, I took help from my project guide. I asked about the basic design principles for Low Voltage (LV) equipment and cost-efficient procurement strategies. For many issues and workflows of the project, I negotiated with the vendors/ suppliers. I was working under the supervision of project procurement coordinator, and he was reporting to the project manager. So, I had interaction with my supervisor almost every day to discuss the progress of the project. I was also communicating with procurement management, project management, engineering, and finance responsible team.</p>
                <p><strong>[CE 1.14]</strong></p>
                <p>During this project, I applied my knowledge of engineering to optimize the design of the substation so I could have saved a cost of a value in the project.</p>
                <p><strong>[CE 1.15]</strong></p>
                <p>During this project, I used an ERP system for material tracking of the project. From material request to material delivery, all information about the project was recorded through such ERP system.</p>
                <p><strong>[CE 1.16]</strong></p>
                <p>While working on this project, I had weekly meetings with the project team to discuss the project’s progress. With the customer, I had monthly meetings to let them be aware of the updates related to the project’s work. I had to meet with management once in a month to share the status of the project. My meetings with suppliers were scheduled according to the work of technical and commercial clarifications. I had daily contact with my functional manager, weekly meetings with the project manager, and monthly meet-ups with customer and management.</p>
                <p><strong>[CE 1.17]</strong></p>
                <p>While working on this project, I was responsible for the handling of electrical material during the project. I had to handle the material from the request phase of the material to the delivery phase of the material. I used a few of techniques to carry out the project effectively. I used to define the problem, analyze the cause of that problem, and then suggest some valuable ideas and appropriate solutions. Among all suggested solutions, I selected the best solution and implemented the corrective action in order to kick out the encountered problem.</p>
                <p><strong>[CE 1.18]</strong></p>
                <p>In this project, I didn’t have any members who were working under my supervision, as I had to report directly to my functional manager. However, I was leading the vendors/ suppliers to assure a successful implementation of the supply and contract management terms.</p>
                <p><strong>[CE 1.19]</strong></p>
                <p>During the project, I needed to prepare a report for Material Tracking which was presented to the manager on the weekly and monthly basis. The topic of the report was the purchasing roadmap of electrical material for the project. I also had to make my own follow-up sheet for my record.</p>
                <p><strong>[CE 1.20]</strong></p>
                <p>While working on this project, I gained a valuable knowledge and experience. I got basic design principles for LV equipment which was used in substations. I learned new time management techniques. I was able to think analytically and solve the problem. I learned to communicate with other team members. I got some advanced negotiation skills.</p>
                <p><strong>[CE 1.21]</strong></p>
                <p>From the beginning of the project, the environmental health and safety measures were strictly advised by the customer to follow. That’s why we hired an EHS coordinator throughout the project in order to meet the customer’s requirement. This project was a construction site project, that’s why it required some precautions. I had to take measures for hazardous energy control. I took an electrical safety orientation which is necessary for dealing with electrical work. I used to wear helmet, goggles, gloves and working suit, and I also advised workers to do the same. As the project involves working from a height, I took precautions for this to avoid an accident. Driving and vehicle safety were also kept to have a sound and safe working environment.</p>
                <p><strong>[CE 1.22]</strong></p>
                <p>In this project, I introduced some new strategies that were effective for material cost saving, especially Bundling, Design to Cost, and 2nd Source Development.</p>
                <p><strong>[CE 1.23]</strong></p>
                <p>While working on this project, I also participated in different social activities. I arranged a public day for school going students to make them aware of the importance of saving in electricity consumption.</p>
                <p><strong>[CE 1.24]</strong></p>
                <p>By the end of the project, I was able to achieve all the goals and the objectives of the project. The project was a complete success. The substation was put on the operation as per the project time schedule with no delays.</p>
