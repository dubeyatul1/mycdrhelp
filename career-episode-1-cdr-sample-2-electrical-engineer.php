                <p>It was an outstanding experience to do this project. I used my various skills and knowledge of engineering degree to complete the project’s activities. I successfully managed to secure the purchasing and delivery of all electrical materials that were required for the project. I was able to complete the project under the allocated budget. The customer was happy with the outcome of the project. Overall, it was a great project, full of challenges and technical gains.</p>
