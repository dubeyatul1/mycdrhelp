                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 1.7]</strong></p>
                <p>In this project the low voltage cables were required and their specification needed to be CU/XLPE/PVC 1KV cables of 9000 meters of 4x16 mm2. The cost of those cables was around 250,000 SAR (60,000 USD approx). That amount was making a high cost of the overall budget. To resolve this issue, I talked with the engineers and we proposed a solution to reduce the cable cross section from 16 mm2 to 10 mm2 making sure that the new cross-section still complies with the project technical specifications. The reduction saved around 100,000 SAR, and that amount didn't disturb the overall budget of the project.</p>
                <p><strong>[CE 1.8]</strong></p>
                <p>The medium voltage switchgear that was used in the project required to supply of switchgear in a building. When it was going to be executed, an issue popped up in the space of switchgear allocated in the building because the dimensions of switchgear were underestimated in the design. To overcome this, I talked to a vendor and got an offer where the switchgear was supplied in a smaller container. The solution was proposed to the customer and they accepted it. Then, the switchgear was supplied in a smaller container in the allocated space of the building.</p>
                <p><strong>[CE 1.9]</strong></p>
                <p>In this project I had to design LV cables and Fire alarm system. To design the LV cables and Fire alarm system, I was consulting with a designer.</p>
                <p><strong>[CE 1.10]</strong></p>
                <p>During this project, also, I was involved in the project cable cross section calculations.</p>
