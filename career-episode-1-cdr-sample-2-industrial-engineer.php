                <p><strong>[CE1.1]</strong></p>
                <p>This career episode links to project titled <strong>“Fox Cylinder Head with Spring Seat Surface Damage’’.</strong> This project was carried out during my tenure at<strong> Ford Motor Company </strong>as an <strong>Industrial Engineer.</strong></p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 1.2]</strong></p>
                <p>There were complaints being lodged regularly from the Engine Sub-assembly area about the cylinder heads being received periodically with damages in the spring seat surface. The part was coming from the head machining department. The machining team knew about the issue that it was related to the aluminum chips being pressed against the surface during Operation 80 – seat and guide assembly. Due to the inefficient cleaning of the head before Seat &amp; Guide press; the aluminum chips from OP.50 (CNC Machining) were not entirely removed at OP.70 (washer), moving towards the spring seat surfaces during OP.80 (press machine) thus causing marks/damages in the surface. This issue upsurge the engine oil consumption &amp; manufacturing rejections.</p>
                <p><strong>[CE 1.3]</strong></p>
                <p>The scope of this project was to improve the process between OP.50 and OP.80 with focus on reducing 90% of damaged heads and eliminating current ICA (Interim Containment Actions).</p>
                <p><strong>[CE 1.4]</strong></p>
                <p><b>The project Hierarchy is given below. I was answerable to the Engine Plant Manager -</b></p>
				<picture>
				  <source type="image/webp" data-srcset="<?php echo $siteUrl;?>images/IE2-1-4.webp">
