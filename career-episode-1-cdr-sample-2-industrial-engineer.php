                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 1.5]</strong></p>
                <p>I was responsible for the management of the whole scenario. I drafted all the required documentation of the project &amp; made it possible to have a seamless synchronization amid departments. I also took care of all the project documentation.</p>
                <p><strong>[CE 1.6]</strong></p>
                <h2>My Roles &amp; Responsibilities</h2>
                <ul>
                  <li>Analysing the quality rejections</li>
                  <li>Analysing the Cause &amp; Effect of the Quality Issue and identify the root cause for spring seat damaging problem.</li>
                  <li>Profoundly investigate the root cause of the quality issue. Conduct a benchmarking study with other Ford Plants to look for opportunities.</li>
                  <li>Identify the Escape Point of the quality issue</li>
                  <li>Define and implement an Interim Corrective Action to immediately reduce the quantity of quality rejections</li>
                  <li>Establish a measurement process to collect data where it is being generated, before the delivery to the internal client</li>
                  <li>Define and Implement Corrective Actions and control the data to ensure that the performance is maintained</li>
                  <li>Replicate the study results and actions to other plants with the lessons learned</li>
                </ul>
                <p><strong>[CE 1.7]</strong></p>
                <p>The 1st &amp; foremost difficulty was how to identify &amp; remove the chips completely before going to the press operation. So I started my investigation of the design. The investigation sorted the possible route cause which was low quality design of the entire cleaning process (hard to remove chips).</p>
                <p>By cutting the cylinder head in pieces, it was possible to see that its design had many cavities/galleries. These narrow areas were hard to conquer with water/air in the intermediate washing system. Also, it was possible to identify that between all the cavities/galleries of the cylinder head some of them had more impact on chips going to the spring seat area, such as the area related to oil return galleries.</p>
                <p>By cutting the cylinder head in many different positions, it was possible to see that exhaust side was the most critical region. Due to less oil return galleries it was much more difficult to drain water and chips.</p>
                <p><strong>[CE 1.8]</strong></p>
                <p>I collected data from measurement phase which was based on 669 defects of spring seat damages. I noticed that exhaust side of the cylinder head was responsible for more than 70% (478) of the spring seat damages. Intake side was less than 30% (191).</p>
                <p>In another analysis I divided exhaust side by spring seat number/position, with exhaust 1 &amp; 2 a little bit better due to being closer to the oil return gallery.</p>
                <p>I concluded from this 1st investigation that cylinder head design is a strong contributor for spring seat surface damaged due to chips pressed on this surface. Exhaust side was more impacted due to few oil return galleries in its design, but there was no big difference between each spring seat number/position from the exhaust side.</p>
                <p>Although the design was a major contributor bu its nearly impossible to change it since this will demand huge time and investment. So I kept it as an opportunity to be studied by the Product Development team for the next Engine Project.</p>
                <p><strong>[CE 1.9]</strong></p>
                <p>My 2nd investigation refers to a pack of potential causes from OP50 machining. It was basically related to the generation of chips during the machining process, involving chips size, form, quantity, length, etc. Prime point was to verify if current chip generation in each machine is according to initial plan, if it is so then are current chips size, form, quantity, length from OP 50 machining was effective to be removed by OP 70 intermediate washer?</p>
                <p>I did comparison between all four CNC machines from OP 50, with an amount of 63 defective parts in 3 days production; I observed that no significant difference was found. First conclusion I made was that it was not an specific machine issue but this could be a process issue affecting all four machines:</p>
                <p>I continued investigation on OP 50 process with in the specific region that had excessive impacts on chips going to the spring seat surface. There were several machining operations, some of them were mandatory for OP 50 &amp; some were optional. <br/><b>All were generating chips in a critical region: </b></p>
                <ol>
                  <li>Rough/semi-finish bore tappet bores (tool # T05002) – Mandatory on OP50</li>
                  <li>Finish bore and counter bore spring seat surfaces (tool # T05004) – Mandatory on OP50</li>
                  <li>Finish drill head bolt holes (tool # T05009) – Optional on OP50</li>
                  <li>Tap head bolt holes (tool # T05010) – Optional on OP50</li>
                </ol>
                <p><strong>[CE 1.10]</strong></p>
                <p>In order to reduce the amount of chips in this region; helping the OP70 washers I moved the optional features (3 &amp; 4) to other machining operations after seat &amp; guide press machines (OP80).</p>
                <p>The mandatory features for OP50 (1 &amp; 2) could not be moved, so actions to reduce/improve chips form, size, length, etc, might be required for the related tools. I worked with the tool supplier on tooling design and machining parameters.</p>
                <p><strong>[CE 1.11]</strong></p>
                <p>During machining of both tools T05002 (tappet bores) and T05004 (spring seat) in order to avoid chips being going to the fixture of the machine I placed the cylinder head up side-down. I did the same when machining exhaust side features since OP.70 Intermediate Washer wasn’t capable to remove chips competently. Later on in OP80 Seat &amp; Guide press machine the machine fixture was of standard design for folding the part in the opposite position:</p>
                <p><strong>[CE 1.12]</strong></p>
                <p><b>From the investigation #2 I concluded following -</b></p>
                <ol>
                  <li>It was very Important to reduce amount of chips in the critical area identified (minimum quantity of chips)</li>
                  <li>The inherent chips must be optimized in terms of form, size, etc.</li>
                  <li>Combined with investigation #1, inherent chips must be re-directed during machining for the areas with less impact; less on the related failure mode.</li>
                </ol>
                <p><strong>[CE 1.13]</strong></p>
                <p>While carrying out investigation for the capacity of the OP70 HMM-UK intermediate washers in order to assure proper cleaning for OP80 Seat &amp; Guide press machines; I started looking for benchmarks. Process in Cologne Engine Plat (Germany) showed same wash inefficiency. Implying different washer design (rotary washer), but also from HMM-UK, requiring 100% of cleaning being made manually by the operator in order to contain pressing fails. So different washers perofrmed same process but had same cleaning issues. I concluded that it was not a maintenance issue from a specific machine rather it was a design issue of the process.</p>
                <p>Even with water jets conquring from everywhere inside the machine I was still hard to achieve the critical region with pressurized water. So it was nearly impossible to remove big size and the amount of chips during the washing cycle.</p>
                <p><strong>[CE 1.14]</strong></p>
                <p><u>I took following improvement Actions:</u></p>
                <ul>
                  <li>Implemented new chip breaker design for tool T5004 (spring seat surface)
                      <p>Target: Reduce size of chips</p>
                    <p>Implemented successfully.</p>
                  </li>
                  <li>Changed OP.50 from “upside down” machining to “right side up”, for tappet bore and spring seat tools (first tools in the machining sequence), then continued the process as “upside down” machining.</li>
                </ul>
                <p><strong>[CE 1.15]</strong></p>
                <p>I lead the project from front and confronted any issue professionally. I drafted all the relevant documentation, presentations and reports. I was also liable to present project progress to the company executives. I also compiled the project feasibility study and my report was accepted by the company executives.</p>
                <p><strong>[CE 1.16]</strong></p>
                <h2 class="sample_heading">Summary</h2>
                <p>Prime target was to avoid residual chips to go into exhaust side galleries, then later dropping down to the spring seat area during Seat &amp; Guide assembly.</p>
                <p>I implemented the project with success - Now chips accumulate on intake side of the galleries. Even with chips on the intake side, intermediate washers still have high potential not to remove all chips after washing. However, chips not removed by the washer, now are already down therefore they are not dropping onto spring seat surface. Also, internal area of intake side has 4 oil return galleries, which makes it easier to clean. As a final result, the quantity of rejections on the sub-assembly was reduced from 15% to 3%.</p>
                <p>The project gave upsurge to my professional project management topology and my ability to use all types of resources in resolving issues accordingly. The project as a whole elevated my professional existence in all manners.</p>

