               <p align="justify">During acceleration, the foot pedal rotates about the downward direction and lower half of pedal moves in upward direction i.e. rotation of foot pedal would be in anticlockwise direction. I kept the support below the foot pedal during acceleration, this opposite force is done by using helical spring. The main purpose of spring is to provide Resisting force to the support during throttling. Hence, by using upper half of the foot, driver can accelerate the vehicle without actuation of brakes.</p>
