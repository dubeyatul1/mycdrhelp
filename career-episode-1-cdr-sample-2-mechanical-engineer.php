               <h2>Fabrication Methodology</h2>
               <p align="justify">I played key role in fabrication. The C40 steel square-section is cut into two or more pieces to the required size of the material. Then I assembled it to make the chase frame. The welding process was carried out. Before finishing welding the structure, I verified whether the structure is in right angle or not. I used L-angle to make support stand for fuel tank and engine. I connected the four wheels to the chase frame. The engine was mounted on the chase frame and bolted for the support. Then I connected the acceleration cable to the lower end of the foot pedal. The foot pedal is a 3mm metal plate which was supported on the column. After that I connected the brake line from two wheels are connected to the support column.</p>
