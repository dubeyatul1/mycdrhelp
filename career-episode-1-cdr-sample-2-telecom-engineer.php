                <p><strong>[CE 1.4]</strong></p>
                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p class="para">PTCL already have more than 2,000 Local Exchanges and 3000 MSAG/ONUs nationwide. To further expand and optimize 65 Exchanges in South was a challenging task. My Duty was to configure new or expansion nodes for triple play services and get sites Acceptance done.</p>
                <p class="para">On Daily basis I was receiving plan for network up-gradation/implementation of new nodes, I checked complete scope and made execution plans. Initially I had to check present and planned configuration of all the sites from survey reports and NMS Configuration logs. After receiving scope of work I prepared SMR’s (site material request) according to configuration. This SMR was then forwarded to Customer and after its approval it was sent to warehouse. Then I arranged logistics and managed to collect the equipment from warehouse along with Delivery Note which was verified at time of equipment loading and offloading at site and the Delivery Note was needed to be signed by customer onsite representative. When equipment was delivered to site, I along with my team used to reach site &amp; perform installation. The installation of equipment was supervised by me and a quality audit report was prepared after installation of site. This report was reviewed and approved by customer before commissioning.</p>
                <p class="para">Upon reaching the site, open box was performed in presence of customer representative to confirm equipment reached in good state and is according to Delivery note and new site configuration. Then team start Installation work according to survey Report and layout. Usually Installation involved fitting of DSLAM Cabinet, MDF and Cable Laying from DSLAM to MDF and laying of Optical Patch Cords for uplink. Additional work included installation of battery Banks (4*12v batteries per bank) and Rectifier Installation.</p>
                <p><strong>[CE 1.5]</strong></p>
                <p class="para">After Installation Quality Audit Report was prepared, this includes the Pictures of newly installed Equipment according to Quality standards. These reports include and check whether DSLAM Cabinet, MDF &amp; Rectifier are properly grounded and Grounding/Earthing Bar is also connected to Main Earthing Pit. Subscriber Cable lying is done properly on Cable Ladders and is terminated on to MDF’s system and External side. Optical Patch cords lying is done within Flexible PVC pipe. Power Cable Lying was done on separate cable Ladders which did not had subscribers cable or Patch Cords to avoid Electromagnetic effect. All Power connections are made using naked Terminal with Heat Shrink wrapped around it.</p>
                <p class="para">For Expansion Site Commissioning I had to apply for Network Outage of 45 Minutes, all the New Boards and sub racks were connected during this time and the IPs and VLANs were defined using Hyper Terminal Software. Status of Boards and newly installed Sub rack were needed to be normal &amp; if found abnormal than had to diagnose and rectify, usually software up gradation resolved the issues. Once Commissioning is done I checked that all the gateways IPs are accessible. And service testing was done afterwards. Whereas commissioning of new nodes &amp; no network outage was required and rest of procedure was same. Meanwhile I had to coordinate with PTCL transmission &amp; IP team before commissioning of site so that they can Configure Media according to Number of Pots that where being deployed. Usually 1Gbps Link is provided on which Voice, Data &amp; Multicast service (IPTV) was provided.</p>
                <p class="para">For Service testing I had to check that whether Telephone is working &amp; no abnormality is observed in dial tone or in the call behavior. For Internet I tested RAW internet speed with different ADSL2+ Profile configuration and then checked the ping result and Latency. For IPTV there should be no jitter on screen while playing channels consumer must be able to forward and rewind along with recording option. If jitter or delay in ping result occurs then there is an issue of bandwidth and in order to rectify the same I run Bandwidth analyzer on NMS to check choking and this was resolved by providing more bandwidth on the uplink of DSLAM.</p>
                <p class="para">After Service testing site was offered for ATP (Acceptance testing procedure). In this all the acceptance protocols were performed and shown to Customer. It Included verification of BOQ (Bill of Quantity) and service and reliability testing protocols. After completion sites were handed over to Customer for commercial usage.</p>
                <p class="para">BOQ, ATP Documents along with DNs was submitted to Vendor site wise. Final Reconcile BOQ is prepared in junction with Vendor and Operator for successful completion of project.</p>
                <p><strong>[CE 1.6]</strong></p>
                <h2>KPIs for network optimization and expansion</h2>
                <p class="para">After Execution of Plan, Site is kept under observation for one month time to check post installation KPIs. I coordinated with NOC team to take KPIs stats after one week and if stats were not up to bench mark then troubleshooting was required, there were many factors on which these KPIs effect. The major KPIs which we check and maintain were</p>
                <p><strong>[CE 1.7]</strong></p>
                <ul>
                  <li>Dial-up Call Setup Time</li>
                  <li>ADSL2+ Service Availability &amp; Throughput</li>
                  <li>Unsuccessful Connect Ratio</li>
                  <li>Ratio of Packet Loss</li>
                </ul>
                <p><strong>[CE 1.8]</strong></p>
                <h2>Dial-up Call Setup Time</h2>
                <p class="para">Call setup time should not be more than 1.9 seconds. If time is above 1.9 Seconds then I need to check Configuration at Soft Switch and have to define maximum call setup time while creating H248 Link between Soft Switch and UA5000.</p>
                <p><strong>[CE 1.9]</strong></p>
                <h2>ADSL Service Availability &amp; Throughput -</h2>
                <ol>
                  <li>ADLS Service availability time should be ≥99%. If there are frequent disconnections then Line Test is performed and Line health is checked usually a bad line is the reason for Frequent Disconnection.</li>
                  <li>IF Line health is good and still consumer is facing Disconnection then Connection at MDF are tested, usually loose punching resolve the issue.</li>
                  <li>ADSL line Profile is also checked to ensure proper configuration is done or not.</li>
                  <li>SNR (Signal to Noise Ratio) Value at Downstream must be greater than 10db.If SNR value is lower than 10 dB then Change of Line (Drop Wire) or Jumper or Vertical or DP or Line Pair. Broadband may work on a value lower than 10 dB but there would be likely a slow speed, frequent disconnections &amp; Time outs.</li>
                  <li>Attenuation value at Downstream must be less than 45 dB. If the value is greater than 45 dB then insist for Change of Phone Line (Drop Wire), Jumper, Vertical, DP or Line Pair. Broadband may work on a value higher than 45 dB but there would be likely a slow speed, frequent Disconnections and Time outs.</li>
                  <li>ADSL Splitter must be used on a Telephone Line at the subscriber premises having a Broadband connection. It divides main (frequency) Telephone Line coming from the Telephone Exchange into Phone (Low frequency) &amp; Broadband (High frequency) connection. So Modem must be feed with pure ADSL signal (Link)</li>
                </ol>
                <p><strong>[CE 1.10]</strong></p>
                <h2>Unsuccessful Connect Ratio</h2>
                <p><strong>[CE 1.11]</strong></p>
                <h2>Ratio of Packet Loss</h2>
                <p class="para">Packet loss ratios that are harmless to the quality of data and Voice over Internet Protocol (VoIP) services may still seriously jeopardize Internet Protocol television (IPTV) services. This can happen due to Link Congestion, Router/Switch/Firewall Performance, Software Issue and Faulty Hardware or cabling in the network. For Link Congestion Increase of bandwidth resolve the issue. But if this is not the case there is possibility that Cable are faulty for the device has reached is maximum throughput capacity and replacing with new hardware which can stands the throughput requirement will work. Sometime updating Software works. For IPTV 1% packet loss is acceptable more than that will cause content to disappear while streaming. Usually joint testing was conducted with Customer Team to check Network and identify the issue and in this testing I would require to check IPTV service on Layer-2 Switch by disconnecting DLSAM, so that I can check whether this packet loss is happening due to DSLAM or Other network Elements</p>
                <p><strong>[CE 1.12]</strong></p>
                <h2 class="sample_heading">Summary</h2>
                <p class="para">It was very important engineering project in my career as I have to deal with different departments, i.e. Transmission (SDH &amp; DWDM), IP Core (Soft switch, BRAS And IP network). I was also involved in dealing with warehouse and provided logistics support. I was also involved in management of installation teams &amp; was directly involved with vendor and Operator technical team for successful completion of tasks. In this Project I learned a lot about Fixed Line Network</p>
