                <p><strong>[CE 1.1]</strong></p>
                <p class="para">My First episode is about my project in LCC International. LCC was established in 1983 and remains a leading pioneer in providing fixed network, wireless voice and data engineering services to the telecommunications industry. LCC is working in more than 50 countries worldwide and providing services in network management, business consulting, tools-based solutions, and training. Since November 2014 it’s been acquired by Tech Mahindra Ltd.</p>
                <p><strong>[CE 1.2]</strong></p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p class="para">I worked on this project as Level-3 Engineer and given a task of Implementation of DSLAM (Digital Subscriber Line Access Multiplexer). In this project I was entirely responsible for Installation, Commissioning and Service testing. LCC was awarded this Project by Huawei Technologies which was the sole Equipment Provider; PTCL was deploying the Equipment in South Region of Pakistan. For work scope finalization I have to coordinate with customers, both PTCL &amp; Huawei and in those meeting all the technical details were discussed.</p>
                <p class="para">I had 5 teams each comprised of 1 Technician &amp; 1 Helper who worked directly with me. Teams were dedicated area wise. During Kick Off I distributed scope among all teams and shared technical details along with logistics plan. I assigned and coordinated with teams on daily basis to review their performance. In order to do so in a better manner, I prepared daily and weekly reports of all the teams, and kept KPIs of their works. The performance of each team was also scrutinized.</p>
                <p><strong>[CE 1.3]</strong></p>
                <p class="para">LCC was also given the responsibility of equipment logistic. I was also responsible to coordinate with WH and provide them with Delivery Note. Transport Company would pick and deliver equipment as per Delivery Note details.</p>
                <p class="para">After Installation of Equipment, it was my Responsibility for commissioning of Sites and to thoroughly test all services before offering the Site for acceptance issuance.</p>
                <p class="para">Network Expansion is very important factor for operator and each operator desire to cover this aspect in most cost effective manner and with zero tolerance for errors. PTCL is the largest service provider of triple play service (Telephony, Broadband &amp; IPTV) in Pakistan so it was great opportunity for me to work on this project and develop new skills. In this Project Huawei was Vendor which is well known for very fast pace of work. I coordinated with different teams and departments directly and indirectly which in turn augmented and refined my communication, management and technical skills.</p>
                <p class="para"><b>My Team hierarchy is shown in the following diagram -</b></p>
