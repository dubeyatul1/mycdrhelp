                      <h2>PERSONAL ENGINEERING ACTIVITY</h2>
                      <p><b>[CE 1.5]</b></p>
                      <h2>Roles &amp; Responsibilities</h2>
                      <p><b>My Roles &amp; Responsibilities were -</b></p>
                      <ul>
                        <li>Learning basics of WCDMA technology</li>
                        <li>New sites monitoring</li>
                        <li>Neighbor script preparation for new sites</li>
                        <li>Preparation and performing Drive test for new sites</li>
                        <li>3G drive test log files analysis and preparation of the required report</li>
                        <li>Weekly report submission task</li>
                        <li>Provide optimization recommendation based on DT analysis results</li>
                      </ul>
                      <p><b>[CE 1.6]</b></p>
                      <p>Since we have the target of 48 site to be on air; but with our team leader on sick leave I was held liable to carry on the task and handle the team and collect, analyze data. At the end I successfully submitted 53 sites and out of these 49 were accepted.</p>
                      <p>At the beginning of the project I was on the site and since I was a new engineer and here for training purpose so it was quite hard scenario but at the end of the first week I showed a great passion and interest that made my team leader consider me as a real member of the team and started assigning duties to me. I was able to complete them on time and with sound professionalism.</p>
                      <p>A new site was needed to be on air but required a rigger to perform the obligatory configurations. With no time to lose I climbed the tower and adjusted the antennas based on the team leader recommendation and with the support of contact visa mobile with our rigger who worked on another site. I did the job quite swiftly and with perfection.</p>
                      <p><b>[CE 1.7]</b></p>
                      <p><b>In this project I also did -</b></p>
                      <ul>
                        <li>Single Site Survey (SSV) to test any new 3G site</li>
                        <li>Cluster Survey to test multiple site together and optimize their corresponding performance</li>
                        <li>Estimation of the recommended tilts for each sector to cover the targeted area.</li>
                        <li>Provide neighbor script for new site.</li>
                      </ul>
                      <p><b>Testing Performed by me -</b></p>
                      <ul>
                        <li>Genex probe to perform drive Test</li>
                        <li>Genex Assistant to analyse log file</li>
                        <li>Map info to provide neighboring</li>
                        <li>Excel to provide neighbor script</li>
                      </ul>
                      <p><b>During the drive test I used -</b></p>
                      <ul>
                        <li>Scanner to test 3G frequency power</li>
                        <li>Huawei phone to test call set up</li>
                        <li>Laptop to record data</li>
                        <li>GPS to record location</li>
                      </ul>
                      <p><b>[CE 1.8]</b></p>
                      <p>I used Google earth and the Katherine antenna simulator to simulate the performance of antennas before visiting a site. This helped in lessening the number of visits to sites and reduced the time taken to accept the site and provide flawless services to subscribers.</p>
                      <p>After 4 months as a junior engineer I was able to give recommendation to senior engineer to be approved by MTC Touch. These recommendations were correct in most of the situation and approved by higher management</p>
                      <p><b>[CE 1.9]</b></p>
                      <p>As most of the work was in the ca, the safety measures were essential to be considered while driving; making sure that driver is performing as required and respecting rules was the foremost safety concern.</p>
                      <p><b>[CE 1.10]</b></p>
                      <p>As the working days started I used to be straight away in direct contact with my colleagues to allocate sites and errands. After that each person leave for the allocated sites. At the end of the day I met with the colleagues and site operators to have some discussion over the results.</p>
                      <p>At the end of each working day log files were needed to be sent to the manager. Also, a weekly report was generated to summarize the daily performance</p>
                      <p>During the period when the team leader was on leave; I was accountable for allocating the tasks, run drive tests &amp; collect data from the team. I also analyzed the collected data and generated report for evaluation.</p>
                      <p>At the beginning of the job each junior engineer was allied with different topic to study and present to his peer. Presentations were delivered at the end of the week and I was liable to answer all concerns and questions.</p>
                      <p><b>CE 1.11</b></p>
                      <h2>Summary</h2>
                      <p>Project was successfully completed and the radio department won the best performance team award for 2012.</p>
                      <p>Having the basic knowledge of 2 G I have understood and transferred the knowledge of 3g spectrum. Task allocation and prioritization that I learned in university was successfully implied during this project.</p>
                      <p>As a junior engineer I was working with the senior engineer and team leader to provide solution and data to higher management. This data and reports played key role in the success of the project from our department and led to consider the radio department the team of the year.</p>
