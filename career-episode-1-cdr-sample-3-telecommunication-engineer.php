                      <p><b>[CE 1.1]</b></p>
                      <p>This career episode narrates my project <strong>“MTC Touch 3G Project’’. </strong>This project was carried out during my tenure at <strong>Huawei Co Ltd as a Junior Radio Engineering.</strong></p>
                      <p>Huawei is a principal telecommunication company originally started in China and then spread globally. It entered in to the Lebanese market to provide Lebanese Network Operator the latest mobile technologies. The Company is spread into various divisions such as human resource department, radio department, wireless department, microwave department and logistics department. I am working as a junior radio engineer.</p>
                      <h2>BACKGROUND</h2>
                      <p><b>[CE 1.2]</b></p>
                      <p>The telecom sector is having rapid growth in technology acceptance and new resources of technology is showing pace to provide a fast &amp; uninterrupted internet with better customer services round the clock. MTC touch signed a contract with Huawei for the implementation and realization of the 3g technology to its valuable existing network. The said contracts were signed in February 2011 to implement 850 node B that can serve up to 450 K subscribers with state of the art technology.</p>
                      <p><b>[CE 1.3]</b></p>
                      <p>Due to immense growing demand for high speed internet and better than good services from our esteemed customers, MTC touch was forced to invest into the hardcore upgradation of their own network. The new path was to realize the 3g network in 2011.</p>
                      <p>The network was envisioned to enable users to have fast and seamless access to data for performing all their need e.g. email, downloading and browsing.</p>
                      <p>Huawei aided MTC touch with planning of the project and its execution topology long with complete execution. Huawei was entirely responsible for guaranteeing the services &amp; maintaining top notch quality of services. Huawei was also assigned to provide training and assistance to the MTC Touch engineers for future network operations.</p>
