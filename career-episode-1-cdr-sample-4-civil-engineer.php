                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 1.5]</strong></p>
                <h2 class="">My Roles &amp; Responsibilities</h2>
                <p><b>As a Structural Engineer, my key responsibilities and the role I played in the project were include: </b></p>
                <ul>
                  <li>Provide the structural concept of the building to meet the international standards specially the British standards BS 8110 (structural use of concrete) BS5950 (structural use of steel works) and UBC97 for seismic resistance structures.</li>
                  <li>Propose the proper columns location in order to qualify the structure system chooses and achieve safe, economic structure, as well as comply with the architectural design.</li>
                  <li>Model the 3D structure using Etabs software, applying the static and dynamic loads occurred, export the slabs to be designed using the CSi Safe software.</li>
                  <li>Design the vertical elements and insure the reality in modeling to generate the accurate design results.</li>
                  <li>Discuss the design and obtain the building permit from the local authority.</li>
                  <li>My role also includes the tender preparation. Where I must specify the concrete mixture, steel grade, types of mechanisms to be applied, which detailed in the specifications and Bill of Quantities. Review the tender bids from various contractor and insure that they have read the drawings and BOQ properly and comply with the project specifications.</li>
                  <li>I also oversee specialized contractors and other personnel, schedule and coordinate all design and construction processes to ensure a productive and safe work environment. I also making sure that that the job are completer with the right amount of tools, equipment, and materials</li>
                  <li>To maximize efficiency and accuracy, as a structural engineer, I requested the soil report to be provided from the specialized soil lab, I fulfill the code requirement to investigate at least one borehole for every 30m2, with depth of 30m for the SC soil type. This thing considered as essential for the raft and shoring design.</li>
                </ul>
                <p><strong>[CE 1.6]</strong></p>
                <p>Once the architect had briefed me his architectural design, his vision, aim and the purpose of each space and the main elements should be designed to be as a project icon, I realized that there will be such challenges and circumstances would be faced and should be solved, taking in consideration that these requirements should be included in this project, without affecting the safety of the building. For example, one of these issues that the architect wants to have bid hall of 18x22 m without any columns inside, and wants to create a water fall feature for along of 20m and height of 6m at the mezzanine floor. All these issues and more will be discussed in more details later in CE1.</p>
                <p><strong>[CE 1.7]</strong></p>
                <p>Apart from the architectural design, the geotechnical information of the soil was studied and found out that the under-ground water table encountered at a level of -3.5m depth where, we have two basements in addition to the proposed raft thickness, which giving the indication that our minimum excavation should reach the level of -9.0m. therefore I specified a proper dewatering while excavation should be considered. And un uplift force should be considered while designing the raft. That’s in addition to the shoring system, where I choose the secant piles to resist the lateral soil force and absorb/replace the underground water.</p>
                <p><strong>[CE 1.8]</strong></p>
                <p>To comply between the structural system, I proposed, and the possible locations of the columns, to avoid interrupting the architectural space division, and the changeable use of the different floors, from car park and driving ways at the basements, to restaurants and reception at the ground and mezzanine to luxury hotel rooms and suits at the typical upper floors. I spent more than two weeks to identify the possible locations of the columns to fulfill the required structural and architectural without load transfer.</p>
                <p>And at some locations I decided to use circular columns at the lower floors, and to be converted to rectangular columns at the upper floors, to be hidden inside the block walls, and this arrangement took place for about 20% of the overall project’s columns.</p>
                <p><strong>[CE 1.9]</strong></p>
                <p>Moreover, I faced a challenge while designing the conference, multi-purpose hall of a size of 18x22m, because the hotel operator requests to be as free of internal columns. So, in the future foldable partitions can divide the space at any location. Therefore, after primary calculations and mathematical checks, I decided to use the waffle slab system, because it can used for big spans until 25m, with acceptable deflection not exceeding 25mm, and this due to the interacting ribs with minimal depth which carrying the thin slab cover the whole space. And I provided the proper periphery columns and marginal beams designed particularly to carry the waffle slab safely.</p>
                <p><strong>[CE 1.10]</strong></p>
                <p>As mentioned earlier regarding the circular columns to be changed in to rectangular ones, I released that the transaction element would be a very critical position, to ensure that the axial load, shear force, and moments been transferred properly from the upper column to lower one. Therefore, I decided to design this transaction element as a corbel, specially it’s been exaggerated with a tension force mainly and a shear force.</p>
                <p><strong>[CE 1.11]</strong></p>
                <p>I studied the strut and tie model system, which really enrich my knowledge and remove the question marks of different issues faced before in different projects. And particularly in this project, I’ve been able to design the corbel section as strut and tie model, and the deep beams with opening required for MEP. Moreover, the structural details of the reinforcement of these elements been provided, to be as a main guide for the main contractor while construction.</p>
                <p><strong>[CE 1.12]</strong></p>
                <p>I gathered this information properly and note them down. Then, I started to model my project using Etabs software. I have meshed the slabs in advance using AUTOCAD software, then import these meshed slabs as dxf to the Etabs software, then I completed the model, defined the sections, defined the materials, the seismic lateral loads, wind loads, the static and dynamic loads been assigned to comply with UBC 97 code.</p>
                <p><strong>[CE 1.13]</strong></p>
                <p>I tried to develop my skills and knowledge to enhance the design ability and achieve economic, stable and accurate results. Therefore, I reviewed the meshing techniques and possibilities using the Etabs software, I reviewed the design preferences to be updated as per UAE seismic zone and define the input reinforcements in the vertical elements to be checked.</p>
                <p><strong>[CE 1.14]</strong></p>
                <p>Because the step of modeling the project, input and outputs are essential and critical, I worked at this stage as a team member, compiled the required information from my colleagues and convey them any updates may be occurred, to be considered in their outputs as well and in the overall project drawings before tender and approval.</p>
                <p><strong>[CE 1.15]</strong></p>
