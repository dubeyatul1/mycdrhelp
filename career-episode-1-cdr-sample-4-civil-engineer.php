                <p>At the duration mentioned above, I was assigned to provide the main structural concept and design of a four-star hotel consists of two basements, ground, mezzanine and 10 stories. Being the structural engineer, it was required from me to provide the fundamental structural concept, columns locations, and then to model, analyze, design and produce the full details for each element in the structure.</p>
