                <p>This career episode relates to project titled “__________________________________’’. This project was carried out during my professional tenure at <strong>Al Baha Engineering Consultants</strong> as <strong>Structural Design Engineer</strong>. Keltron is a control &amp; instrumentation division of Kerala State Electronics Development Corporation Limited (KSEDC), which is India’s first &amp; largest state electronics enterprise.</p>
