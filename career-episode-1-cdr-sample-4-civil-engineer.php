                <p><strong>[CE 1.3]</strong></p>
                <p>AL Baha Engineering and Consultant is an engineering consultancy firm, which provides the complete engineering services to the real state companies and developers. It is composed of a group of well-trained engineers in all engineering disciplines. Provides the architectural concept, design of the residential, hospitality and public buildings, to meet the client requirements and expectation as well as the local authority regulation and international standards. That’s in addition to the complete structural, mechanical and electrical design and approvals which required prior to the construction.</p>
