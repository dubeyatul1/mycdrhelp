                      <p><strong>Admixture:</strong> A substance added to the basic concrete mixture to alter one or more properties of the concrete; i.e. fibrous materials for reinforcing, water repellent treatments, and coloring compounds.</p>
                      <ul>
                        <li>Air-entraining admixtures (mainly used in concrete exposed to freezing and thawing cycles)</li>
                        <li>Water-reducing admixtures, plasticizers (reduce the dosage of water while maintaining the workability)</li>
                        <li>Retarding admixtures (mainly used in hot weather to retard the reaction of hydration)</li>
                        <li>Accelerating admixtures (mainly used in cold weather to accelerate the reaction of hydration)</li>
                        <li>Superplasticizer or high range water-reducer (significantly reduce the dosage of water while maintaining the workability)</li>
                        <li>Miscellaneous admixtures such as corrosion inhibiting, shrinkage reducing, coloring, pumping etc.</li>
                      </ul>
                      <p><strong>Aggregate:</strong> Inert particles (i.e. gravel, sand, and stone) added to cement and water to form concrete.</p>
                      <p><strong>Cement:</strong> Dry powder that reacts chemically with water to bind the particles of aggregate, forming concrete. Portland cement is typically used in concrete production.</p>
                      <p><strong>Fly ash:</strong> Fly ash is a by-product from coal-fired electricity generating power plants. The coal used in these power plants is mainly composed of combustible elements such as carbon, hydrogen and oxygen (nitrogen and sulfur being minor elements), and non-combustible impurities (10 to 40%) usually present in the form of clay, shale, quartz, feldspar and limestone. As the coal travels through the high-temperature zone in the furnace, the combustible elements of the coal are burnt off, whereas the mineral impurities of the coal fuse and chemically recombine to produce various crystalline phases of the molten ash. The molten ash is entrained in the flue gas and cools rapidly, when leaving the combustion zone (e.g. from 1500°C to 200°C in few seconds), into spherical, glassy particles. Most of these particles fly out with the flue gas stream and are therefore called fly ash. The fly ash is then collected in electrostatic precipitators or bag houses and the fineness of the fly ash can be controlled by how and where the particles are collected.</p>
