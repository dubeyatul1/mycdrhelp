                      <p>Cement and Fly ash are fed to holding hopper with the help of a screw conveyer.</p>
                      <p>A heavy-duty cement screw conveyor is fixed in inclined position to convey the cement from Manual Feeding Hopper to Cement Hopper. A suitable drive unit is also provided to drive the screw</p>
                      <p>The screw conveyor body and the screw are manufactured from heavy duty ‘C’ class pipe and the flutes are fabricated from 5mm plate. Running clearances provided between body and flutes for smooth running. The screw is supported on both ends by bearing and at center by hanger bearing having renewable hard bush. These bearing can b adjusted with setting nuts so as to have proper alignment.</p>
                      <p>The screw conveyor is provided with suitable vertical supports. One inlet connection is provided at the bottom end where manual-feeding hopper is connection &amp; one discharge connection at the top from where the cement is discharged to cement weighing hopper. Flexible joint is provided between discharge connection &amp; cement weighing hopper.</p>
