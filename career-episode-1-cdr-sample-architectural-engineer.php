                      <p><strong>[CE 1.1]</strong></p>
                      <p>This career episode relates to project titled <strong>“Ready Mix Concrete Mix (RMC)’’. </strong>The project was assigned to me as a part of my 3rd Year 1st Semester of engineering at International Islamic University Malaysia.</p>
                      <h2>Background</h2>
                      <p><strong>[CE 1.2]</strong></p>
                      <p>Ready mix concrete was first patented in Germany in 1903, its commercial delivery was not possible due to lack of transportation needs. The first commercial delivery was made in Baltimore USA in 1913.The first revolving drum type transit mixer was developed in 1926.</p>
                      <p>The National Ready-Mixed Concrete Association of Malaysia (NRMCA) was established in 1987 and has been recognized as the national association for the suppliers of ready-mixed concrete in the Peninsular Malaysia. Members of NRMCA are mainly the major players in the industry and our 26 members supply about 70% of the total consumption of concrete.</p>
                      <p><strong>[CE 1.3]</strong></p>
                      <p>Few things are more aggravating to produce on a worksite than concrete. Bags of cement, sand, aggregate (gravel) and possibly other additives must be delivered to the construction area. A supply of clean water is also necessary, along with a rented concrete mixing hopper. Even after all the dusty and heavy ingredients have been loaded into the hopper, one small error in the wet/dry ratio can render an entire batch of concrete unusable. One common solution to this messy and time-consuming problem is <u>“READY MIX CONCRETE”</u></p>
                      <p>Ready-mix concrete (RMC) is a ready-to-use material, with predetermined mixture of cement, sand, aggregates and water. RMC is a type of concrete manufactured in a factory according to a set recipe or as per specifications of the customer, at a centrally located batching plant.</p>
                      <p>It is delivered to a worksite, often in truck mixers capable of mixing the ingredients of the concrete en-route or just before delivery of the batch. This results in a precise mixture, allowing specialty concrete mixtures to be developed and implemented on construction sites. The second option available is to mix the concrete at the batching plant and deliver the mixed concrete to the site in an agitator truck, which keeps the mixed concrete in correct form.</p>
                      <p>For growth of the industry, government bodies, private builders, architects/engineers, contractors, and individuals required to be made fully aware about the advantages of using ready mix concrete, government bodies/consultants need to include ready mix concrete as mandatory in their specification for execution, government specifications for CPWD and PWD jobs should include Ready mix concrete as a mandatory item. Apart from this tax breaks are required for the growth of RMC and developers/contractors needs to be discouraged from piling up materials like metal, sand etc. on roads/foot paths.</p>
