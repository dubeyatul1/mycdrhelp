               <h2 class="sample_heading">BACKGROUND</h2>
               <p><strong>[CE 1.2]</strong></p>
               <p>My first career episode is related to a project which I did when I was in ______ year of engineering studies. I have studied the Hydrogenation in my _______ year in details. Since then the topic has intrigued me to work in this segment on a broader scale. This project was based on the Methanol Production via CO2,, Hydrogenation on Promoted Cu-based Catalyst. I have chosen this project in consent with my Project Supervisor. I worked closely for this project and got the desired results likewise. My project was completed well before deadline and was acknowledged in a respective manner by the Project Supervisor &amp; Dean of the Petroleum Engineering Department.</p>
               <p>A number of studies on methanol production have been reported over copper Cu-based catalysts on Alumina Support (Cu/ZnO/Al2O3). This catalyst undergoes a deactivation process due to the water produced and the heat released from the reaction. This project focused primarily on synthesizing the copper based catalyst on the Carbon Nanotubes (CNTs) as catalyst support for CO2 hydrogenation to synthesize methanol.</p>
               <p><strong>[CE 1.3]</strong></p>
               <p>The purpose of starting this project was to fulfil a major milestone, dissertation, in partial fulfilment of the requirements for a Bachelor of Engineering (Hons). I worked under direct supervision of Dr. Pradip Chandra Mandal, Lecturer in the Department of Chemical Engineering with consent to the Department of Petroleum Engineering at Universiti Teknologi PETRONAS.</p>
               <p>This project focused on the development of the methanol synthesis catalyst to find a promising catalyst that increases the methanol selectivity and long life activity.</p>
               <p>I primarily focused on the effect of adding promoters such as niobium (Nb) and zirconium (Zr) on the properties of the catalyst. Besides that, the job included characterizing the physical and chemical properties of the catalyst using some characterization techniques such as Scanning Electron Microscopy (SEM), EDX and TPR. The catalytic performance of the synthesized catalysts was carried out on a high pressure reactor analysis system made up of micro-activity fixed-bed reactor with an on-line gas chromatograph with flow ratio of H2/CO2 is 3:1 under operating temperature and pressure of 250℃ and 2.25 MPa respectively.</p>
