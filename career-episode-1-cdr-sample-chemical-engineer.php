               <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
               <p><strong>[CE 1.6]</strong></p>
               <p>I did the Project management of the system which involved the modularization of the project in to definite number of stages &amp; created a classification of the activities to be accomplished. I prearranged the entire project in two stages.</p>
               <p><b>PHASE 1: </b>During this phase I finalized the study of the conceptual as well as philosophical nitty-gritties of the proposed work &amp; drew up an approach to achieve the desired goal.</p>
               <p><b>PHASE 2: </b>In this phase; I have employed strategies for successful execution. After execution I carried out all the necessary proceedings of the project.</p>
               <p><strong>[CE 1.7]</strong></p>
               <p><b>My Roles &amp; Responsibilities were -</b></p>
               <ul>
                 <li>Preparation of Methanol Synthesis Catalyst using impregnation method which is the most suitable method to prepare Cu-based Nano-catalyst. The support for Cu/ZnO-based catalyst was carbon nanotubes (CNTs) and Al2O3 and promoted by zirconium (Zr) and niobium (Nb).</li>
                 <li>Dissolving the required amount of each precursor in 50 ml beaker filled with specific amount of deionized water to produce aqueous solution with molarity of 0.5 M. Therefore, to achieve a homogenous solution, this solution must be subjected to stirring for 1 hour.</li>
                 <li>Adding the homogeneous solution to the supports (CNT &amp; Al2O3) in a dropwise manner using pipette and the pH value for the mixture was controlled by adding ammonia solution using pH meter.</li>
                 <li>Stirring the mixture for 24 hours at room temperature. The prepared solution then was washed with deionized water and filtered using vacuum pump filtration set.</li>
                 <li>Conducting the drying process in a muffle furnace for 8 hours at 110 (_ ^o)C. Then followed by calcination the catalyst under Argon gas flow for 5 hour at 350 (_ ^o)C using tube furnace.</li>
                 <li>Characterizing the prepared catalysts using some characterization techniques such as Scanning Electron Microscopy (SEM), Energy Dispersive X-ray Spectroscopy (EDX) and Temperature-Programmed Reduction (TPR).</li>
                 <li>Evaluating the prepared catalysts using CO2 hydrogenation process to produce methanol using micro-activity fixed bed reactor.</li>
                 <li>Computer-controlled an on-line gas chromatography to analyze the withdrawn gases from the reactor every 30 minutes and the evaluation was made based on approximately 8 reaction hours.</li>
               </ul>
               <p><strong>[CE 1.8]</strong></p>
               <p><b>Problem A</b></p>
               <p>I faced the issue of water presence as a by-product in methanol production via CO2 hydrogenation process. Some amount of water was adsorbed by alumina supported copper catalyst and this has deactivated the catalyst. Furthermore, Cu particles have undergone sintering process due to the exothermic reaction.</p>
               <p><b>Solution</b></p>
               <p>To overcome this issue, I used the carbon nanotubes (CNTs) and found that it does not adsorb H2O and has very good thermal conductivity. These properties make CNTs the most suitable and highly dispersed catalyst for CO2 hydrogenation process.</p>
               <p><strong>[CE 1.9]</strong></p>
               <p><b>Problem B</b></p>
               <p>Another issue occurred during the reaction process when determining the most optimal temperature and pressure for the reaction to take place inside the reactor.</p>
               <p><b>Solution</b></p>
               <p>After several trial-and-error attempts, I found that the most optimal operating conditions for the reaction in the tube of the micro-activity fixed-bed reactor under temperature and pressure of 250 oC and 1 bar respectively. Generally, the reaction of the methanol production from synthesis gas is conducted for 8 hours. The reactor lines, tubes and valves were heated up to 180 oC to prevent product condensation process.</p>
               <p><strong>[CE 1.10]</strong></p>
               <h2 class="sample_heading">ENGINEERING DESIGN</h2>
               <p>After the synthesis of the catalyst, I designed a suitable reactor for the reaction of CO2 hydrogenation to produce methanol to evaluate the performance of the synthesized Cu-ZnO catalysts.</p>
               <p>The CO2 hydrogenation reaction takes place in a micro-activity fixed-bed reactor. My design was based on some calculations to determine the dimensions of the reactor. I found that outer diameter = 14.5 mm and inner diameter = 9 mm and the length of the tube = 305 mm with total volume of 20 ml. The catalytic performance test was conducted under the same operating condition for all the prepared catalysts in order to have reasonable comparison for all catalysts.</p>
               <p>I started the reaction with holding 0.1 g of the prepared catalyst in the tube of the micro-activity fixed-bed reactor between two layers of quartz wool. Then, the tube filled with the catalyst was placed inside the furnace of the reactor. The door of the reactor was closed and helium gas was purged to the reactor at flow rate of 15 ml/min for period of 30 minutes for removing the existing air and contaminants out of the tubes of reactor and gas chromatography. The reduction of the catalyst was in 5 vol% H2 in helium gas at 20 ml/min for 2 hours under temperature and pressure of 250 oC and 1 bar respectively.</p>
               <p><strong>[CE 1.11]</strong></p>
               <h2 class="sample_heading">ENGINEERING CALCULATIONS</h2>
               <p>I did a handful of calculations to prepare the CNT supported Cu/ZnO based catalyst (15wt%Cu-ZnO /84wt% CNT). The calculations involved the amount of materials used in the project as well as the preparation of the solutions. The following are some calculations for synthesizing 1 g of catalyst. Weight of metals (Cu/ZnO) (g) is calculated by the following equation: weight of metal (g)= total weight of the catalyst × percentage of desired metal loading weight of metal (g)=1 g ×15⁄100 = 0.15 g. Weight of Cu (g)=0.15×70⁄100 =0.105 g. Weight of ZnO (g) =0.15×30⁄100= 0.045 g. The weight of precursors: weight of 〖(Cu(〖NO〗_3)〗_2.3H_2 O) (g)=(0.105 g Cu ×241.6 g/mol 〖Cu(〖NO〗_3)〗_2.3H_2 O)/(63.5 g/mol Cu)=0.3995 g weight of 〖(Zn(〖NO〗_3)〗_2.3H_2 O) (g)=(0.045 g ZnO ×297.48 g/mol 〖Zn(〖NO〗_3)〗_2.3H_2 O)/(81.41 g ZnO)=0.1644 g Weight of support (CNT): weight of support (g)=total weight of the catalyst -weight of metal weight of Alumina (g)=1.0 -0.15=0.85 g Preparation of precursor solution (0.5M): number of moles of 〖(Cu(〖NO〗_3)〗_2.3H_2 O) =Molarity ×volume number of moles of 〖(Cu(〖NO〗_3)〗_2.3H_2 O) = Mass/(Molar Mass) = (0.3995 g)/(241.6 g/mol)=0.00165 mol number of moles of 〖(Zn(〖NO〗_3)〗_2.3H_2 O) = Mass/(Molar Mass)= (0.1644 g)/(297.48 g/mol) =0.000553 mol Total number of moles = 0.00165+0.000553 = 0.0022 mol Volume = (Number of moles)/Molarity= 0.0022/0.5=0.0044 L=4.4 ml H_2 O</p>
