                          <li>The peak stormwater runoff to the council drains from proposed new allotment B assuming that predevelopment, all stormwater was contained within the allotment.</li>
                          <li>The ARI (AEP) set by the council and any other regulatory requirements.</li>
                          <li>A pipe layout including RL’s to convey the stormwater and details for pipe and drain connections.</li>
                          <li>The pipe diameters required by council.</li>
                          <li>Standard pipe and drain connections required by council.</li>
                        </ul>
                      </li>
                      <li><b>Task B</b>
                          <p>A construction report along with soil report was provided to me. I needed to -</p>
