                    <h2>BACKGROUND</h2>
                    <p><b>[CE 1.2]</b></p>
                    <p>A residential project in Adelaide involved land division, a new dwelling and modifications to the existing allotment. Referring to the Proposed Development Plan, the new allotment, will have a dwelling built on land that was previously the garden and rear yard of the existing allotment. The Proposed Dwelling including Garage and the Store in the existing allotment required the design of new footings. The Proposed Carport in the existing allotment required the design of structural framing and pad footings. Due consideration was to be given to the disposal of the additional stormwater runoff resulting from the development.</p>
