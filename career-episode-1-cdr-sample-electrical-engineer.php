                <p>I computed the Voltage Drop Calculations for Notification Appliance Circuit (NAC), which has helped me to further compute the resistance of the conductor. By using these parameters I have calculated total alarm notification device amperage. And then the total voltage drops in each branch circuit. A per NFPA Guide Lines if the branch Circuit Voltage Drops exceeds 4.4 VDC it will not be compatible with the standard as it can consume th battery life in – no time. Following is the example for a Branch Circuit: Wire Length (From Main Panel to End of Circuit = 200 Ft X 2 (Trans / Recev) = 400 Ft Used Wire Size = # 16 AWG (American Wire Guage) [BELDED SHEILEDED MULTI CONDUCTOR CABLE] OHMS / Ft = 1 / 1000 Ft = 0.4 OHMS / FT Circuit Resistance (R)= 440 ft X 0.44 OHMS / FT = 160 OHMS Total Alarm Appliance Amperage = No. of Appliance X Current Amperage Each = 23 mA Voltage Drop (E) = 23 m (A) X 160 (OHM) = 3.68 V (Should Not Exceed 4.4 V) As per NFPA Fire Alarm Control Units (FACU) are tested to UL 864 and are required to operate at the End of Use Full Battery Life 20.4V Voltage Drop (V) = 20.4 V – (E) = 17 V [Should Not be Less Than 16 V]</p>
                <p>Keeping in view of above criteria all the devices in each loop has been balanced.Further More, I created the Detector / Sensor Profile sheet over MS Excel in-order to display the actual Location which will be programmed in the Main Control Panel for each device, as each component of the circuit has designated address and it cannot exceed 40 digits as maximum limit.</p>
