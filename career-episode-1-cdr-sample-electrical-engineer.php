                <p><strong>[CE 1.1]</strong></p>
                <p>Al-Inma Medical Services is the well renowned healthcare service providing company in Kingdom of Saudi Arabia. The Company’s head office operates in Capital City Riyadh, and is committed to provide state of the art health care facilities to its patients. The company also promotes the program to maintain and upgrade all of its facilities in Jazan, Riyadh, Khamis and Al Qaseem region respectively, where as Facility Management - Engineering and Safety has been the most established department dealing with Electrical, Electro – Mechanical and Weak Current Electronics Systems designed and executed by experienced specialized engineers and consultants.</p>
                <p><strong>[CE 1.2]</strong></p>
                <p>Al Hayat National Hospital Jazan has been established to provide state of the art health care facilities to local Jazan residents and suburban community, as the region has been under development since then and limited healthcare facilities have been available. I joined Al Inma Medical Services Company in March 2014. Where I have been able to establish myself as a multirole engineer in the field of electrical &amp; electronics engineering. I have designed and accomplish multiple projects in Electrical System, Electrical Controls and Low Current Electronics Systems such as Addressable Fire Alarm System Design &amp; Installations, CCTV Surveillance System Design &amp; Installations, Nurse Calling and Paging System etc.</p>
                <p><strong>[CE 1.3]</strong></p>
                <p>This career episode is related to the Design Implementation and Installation of Addressable Fire Alarm System. The project is executed keeping n view of the various safety concerned for 200 Beds Multi-Storied Hospital Building under the guide lines of Civil Defense, NFPA and IEEE standards for Fire Alarm Systems. Keeping in view - one of the major Disaster in the history of Saudi Arabia which happened in Jazan General Hospital on 25th December 2015 and claimed the lives of 24 people, a deadly blaze broke out; in a matter of minutes the smoke spread out throughout the facility injuring at least 84 staff &amp; patients. While the investigations have been carried out the main reason has been found due to defects in Fire Alarm System and poor smoke management system which cause the spread of smoke in the facility and caused suffocation among the people.</p>
                <p><strong>[CE 1.4]</strong></p>
                <p>Immediately after the incident, special steps has been taken by the Ministry of Health &amp; Civil Defense of Saudi Arabia in order to ensure the safety of staff and patients working in the Healthcare facilities. So in-order to meet the criteria of Civil Defense (and to acquire the safety license as per new guide lines) I have been assigned the project for the design and installation of fully automated fire alarm system.</p>
                <p><strong>[CE 1.5]</strong></p>
                <h2 class="sample_heading">PROJECT SCOPE</h2>
                <p>The aim of the project was to design an efficient, reliable, cost effective and multifunctional (Full Automated) Addressable Fire Alarm System for Al Hayat National Hospital Jazan. Previously the old system was Conventional Type along with multiple loops with un-balanced number of Detectors (Smoke &amp; Heat), the system was very hard to trouble shoot secondly no location is identified suppose if the smoke broke out in one patient room the alarm will be triggered all over the facility, but it will be very difficult to trace the location in a huge building which can cause a major internal disaster and compromises the safety of the building.</p>
                <p>I proposed a totally robust system which has been implemented across the world class facilities and suggested to upgrade the Conventional system into Programmable Fire Alarm System. So that all the locations can be programmed in the main control panel upon triggering the location will be pin pointed exactly so one can reach and access the location in no time which will reduce any un-expected disaster while the smoke management system (Pressurize Stair Case) will work in parallel with the Fair Alarm System.</p>
                <p><b>I designed the system with following functionality dealing with various entities involved in the design of the project:</b></p>
                <ul>
                  <li>Configuration &amp; Installation of Programmable Addressable Control Panel (Wide LCD Display)</li>
                  <li>Installation of addressable Smoke, Heat Detectors and Manual Call Points in Various Loops</li>
                  <li>Installation of Repeater Panels on 03 Floors of the Building with Narrow LCD Display</li>
                  <li>Magnetic Door Locks Automation with Installations (Buzzer Provided)</li>
                  <li>Automation of Elevators with Fire Alarm System</li>
                  <li>Automation of Fresh Air Handling Unit System Shut Down Control System</li>
                  <li>Automation of Pressurized Stair Case System</li>
                  <li>Installation of Electronic Flow Switches beside the zone valves of Fire Hydrant Main Line on each floor (In-case of No Pressure Mechanical Fault will be identified in the System)</li>
                </ul>
                <p>The system will ensure complete operating safety and multiple options to facilitate the smooth operations of building.</p>
