                <p><strong>[CE 1.8]</strong></p>
                <h2 class="sample_heading">Problems faced &amp; their Rectifications</h2>
                <p><b>Elevator Integration</b></p>
                <p>During an event of Fire it is highly recommended not to use the Elevators which can cause death due to suffocation due to any expected breakdown or smoke enters the elevator shell. But how to avoid this situation in case is someone is using the elevator? If fire break out at the same time. Different control options are provided by manufactures so I ordered fire safety control cards to be installed and programmed in the Elevator by Mitsubishi (Manufacturer).</p>
                <p><b>Final Solution:</b></p>
                <p>I integrated the control card along with Fire Alarm System’s addressable control module. When the Fire Alarm will trigger immediately the signal will reach the NO control Module which will return to NC immediately giving the signal to the elevator (As a result the elevator will cancel its operational movement immediately reach the ground floor the door will remain open and the power will shut down until the alarm will reset.</p>
                <h2>Design of Automatic 24 VDC Control Panels for HVAC &amp; Pressurize Stair Case System</h2>
                <p>Effect of problem: In an event of Fire the smoke travel via duct network and spreads various parts of the facility. I found out there were no smoke dampers &amp; fire dampers installed in the Duct Network which can cause a major disaster.</p>
				<picture>
