                <p><b>Root Cause of Problem: </b></p>
                <p>This was indeed a major concern as no BMS System has been installed in the Facility for the automaton purpose and all the Air is directed in-side the facility as fresh oxygen supply which can be a fuel for any un expected fire and can let the smoke travel throughout the facility.</p>
                <p><b>Final Solution:</b></p>
                <p>I deigned a relay based automated 24 VDC FAHU Shut Down System which will control all the FHUS automatically and will be integrated with the main power distribution Panel of the FAHU’s, in-case when the Fire will trigger it will immediately shut down the Fresh Air Supply to the Hospital saving the spread of smoke and cut of fresh air oxygen supply to the hospital. The same circuit has been implemented for various utilities such as: Stair Case Pressurized System Control, Magnetic Door Lock Control etc</p>
                <h2>Electronic Flow Switches</h2>
                <p>In order to enhance the complete safety system electronic flow switches has been added beside each main valve of Main Fire Fighting Pipe line (This part was covered along with mechanical engineering division). In case if the pressure will fall which will trigger the flow switch and the mechanical fault will be identified in the main panel</p>
