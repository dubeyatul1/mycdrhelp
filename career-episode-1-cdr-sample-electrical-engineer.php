                <p><strong>[CE 1.7]</strong></p>
                <p><b>My Roles &amp; Responsibilities</b></p>
                <ul>
                  <li>My task involved the complete study of various safety parameters, requirements, risk assessment, conditions and options available for the implementation of efficient, cost effective and reliable Addressable Fire Alarm System.</li>
                  <li>I carried out general site visits on various critical locations of the facility with Chief Operating Officer to indentify the possible issues in case of any blaze.</li>
                  <li>I acquired the complete plan of the building from Maintenance Manager for all the floors including the ancillary and utility buildings around the premises in-order to make estimation in regard with the total number of devices needed for the purpose including the cost estimation of the whole system along with 5 – Years cost of maintenance.</li>
                  <li>After days of brain storming, research activities and studying various type of system available in the market and conducting site visits with my technical team I have been able to propose the installation of SIMPLEX Addressable Fire Alarm System (4100 ES - Made in USA) along with complete programming kit.</li>
                  <li>The system is totally reliable with each detector / device has been provided with a specific 8 – bit address code changeable at the back of Detector Base. The system has multiple electronic control cards compatible with loop control cards installed as per the requirements. Signal Transmission and Receiving takes place in designated loops through all the devices. In- case if any device triggers (Smoke or Fire Detected) the Fire Alarm Control Panel will alarm in the defined zone and the designated location / area will appear on the display screen of Main Control Panel along with repeater Panels until Re-Set.</li>
                  <li>I further contacted various vendors and suppliers for the availability of current system by acquiring quotations and comparing the type of systems with minimum cost and ease of trouble shooting solutions, submission of purchase order and completion of financial issues in order to receive the equipment in minimum time.</li>
