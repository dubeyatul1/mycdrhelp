                  <p><strong>[CE 1.1]</strong></p>
                  <p>This career episode relates to project titled <strong>“Riyadh Rail Build Operations’’.</strong> The project was done during my tenure with <strong>SERCO Arabia.</strong></p>
                  <h2>Background</h2>
                  <p><strong>[CE 1.2]</strong></p>
                  <p>Microland is a privately-owned IT Services company headquartered in Bangalore, India. It was founded in 1989 is the first Hybrid IT infrastructure provider in India. It’s revenue as of 2017 was USD150 million. The company employs more than 3,800+ professionals across its offices in Europe, Midle East, North America, and India. Microland portfolio of services includes Digital Networks, Digital Computing, Digital Application, Digital Workplace and Cyber Security.</p>
                  <p><strong>[CE 1.3]</strong></p>
                  <p>The client, Serco is a UK based public services provider and the transport business unit of SERCO was awarded the complete turnkey project of building the Riyadh Metro Rail infrastructure for lines 1 and 2 of the railway system. The duration of the project was 24 months. </p>
				  <p><b>SERCO employed Microland as its System Integration partner through competitive bidding process for -</b></p>
                  <ul>
                    <li>Setting up its ICT infrastructure including Data Center, network, CCTV and technical contact center</li>
                    <li>Installing the Computerized Maintenance Management System as the backbone of the business is based on MAXIMO</li>
                    <li>Developing a Document Management System based on SharePoint.</li>
                  </ul>
                  <p>The actual duration of project for Microland was 12 months to complete and handover the ICT components to SERCO.</p>
                  <p><strong>[CE 1.4]</strong></p>
                  <p>The ICT project awarded to my company was turnkey and encompassed both infrastructure setup and system integration. The infrastructure scope addressed sourcing hardware/software, configuring and installation of the same.</p>
                  <p><b>The System Integration included two components -</b></p>
                  <ul>
                    <li>Installing and integrating the CMMS, which mainly comprised of Maximo</li>
                    <li>Development of share point based DMS.</li>
                  </ul>
                  <p>My primary role included providing business support to MAXIMO installation and managing the Share point implementation.</p>
                  <p>The project was required to be completed within 12 months of time and within the allocated budget.</p>
