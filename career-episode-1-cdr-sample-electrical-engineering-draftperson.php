                  <p><strong>[CE 1.5]</strong></p>
                  <h3>My Roles &amp; Responsibilities</h3>
                  <ul>
                    <li>Leading the BA team across the MAXIMO project and Share point project implementation. The methodology I followed was modified agile &amp; utilization of BA tool kit for ensuring uniform documentation.</li>
                    <li>Plan, design, develop, and deploy MAXIMO system supporting core metro system. I was also responsible for generating comprehensive reports with causes and possible solutions to systems issues.</li>
                    <li>Planning the business analysis activities</li>
                    <li>Developing key requirement extraction templates.</li>
                    <li>Information management and maintenance of overall quality and consistency across the project.</li>
                    <li>Providing expertise in mapping business with technology and data flow across the complete architecture.</li>
                  </ul>
                  <p><strong>[CE 1.6]</strong></p>
                  <p><b>The critical factors in this project were</b></p>
                  <ul>
                    <li>Installation of MAXIMO and integration with other business systems</li>
                    <li>Mapping data flow across the complete architecture</li>
                  </ul>
                  <p>The <strong>main problem</strong> was visualizing the complete three tier architecture including business systems, integration touch points and data flow. Technical team needed the same to start any activities. To address this, the first step was to map business need to the technology involved.</p>
                  <p><strong>Solution: </strong>I spent 47 man-hours developing the system integration touchpoints in MS Visio for showcasing the bigger picture.</p>
