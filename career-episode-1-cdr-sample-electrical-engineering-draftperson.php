                  <p><strong>Problem 2: </strong>The second issue was on the timelines and requirement sign-off, namely MAXIMO installation was not to be started till the infrastructure is ready. DR plan was in place and rail enablement servers were in place and to be finished before appropriate network was setup across the metro-rail lines.</p>
                  <p><strong>Solution:</strong> In my experience, under tight project timelines, the requirements have to be accepted along with planned project deliverables. I took the necessary steps &amp;</p>
                  <ul>
                    <li>Worked closely with the infrastructure team and the MAXIMO project team involved in setting up resources, and calculation of costs for the standard activities.</li>
                    <li>Did setup of ‘WAR-ROOM” which mandated all key stakeholders to deliberate, confirm and sign-off on the business requirements which helped create high-level documents and map to technical requirements. This ensured any critical scope and assumptions to be addressed beforehand.</li>
                    <li>Created a comprehensive RACI matrix and FIT-GAP file on each line item of project plan associated to bring maximum transparency on daily activities being competed</li>
                  </ul>
                  <p><strong>Problem 3:</strong>Conflicts between BA created specifications and converting them in to solution by technical teams. This pitfall was mainly due to inadequate time was allotted for eliciting requirements and documents compilation.</p>
                  <p><strong>Solution:</strong> While this initially was a hiccup, I resolved this issue by having the BA team use approved templates &amp; use modeling techniques for missing information and getting sign-off on the high-level documentation before creating the tedious detailed documentation.</p>
                  <p><strong>[CE 1.7]</strong></p>
                  <p>The success of this project depends on effective communication and documentation. In this project, the templates, RACI, data-flow Visio diagrams, were all defined by me.</p>
                  <p>I personally designed the CMMS system which captured all integration points and connectors. With this as the baseline, I also designed the sub-components with inputs from the technical architect.</p>
