                  <p><strong>[CE 1.8]</strong></p>
                  <p>I carried out Integration tests, API checks, stress testing on the Maximo system. For the integration points, I referred back to the COE library in my company to identify the key elements and business impacts. I also regularly took advise from enterprise architect who had worked on similar project in US for risk mitigation.</p>
                  <p>I was involved in the man-hours and timeline calculations for the project work related to BA, overlapping with project teams and deployment. As part of the project management, I was also involved in work breakdown structure calculation.</p>
                  <p>The software that was to be implemented was MAXIMO, I used MS Office, Project and Visio primarily for developing the functional mapping, documentation, design, and communication.</p>
                  <p>I introduced the RACI matrix which was highly appreciated. I also introduced modelling design from which entire project team benefitted during the course of the project.</p>
                  <p><strong>[CE 1.9]</strong></p>
                  <p>Communication was the key in successful completion of this project. I daily had briefing/de-briefing meetings with my team. I also had WARROOM discussions with key stakeholders on weekly basis. As part of this project I attended weekly review meetings on project status &amp; I was part of executive meetings as well.</p>
                  <p>I had daily 1-hour meeting with the program manager and project managers for application and infrastructure teams.</p>
                  <p>I needed to deliver multiple reports on daily, weekly &amp; monthly. The key reports were related to project timelines and issues which my team foresaw and provided necessary remedy.</p>
                  <p>I was involved in presenting the charter over the project kick-off and weekly presentation on the BA components to project teams. The RACI presentation was run by me and I was the SPOC for WARROOM presentations.</p>
                  <p><strong>[CE 1.10]</strong></p>
                  <h2>Summary</h2>
                  <p>The project was successfully completed on time with zero redundancy. This was a critical project and we got a CSAT rating of 4.3/5 in meeting the objectives. Overall project was appreciated by the COO of the Metro-rail for its delivery within the stipulated time frame.</p>
