                <p><strong>[CE 1.2]</strong></p>
                <p>It is quite common globally for the Engineers to carry out certain projects during their academic period. I was enthralled when I went through the project topics from my project supervisor. I was quite happy to take project related to the RF communication. The topic really fascinated me and I took this project as a challenge and completed in a group of three Engineers including myself. I was also given the role of Teal lead for this project by my project supervisor.</p>
                <p><strong>[CE 1.3]</strong></p>
                <p>I organized this project grounded on the Radio Frequency &amp; Embedded expertise. The project was chiefly implied for controlling the speed of the vehicle primarily following the received signals from the corresponding transmitting body.</p>
                <p><strong>[CE 1.4]</strong></p>
                <p>I went through numerous offline &amp; online sources to get information about the RF &amp; Embedded technologies. I studied the topic in detail in order to execute the project accordingly. I also had numerous meetings with the project supervisor in order to have in depth knowledge and resolve any issues related to the project. I also documented the complete project as per requirements.</p>
