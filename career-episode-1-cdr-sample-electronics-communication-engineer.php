                <p><strong>[CE 1.6]</strong></p>
                <h2 class="sample_heading">METHODOLOGY</h2>
                <p>I grounded the entire project on Radio frequency &amp; Embedded system. When the Car enters a restricted area the signal was read by the Receiver &amp; was correspondingly transmitted by the Transmitter previously situated in the restricted area. The signal thus received was later decoded by the micro-controller &amp; make the driver of the Car on high-alert through Liquid Crystal Display (LCD). Depending upon the signal the micro-controller plays its part by limiting the speed of the DC-motor which in turn lessen the speed of the vehicle.</p>
                <p><strong>[CE 1.7]</strong></p>
                <p>As a lead Engineer I first presented the methodology of the system along with block diagram &amp; schematic diagram of the whole project as shown below.</p>
                <img src="images/CE-1-7.png" alt="Electronics and communication CDR report"><img src="images/CE-1-7-0.png" alt="Electronics and communication CDR report">
                <p><strong>[CE 1.8]</strong></p>
                <p>I designed the complete electronics of the power supply. The power supply was incorporated with a stepdown transformer, full bridge rectifier and a Regulator IC for providing proper DC5V to the system circuit. The block diagram &amp; circuit diagram of power supply unit is given below. It was entirely my work of art.</p>
                <img src="images/CE-1-8.png" alt="Electronics and communication CDR report">
                <p>I used full bridge rectifier in order to completely convert AC supply in to pure DC supply. For the purpose of delivering clean 5VDC I used 7805 Voltage Regulating IC. While testing of the power supply I saw that the voltage regulator was getting way hotter than the limits according to its datasheet. I worked on decreasing the heat and found a solution in the form of heat sink. I attached the shaped heat sink with the body of regulator. This solved the issue of overheating and brought the temperature of the regulator equivalent to the ambient temperature.</p>
                <p><strong>[CE 1.9]</strong></p>
                <p>I searched a lot to find the most efficient yet cost effective microcontroller. I went for 89C51 micro-controller made by A.T.M.E.L Company. I chose this micro-controller because of its 33MHz speed, extendable R.A.M to 64K.B.s, four X 8-bit inputs &amp; output ports and last but not the least due to 128 Bytes of internal memory. I studied all of its pins through the datasheet and drafted a schematic for its flawless connections. The most difficult task was connecting the LCD with the microcontroller. I worked hard in this aspect and studied the LCD and micro-controller pin-outs for seamlessly connecting both the prime components. My approach toward the LCD &amp; microcontroller connection worked fine and the LCD displayed all the required parameters without any fail.</p>
                <p><strong>[CE 1.10]</strong></p>
                <p>I implied a serial-communication technique for synchronizing with the microcontroller and the entire system to be controller. It’s the most common and extra reliable technique for communication amid microcontroller and the system. Since RS-232 was not directly attuned with micro-controller I used an integrated circuit M.A.X.X 232 for this operation. It had a 16 pin topology. The function of this IC was to convert the Rs-232 signals in to T.T.L voltage echelons. I implied S.B.U.F 8 bit register for performing the serial communication among 89C51 micro-controller.</p>
