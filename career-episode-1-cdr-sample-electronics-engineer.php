                <p class="para">The “Door Locking System” was my first major project. A group of three students was formed to work as a team. We worked under guidance of our professors who were always ready to help whenever required. I implied microcontrollers of 8051 family and Kiel Uvision software. Project was a success and a great learning experience for me.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 1.2]</strong></p>
                <p class="para">My field of study intrigued me since I started the course. I had learned some very interesting concepts about basic electronics and communication systems during my study time period. So, I wanted to utilize &amp; implement those concepts in some practical world applications to understand them further. The idea of working on a project for broadening my knowledge highly fascinated me. Unpredictably, I had to make a project during my pre-final year in order to fulfil the course requirement. So, it turned out to be a good opportunity for me to use my knowledge and programming skills and put them to a rigorous test in practical world. Hence, I approached my colleagues and proposed this project to them who were enthusiastic to be my team members. Finally, we three colleagues formed a group &amp; decided to go ahead with the project.</p>
                <p><strong>[CE 1.3]</strong></p>
                <p class="para">All the team members had some tasks to do in the project which were evenly divided amongst us, in line with each person’s strengths and interests.</p>
                <p class="para"><b>My tasks(Major) were as followed: </b></p>
                <ul>
                  <li>To carry out industrial survey and brief internet search for hardware components.</li>
                  <li>Updating my professors regarding each developmental phase of this project through updated reports.</li>
                  <li>Contact with distributors to buy different parts and equipment.</li>
                  <li>Perform Project Cost &amp; Feasibility Analysis.</li>
                  <li>Help in assembling the parts and programming the hardware for anticipated results.</li>
                  <li>Checking the project (both hardware and software) for any errors.</li>
                  <li>Preparing a detailed final report on this project that was presented in front of the panel during final submission.</li>
                </ul>
                <h2 class="sample_heading">PERSONAL ACTIVITY AREA</h2>
                <p><strong>[CE 1.4]</strong></p>
                <p class="para">During initial phase of the project, I arranged a group meeting. I divided the tasks amongst group members. After a mutual consent, I prepared a synopsis of the project, covering what we wanted to do, how to approach it, role of each individual in the team, etc. and presented it to our professor. The report was highly admired by the project supervisor.</p>
                <p><strong>[CE 1.5]</strong></p>
                <p class="para">My first task was to do a brief internet search that included the components required, their model and type, how to use them, and the expected results of the working of the project. So, I browsed various websites, contacted local industries and finally decided to use 8051 microcontrollers for this purpose. <br/><b>The major components of the project were: </b></p>
                <ul>
                  <li>8051 microcontroller IC (AT89C51) (Fig. 1)</li>
                  <li>RFID (Radio Frequency Identification) module (Fig. 2)</li>
                  <li>Diodes and transformer for rectifier circuit</li>
                  <li>Regulator IC (IC 7805)</li>
                  <li>4 x 4 matrix keypad</li>
                  <li>16 x 2 LCD display and 5V relay</li>
                  <li>L293D motor driver</li>
                  <li>Connecting wires</li>
                  <li>Passive components like resistors, capacitors, transistors, etc. of varied values</li>
                </ul>
                <p class="para">I purchased the hardware from a local hardware store with a total cost of Rs. 2200 (approx.) which was uniformly divided amongst all of us.</p>
                <p><strong>[CE 1.6]</strong></p>
                <p class="para">My next task included understanding the overall working of the project. For this, I studied how to interface various components with 8051, the connections required to be done, and how to program them. With the help of a block diagram (fig. 3), I divided the circuit into five major parts to understand it better.</p>
                <p class="para"><b>The five major components of the circuit were:</b></p>
                <ul>
                  <li><b>Reader: </b>It included the RFID reader and tag/card. The tag stored the pre-defined data (correct password) and the reader read it and sent it to the controller for verification.</li>
                  <li><b>Keypad: </b>The keypad entered the password of user’s liking. 4x4 keypad was used in this project.</li>
                  <li><b>Driver: </b>The DC motor and the buzzer formed this section. The motor was used to show the opening of the gate and the buzzer acted as alarm in case of a mismatch.</li>
                  <li><b>Display: </b>The LCD was used to display everything, be it the password entered, or messages for denied or granted access. 16x2 LCD was used in this project.</li>
                  <li><b>Control: </b>the 8051 microcontroller was used to monitor all the parts of the circuit.</li>
                </ul>
                <p class="para">The working was as followed: the RFID reader read the predefined password from the RFID tag, the keypad accepted the entered password. Both were sent to 8051 for verification. In case they were same, the LCD would display “Correct Password Access Granted” and open the gate controlled by the motor. In case of any sort of mismatch, the LCD would display “Incorrect Password Access Denied” and start the buzzer for alarm.</p>
                <p><strong>[CE 1.7]</strong></p>
                <p class="para">The next step was hardware assembly of the project for which I referred to the circuit diagram (shown in fig. 4). I connected the LCD in four bit mode with 8051. The lower three pins of Port1 of 8051 were connected with RS, RW and EN pin of the LCD whereas the upper four pins were used for sending data (D4-D7). The motor and the buzzer were interfaced via Port2 of 8051 and Port0 was used to interface the keypad, in which the lower four pins were used for columns while the upper four for rows.</p>
                <p class="para">I did the hardware connections and soldering of the components. I took all sort of precautions during the assembly, like correct orientation of the component, grounding all the components, checking for loose connections, etc.</p>
                <p class="para">Once the hardware was ready, I prepared an update report with the help of my team members, summarizing all the work done till then and submitted it to our professor.</p>
                <p><strong>[CE 1.8]</strong></p>
                <p class="para">Our professor was happy with the way our project progressed. He was happy how I kept records of each and every task involved and appreciated my leadership skills and further encouraged us to enthusiastically continue with the project. So I started off with the programming of the hardware for which I used Kiel Uvision software (fig. 5).</p>
                <p><strong>[CE 1.9]</strong></p>
                <p class="para">As mentioned earlier, I programmed the hardware using Kiel Uvision software and C programming language. I included all the header files in the code, created functions, defined the pins which were used for both input and output. Then, I created functions for my components, for example, the following is the snippet of function that was used for delay in the program in accordance to the 8051 microcontroller frequency.</p>
                <p class="para">The function LCD initialize initialized the LCD.</p>
                <p class="para">where lcdcmd was another function used for command register of 8051.</p>
                <p class="para">Likewise, several functions were created and compiled into a single C program to program the hardware. After this, it was converted into Hex file and burned into 8051 microcontroller.</p>
                <p><strong>[CE 1.10]</strong></p>
                <p class="para">Now came the most crucial task of the project, i.e., testing of the project. For that, I made an algorithm (explained in fig. 6) that worked in such a way that it compared the password entered by the user with the password stored in 8051. In our case, the correct password was “9968”. If the entered password matched “9968”, the DC motor would open the gate otherwise the buzzer sounded and displayed “Incorrect Password Access Denied” on the LCD.</p>
                <p class="para">I tested it with many types of passwords and although I encountered some minute problems, they were easily rectified. The project worked the way it was programmed to perform. I was happy to see that the project worked successfully and exceeded my expectations.</p>
                <p><strong>[CE 1.11]</strong></p>
                <p class="para">During the final presentation of the project, I prepared the final report on the same summarizing every detail about it i.e. the tools used, motivation behind the project, pros and cons, any scope of improvement, etc. My professor was very happy with the project and my team was able to impress the panel with our project and the efforts we had put in its completion.</p>
