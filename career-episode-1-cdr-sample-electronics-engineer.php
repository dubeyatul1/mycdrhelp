                <p><strong>[CE 1.12]</strong></p>
                <p class="para">The “Door Locking System” project was really exciting and full of learning for me. I learned a lot from this project. It turned out to be a good opportunity for me to test my theoretical knowledge and coding skills. I was also appreciated for my leadership skills and my approach. We were praised for our efforts and how well organized our work was, how we had planned everything before doing anything. Our presentation and efficient working was highly appreciated and me and my team members were able to pass in this subject with distinction.</p>
