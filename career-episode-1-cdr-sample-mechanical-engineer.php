                <p><strong>[CE 1.1]</strong></p>
                <p>I joined GE in a Services Leadership Program which was designed to train experienced new hires about GE’s design, operation and financial processes on fast track basis. I had the opportunity to learn and get hands-on experience of maintenance and troubleshooting of several Industrial Gas Turbine ranging from 50MW to 180MW. After completing my one year on job training, I was assigned as Lead Contract Performance Manager to a multi-million dollar long-term maintenance contract with one of prestigious customer i.e. ENGIE. After my hiring I was responsible for preparing computerized maintenance plans, budgeting, coordinating to resolve all technical and contractual issues, executing planned &amp; unplanned outages of assigned power plants. I was also responsible for availability and reliability guarantees of the plants.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 1.2]</strong></p>
                <p>My assigned Power Plant was having 9 x GE Frame 7FA.03 &amp; 2 x 7EA Gas Turbines with the total capacity of 1800 MW. After completing last major inspection and implementing Enhanced Compressor upgrade Package on 2 turbines I noticed that although both machines were on baseload but power output of one unit was less by 5MW than the sister unit. Upon further troubleshooting I found that IGV angle of one machine was 84 degree however other unit was operating at 87 degrees and thus giving more output. Therefore, I decided to increase the angle to get equally higher output. In fact, later this project was implemented on all 6 machines.</p>
                <p><strong>[CE 1.3]</strong></p>
                <p>Upon further analyses I found that the change was not simple. Effect of IGV angle change can lead to some operational issues while running the machine. I was given the task by my manager to carry out a detailed study of this project and bring best possible solution.</p>
