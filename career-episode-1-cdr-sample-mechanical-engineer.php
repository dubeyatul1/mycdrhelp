                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 1.5]</strong></p>
                <p>The first critical phase was to make design calculation and simulate different operational scenarios of turbine modes to see the effect of IGV angle change on machine.<br/><b> Below was the basic information on which study was based: </b></p>
				<picture>
				  <source type="image/webp" data-srcset="<?php echo $siteUrl;?>images/CE-1-1.5.webp">
