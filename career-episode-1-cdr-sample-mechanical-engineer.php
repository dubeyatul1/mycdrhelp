                <h2 class="sample_heading">SUMMARY</h2>
                <p>The change of IGV angle on all 6 gas turbines proved to be very valuable in enhancing my engineering experience. This was quite complex project involving multiple teams including design, requisition and application engineering. For each GT; increase in efficiency improved the customer revenue by 50,000 USD per month, total $ 3.6 MM per year for 6 turbines. For GE, this project increased our cash flow by 65000 USD per quarter. I completed the project within the timeline in addition to my everyday tasks. The responsibilities assigned to me included both Technical, Managerial &amp; Administrative aspects. I was the team leader of the tasks but never hesitated to seek guidance either from my senior co-workers or experienced subordinates. This project greatly increased the reliability of the overall power plant and I secured Regional GM Award in category of “Stay Lean to go fast”.</p> 
