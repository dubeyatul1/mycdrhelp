                <p><strong>[CE 1.6]</strong></p>
                <p>I found that out of the six turbines, three may have the IGV settings increased to 88⁰ without any alarming. The maximum ambient which I used for this evaluation was 118⁰F. If the maximum ambient temperature was higher, calculations were expected to be changed. This was based upon the 100% maximum air temp at Saudi Arabia which was the closest airport that I could find data for.</p>
                <p>Note that for each of these three, it was very likely the flow can be reduced to the Stage 2 nozzle which implies implementing a smaller Stg 13 extraction line orifice, while still meeting the wheel space limits for the maximum air temperature extreme. The effect of this action would be to increase the gas turbine output.</p>
                <p><strong>[CE 1.7]</strong></p>
                <p>I found that GT-1 and GT-2/GT-3 will continue to meet the wheel-space limits at 88⁰ IGV. The increase in ambient air temperature was not sufficient to change the potential decrease in orifice size. In that case the potential increase in GT performance would be the same for the ambient of 118⁰F. For the remaining three, these are described below with an estimated maximum IGV angle which will protect the 2FO (tag# of wheel space thermocouple) at the maximum air temperature with 80% confidence.</p>
                <ul>
                  <li>GT-2; IGV max = 83.6⁰ (88⁰ IGV, yields a 2FO of 999.1⁰F at an ambient of 124⁰F. This has a 59% confidence level of NOT alarming)</li>
                  <li>GT-5; IGV max = 84.0⁰ (88⁰ IGV, yields a 2FO of 1003.5⁰F at an ambient of 120⁰F. This has a 8% confidence level of NOT alarming)</li>
                  <li>GT-6; IGV max = 76.2⁰(88⁰ IGV, yields a 2FO of 1017.9⁰F at an ambient of 120⁰F. This has a 0% confidence level of NOT alarming)</li>
                </ul>
                <p><strong>[CE 1.8]</strong></p>
                <p>With the help of simulating software; I discovered for these three units that the 2FO wheel-space limit will be protected by limiting the IGVs to the displayed maximum settings. (1FI and 1AO max temps have sufficient margin and will not alarm). An alternative would be to add the TCD Limit Logic which would allow a TCD limit to be set to protect the wheel-space on the hottest days but all the IGV’s to run at 88 at lower ambient temperatures.</p>
                <p><strong>[CE 1.9]</strong></p>
                <p>If any further increase was required (possibly to the 88⁰ setting), I would need to mitigate the likely 2FO alarm by increasing the Stg 13 orifice size.<br/><b> For these three units; an initial estimate for the increase in orifice size was as following -</b></p>
                <ul>
                  <li>GT-2 = 0.5”</li>
                  <li>GT-5 = 2.3”</li>
                  <li>GT-6 = 10.0” (A mathematical value only as the orifice cannot increase by 10”</li>
                </ul>
                <p><strong>[CE 1.10]</strong></p>
                <p>As per my analyses the values represented the increase for each and the new design would likely be a custom machined orifice plate. For GT-6, the increase was so large that it was unlikely it could be manufactured at all (orifice opening possibly being larger than ID of extraction pipe ID). If I was to use standard sizes then the next largest standard size relative to the diameter needed would be the best choice and that would reduce system performance by the difference between standard size and orifice size needed. Custom gives us a better operational result here.</p>
                <p><strong>[CE 1.11]</strong></p>
                <p>As a general statement; upgrading the control system to utilize the TCD limit control would be very helpful as we can set the limit via the TCD limit metric rather than the IGV angle. As the system accumulates more dirt the IGV response may change but the TCD limit would always be the limit and would protect the 2FO limit.</p>
                <p><strong>[CE 1.12]</strong></p>
                <p>I also performed analyses for dealing with high wheel space temperature where we can remove borescope plugs (BP). I found that for GT-1, GT-3, GT-4, removing Borescope plugs (BP) will reduce performance while continuing to meet the existing wheel space (WS) limits.</p>
                <p><strong>[CE 1.13]</strong></p>
                <p>For rest of 3 turbines, only ~ 2 degree F per BP removed can be recovered here. Additionally, for the three units which cannot meet the 88 IGV at their full rated ambient air temperature and with their current bore plug count; the units can operate up to the following air temps while remaining at the 88 IGV setting:</p>
                <ul>
                  <li>GT-2: 88 IGV up to ~ 116F with 80% confidence (20% chance of alarming)</li>
                  <li>GT-5: 88 IGV up to ~ 112F with 80% confidence (20% chance of alarming)</li>
                  <li>GT-6: 88 IGV up to ~ 98F with 80% confidence (20% chance of alarming)</li>
                </ul>
                <p><strong>[CE 1.14]</strong></p>
                <p>If I assume 2F = one (1) BP removed, then four (4) more BPs would need to be removed for the GT-2 (124F); four (4) would need to be removed for the GT-5 (120F) and eleven (11) for the GT-6 (120F).</p>
                <p>Each BP removed decreases the GT performance 100% of the time. Its depends upon what percent of the time, does the local air temperature exceed the limits shown above for the 88 IGV setting for each of these GTs…The more BPs removed the greater the reduction in performance. As a ”fine tuning” feature, BPs can be removed but should be considered only if other options are not viable.</p>
                <p><strong>[CE 1.15]</strong></p>
                <p>Another approach is to increase the Stg 13 orifice opening. The orifice size change would be much more efficient than BP removal since the S2D (affecting the 2FO region) would be redesigned to deliver most of this additional flow from the new orifice, to the 2FO. The key here is the “S2D would be designed to provide most of the additional flow to the 2FO”. This could be costly and would require S2D modification or replacement as well as design work.</p>
                <p><strong>[CE 1.16]</strong></p>
                <p>As suspected when the initial queries on the effects of opening IGV’s became focused on these six units the answer to the question – can we open the IGV’s to 88? – is a complicated one from the management of wheel-space temperature standpoint. As per initial assessment of the data; it showed that the first forward wheel-space (1FI) is being managed well with the current number of bore plugs and suggests that reinstalling some plugs may be possible (although I was worried about the compressor performance recovery that might accompany the change to enhanced package-3 upgrade on the units not yet upgraded). However, the same data showed that the second forward wheel-space (2FO) was not managed well on some of the units with the current configuration of 13th stage orifice plates, etc. My further study revealed that just changing the 13th stage extraction orifice does reduce the 2FO temperature but wastes a lot of flow and modifying the diaphragm can be more efficient with how much flow is added. Since some of these units are in the population with suspect Stage-2 nozzle, just replacing the stage-2 nozzle with new ones and re-checking the 2FO temperature before modifying the diaphragms was better option.</p>
                <p>With the help of all above analyses, calculation and computer modelling I concluded that I can increase the angle of all 6 GTs without removing borescope plugs by implementing Opflex Airflow suite. This was the control logic modification through which additional features were added in Mark-6 software and HMI. These features can control GT operation especially with respect to wheel space temperature issue while operating at 88 degree IGV angle. <br/><b>With the help of application engineering I successfully implemented the changes and effect of this change on GT output and heat rate is given below: </b></p>
