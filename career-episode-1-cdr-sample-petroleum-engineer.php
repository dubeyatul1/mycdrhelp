<p><b>[CE 1.8]</b></p>
<p>I also designed the control loops for the Central Control Unit. The Central control unit was where all operations were carried out from with a PLC. On the main flow pipeline, I connected two ball valves and a needle valve, this combination of valves allowed pressure to be measured within the pipeline without allowing any backflow of the liquid being transported. A pressure indicator measured the pressure and sent the pressure value as a 4-20mA signal towards the central control unit. On the main flow pipeline, I placed a Temperature Sensing Element which measured the temperature of the fluid and sent the value to a Temperature Indicating Transmitter which again sent the 4-20mA output to the Central Control Unit. The Central Control Unit constantly monitors these values and detected any extreme values raising an alarm or doing a complete shutdown of the pipeline if the values were off limits.</p>
