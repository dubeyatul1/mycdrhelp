<p><b>[CE 1.9]</b></p>
<p>I conducted Lab testing using a miniature prototype representing the actual 250 Km of pipeline. I modified the field devices provided by the Companyfor reduced flow and pressure conditions in lab. The setup included two gathering manifolds and the collection of data at a local Remote Terminal Unit (RTU) via fiber cable.  The instruments were installed along the pipeline on specific locations including the manifolds and delivery stations </p>
<p>The Remote Terminal Units (RTU) transferred the data to a central location in real time using fiber optical cable. Pipelines was being controlled remotely from The Main Control Room. The pipeline control room used Programmable Logic Control PLC (SCADA system was designed to be used for the actual project) that returned real-time information about the flow rate, pressure, etc. The PLC received all the field data and presented me with the Human Machine Interface needed to control the hydraulic conditions of the pipeline and send operational commands like opening and closing of valves. The Cisco® ONS 15454 Multiservice Transport Platform was used in lab for the Optical system and was simulated using the software Optisystem Lite. The system comprised of wavelength division multiplexing technique. </p>

<p><b>[CE 1.10]</b></p>

<p>I performed the calculation for the minimum detection time of any alarms being triggered at the Central Control Unit. Based on this I calculated the distance in placement of the flow, pressure and temperature sensors based on the flow data provided from the Company.</p>
<p>If we are to assume  </p>
<p>Flow rate of 56000 barrels per day and keeping a safety limit of 15 min detection time; </p>
<p>Since Flow= Cross Sectional Area X Velocity</p>
<p>It turns out that at a flow rate of 56000bbd, crude oil has a flow velocity of 4.184 ft/sec. </p>
<p>At this velocity and a time of 15min before shutdown translates to a distance of 1.147km between each sensing module. </p>
<p>A total of 250/1.147 = 217 sensors were required. </p>
<p>I carried out similar calculation for pressure and temperature sensors based on their minimum detection time + safety factor through the entire optical system. </p>


<p><b>[CE 1.11]</b></p>


<p>This project was already using a new technique being used first time in the oil and gas industry of Pakistan i.e. the use of fiber optic cable to transfer control data and providing remote surveillance of crude oil pipeline from a centralized location. The complete design comprised of control loops for regulation of the parameters. I designed the pump stations to stand at three nodes; MOL CPF, OGDCL CPF and an inter-system node at Kharappa Valve Assembly. The pressure, temperature and flow from all the valves along the pipeline and the regulatory action at the pumping stations were carried out over optic fibers which than connected the central control room (CCR) to the field devices and the stations. </p>
