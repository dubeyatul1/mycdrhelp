<h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
<p><b>[CE 1.5]</b></p>
<h2>My Roles & Responsibilities</h2>

 <p><b>As the team lead, I was accountable for: </b></p>
 <ul>
 <li>Maintaining the correspondence between the University and the Multinational Company (MOL) funding our project, including arranging all meetings and managing to keep MOL updated with the recent progress of the project via weekly update reports and monthly presentations covering from research to technical and operational execution for this project
<li>Determining the project timelines for the various phases of the project and promoting and synchronizing with the team members to meet core objectives in a timely manner
<li>Organizing with the university’s faculty and department heads to keep them updated on the project progress and to reach them for aid in accessing universities facilities for research

</ul>
<p><b>As the team member, I was responsible for</b></p>
<ul>
<li>Developing in-depth knowledge of the benefits and constraints of communication via fiber optic cables
<li>Developing an efficient fiber optic relay system including the optical transmitters, fiber cable, optical regenerators and optical receiver. 
<li>Software simulation of the designed optical system for testing the optical links in our control loops
<li>Congregationof all research data on the industrial instrumentation gauges, transmitters and other devices for managing our systems relevant parameters of flow, pressure and temperature by studying the old installed system at site. This step included gathering all the technical manuals from the field and office staff and going through them. The next step was to establish the most reliable vendor list on top of the current list of suppliers. After short listing, the finalized vendors were approached to send their technical sheets for the instrument selected in relevance to our parameters.The instrument list along with the vendor list were forwarded to MOL, who then provided us with the following to develop our lab prototype: 
<ul>
<li>Rosemount 3051T In-Line Pressure Transmitter
<li>Rosemount 3144P Temperature Transmitter
<li>Daniel Model 3804 Liquid Ultrasonic Flow Meter
<li>Fisher 8580 Rotary Control Valve
</ul>
</ul>

<p><b>[CE 1.6]</b></p>

<p>Being a university student, I was unaware of the Oil and Gas industry standards and protocols being used for determining the selection of instrumentation devices. This was the first problem where I had to study the old systems in place and understand the complete working of a system that the Company wanted to replace. After a difficult process of gathering all technical data sheets from their office and field staff, I developed in-depth knowledge of the installed systems. I developed the current vendor list the Company was using and approached the vendors for their updated technology brochures and data sheets for further information. </p>
<p>Second challenge was to develop a miniature prototype of a 250 KM pipeline in lab and simulate control loops of the plausible flow conditions at site. I had to make a lot of alterations to the instrumentation devices provided to us by the Company for measuring flow, pressure and temperature. Since the actual flow could not be simulated in lab I had to install additional pumps to match the flowrates. In other control loops the pressure and temperature conditions were modified to represent changing conditions at site, for instance, a small pressure drop of 10 psi was designed to trigger an alarm depicting 500 psi drop on field (in case of crude theft from pipeline). </p>
<p>The biggest challenge however was the lab simulation for the optical system designed which meant selecting a new software for the project and learning it along the way. I had to ensure to select a software that would have fast throughput and communication speeds given the nature of the product (crude oil) being handled</p>
 

<p><b>[CE 1.7]</b></p>

<p>I selected Optisystem lite software on grounds of many benefits in comparison to the other software in market including free trial version for students like myself for quick learning. Below is the schematic of the final optical system designed using Optisystem Lite.</p>
