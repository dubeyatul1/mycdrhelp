<p><b>[CE 1.12]</b></p>

<p>This job was conducted on rig less operations. I performed proper job risk index and Hazard analysis as this placement was being done for the first time. I conducted pre-job safety meetingson the field before the initiation of the project to ensure that everybody understands job procedures and is well trained. Cement slurry was properly handled in steel batch mixers in order to avoid any spill of the slurry which could result in environmental hazard. This job required slurry to be pumped under high pressure of 5000 psi at surface. It was ensured that all equipment is rated to 10000 psi safe working pressure and has all the certifications are current and up to date.</p>

