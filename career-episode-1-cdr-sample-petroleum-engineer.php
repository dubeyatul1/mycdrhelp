<h2 class="sample_heading">Summary</h2>

<p>I was heading a team which included technical engineers, lab technicians and chemical management personnel. At the start of the project I analyzed weakness and strength of all the personnel and assigned objectives to everyone for the projects. I prepared key performance indicator of this project to monitor progress of all personnel on weekly basis. I ensured that all team members are equipped with fit for purpose training and tools to achieve their objectives. I had 15 minutes dailymeeting in the morning to discuss the project progress with team members and discuss any issues if faced by anyone. I personally worked with employees who were behind their schedules and ensured that they complete their tasks in time.</p>

<p>I gave presentations monthly to the head of departments at our university and to the concerned personnel at the company funding our project.</p>
