<p><b>[CE 1.14]</b></p>
<h2 class="sample_heading">Summary</h2>
<p>Project was successfully completed. On the day of the final year project exhibition my project secured the second place amongst all the university projects. One year after the completion of the project the actual pipeline project was initiated and completed successfully within three years. </p>
<p>I applied my knowledge of Optical Communication and Instrumentation to design an effective system for remote surveillance of crude oil pipeline from a centralized location.</p>
<p>Working on this project I learnt and improved the design of cement slurry and how to reduce friction pressure while designing cement slurries. I also learned how to manage simultaneous operations of Coil tubing and Cementing. </p>
<p>As a Lead Design Engineer, I was following up with the complete Design, Execution and Evaluation of the project utilizing my field experience and trainings. I implemented all the procedures in the Cementing lab to simulate the impact of this job.  I was the sole contact of the client and managing both teams of Coil tubing and Cementing. </p>
