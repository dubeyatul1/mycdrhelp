<li>MOL CPF (central processing facility) includes flow from Manzalai, Makori, Mamikhel and Future Discoveries</li>
<li>OGDCL CPF (central processing facility) includes flow from Chanda, Mela and Future Discoveries A, B and C</li>
</ul>
<p><b>[CE 1.3]</b></p>

<p>The foremost objective of the project was to design a pipeline system, counting all instrumentation and valve assemblies, to be remotely monitored and operated over fiber optic cables</p>
<p><b>Following were the deliverables of the project -</b></p>
<ul>
<li>Defining Fiber Optic Cable specifications and constraints</li>
<li>Establishing system and component behavior and restraints</li>
<li>Enterprise the integrated system and drafting process and instrumentation diagrams</li>
<li>Determining the parts of control system to be handled by the Programmable logic control and parts to be handled by logic external to the PLC (e.g. hardwired, relay-controlled, or directly controlled by specialized instrument/controllers.)</li>
<li>Assigning I/O and memory ports</li>
<li>User system manual drafting</li>
<li>Testing PLC code</li>
<li>Building & testing prototype</li>
<li>Modifying for full scale pipeline</li>
</ul>
