<p>I designed the simulation system for the optical system using the software Optisystem Lite. The above figure is a layout of the optical system I designed with nine inputs from sensor heads to the central control room (CCR) and control signals from CCR to the final control elements on field. The system comprised of wavelength division multiplexing technique. Signals from the field were wavelength division multiplexed and added onto a single fiber which is then carried on to the CCR. </p>
<p>Another thing I designed was the cause and effect diagram of our project which enabled us to develop the control loops for different field flowing conditions. To design this, I went through extensive study sessions with the actual field crew at the Company’s office to understand the different situations that could trigger alarm. Below isthe abridged version of the C&E diagram. </p>
