<p><b>[CE 1.1]</b></p>


<p>This career episode relates to project titled“Remote Surveillance & Control of Cross-Country Liquid Hydrocarbon Transmission’’. The project was carried out for MOL Pakistan.</p>
<h2 class="sample_heading">BACKGROUND</h2>
<p><b>[CE 1.2]</b></p>
<p>In lieu of the growing crude oil supply projections from its Northern fields situated in Khyber Pakhtunkhwa (KPK) region, MOL Pakistan Oil and Gas Company B.V in collaboration with the national oil and gas company of Pakistan OGDCL, intended on laying a pipeline for the transportation of oil from KPK to Attock Oil Refinery Limited (ARL) in Rawalpindi, with a total length of around 250 KM. The pipeline already in place was old and not able to handle the envisioned throughput capacity of 56000 bpd. </p>
