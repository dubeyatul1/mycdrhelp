                   <h2>Problem faced</h2>
                   <p>While engaging in project activities I faced a problematic situation at Pumping Facility No 5; which was about the dual fuel downloading system which consisted of storage tanks for both crude oil and diesel. It was used to provide fuel for main power engines, pumping units and oil heaters. The fuel was needed to be supplied through booster pumps at certain pressure and temperature and automatically controlled by temperature control valves &amp; pressure switches.</p>
                   <p>The problem occurred when I noticed a sharp drop in the fuel inlet pressure causing the running engine speed to be fluctuated and almost tripped out. After carefully overriding the emergency response system I was able to stop the running units. Then I started to investigate the entire system components and analyzed the events sequences using cause and affect diagram. It was given that the booster pump output value was 4 bars, the landing pressure was needed to be at least 2.2 bar and inlet temperature 80 C for proper combustion. But the case was only 1.4 bars for pressure at the engine fuel module. It was also given that the engine fuel supply network was dual; serving both diesel and crude. With more intensive investigation the problem appeared to be multistage. I figured out that the engine fuel module (which was designed with level transmitter to indicate high and low level of fuel) had a functional fault that caused the transmitter to stick at the high alarm.</p>
                   <h2>Adequate Problem Resolution</h2>
                   <p>I made a detailed P&amp;ID drawing in correlation with data collected from the site. I concluded that the main reason was a misstructured pipe layout.</p>
                   <p>Since the line was interchangeably filled with different types of fuel with varying viscosities, with incompetent heat tracing cables; a three-way check valve (positioned at intersection point) got pretentious with the viscosity of crude oil and kept in reversed position thus leading the fuel module to pass depressurized diesel fuel into engine through crude fuel line while running on diesel; the process by which the fuel level transmitter would prevent the diesel fuel system from being refilled.</p>
                   <p>My course of actions for addressing this situation was initially a short-term solution by changing running mode to crude line manually. I requested a viscometer to be installed &amp; directed electrical team to connect heat tracing cables to fuel module. For permanent solution I suggested for the fuel downloading lines to be structurally separated.</p>
                 
                  <h2>Problem faced</h2>
                      <p>Another technical difficulty I faced took place in Pumping Facility No 2.</p>
                    <p>Station operation was controlled by SCS system provided by CSE Servlec Company. The system actuated all site equipment start and stops commands, communicates wide-ranging operating matrix signals between station PLC and site device and resets active system alarms into healthy status.</p>
                    <p>During normal operation, an alarm of over speed module trip in Rolls Royse generator was activated. Few minutes later &amp; before system could recognize and reset the alarm; station experienced total black out. I performed an expounded check and on-site inspection of generator set. I started from 500 KVA emergency generator, terminated unnecessary electrical loads and informed main control system and maintenance team. During the attempts to restart main power generator and orchestrate the electrical outputs a problem of starting failure and repeated shutdown of main generator continued to happen. I called system engineer for specialized problem diagnostics. After investigations were done by the specialist, the logical sequences of the situation were clarified; the prime source of repeated generator shutdowns was Uninterruptible Power Supply (UPS) failure. UPS system was designed and installed to supply power to station PLC and SCS in case of power total loss and due to the chargeability defect of UPS system; it was incompetent to supply the essential power for SCS system thus leading to alarms being hold on active status and main generator to be interlocked.</p>
                    <h2>Adequate Problem Resolution</h2>
                    <p>In order to resolve the issue adequately; system engineer rectified UPS system and overrode the SCS interlock so I could reset system active alarms, restart and synchronize main generator and resume station processes.</p>
