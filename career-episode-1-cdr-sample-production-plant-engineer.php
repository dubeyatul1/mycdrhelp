                <h2>Engineering Design</h2>
                <p>I was entitled by station manager to get in touch with technical services personnel to arrange the execution of design upgrade of the fuel module lines layout.</p>
                <p>The total length of feeding line was 45 m for diesel, and 32 m for crude both having the same 2" diameter. The three-way check value was located 4m before the injecting pump. My modification proposal recommended a complete replacement of the combined fuel network with new separately constructed lines for each fuel line. The new diesel line was welded with total length of 12 m; linking the rest of the line to the new one, bypassing the intersection point &amp; controlled with hand valve.</p>
                <p>The functional improvements of using that new upgraded fuel downloading system helped in protecting station assets and emerging operation procedures by diminishing the downtime related to equipment deficits.</p>
                <p>The redesigning process was achieved in full cooperation with piping engineer, electrical supervisor and welding team. I also used Pipe-Sem software for hydrostatic pressure gradient computation.</p>
