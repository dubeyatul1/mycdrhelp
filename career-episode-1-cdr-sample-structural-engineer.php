<h2 class="sample_heading"> BACKGROUND </h2>
<p>M.S. Ramaiah Institute of Technology is a privately owned engineering college located in Bangalore, Karnataka. The college was established in 1962 and is affiliated to the Visvesvaraya Technological University  (VTU). Since August 2007, RIT has been offering undergraduate and postgraduate Programs in Engineering and management sciences. It also offers programs in various disciplines leading to award of PhD. In 2013, RIT was ranked 16th best engineering college in India by India Today and Neilson.  MSRIT is renowned because of providing practical based learning to its students. There are laboratories containing state of the art equipment that provide opportunities for students to gain hands on experience. </p>
<p>The project was solely based on certain desired objectives. It was mainly aimed to compare ultimate load with existing method (Johansen’s load) and proposed method (Enhanced yield line method), with practical values. Usually the design of slab involves elastic analysis, leading to uneconomical design; however due to the involvement of membrane force within the slab, I predicted a method to calculate ultimate load relatively accurate for two-way slab with three sides fixed and one side simply supported. The material used for this specimens are reinforced high strength self compacting concrete (HSSCC) with a grade of M70, and the thickness of slab is 75mm. A parametric study on membrane action for rectangular slabs was also conducted in order to comprehend the effects of changing parameters in the development of ultimate load. For this purpose, we casted six slabs and compared by changing influential parameters such as aspect ratio, concrete cube strength, grade of steel, coefficient of orthotropy, percentage reinforcement, and effective dept.</p>
