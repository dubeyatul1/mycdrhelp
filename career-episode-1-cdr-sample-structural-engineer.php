<h2 class="sample_heading"> PERSONAL ENGINEERING ACTTIVITY</h2>
<p>Firstly, I designed a line diagram to distribute loads equally as shown in the following figure, where the pressure load was represented by a point load, which approximately distributed to the total load as a uniform pressure on entire area, thereby recorded the deflections and crack widths from the specimens.</p>
<p>Thereafter, I manually calculated different parameters, such as crack width and deflection. I found difficulties while handling this task. Therefore, I made a spreadsheet to make the calculations easier. I was also obligated to calculate deflections by using stiffness matrix method of 6 rows and 6 columns. For that purpose, I made a program in MAT lab and simplified the calculations. </p>
<p>I was also responsible to perform calculations for deflections pertaining to different formulae. At first, I calculated deflections by Park’s equation. The computed ultimate load was obtained from an assumed deflection of ultimate load for all HSSCC slabs with an average value of 0.37 times the thickness of the slab was used. The ultimate loads had been computed and compared with the tested data.</p>
<p>Afterwards, I conducted a parametric study to find out the effects of changed parameters against the load enhancement of the tested slabs. For that purpose, I preferred to use membrane action of slab using park’s formula with three sides fixed and one simply supported by changing all parameters such as reinforcement ratio, concrete grade, steel grade, span/depth, aspect ratio and coefficient of orthography. The Load enhancement was expressed as the load over and above the yield line load.  
                Load Enhancement = (qu-qj/qj)*100 
	            Where, qu = intensity of load at ultimate   
	            qj = intensity of Johansen’s load  </p>
<p>I carried out analysis to observe the effects of modified parameters individually. I observed that the change in coefficient of orthotropy from 0.3 to 1 with an increment of 0.1, caused a decrease in load enhancement. Moreover, the concrete cube strength was changed from 40 N/mm2 to 100 N/mm2 with an increment of 10 N/mm2 keeping all other variables constant.  The results had shown that the load enhancement increases with the increase in the cube strength. Similarly, I observed the corresponding changes in load enhancement by varying all the remaining parameters that are mentioned above. All the results can be summarized as: the increase in load enhancement was experienced only by increasing grade of concrete and span / depth ratio. On the other hand the load enhancement decreased by the increase in coefficient of orthotropy, aspect ratio, steel grade and percentage of reinforcement. </p>
<p>Furthermore, I found that experimental ultimate load for the partially clamped slab with the edges fully fixed and one longer edged simply supported was more than the computed Johansen’s load. The load enhancement of the tested slabs varied from 25% to 52%. I found out that the load enhancement was established due to the development of membranes force at the boundaries.  </p>
<p>In order to predict the ultimate load of the tested slabs, the yield line load predictions had been calibrated and I proposed the equation as follows: </p>
<p>Intensity of ultimate load (N/mm2) = 0.514 * intensity of yield line load (N/mm2) + 0.105  </p>
<p>The calibrated enhanced yielding load was able to predict the ultimate load satisfactorily. </p>
<p>After carrying out parametric study, I concluded that the load enhancement is more if thinner slabs are used with higher grade of concrete and lesser grade of steel with lesser percentage of reinforcement and aspect ratio.</p>
