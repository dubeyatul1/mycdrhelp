               <h2>PERSONAL ENGINEERING ACTIVITY</h2>
               <p><b>CE [1.5]</b></p>
               <p>Daily and monthly progress reports were prepared by me for all the rehoming activities, which included the details of all the pre and post migration KPIs. Other than that I collected daily report from NOC engineers and evaluate their performance and discuss the issues they faced during activities and provided them adequate solutions</p>
               <p>I also have to prepare a separate report which contained data of all the activities done by Vendor and in case of outages, I investigated and calculated damages. This report was shared with my manager and discussed in detailed with vendor once a week or in case issue created on network by them.</p>
               <p>Migrating of different BSC/RNC from One MSC to another MSC is very critical; the entire consumers connected to that BSC/RNC get affected for a short period of time. It was my responsibility to ensure that this migration is done proficiently and completed well within time limits. In case of failure the activity was rolled back and after that I have to verify services are running normally. Moreover, in detail it was discussed in meeting why activity is rolled and how it can be performed again with success.</p>
               <h2>PERSONAL ENGINEERING ACTIVITY</h2>
               <p><b>CE [1.6]</b></p>
               <p>SFR France has more than 21 million customers, which uses voice and data services through GSM network. And it was a challenge to regularly check network health and performance and to ensure there is no traffic congestion in network affecting the voice and data services.</p>
               <p>On weekly basics I run Network health and backups in which various VAS services, alarms were checked and a report was prepared which showed potential threats to network performance. This report was discussed in detailed with my Manger and relevant teams especially BSS team. In this discussion possible solution were discussed and then finalized by Network manager.</p>
               <p><b>CE [1.7]</b></p>
               <p>After finalization of solution a work order was generated by me; it was then shared with all potential stake holders such as BSS &amp; NSS departments. In this work order the severity of activity was mention and along with completed implementation steps which indicates what will happen to network after activity will complete. After generating the work order I collected all the configuration of BSC along with complete detail of all the BTS connected to the BSC.</p>
               <p><b>CE [1.8]</b></p>
               <p>Configuration script which will be executed at approved downtime was prepared and shared with all stake holders so that it can be verified that the BSC, BTS Ids are correctly mention and proper bandwidth is given to each node.</p>
               <p>Once script was approved by all stakeholders, then work order was approved by Network Manager. Before the start of activity, I collected all pre-migration KPIs and alarms for comparisons. At Night time, activity was started in which BSCs were removed from old MSC and during this time the Media was patched by BSS team from the BSCs to new MSC in which these BSC will be added. Before media patching I used Fluke tester to verify in case of LAN cable and if Optical fiber was used then Optical Ethernet tester was used. In case of LAN cable it was verified that there is no packet loss, and data is not affected by electromagnetic effect; such as NEXT, PSNEXT, Attenuation and resistance. For Optical fiber I verified the SNR, jitter, attenuation and packet loss. After Verification of media it was patched and Link status was checked by me and defined in which Link throughput and duplex type was defined. Usually it was 1000Mbps full duplex.</p>
               <p>Once Link Between BSCs and New MSC is established and verified then I loaded the script and executed at MSC terminal, this configuration script includes BSC ID, Site ID, LAC Site name, Cell ID, Existing configuration, planned configuration, BCF ID, BBTS ID, TRX no, Signaling, TCH, Frequency hopping, HSN, MA-list, MAIO Offset, MAIO and OMU signaling. After completion of the entire configuration on MSC status of each BSC was checked and BSS team was asked to verify from its filed team to check voice and data services are working normally.</p>
               <p><b>CE [1.9]</b></p>
               <p>All the migration activities occurred in approved downtime are required to close then within approved time window. If activity was prolonged then special approval was taken otherwise migration was revert</p>
               <p>If any of KPIs or services doesn’t work or meet expectations then it I was liable to diagnose and resolve accordingly. Usually slight change in LAC, Cell ID or BSC ID was done by and issuea were resolved.</p>
               <p>Once verification was completed from filed team post alarms and KPI’s were collected and mention in report. Usually there should be no major alarm in network after migration.</p>
               <p><b>CE [1.10]</b></p>
               <h2>KPIs for network optimization and expansion</h2>
               <p>After execution of rehoming activity, alarms and KPI’s were monitored for two days. I regularly checked the network performance and have to decide which sites required migration in network for smooth operation. Rehoming activity is performed in network due to two major reason and these are the major factor which I have check in entire network and act accordingly so that there is no degradation faced by consumers</p>
               <ol>
                 <li>Traffic Congestion</li>
                 <li>Change of MSC technology from CPCI to ATCA</li>
               </ol>
               <h2>Traffic Congestion</h2>
               <ul>
                 <li>On regular basics network traffic/bandwidth were checked by me between MSC and BSC and if congestion is observed then some of the BSC were migrated to another MSC. If this was not done then GSM consumer would have faced degradation in voice and data services. As in case of data there would have been jitter, and due to this data packets get lost resulting in delay and hindrance of services. For voice services consumer calls would not be established and muting or distortion would have been observed.
                     <h2>Change of MSC technology from CPCI to ATCA</h2>
                   <p>Change of MSC technology is done because of two reasons</p>
                 </li>
                 <li>CPCI platform is based on TDM technology and ATCA is based on IP. As 3G and 4G services utilized IP technology so it is favorable to switch to ATCA platform and by doing this there is then no need of Media gateway for switching (TDM to IP) purposes. One less entity in network means the network will be less complex and easy to maintain.</li>
                 <li>Second reason is that the capacity of ATCA platform is very high compared to CPCI and thus it can support far greater number of BSC’s and BTS. Also future expansion of ATCA platform is very easy and occupies very less space.</li>
               </ul>
