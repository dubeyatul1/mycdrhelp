               <p><b>CE [1.1]</b></p>
               <p>My first episode is about my job in Aricent Technologies Holding Ltd, which I did for SFR France telecom operator. SFR France provide voice and data service through wireless and wired technologies. Currently SFR France is providing voice and data service to more than 21 million customers where more than 6 million customers are using high speed internet (DSL).</p>
               <h2>BACKGROUND</h2>
               <p><b>CE [1.2]</b></p>
               <p>I worked on this project as Product Specialist and was responsible for all the core network operation, which include Hardware installation, software commissioning, O&amp;M, and Network expansion and Upgradation and testing of nodes. My daily tasks include rectification of operation and maintenance, issue handling, all software and hardware issues, and integration of new RNC/BSC/BTS/POI. During this project, I learned a lot about MSC role in Core network, I was assigned a project relating to shifting and integration of new RNC/BSC, BTS and this career episode is about this. Every day I have to present daily report to my Network Operation Manger. For this project daily meeting were conducted between BSS operation team and myself, in which best possible scenario were finalized for different BSC/RNC and BTS so that consumer can have voice and data service without any trouble and delay/congestion. Later it was presented to my manager for its approval and after activity all the alarms were verified and rectified</p>
               <p><b>CE [1.3]</b></p>
               <p>I was directly reporting to my Network Operation Manger and under my supervision was a team of NOC engineer (7 Engineers) who were responsible to perform all activities given to them for smooth core network operation. I have divided the team to perform activities in three shift, evening and night shift have two engineers while morning shift have three engineers. Usually I do night shift in which I have to perform rehoming/re-parenting activities.</p>
               <p>For rehoming activities daily meeting were done with BSS operation team, in which I presented them the reports where network is being congested and various solutions for its resolution. In some cases BSS team asked me for shifting of BSC to New ATCA based MSC.</p>
               <p>For this strict compliance regarding network, safety was implemented and ensured by myself, so that no harm could be done to network and to complete all the activities within approved network downtime. After activity, I did proper verification and then a report was prepared and sent to management about completion of activities and details regarding verification done and its status.</p>
               <p><b>CE [1.4]</b></p>
               <p>The project Hierarchy is given below.I was answerable to the NOC Manager.</p>
