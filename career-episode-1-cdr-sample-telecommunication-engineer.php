               <h2>Summary</h2>
               <p>It was very important engineering project of my career as I have to look after SFR entire core network. In this project learned a lot about network nitty-gritty. Also I have to deal with different departments and my team which not only increased my technical but managerial skills as well. I was able to manage a group of engineers and the project management skill was upsurge as a result of this experience.</p>
