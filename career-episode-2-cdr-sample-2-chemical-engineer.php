                <p><strong>[CE 2.1]</strong></p>
                <p align="justify">Sui Northern Gas Pipeline Limited (SNGPL) is the largest integrated company serving more than 5.3 million consumers by covering 250 mains town in North central Pakistan and is certified against ISO 14001:2004 &amp; OHSAS 18001:2007 Standards. SNGPL’s 11 sites have been registered under the "SMART2" Program by Pakistan Environmental Protection Agency (PAK-EPA). The company has 50 years of experience in operation and maintenance of high pressure gas transmission and distribution system. Sui Northern Gas Pipeline Limited operates with 10,000 employees in a region of nation that has rapidly growing demands for Natural Gas. In accordance with growth in Oil and Gas industry of Pakistan. It has also expanded its activities as Engineering, Procurement and Construction (EPC) Contractor to undertake the planning, designing and construction of pipelines, both for itself and other organizations.</p>
                <p><strong>[CE 2.2]</strong></p>
                <p align="justify">The LNG-LPG department is established to make necessary arrangements to equate the supply and demand requirements. Pakistan is facing a n acute shortage of natural gas, therefore Government of Pakistan directed the Gas utility companies operating in the country to work on alternate means to address the gas shortage. LPG-LNG department undertakes project on LPG &amp; LNG &amp; also deals with supply of LPG-Air mix to specific industrial/domestic areas through distribution pipeline networks &amp; or standalone basis to cater to shortfall of gas. It also deals with LNG projects to cater a long term gas shortage issue &amp; LPG pipeline project to cater country energy requirements.</p>
                <h2>Background</h2>
                <p><strong>[CE 2.3]</strong></p>
                <p align="justify">The overall project was to develop simulation study by using Hysis for LPG transportation through pipeline from Karachi Port Qasim to Sheikhupura (Punjab) by drawing 1000 ton per day at Multan, 1500 Ton per day at Faisalabad and 2500 Tons per day at Sheikhupura. The locations of existing refineries and LPG plants in the country which are major source of LPG have also been taken into account while short listing the route corridors. The total length of pipeline was 1098Kms</p>
