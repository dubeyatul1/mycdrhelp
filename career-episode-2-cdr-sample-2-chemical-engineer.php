                <h2>Personal Engineering activity</h2>
                <p><strong>[CE 2.5]</strong></p>
                <p align="justify"><b>My responsibilities were as following but not limited to -</b></p>
                <h2>LPG Auto Gas Business Plan</h2>
                <p align="justify">I successfully implemented the LPG Auto Gas business plan under the head of SNGPL Franchise scheme. I was responsible to ensure the compliance of Regulatory body’s defined(OGRA) standard of lay out plan, equipment installation and legal requirement. Under this scheme I accomplished the targets first in Lahore and then to other cities of Punjab.</p>
                <h2>Energy Audits</h2>
                <p align="justify">I prepared time schedule to carry out Energy Audits of different industrial consumers under the Government Scheme of Energy conversation. I discovered the boilers installed at various commercial units for captive power production were operating below their rated heat efficiency. All the consumers with low efficiency were advised to upgrade their existing system or to add waste heat recovery units in order to increase the heat efficiency.</p>
                <h2>Revamping of LPG Bottling Plants</h2>
                <p align="justify">I was assigned to prepare a feasibility study for revamping of two Liquefied Petroleum Gas bottling plants installed near Lahore and Kohat area which were used to transfer LPG from storage facility to cylinders of different capacities. I prepared a report for senior management explaining all the different scenarios which were self-explanatory. I discussed the complete requirement of new machines and also the up gradation of some existing gears.</p>
                <h2>Job Hazard Analysis</h2>
                <p align="justify">I successfully carried out the Job Hazard Analysis of different section at LPG bottling and synthetic Natural Gas facilities. The synthetic Gas was the mixture of LPG and Air with varied composition to meet the Wobbe Index. I fragmented the whole process schematic flow in different sections comprising of Pumping area, LPG Heater, Storage Area, LPG-Air Mixer, Gas Chromatograph, measuring units with AGA standards and defined the potential hazards associated with each unit.</p>
                <h2>Prequalification of Bidder</h2>
                <p align="justify">I was responsible to prepare tender documents of various LNG and LPG projects. I formulated different tenders comprising of LPG + Air mix with capacity of 05, 10 MMSCFD (Million Standard Cubic Feet per Day) capacities and successfully evaluated the coming bids in accordance to PEPRA (Public Procurement Regularity Authority) rules. I also prepared a tender document for solar water heater scheme. Its purpose was to augment it with gas heater for domestic gas consumers. I answered the quarries of different bidders promptly which was dully appraised by senior management. I presented the summary of ongoing tenders and their progress to senior management weekly.</p>
                <p><strong>[CE 2.6]</strong></p>
                <p align="justify">I did a lot of problem solving activities. Some of the most prominent were</p>
                <p align="justify">Our Department was assigned a project to prepare a feasibility report to mix the Biro Gas produced and the Natural gas to expand the distribution network. The team of five members worked very hard to prepare the report but it got stuck at the convergence of Column which was used to remove impurities from bio gas before the mixing of both gases. The task was assigned to me to carry out simulation work. I requested to provide all the relevant data before I could start the work. After a thoughtful process I succeeded in converging the contactor column and acquired the desirable range of impurities in stream coming out from the column.</p>
                <p align="justify">During winter season, we had a severe shortage of Natural Gas in our distribution networks; therefore company decided to install Synthetic Gas (LPG+ Air) plant at different localities. Firstly, we have to install the system on domestic consumers only, for that purpose the selection of LPG + Air mix site was a crucial activity. I played a vital role in order to locate the cluster of domestic consumer and assisted the management to a well-informed and rational decision. I scrutinized the existing networks of far-flung areas, which could easily be connected to Synthetic Gas network. I suggested two locations, one near Faisalabad city and the other near Gujranwala city.</p>
                <p><strong>[CE 2.7]</strong></p>
                <h2>Engineering Calculation</h2>
                <p align="justify">I had performed calculations by using Aspen HYSYS; all the options of LPG pipeline system are modeled along with the simulation of LPG storage requirements and calculation of horsepower of intermediate pumping stations. The pressure drop due to friction in a pipeline depends on the flow rate, pipe diameter, pipe roughness, liquid specific gravity, and viscosity. In addition, the frictional pressure drop depends on the Reynolds number (and hence the flow regime). For this study the pressure drop due to friction in pipeline, expressed in feet of liquid head (h), is calculated by using the Darcy-Weisbach equation. Similarly, for the calculation of friction/transmission factor, Colebrook White equation is used in Aspen HYSYS. The operating pressure was 250 Psig and this is the function of pipe diameter, length, pipeline elevation profile, liquid viscosity and flow rate.</p>
                <p><strong>[CE 2.8]</strong></p>
                <h2>Engineering Standard &amp; Codes</h2>
                <p align="justify">I followed ASME B 31.4, NFPA 59, ANSI B16.5 – 1988, API 5L</p>
                <p><strong>[CE 2.9]</strong></p>
                <h2>Safety</h2>
                <p align="justify">I selected that route of pipeline In order to avoid the areas such as National Parks, Special Military protection areas and special areas of conservation. I also considered the route where least densely populated areas were present.</p>
                <p><strong>[CE 2.10]</strong></p>
                <p align="justify">A team of five subordinate staff were deputed to assist me. We had meeting on daily basis to plan and forecast the stated objectives. I attend meetings with my senior on weekly basis to update regarding the progress.</p>
                <p><strong>[CE 2.11]</strong></p>
                <p align="justify">A third part contractor was engaged for the Revamping of LPG Bottling plant. I coordinated and assisted him regarding the completion of job.</p>
                <p><strong>[CE 2.12]</strong></p>
                <p align="justify">I presented two different scenarios for using different roots of pipeline and calculated the distance. Finally, the seniors considered the option with least distance i.e. 1098 Km and the rest was 1128 Km.</p>
                <p><strong>[CE 2.13]</strong></p>
                <h2>My Recommendation</h2>
                <p align="justify">I started “In House Training and Routine Safety Audit “system for dissemination of job related knowledge. I also presented a suggestion to senior management that all Executive should be advised to take part in that initiative which was also appraised by management. By implementing this system our job related incidents had been drastically reduced and in office agronomic conditions was improved.</p>
