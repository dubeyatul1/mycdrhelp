                <p align="justify">I prepared a total of four different mixes. I swapped cement by waste-marble-powder and used diverse percentages of waste-marble-powder i.e.5%, 10%, 15% and 20% respectively. I designated the mixes as MPR-5, MPR-10, MPR-15 and MPR-20 respectively. But, I managed to keep the quantity all other materials constant with respect to weight of cement. In this project I prepared mixes and investigated their fresh properties. And as Stopover point was reached at MPR-20 concrete mix, that’s why four different types of concrete mixes were prepared. MPR-20 also exceeded the water cement weight ratio range laid down by American Concrete Institute (ACI) 237 i.e. 0.32 to 0.45.With the addition of waste marble powder, the required quantity of cement decreased to keep the water powder ratio constant. In this way an inverse relationship was established between waste marble powder and the required quantity of cement.</p>
