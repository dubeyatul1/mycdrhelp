                <p align="justify">I successfully completed this project. The fallouts of my study were in contrast with any other study done before in the same phenomenon. I came to a conclusion that waste marble powder can be productive filler when used in self compacting concrete. It can be instantly mixed in concrete without much processing. I found out that mixing of 15 percent by weight of waste marble powder yielded positive results. I also found out that strength of self compacting concrete attained by adding 15 percent of waste marble powder is quite similar to strength of conventional concrete that uses cement in its composition. Similarly, I found out that this special type on concrete prepared by using marble powder can also be cost effective when compared to conventional concrete as cement is quite expensive when compared with marble powder.</p>
