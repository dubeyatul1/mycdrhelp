                <p align="justify">I did the mixing following ACI committee report ACI 304R. Mixing was executed in a high speed mixer, with a speed of 300 revolutions per minute. I initiated mixing of ingredients with preparation of paste i.e. mixing of water, cement and fillers in two thirds of water. Preparation of paste took a minute of mixing. After preparation of paste, I added the aggregates to the mixer and admixtures were also added to the mixer. Thus the whole process of preparation was done in 5 minutes. The water was added in two rounds by pouring two thirds part of water during paste preparation and the left on third was used in preparation of aqueous solution of blend of admixtures, which was added after the aggregates were poured in mixer. I faced some issues during the mixing process. I was unable to set the speed of mixer which initially resulted in ill formed mixture. I consulted my supervisor and used high speed mixer with a speed of 300 Rpm. This resolved the mixture texture issue. Other problem was faced by me when I added water. I added water all in one go which resulted in very less viscous paste formation. I concluded on my own to pour water in two or three steps in order to form a paste of required viscosity.</p>
