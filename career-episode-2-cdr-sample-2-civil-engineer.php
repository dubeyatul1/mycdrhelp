                <p align="justify">I performed laboratory tests on fresh as well as hardened self compacting concrete to know more about its properties as each mix of self compacting concrete contained different amount of waste marble powder. I used the guidelines laid down by American concrete institute as well as by EFNARC for making a control concrete mix which was in conformity with standards of self compatibility.I prepared the concrete mixes using construction materials available in the local market.</p>
                <p align="justify">I used Lawrencepur sand and Chenab sand as fine aggregates.These sands are frequently used as fine aggegates in Pakistan and both of the used fines were of highest quality available in local market.Both of these fines fulfilled the requirements laid down by American Sociey for Testing and Materials (ASTM) C33 .</p>
