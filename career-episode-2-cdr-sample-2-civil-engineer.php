                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p align="justify">Self-Compacting Concrete (SCC) is special type of concrete and is different from ordinary concrete. One of the most distinguishing qualities of self compacting concrete is its ability to flow through reinforcements and filling of space without any need of shaking it. The composition of self compacting concrete is also different from ordinary concrete as it contains greater quantity of fines like crushed limestone or sandstone. When compared to other industries, concrete industry uses huge amounts of raw material due to large scale construction activities happening all around the globe. Of many other types of filler, limestone is one of most frequently used in making of concrete. Marble powder can be used in preparation of self compacting concrete as marble stone originates from limestone. So, I used waste marble powder to produce self compacting concrete. I used different amounts of waste marble powder to produce self-compacting-concrete &amp; in this way I managed to have four different samples of self-compacting-concrete &amp; each of them contained different amount of waste marble powder. I performed different tests on fresh concrete like flow ability and resistance to segregation. In order to measure compressive strength of SCC, I allowed it to harden and performed tests on hardened concrete samples having different quantities of waste marble powder.</p>
                <p><strong>[CE 2.3]</strong></p>
                <p align="justify">Marble is a rock produced as a result of metamorphism of limestone and dolomite. It is angular in shape. The term marble is also applied to superfine rock that can be polished. During the process of cutting of marble for its domestic use, a large quantity of waste marble powder is left behind which I will use in the formation of self compacting concrete.</p>
                <p align="justify">In preparation of Self-Compacting Concrete filler materials are used to fill the spaces between aggregate particles and creating impermeable concrete. This enhances durability of concrete. Lime stone fillers are used for preparation of Self-Compacting Concrete and have shown good performance.</p>
                <p align="justify">Self-Compacting-Concrete has been extensively implied around the globe due to nonexistence of research, it has not expanded admiration in Pakistan. This project was a conscious effort in the right direction to make the use of Self-Compacting Concrete more frequent in Pakistan. International researchers have made serious efforts to use waste marble powder in making of Self-Compacting Concrete but Pakistani researchers are lagging behind in this particular area and conventional concrete is more predominantly used in the construction industry of Pakistan.</p>
                <p><strong>[CE 2.4]</strong></p>
                <p align="justify"><b>The prime objectives of the project were -</b></p>
                <ul>
                  <li>To check the efficacy of waste marble powder in self compacting concrete thereby reducing burden of waste on environment.</li>
                  <li>To know the properties of fresh self compacting concrete when waste marble powder is added in it.</li>
                  <li>To check effect of the addition of waste marble powder on some hardened properties of concrete at different ages.</li>
                  <li>To find the best amount of waste marble powder that will lead to highest quality of self compacting concrete.</li>
                </ul>
