                <p><strong>[CE 2.6]</strong></p>
                <p><b>The principal role and responsibilities that I carried out as Senior Procurement Engineer during this project, included -</b></p>
                <ul>
                  <li>Handle the procurement of electrical systems needed for constructing the power plant. Such types of equipment were Main Power Transformers, Isolated Phase Bus Duct, Generator Circuit Breaker, Auxiliary Transformers, HV/MV/LV Cables, MV &amp; LV Switchgears, Diesel Generators, Cable Support Structures, Protection and Synchronization, Batteries and Chargers, Lightning and Small Power, Communication and Security Systems and others.</li>
                  <li>Check the purchase requests to confirm the clarity of the specification and quantities.</li>
                  <li>Issue the Request for Quotation (RFQ) and follow up with vendors/suppliers to get the offers.</li>
                  <li>Review the received offers commercially and seek the technical evaluation from the Project Engineering Manager.</li>
                  <li>Conduct negotiations for all purchasing terms and conditions. Coordinate with the vendors/suppliers for the technical clarifications.</li>
                  <li>Prepare the price comparison sheet and define the selected supplier/vendor based on the total cost of ownership in coordination with the Project Engineering, Procurement, Commercial, and General Managers.</li>
                  <li>Prepare the final contract and send it to the selected supplier/vendor and got their acknowledgment.</li>
                  <li>Interfere with the supplier and project team to ensure a smooth execution of the contract.</li>
                  <li>Plan the project sourcing and procurement strategies and develop the project procurement schedule</li>
                </ul>
                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 2.7]</strong></p>
                <p>In the project, it was required to use Aluminum in the LV cables. But using Aluminum could lead to increased cross-section, connectivity issues and significantly increased space for the erection. To avoid all those issues, I used Copper in the LV cables because Copper cable had a less cross-section and better connectivity than Aluminum in addition to less space for the erection. I raised a technical change request to the customer and successfully, they didn’t have any issues with the request.</p>
                <p><strong>[CE 2.8]</strong></p>
                <p>In this project, I had to design LV cables and Security System. To design the LV cables and Fire alarm system, I was consulting with a designer.</p>
