                <p><strong>[CE 2.10]</strong></p>
                <p>During this project, I used a few standards of ISO, IEC, and NFPA during my project. Also, I applied my knowledge of engineering to solve the technical issues and optimize the design for cost saving.</p>
                <p><strong>[CE 2.11]</strong></p>
                <p>During the project, I took help from my project guide. I asked about the basic design principles for Low Voltage (LV) equipment, Electrical Cables Calculations, eBidding and eAuctions and Supplier Relationship Management. For many issues, I negotiated with the vendors/suppliers. I did strategic sourcing, technical clarifications with vendors/ suppliers and used frame agreements for contracts.</p>
                <p><strong>[CE 2.12]</strong></p>
                <p>While working on this project, I used many software tools during the project that included SAP for contracts, Click for Suppliers (C4S) for the registration and qualification of suppliers, New Costing Tool for uploading the calculated costs and SisyPro for material tracking of the project.</p>
                <p><strong>[CE 2.13]</strong></p>
                <p>I followed all the safety rules and regulations according to the policy of the company while working on this project. From the beginning of the project, the environmental health and safety measures were strictly advised by the customer to follow. That’s why we hired an EHS coordinator throughout the project in order to meet the customer’s requirement. This project was a construction site project, that’s why it required a few of precautions. I had to take measures for hazardous energy control. I took an electrical safety course for doing electrical work. I used to wear helmet, goggles, gloves and working suit, and I also advised workers to do the same. As the project needs working from a height, I took precautions for it to avoid any accidents. Driving and vehicle safety were also kept to have a sound and safe working environment. The machine guarding, signage, and safety interlocks were used.</p>
                <p><strong>[CE 2.14]</strong></p>
                <p>As a Senior Procurement Engineer, I was responsible for the handling of electrical material during the project. I had to handle the material from the request phase of the material to the delivery phase of the material. I used a few techniques to carry out the project effectively. I defined the problem, analyzed the cause of that problem, and then suggested some valuable ideas and appropriate solutions. Among all suggested solutions, I selected the best solution and implemented the corrective action in order to kick out the encountered problem.</p>
                <p><strong>[CE 2.15]</strong></p>
                <p>In this project, I didn’t have any members who were working under my supervision as I had to report directly to my functional manager. However, I was leading the vendors/ suppliers for managing and handling the purchases of material for the project according to the contract terms. I had strong and regular interaction with regional and global electrical commodity managers for the project sourcing and purchasing activities. I needed to prepare the Purchasing Manual, Purchasing Schedule, Project Sourcing Strategy and Project status Sheet-Electrical Commodity.</p>
                <p><strong>[CE 2.16]</strong></p>
                <p>I was working under the supervision of the project procurement manager and he was reporting to the project manager. That’s why I had to communicate with him almost daily. I also needed the cooperation of procurement management, general and commercial project management and engineering project management. I had weekly meetings with the project team to discuss the project’s progress. I had to meet with management once in a month to share the status of the project. My meetings with suppliers were scheduled according to the work of technical and commercial clarifications. I had daily contact with my functional manager, weekly meetings with the project manager and project team, and monthly meet-ups with higher management.</p>
                <p><strong>[CE 2.17]</strong></p>
                <p>During this project, I gained a professional experience by doing this project. I learned basic design principles for LV equipment which was used in power plants, time management skills, analytical thinking and problem-solving ways. I got to know the measuring of electrical cables. I learned about the strategic procurement and frame agreements. I had the concept of Supplier Relationship Management skills throughout the project.</p>
                <p><strong>[CE 2.18]</strong></p>
                <p>presented a presentation for the Purchasing Manual, Purchasing Schedule, Project Sourcing Strategy, and Project Status Sheet-Electrical Commodity. My main topics were purchasing status and schedule. Also, I introduced new strategies for purchasing that were Design to Cost, eAuction, Bundling, Supplier Day and Introduction of New Challengers. I arranged an open day for project team so they could gather the information and data regarding the project.</p>
                <p><strong>[CE 2.19]</strong></p>
                <p>The project is going well, according to the time schedule, and I am hoping that it will be completed in the fourth quarter of 2018 as per the plan. The working of the project has been done so far is perfectly alright and effective.</p>
