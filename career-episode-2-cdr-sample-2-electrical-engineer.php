                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.3]</strong></p>
                <p>Siemens is known as the powerhouse of technology throughout the world. It has been giving services in engineering excellence, technology, innovation, reliability, creativity, and quality for more than 165 years. About an estimate, in 2016, Siemens has around 351,000 employees. It is the world’s largest producer of energy-efficient, resource-saving technologies and is a leading supplier of efficient power generation and power transmission solutions. Siemens is operating in more than 200 countries and is known as a pioneer in infrastructure solutions, automation, drive and software solutions for industry. Siemens generated revenues of €79.6 billion in Fiscal 2016. One of its regional offices is located in Dubai, which prepares tender projects for power plants in the Middle East &amp; North Africa (MENA) region. It also handles the onshore commodities for the ongoing projects.</p>
                <p><strong>[CE 2.4]</strong></p>
                <p>The main objective of this project was to satisfy and manage the increasing demand on electricity in the Emirate of Dubai. For this, a 700 MW was decided to be added to the Jebel Ali M Power Plant. The project needed to design, process, and construct the expansion of the Jebel Ali M Power Station, and for this we had to deliver and build a block of two SGT5-4000F gas turbines, two SGen5-1200A series generators, one SST5-4000 steam turbine, one 5Gen5-100A series generator, and two Heat Recovery Steam Generators (HRSG) in combined cycle operation in addition to the Balance of Plant equipment. Overall, the project needed Civil, Mechanical, Electrical, and Process operations. The project is about to add the 700 MW to the grid to fulfill the increasing demand for electricity in the Emirate of Dubai.</p>
