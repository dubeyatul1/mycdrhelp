                <p>It is a good experience to do this project. I used my various skills and knowledge of engineering degree to complete the project’s activities. I successfully managed to secure the purchasing and delivery of all electrical materials that were required for the project. In some commodities, I managed to reduce the cost up to 17% less than the allocated budget. On the completion of this project, I and my team will be able to add 700 MW to the Jebel Ali M Power Plant.</p>
