                <p><strong>[CE 2.1]</strong></p>
                <p class="para">This career episode is about my project titled “Design &amp; Installation of PLC &amp; SCADA in an Inverter Cooling System’’. This project was carried out during my tenure at GC Technology as an Electronic Engineering.</p>
                <p class="para">GC Technology is a dedicated company to provide IT, electrical/electronic, mechanic and industrial automation services to other companies as an outsourcing resource. Our company signed contract with XYZ CA to update the PLC and SCADA control system.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p class="para">The project purpose was to replace an existing Omron PLC and associated control wiring works with an Allan Bradley system to suit the inverter cooling system and existing auxiliary equipment of the plant. I was assigned to develop a control system based on the existing installation requirements using a solution based on a PLC and associated SCADA.</p>
                <p class="para">The inverter cooling system was used to cool the coil line and the mill motor. My responsibility was to run the system automatically (PLC and SCADA based) via a start button on the water control system page. Each drive or valve also needed to be controlled manually by clicking the corresponding graphics on computer. The cooling system consisted of a main open loop system and two closed loop systems for inverter cooling and the coil line cooling.</p>
                <p class="para">During the project, I had to bypass the inverter cooling system from the control panel, which will allow the operator to start the inverter cooling pumps without the PLC being energized. Moreover, all three pumps required to be electrically isolated by switching the OFF/ON isolator switch at the panel. Each pump had three states, ‘Man’, ‘Remote’ and ‘DOL’. ‘Man’ will allow the operation of the pumps from the control panel (using the start and stop pushbuttons). ‘DOL’ will allow direct electrical feed to the motor (i.e. bypassing both the PLC and the start/stop pushbuttons) and ‘remote’ will give control of the pump to the controller. To control the pumps from the graphics monitor, the pumps must be set to ‘remote’ state.</p>
                <p><strong>[CE 2.3]</strong></p>
                <p class="para"><b>The prime objectives of the project were enlisted as following with the current implementation context -</b></p>
                <ul>
                  <li>To determine the requirements for the installation of the existing control equipment</li>
                  <li>To perform integrity checks and assessment of current wiring installation for potential reuse</li>
                  <li>Where no re-use was possible, to design the required new wiring</li>
                  <li>Ensure that the disposal of non re-usable wiring and components is performed according to company and environmental regulations.</li>
                  <li>Disconnect the existing Omron system and replace it with suitable AB installation</li>
                  <li>Supply and install new SCADA workstation and programming</li>
                  <li>Provide assistance to test the installed PLC/SCADA system, including related control equipment and interfaces</li>
                </ul>
                <p><strong>[CE 2.4]</strong></p>
                <p class="para"><b>The project Hierarchy is given below. I was answerable to the project engineer -</b></p>
                <img src="images/electronics-engineer-cdr-sample-2-career-episode-2-2.4.png">
                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 2.5]</strong></p>
                <p><b>My Roles &amp; Responsibilities were as following -</b></p>
                <ul>
                  <li>Ensure compliance of site standard operational procedure (SOP) and occupational, health and safety standards.</li>
                  <li>Develop the PLC and SCADA system.</li>
                  <li>Prepare all technical details for the PLC, SCADA and all auxiliary equipment.</li>
                  <li>Programming the system which includes: PLC hardware configuration, translation of Omron program to AB PLC, implementation of control logic for cooling water pump and associated equipment, configuration of SCADA, access and communications setup.</li>
                  <li>Testing the devices of all supplied PLC, SCADA and all auxiliary equipment.</li>
                  <li>To delegate tasks among the technical staffs and maximize the manpower.</li>
                  <li>Provide training to Hierro Barquisimeto’s technician on PLC troubleshooting &amp; process monitoring capabilities.</li>
                  <li>Finally, submit the detailed report of works performed.</li>
                </ul>
                <p><strong>[CE 2.6]</strong></p>
                <p class="para">On March 2009, I performed an inspection and assessment of the existing installation. I took notes and checked against archived electrical drawings to ascertain the technical requirements for the new installation. I checked and assessed the existing wiring for re-use. Where cables could not be re-used, I made notes and recorded them in a register for future reference and creation of a disposal plan. I clearly tagged all the input/output field cables connected to the PLC, which would allow me to reuse them with the new PLC to be installed in its place.</p>
                <p class="para">Once I defined the technical requirements, I proceeded to evaluate the new AB PLC to ensure that its capabilities match the established requirements. I have developed technical drawings following international standard ICS-31 and submitted them for approval by the head of the technical department.</p>
                <p class="para">After approval, I started to disconnect the existing PLC and instrument wiring so I could install new PLC. In the meantime, I organized the technician’s team to proceed with the removal of non-usable cabling and installation of new electrical wiring as required and indicated by the drawings I have created. I carefully observed and monitored that the project would not cause any environmental issue. I made sure that the project site was clear of debris and surplus material resulting from the works meeting all relevant local authority regulations and our company requirements</p>
                <p class="para">Then, I removed the devices, switch and pushbuttons on the control desk, which functions were envisaged to be replaced as part of the new SCADA.</p>
                <p><strong>[CE 2.7]</strong></p>
                <p class="para">At the end of March 2009, I installed the new AB PLC and those meters, pushbuttons and switches that would still be part of the control desk. Existing Omron racks were replaced completely by their AB counterpart. I also installed new cabling from the PLC to the terminal blocks that connect to the field devices. I connected all the input/outputs of I/O cards similar to the previous connections as determined by the previous assessment that I did.</p>
                <p><strong>[CE 2.8]</strong></p>
                <p class="para">Once I completed the hardware installation I proceeded to start the configuration of the PLC. In order to do this, I used my notes taken as part of the assessment of the previous system and translated the ladder Omron program to the AB unit. This way, I managed to replicate properly the operation of the previous system.</p>
                <p class="para">I implemented a PLC logic that allowed the user to start the pumps in local or remote mode as well as the monitoring of associated temperature, pressure, level and flow sensors.</p>
                <p><strong>[CE 2.9]</strong></p>
                <p class="para">After that, I focused on the parameters that were required to be shown on the graphic screen so the operators can easily monitor the preferred values. I added the parameters like the cooling water pumps’ status and connected instrumentation signals in addition to those from the inverter operation parameters like Kilowatts, frequency, current, bar speed, temperatures, etc. I also integrated the safety PLC screen to the SCADA.</p>
                <img src="images/electronics-engineer-cdr-sample-2-career-episode-2-2.9.png">
                <p><strong>[CE 2.10]</strong></p>
                <p class="para">On April 2009, after completing the PLC and SCADA programming, I proceeded to start testing the system. I performed the Factory Acceptance Test (FAT) and Site Acceptance Test (SAT) which were the basis to determine if the electrical and electronic devices were manufactured and delivered in accordance with the Hierro Barquisimeto and international standards.</p>
                <p><strong>[CE 2.11]</strong></p>
                <p class="para">I conducted the SAT test on site. The SAT was a set of tests aiming to inspect the machine and installation on location to ensure smooth start up and verify that the devices fulfils its acceptance requirements. I checked these items with the “URS modules”, “Machinery Safety Checklists” (as part of the Pre-Start-up Safety Review), “FAT Checklist” (with special attention to the action list), “SAT Checklists” and “Efficiency Test”. I executed the SAT for a minimum of 24 hours over consecutive day shifts with supplier’s engineer on site.</p>
                <p><strong>[CE 2.12]</strong></p>
                <p class="para">On May 2009, as part of my responsibilities, I organized two training sessions of 8 hours each where I explained to Hierro Barquisimeto’s site engineers PLC troubleshooting steps and the capabilities of the new system in regards of monitoring, trending, and access capabilities. I also trained the operators about the function of PLC and SCADA systems.</p>
                <p class="para">I wrote the final report for this project where I compiled detailed functional specifications, datasheets, drawings for electrical works and control layouts, block diagrams, PLC I/O list, description of the SCADA implementation and associated operator’s manual, maintenance and operational requirements documentation and results of factory and site acceptance tests.</p>
                <p><strong>[CE 2.13]</strong></p>
                <p class="para">During the project, I kept records and daily logs of all operations, observations, and progress measurements. My report was to in writing, clear and concise as necessary to convey the intended information.</p>
                <p class="para"><b>I included the following information in the Daily Site Report -</b></p>
                <ul>
                  <li>Incident reports and additional comments where necessary.</li>
                  <li>Number of personnel onsite and categorized by disciplines/ trades.</li>
                  <li>Staff rotation.</li>
                  <li>A concise list of plant and equipment on site.</li>
                  <li>Description of tasks undertaken and the level of progress achieved, any issues encountered, damaged equipment encountered or caused.</li>
                  <li>Daily task plan</li>
                </ul>
                <p><strong>[CE 2.14]</strong></p>
                <p class="para">I played a key role in the consolidation of safe workplace environment; ensuring adherence to site safety regulations from team members and sub-contractors and also reinforcing safety through toolbox meetings and training. In addition, I was responsible to assure that they were using PPE (Personal Protective Equipment) at all times and keep updated SOP’s (standard operational procedures) and performed a Job Safety Analysis (JSA’s) prior start a new task in order to assess risks associated to the new activity.</p>
                <p class="para">After project completion, I prepared a project report using MS Power Point software and presented it to head of technical department and stakeholders.</p>
                <p><strong>[CE 2.15]</strong></p>
                <h2 class="sample_heading">Summary</h2>
                <p class="para">This project allowed me to reinforce my knowledge and technical skills in PLC and SCADA programming including troubleshooting and equipment commissioning. I have developed technical expertise in migration between Omron and Allen Bradley system.</p>
                <p class="para">This experience provided me an opportunity to gain knowledge about cooling water system, water pump control and associated instruments. My role during commission period was very important because I have provided technical support, troubleshooting and guideline to technical operators, team members and stakeholders.</p>
                <p class="para">I also trained client technicians about PLC troubleshooting through training sessions of two hours each.</p>
