                <p><strong>[CE 2.8]</strong></p>
				
				<h2>Working Principle</h2>
				
				
				<p>In this fabrication model, change in tool within robot arm end effector was performed by me implying elliptic trammel mechanism i.e., double slider crank mechanism. I mounted the tools in cross section of two sliders which were connected with each other using connecting component.  When the sliders move along the path profile, which was connected via connecting component, the center of the connecting component traces circular path. I mounted a pin on the center of connecting component; a crank was connected with one end at the center of connecting component and other end with fixed shaft of stepper motor. Thus, by controlling the step angle of 90ͦ of stepper motor shaft, sliders move to the other end of path profile.  The tool can completely change when it turns to locate in position along with axis of end effector spindle.  So, to control the position of path profile I used another stepper motor which has the step angle of 90ͦ.</p>
								
				<p>I controlled the two stepper motors by Bluetooth board, Bluetooth controller, Timer board, Driver board and Speed controller board. The stepper motors were commanded by mobile phone Bluetooth. When the command is given via mobile phone Bluetooth, the Bluetooth controller board receives signal and converts signals to binary value with ‘1’ as start and ‘0’ as stop.  From Bluetooth board, the signal passes to timer board which controls the time sequence of command given to stepper motor to rotate the shaft.</p>
				
				
				<p><b>[CE 2.9]</b></p>
				
