				<h2 >Engineering Calculations</h2>
				
				<p>Torque Analysis

				I carried out this analysis all by myself.
				Torque of the stepper motor (as specification) = 30 kg-cm
																							  = 2.943 N-m
				Torque due to fabrication = Moment of Inertia x Angular acceleration 
													  T   = I x α
				Moment of Inertia I = I1 + I2 +I3 +I4 +I5
				Mass Moment of Inertia for Guide 1 for path profile I1 = (m x r x r)/2
					= (((π x 95 x 95 x 〖10〗^(-6)  x 8 x 〖10〗^(-3))/4) x 7850 x ((95 x 95 x 〖10〗^(-6)   ))/(4 x 2)))
					= 5.0217 x 〖10〗^(-4) kg m^2                 
				Mass Moment of Inertia for Guide 1 for path profile I2 = (m x r x r)/2
					= (((π x 95 x 95 x 〖10〗^(-6)   x 10 x 〖10〗^(-3))/4) x 7850 x ((95 x 95 x 〖10〗^(-6))/(4 x 2)))
					= 6.277 x 〖10〗^(-4) kg m^2
				Mass Moment of Inertia of Guide 1 for path profile I3 = (m x r x r)/2
					= (((π x 54.7 x 54.7 x 〖10〗^(-6)   x 25 x 〖10〗^(-3)  )/4) x2700 x ((54.7 x 54.7 x  〖10〗^(-6)  )/(4 x 2)))
					= 0.5932 x 〖10〗^(-4) kg m^2
				Mass Moment of Inertia for Path profile I4 = Iz 
					= 4 x ((M x((b^2- d^2)/12)) – (M1 x((〖b1〗^2-〖d1〗^2)/12)))
				   =4 x ((0.165 x((〖21〗^2- 〖20〗^2)/12)) – (0.0883 x((〖15〗^2-〖14〗^2)/12)))
				   = 1.4014 kg m^2
				Mass Moment of Inertia for Sliders and Connecting component I5 = Iz 
				   =   2 x ((M x((b^2- d^2)/12)) – (M1 x((〖b1〗^2-〖d1〗^2)/12)))
				   =   2 x ((0.014 x((〖15〗^2- 〖14〗^2)/12)) – (0.019 x((〖15〗^2-8^2)/12)))
				   = 0.0926 kg m^2
				I = I1 + I2 + I3 + I4 +I5
				I = 0.00050217 + 0.0006277 + 0.5932 + 1.4014 + 0.0926
				I = 1.4952
				α = (ω1- ω2)/(t1 -t2 ) = (3.141-0)/(2-0)   = 1.57 radians/s^2
				T = I x α
				   = 1.4952 x 1.57
				   = 2.3486 N-m
				Since the torque of the stepper motor is greater than torque calculated from fabrication i.e. 2.943 > 2.3486.  So, the design was safe.
