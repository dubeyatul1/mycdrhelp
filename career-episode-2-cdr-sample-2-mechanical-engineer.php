                <p><strong>[CE 2.1]</strong></p>
				
                <p align="justify">This career episode describes my project bearing title “Fabrication of Elliptic Trammel Tool Changer Model of Robot Arm & Effector’’. This project was carried out to fulfill the requirement of Mechanical Engineering Degree completion.</p>
				
				
                <h2>BACKGROUND</h2>
				
                <p><strong>[CE 2.2]</strong></p>
				
				<h2>Nature of the Project</h2>
                <p align="justify">The assembly process having robots at work involves numerous dimensions.  Robots can be used in the assembly process to reduce the time lag of the production.  Moreover, assembling and dismantling of tools on robot arm by pneumatic tool changing concept also takes time for production. This concept of tool change reduces the time for tool change in assembly and drilling operations of robot arm.  This model has four different tools including two sliders consisting of two different tools each within the end effector. This concept works by using stepper motors which are programmed initially to drive as expected by D.C, supply lithium ion battery, controlled by mobile phone Bluetooth by the aid of numerous control boards like Speed control board, Bluetooth control board, Bluetooth controller, Driver board and Timer board.  The design is fabricated as to hold one motor which drives the path profile of sliders and other motor is used to drive the sliders using crank. By controlling the angle rotated by shaft of motors, we can change the tool in robot arm end effector and the whole end effector of robot arm will act as a spindle to perform operations.</p>
                <p><strong>[CE 2.3]</strong></p>
				<h2>Objective of the Project</h2>
                <p align="justify">The main objective of this project was to change the tool within the robot arm end effector using inversion of Double slider crank mechanism i.e. Elliptic Trammel mechanism.  This project described the working of Elliptic Trammel mechanism using Stepper motors to perform tool change operation in four directions. I executed the entire system to have its tool change operation by reciprocating motion in both horizontal and vertical direction.</p>
				
				<p>There are four different tools attached at the end of two sliders; each containing two tools and they perform changes within the end effector when the sliders are in motion. The sliders of the mechanism move perpendicular to each other which is driven by stepper motor.  The midpoint of connecting component connecting the two sliders traces a circular shape.  A crank is made to rotate on the traced path connected to the small pin on the midpoint of the connecting component and crank fixed with the shaft of stepper motor.  Stepper motor carrying the crank guided with the eccentric aluminum cylinder for making the axis of shaft to rotate in constant speed while deflecting. When the shaft of stepper motor is rotated, the crank also rotates which in turn gives rotational movement to the connecting component. This rotation helps to reciprocate the sliders in horizontal and vertical pathway which in turn provides a very convenient mechanism for tool change within robot arm end effector.</p>
