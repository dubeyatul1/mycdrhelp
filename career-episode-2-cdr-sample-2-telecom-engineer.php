                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p class="para">Mobilink has already more then 12,000/ BTS site and 200 BSC nationwide and almost 4,000 BTS sites in North. Expanding them and optimizing them was a challenging task. LCC RF engineers performed the drive test for the BTS sites and whenever it was require changing configuration or expanding the existing setup, new plan was sent to me to execute it directly by me of using available teams and resource.</p>
                <p class="para">When new plan was sent to me to execute, I checked complete scope of work and made a plan for execution. Initially I had to check all sites their present and planned configurations. For this I checked its BSS plan, Antenna plan, Power plan, media plan and new configuration. After receiving scope of work I prepared SMR’s (site material request) according to the configuration given in the scope of work which is assigned to us. This SMR is then forwarded to the costumer warehouses, and then I arranged all logistics and managed collection of telecomm equipment and delivery to site. When equipment was delivered to site, I visited site myself for installation. I supervised and inspected the installation, modification, testing and operation of telecommunication equipment’s.</p>
                <p><strong>[CE 2.5]</strong></p>
                <p class="para">Upon reaching the site, equipment was rechecked with the confirmation of BSS plan, antenna plan, power plan, media plan and new configuration was implemented there. Expansion typically involved the installation of Antenna with mount/diversity frame, feeder cable, TRX’s, duplexer and multi coupler. Additional work included installation of Battery Banks, BTS Cabinet and Microwave dishes and outdoor Units.</p>
                <p class="para">I provided my field engineer the network outage work order number for specific period of time &amp; communicated my team to start work and made sure that they complete all work within give time frame. Equipment’s installation started with the assembling of mount/ diversity frame and feeder connector formation. Once the installation was started it took around 3 to 4 hours for one cell to get completely installed. Newly installed hardware was designed using software U2000 Web LCT. After designing the hardware it was then sent to BTS to get integrated with the existing Hardware then Traffic for newly installed TRX’s was inserted using U2000 Web LCT.</p>
                <p class="para">Meanwhile I prepared CM (configuration data) for the new changes to be implemented. CM data included BSC ID, site ID , site name, cell ID, existing configuration , planned configuration, BCF ID , ET no, BTS ID ,TRX no , signaling , TCH , frequency hopping , HSN , MA-list,MAIO offset, MAIO setup and OMU signaling. Then I communicated with CM team &amp; sent them the CM data and asked them to run CM data at particular time. I also communicated the same plan with my field engineer.</p>
                <p><strong>[CE 2.6]</strong></p>
                <p class="para">Once all these parameters were set, script was run and commissioning was done at site. If everything goes according to plan than new configuration was implemented I asked the field engineer to take BTS files for this record and save all VSWR graphs as well as take pictures of activity done, which was later submitted to me. If the planned configuration got any problem in execution then I coordinated with field engineer and CM team with involvement of O &amp; M to rectify the issue ASAP.</p>
                <p class="para">All the payments of subcontractors were verified by me and then I sent their BOQ to finance for clearance of bills. Transporter bills and their extra labor cost were also approved by me.</p>
                <p><strong>[CE 2.7]</strong></p>
                <h2>KPIs for network optimization and expansion</h2>
                <p class="para">After execution of plan, Site was kept under observation for one month time and benchmark is to maintenance of pre-installation and post-installation KPIs. I coordinated with RF teams and took KPI stats from them after one week on expansion activity. If stats were not up to bench mark I did the troubleshooting and rectified the issue.<br/><b> There are many factors on which these KPIs effect, b/m are major KPIs which we check and maintain -</b></p>
                <ul>
                  <li>CSSR.</li>
                  <li>DCR.</li>
                  <li>HOFR.</li>
                </ul>
                <p><strong>[CE 2.8]</strong></p>
                <h2>CSSR (Call Setup Success Rate)</h2>
                <ul>
                  <li>If CSSR is low after expansion I checked TRX quality stats from back office. If quality stats of TRX are below standard I arranged a site visit and asked team to change the faulty TRX. For this case I also asked CM team to change BCCH TRX with hopping TRX logically.</li>
                  <li>For TCH blocking, I requested RF team to check traffic sharing between GSM and DCS layer and it was controlled by handover parameters or by hard optimization technique. Low TCH was also observed if any neighbor site was down. So I also checked daily network availability report.</li>
                  <li>If issue not solved I requested RF team for joint visit to site, and if interference was found due to over shooting cells antenna tilts were changed after discussion with RF team.</li>
                </ul>
                <p><strong>[CE 2.9]</strong></p>
                <h2>DCR (Drop Call Rate)</h2>
                <ul>
                  <li>If DCR was found high in stats, TCH availability was checked so we checked TRX stats for that sector &amp; it checks TRX quality if it is found below the standard I asked team to change faulty TRX.</li>
                  <li>Since DCR also depends upon TCH blocking so I requested RF team to check it for GSM and DCS and use hard optimization technique to rectify issue.</li>
                  <li>DCR also depends upon interference between overshooting cells. If issue was not resolved I requested a joint visit with RF team &amp; if any issue was found during DT (drive test) the antenna tilts were readjusted.</li>
                  <li>I also requested CM team to recheck neighbors for sites, as they also cause DCR. Properly neighbor plan was sent by RF team and it was executed by CM team during configuration of new cells.</li>
                </ul>
                <h2>HOFR (Handover failure rate)</h2>
                <p><strong>[CE 2.10]</strong></p>
                <ul>
                  <li>If calls were not properly handed over to neighbor cells/sites it also caused KPIs to effect. So I coordinated with RF teams to check handover mechanism and solved it with the help of RF and O &amp; M teams by using hard handover technique.</li>
                  <li>I also checked daily outage details and verified if neighbor sites were working normally otherwise escalated issue to O &amp; M.</li>
                </ul>
                <p><strong>[CE 2.11]</strong></p>
