                <p><strong>[CE 2.1]</strong></p>
                <p class="para">My Second episode is about my project in LCC International. LCC was established in 1983 and remains a leading pioneer in providing fixed network, wireless voice and data engineering services to the telecommunications industry. LCC is working in more than 50 countries worldwide and providing service in network management, business consulting, tools-based solutions, and training. Since November, 2014 it’s been acquired by Tech Mahindra Ltd.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p class="para">I worked on this project as Level-2 Engineer and team lead for North region of Pakistan. In this project I was responsible for complete execution of project. I did meetings with customer and finalized scope of work for each batch. I discussed all technical details relating BSS (base station subsystem), RF (radio frequency) parameters, Transmission issues, Power issues and finalized the scope of work for each batch with customer and subcontractors. I finalized BOQs(Bill of quantity) and extra cost of each activity with customer teams. I used all my previous experience and knowledge to execute this project efficiently.</p>
                <p class="para">I had 13 teams each comprising of 1 engineer, 1 technician &amp; 4 riggers, working directly with me. The Teams were dedicated area wise. Initially I distributed scope among all teams &amp; then arranged meeting with each team to finalize logistics and technical plan. I assigned and coordinated with teams and reviewed their performance.</p>
                <p class="para">Equipment delivery from ware house and transport was also done by me. For logistics a transport company directly worked under my supervision. I coordinated with different warehouses and all equipment was delivered to site on time with complete responsibility, Transport Company all bills are checked and approved by me.</p>
                <p class="para">I prepared daily and weekly reports of all the teams, and kept KPIs of their work. Each team performance was monitored at the same time.</p>
                <p><strong>[CE 2.3]</strong></p>
                <p class="para">Network Swap, Expansion and optimization are very important factor for mobile operators, and each operator desired to cover these aspects of engineering in most cost effective manner. Mobilink is the largest mobile operator of Pakistan and have worldwide experience. It was a great opportunity for me to work on this project and develop new skills. In this project I worked with Huawei, which is the largest telecommunications equipment manufacturer in the world having overtaken Ericsson in 2012. I coordinated with different teams and departments directly and indirectly which in turn increased &amp; polished my communication, management and technical skills.</p>
