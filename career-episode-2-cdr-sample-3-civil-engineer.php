                <p><strong>[CE 2.6]</strong></p>
                <p>The problems/challenges that might be associated with this project were first in the design stage and the construction stage. Furthermore, the key problems have been classified into three sections; the site, office block and the light industrial factory.</p>
                <h2 class="">Site -</h2>
                <p>Talk Talk defines the green field as “Area of land that has not been used for any non-agricultural development. It is therefore usually located in a rural area, or on the edge of a town or city”. The business dictionary defines it as “an area of agricultural or forest land or some other undeveloped site earmarked for commercial development or industrial projects”.</p>
                <p>Therefore, access to the site was put into consideration as the planned vehicular route has to be sourced for by liaising with planning regulatory bodies. Even though the land is said to be fairly flat, the planned or already designed levels of this route has to be gotten so as to plan the ground level elevation of the building at design stage.</p>
                <p>Site clearing commenced from the beginning of the route to the site location to create access (temporal) and based on the nature of the soil, gravels (hard core) were needed on the road access to facilitate movement to and from the site, especially during the wet seasons.</p>
                <p>Other factors to be considered during construction stage would be beefing up site security, and probably temporal electricity.</p>
                <p>The ground present was clay and clayey silt soil. This kind of soil absorbs moisture and retains it for longer period. Therefore, they expand or contract permitting surrounding temperature thereby creating challenge for the foundation choice and design.</p>
               
                <h2 class="">Light Industrial Factory -</h2>
                <p>The factory spans 60m x 60m and free of intermediate columns. This large span poses a challenge to the choice of structural design. Therefore, both portal frame and lattice girder structures were considered as the two are known to span up to 60m or more.</p>
                <p>A centrally located column was to support a bridge crane abutment which in turn carries a 10-ton load. Therefore, the challenge was to design a beam that can span 30m carrying a 10 tons load.</p>
                <p>For the column which stands as a single pier will be reinforced concrete with large diameter for better stability.</p>
                <p>Construction of a 60 m × 60 m floor slab will present another potential problem. The factory will carry heavy duty equipment and vehicle. This heavy loaded factory on the ground condition requires careful consideration.</p>
                <p>The bridge crane runway spans 30 m and needs to support a load of 10 ton. The design of the runway girder will be a challenge, thus involving careful planning.</p>
               
                <h2 class="">Office Block -</h2>
                <p>The key problem with the reinforced office building was the ground floor, free column area which spans 10.0 m. Beam lengths of 10 meters would have to be designed to support the slab above it. The challenge was due to the fact that the reinforced concrete structure has to be cast in-situ, and the structure must be strictly of reinforced concrete.</p>
