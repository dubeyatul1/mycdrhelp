                  <p><strong>[CE 2.1]</strong></p>
                  <p>This career episode narrates my project <strong>“MTC Touch HSPA Project’’. </strong>This project was carried out during my tenure at <strong>SERTA TECHNOLOGIES </strong>as a <strong>Service Engineer.</strong></p>
                  <p>Serta technologies is a part of RGH group whose main concern is to provide telecom service for their customer. Serta is experienced with site commission, site survey and site implementation on turn key basis. In addition, Serta has worked with different service project including Touch HSPA project and Airport project.</p>
                  <h2>Background</h2>
                  <p><strong>[CE 2.2]</strong></p>
                  <p>In the telecom sector technology is moving forward and as a service provider its needed to always keep up to date with the technology. To do so, MTC touch signed contract with Huawei to upgrade its network and include the HSPA technology back in 2013. As part of an internal regulation in Huawei, this project was subcontracted to two companies: Serta technologies and Camusat.</p>
                  <p><strong>[CE 2.3]</strong></p>
                  <h2>Objective</h2>
                  <p><b>The prime objectives of the project were -</b></p>
                  <ul>
                    <li>Unremitting enhancement of the network</li>
                    <li>Provide users with high data rates and seamless transition between technologies</li>
                    <li>Upsurge the coverage on MTC Touch network</li>
                    <li>Diminish and resolve all customer complains.</li>
                  </ul>
                  <p><strong>[CE 2.4]</strong></p>
                  <p><b>The project Hierarchy is given below -</b></p>
