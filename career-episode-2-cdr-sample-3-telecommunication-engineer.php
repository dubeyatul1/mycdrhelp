                  <h2>Personal Engineering Activity</h2>
                  <p><strong>[CE 2.5]</strong></p>
                  <h2>Roles &amp; Responsibilities</h2>
                  <p><b>My Roles &amp; Responsibilities were -</b></p>
                  <ul>
                    <li>Project coordination with customer.</li>
                    <li>Perform 2G, 3G, 4G drive test and cluster test implying Huawei Genex Probe.</li>
                    <li>Analyzing DT logs for new sites and clusters and giving the appropriate RF approvals.</li>
                    <li>Report radio performance problems, recommend solutions and follow up with the concerned entity.</li>
                    <li>Customer complains check.</li>
                    <li>WCDMA Level 1 Certified 7 Network optimization</li>
                  </ul>
                  <p><strong>[CE 2.6]</strong></p>
                  <h2>Problems &amp; Rectifications</h2>
                  <h2>Team Not Willing to Work</h2>
                  <p>A target of 48 sites to be on air was required. Since the team leader was on sick leave so I was assigning tasks to the team and collected, analyzed data. At the end I along with the team managed to submit 53 sites. Out of these 49 sites were accepted.</p>
                  <h2>New Software</h2>
                  <p>As part of my professional approach I used to talk to my colleagues. Once upon a time the commission engineer was struggling in navigating a new software that he used to work on the full version. He was provided with a lite version called Pocket and did not included the same interface or navigation scheme. I assisted him and generated the required reports in a short period which was very helpful.</p>
                  <h2>Moving Forward</h2>
                  <p>When the security situation in my country became a concern, I decided to continue forward and travel to work overseas. The problem was my contract for a year with the company. So I negotiated the issue with my manager and was able to get a 2 month period to train and get new engineer in my place as well as able to quit the job with both side satisfied. Later on, after 2 years I received a job offer from Serta with warm greetings.</p>
                  <p><strong>[CE 2.7]</strong></p>
                  <p><b>In this project the main tasks were -</b></p>
                  <ul>
                    <li>Single Site Survey (SSV) to test any new 3G site</li>
                    <li>Cluster Survey: to test multiple site together and optimize performance</li>
                  </ul>
                  <p><b>I did following tests -</b></p>
                  <ul>
                    <li>Genex probe to perform drive Test</li>
                    <li>Genex Assistant to analyze log file</li>
                    <li>Temps pocket for indoor coverage test</li>
                  </ul>
                  <p><b>During the drive test I used -</b></p>
                  <ul>
                    <li>Scanner to test 3G frequency power</li>
                    <li>Huawei phone to test call set up</li>
                    <li>Laptop to record data</li>
                    <li>GPS to record location</li>
                    <li>Rigger tools &amp; tower climbing safety tools</li>
                    <li>Company car</li>
                  </ul>
                  <p><strong>[CE 2.8]</strong></p>
                  <p>Working with Serta broadened my view and I assessed that relation amid companies &amp; subcontractor’s mater a lot. Also, I successfully understood the hierarchical work requirement and how to address each issue separately.</p>
                  <p>Working with Serta put me in direct contact with suppliers, manager, other companies and security authorities. I was able to address each person need and got hands on experience on how to deal in case of diverse situations.</p>
                  <p>In this project my knowledge and previous colleague at Huawei made the difference. I resolved all snags &amp; glitches by calling them without the need to involve higher management in the process. This helped me outperform and change the company share from 30 % to 50 %.</p>
                  <p><strong>[CE 2.9]</strong></p>
                  <p>As most of the work I was travelling by car, the safety measures were to be considered while driving; making sure that driver is performing as required and respecting rules was the main safety concern.</p>
                  <p>Also during that time, some Lebanese area were under terrorist threat so I needed to make sure every time and contact responsible people in each area before making a visit to guarantee that no one will be in danger.</p>
                  <p><strong>[CE 2.10]</strong></p>
                  <p>A daily corner meeting was required with the team to allocate their respective tasks and start job duties. Every week the project manager asked for a briefing of worked site and their status. The evolution and work progress were made every Monday morning to follow up and provide advice or commendation.</p>
                  <p>This project encompassed of one engineer, rigger and a driver &amp; I was the team leader and I used my leadership skills to solve a strike from the other team member because of extra unpaid working hours. I kept work moving while escalating the issue to the management and I made an agreement at the end that satisfied both parties.</p>
                  <p>SSV report were the main statement to perform based on the analysis of drive test data. Beside a weekly report I was also needed to evaluate the project.</p>
                  <p><strong>[CE 2.11]</strong></p>
                  <h2>Summary</h2>
                  <p>I was the prime person in the project. I was responsible to reply to customer request and perform drive test and cluster test. I was also accountable for sending report and recommendation for each site I visited.</p>
                  <p>The engineering knowledge was best described when performing tasks. I carefully prioritized tasks and adjusted time in a way to secure a high-performance progress.</p>
                  <p><b>Learnings from the project are as below -</b></p>
                  <ul>
                    <li>Task management and prioritization</li>
                    <li>how HSPA work</li>
                    <li>how big project work</li>
                    <li>how to communicate with higher management</li>
                    <li>How to communicate with other companies</li>
                  </ul>
