                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 2.5]</strong></p>
                <h2 class="">My Roles &amp; Responsibilities</h2>
                <ul>
                  <li>Liaising with the client engineer to investigate the affected area and concluding the best method to rectify that.</li>
                  <li>Preparing a probable estimate to complete the work</li>
                  <li>Conducting the traffic impact studies and proposing the best time to work in that area</li>
                  <li>Checking if any service interruptions are occurring during the construction phase of the project applying for the authority approvals and intimating the same to the Dubai police and media after the date is finalized to carry out the work</li>
                  <li>Mobilizing the resources, traffic diversions and executing the work</li>
                  <li>Coordinating with the testing team to take the samples for the work done and submitting it in the laboratory to carry out the required tests.</li>
                  <li>Conducting a joint survey to take the exact measurements for the work done</li>
                  <li>Guiding the draughtsman to prepare the as built drawing and submitting it to the client</li>
                  <li>By giving the inputs to the quantity surveyors to prepare the invoice and submitting it to the client</li>
                </ul>
                <p><strong>[CE 2.6]</strong></p>
                <p>After gathering all the inputs required to model &amp; design the signage and based on the architectural and mechanical drawings, it was obvious for me that the free area to locate the columns is very limited and therefore the structure system should as cantilever frame not a tow hinged or tow fixed frame. Therefore, I started to model the signage using the Staad pro software, and applied the expected wind load pressure and suctions per the ASCE-05 stats.</p>
                <p>Internal parameters should be defined and applied to achieve the accurate and realistic results. All though the cantilever frame was challenging to be economical and safe, I solved the issue of using big sections to resist the bid moments, by connecting these columns with back bracing worked as ties. These ties were supported with RCC columns terminated at the roof slab level.</p>
                <p>Another issue faced in this project while designing was the displacements and deflection amounts at certain elements were exceeding the allowable as per the international codes. And therefore, I was requested to reduce these amounts to meet the code limits. And it was clear for me that deflection at end of the cantilever was higher due to own weight of the beams and bracing, and therefore, instead of adding bracing members, I decided to change the section of the bracing members with less in weight and more rigid section and that was the tube section instead of the angles. These modifications had developed the overall structure response and reduced the displacements and deflection values accordingly.</p>
                <p><strong>[CE 2.7]</strong></p>
                <p>My main role was to design a durable, safe and economic steel frame structure to perform as the main signage of the financial building, and this was including the design of the steel columns, girders, bracings and the connections between the members either bolted or welded connections. That’s in addition to the design of the concrete pedestal, by reflecting the structure max reactions to transfer them to the RCC columns, in check the demand-capacity ratio for these RCC columns to ensure that the loads transferred are within the allowable limits.</p>
                <p><strong>[CE 2.8]</strong></p>
                <p>I used AutoCAD, Staad pro V8i, ProkonV2.0 and Limcon software in this project. Being the civil structural engineer assigned for this job, I generated the takeoff quantity tool, and realized the exact weight required of each steel section considered as a part of the signage and prepared an estimation for the material cost considering the latest market steel prices to be part of the commercial offer were submitted to my line manager.</p>
                <p><strong>[CE 2.9]</strong></p>
                <p>I was coordinating the material delivery to site and installation as well therefore I was requested to initiate all the delivery notes and check the quality of the material delivered. And to handle the transaction process of the steel members by the tower crane from the store location to the roof level to proceed the installation, and this process required a huge coordination between us and the main contractor at site to include our request in the tower crane schedule, with the proper coordination with the project manager, the required materials and resources were provided without any failure.</p>
                <p><strong>[CE 2.10]</strong></p>
                <p>To deliver an economic, durable and safe steel structure frame was the main task and objective in this project. Therefore, I applied several techniques to achieve my goal, I reviewed the minimum loads design code ASCE-05 to figure out the live and wind load should be considered; therefore the realistic results been achieved. To utilize previous projects lessons to be learned also was very effective and helpful. I applied the theoretical technique while facing issues at site to run the problem inside the theoretical lube and ended up with the practical solution covering all the corresponds gabs might be found if a quick decision been taken.</p>
                <p><strong>[CE 2.11]</strong></p>
                <p>While designing the steel structure model, I developed an excel sheet calculating the wind load as per the cases defined in the ASCE code, considering the structure Hight, exposure type and the frame geometries. Also, while the execution, I developed an excel sheet reflecting the daily productivity at site, manpower, machines, materials, and losses. These daily reports were gathered to combine the weekly report should presented to my line manger to highlight any excess or delay may occur.</p>
                <p><strong>[CE 2.12]</strong></p>
                <p>Health and safety procedures were followed strictly to avoid any accident. Adequate briefing sessions were given to the labor before starting any activity, discussing the safety aspects to be taken care while carrying the work, brief discussion on the standard procedure to carry out the work (method statement).</p>
                <p>As the working conditions were at the roof level, strict safety procedures were followed, specially while the welding process.</p>
                <p>Water with extra added minerals were provided to the workers so that workers are kept hydrated all the times in extreme climatic conditions too.</p>
                <p><strong>[CE 2.13]</strong></p>
                <p>I was leading the estimation engineer, draught man, site supervisor and 5 technicians at site. I was successfully guiding them to ensure that all input information was correct and the generated outputs are accurate.</p>
                <p>I used to meet my direct manager once a week, to brief him about the project status wither at the design stage or at the installation stage. Our discussion was based on the weekly report prepared including all the outstanding projects in the pipeline. I followed the recommendations received from my direct manager, especially which included any correspondence with the client or with the authority, to ensure that the company’s rights are reserved.</p>
                <p>At the construction stage of this project, I use to join the project supervisor to check the daily productivity and the work done, and balanced. I was insisting to carry over all the site issues may appear and discuss them with my team to be resolved immediately.</p>
                <p>It was required from me to combine the design report to be submitted to the authority showing the design criteria, inputs and outputs. That’s in addition to the weekly project report, which was including the projects details and status to be discussed along with my direct manager.</p>
                <p><strong>[CE 2.14]</strong></p>
                <h2 class="sample_heading">SUMMARY</h2>
                <p>Project was successfully completed and delivered, we met the project schedule and quality planed, and even was able to provide a cost saving than the forecast estimation for about 15% savings. Therefore, we were able to achieve the project goals and objectives without any failure.</p>
                <p>I utilized my engineering knowledge and background in this project at different aspects and stages. For example, at the design stage I was able to define the proper structure system of the signage based on the information gathered from the structural and architectural drawings received. Furthermore, I was able to predict the suitable steel section and connection details before designing them, and the design outputs doesn’t vary a lot from my prediction.</p>
