                <p><strong>[CE 2.1]</strong></p>
                <p>AWRostamani Building Industries was established in the year 1954, having more than 60 years of experience in Construction Industry with the full time working staff of 3800 and an operative work force of 4500+, The Company is spread into various divisions such as construction, Building Industries, property services, facilities management, Automobiles and money exchange.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p>My employer was requested to design, fabricate and build the steel structure frame for the main signage of the building located on the roof level. The height of the building was 36.4m, the signage should resist the wind load would affect at this height, in addition to the self-weight and the aluminum weight considering the dynamic load may affect at this level as per the authority regulations.</p>
                <p>Our company was requested to provide the full designed frame, authority approval and installation within three months as per the project schedule planed, in order to let the cladding company, proceed their work and finish the external façade.</p>
                <p>Finishing the work in this quit short period was a challenge, in addition to the design challenge for a signage 7.2m height, 3.5 width and 20m length, to be erected on a free base area equal to 1.2 m width.</p>
                <p><strong>[CE 2.3]</strong></p>
                <p>Before casting the roof slab at the above-mentioned project, we were requested to provide the full details of the steel frame signage, the concrete pedestals details and anchorage base plate details in order to be inserted before the concrete casting. Therefore, my role started at this level, I visited the site and gathered all the information required for the building exposure category, studying the drawings, meeting the project manager and discuss all the issues were required to obtain the authority approval at the earliest.</p>
                <p><strong>[CE 2.4]</strong></p>
                <p><b>The Hierarchy of the project is shown below:</b></p>
