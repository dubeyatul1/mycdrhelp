                     <h2 class="sample_heading">C)Personal Engineering Activity</h2>
                      <p><strong>[CE 2.7] </strong>I was assigned to perform testing of piles in this project.</p>
                      <ul>
                        <li><b>Pile load testing - </b>I performed the test for confirming the design load calculations and provided guidelines for accepting the routine tests. It also provided the piling system suitability. I carried out the initial testing on piles at locations that were dependent on the pile's number needed.</li>
                        <li><b>Static Pile load testing -</b> I conducted the static load tests for the pile response measurement under an applied load for the determination of the pile capacities. I determined the ultimate failure foundation pile and determined the capacity for supporting the load without continuous displacement. I verified the test for the load capacity in the constructed pile which was higher than the nominal resistance.</li>
                      </ul>
                      <p><strong>[CE 2.8] </strong>I did the calculation of the quantity of material, cost for performing jobs. I executed the bar bending schedule. I worked on the bills for subcontractors. I did the calculation of loads for pile testing. I also conducted the load settlement calculation and curve. I implemented the time load settlement curve. I obtained the value of ‘n’ in the standard penetration test. I used the excavators for excavating soil in the pile cap area. I used DMC piling machine for boring piles. I selected compactors for compacting the earth. I selected the concrete pumps for transferring the concrete to different areas. I selected the concrete transit mixer for carrying out the concrete from batching plant to site. I applied loader to load excavated earth to trucks. I implemented the PDA for the measurement of the dynamic pile response. I used the bar bending machine for bending the steel.</p>
                      <p><strong>[CE 2.9] </strong>The main difficulty involved during the project was the excavation of the land for the pile cap. Since the land was nearby the backwater the water seepage was high during rainy days and high tide. During the time it was heavily raining since Kerala’s climate was mainly wet and maritime tropical seasonal downpours happen. Excavated area soil consisted of clayey silt / sandy silt and the stability of slope was very poor. Water seepage destabilized the slope. For dewatering the seepage water in the excavated area made the work delay every morning. As I had to find an economical solution and I referred to many journals. The piles on the land had steel casings up to ground level, which was cut till the top of the plain concrete level of the pile cap. These casings were sent as scraps from the project. I took permission from the project manager to use these scraps to protect the destabilized soil. The thickness of the casing was 10mm. I used these scrap casings and bent it with the help of the excavator to straighten like a sheet. I took the help of labors and excavators to insert the sheet into the soil. I placed the steel beams which were inserted in front of the sheet to support the sheet and soil.</p>
                      <p><strong>[CE 2.10] </strong>I designed a dewatering system for the excavated area by excavating a pit and made a channel throughout the periphery of an excavated area with the help of labor sloping towards the excavated pit, so that the water will be the collected in the pit. I installed one 5 HP and one 3HP pumps to dewater and assigned one labor to check the whole system and water level in pit. By doing this design I saved a lot of time and work was more productive.</p>
                      <p><strong>[CE 2.11] </strong>I got the chance to perform pile testing in my company before in other projects tests were given subcontract. Later, I was given training for engineers and staff in other projects. This in house testing saved huge money. By referring to the internet and journals, I got the knowledge and idea of doing pile load tests independently. I also managed the manpower and equipment from colleagues. The next main problem was the disposal of bentonite slurry. The bentonite used for DMC piling must be disposed of carefully and properly to avoid environmental issues. For this, management has given strict instructions to the subcontractor to carry and dispose of. They have contacted waste disposal agency and they cleared every week from bentonite slurry tank. I confirmed that it was not mixing with any drainage system or circulation with other water.</p>
                      <p><strong>[CE 2.12] </strong>Safety was given high priority at the workplace. I made sure that the working at height was done while wearing a safety harness. I also addressed the environmental issue as the lake was near to the project, there were strict instructions from the management to protect the environment. I did not throw any waste into the backwaters. For that, I have given clear instructions to all the staffs and labors during site induction training. I attended weekly progress meetings with the deputy project manager. I reported to senior engineers and construction managers daily. I conducted the daily work based instructions and interactions with colleagues, supervisors, and foreman.</p>
