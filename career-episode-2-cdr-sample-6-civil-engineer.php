                     <h2 class="sample_heading">B) Background</h2>
                      <p><strong>[CE 2.2]</strong>PRS Lake Ville at Trivandrum, India is a multistory Apartment near Akullam Lake owned by the PRS group. A total of 2 years and 3 months was taken to complete the project. PRS Lake Ville is a luxury lakefront G+17 story building with 1 basement. ADS Associates was the main contractor for the project.</p>
