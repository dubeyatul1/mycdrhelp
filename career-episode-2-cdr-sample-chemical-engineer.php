               <p><strong>[CE 2.6]</strong></p>
               <p>The project was aimed at developing a crude separation system for a client operating a FPSO in the Wahoo field. It was at the proposal and project bidding stage which encompassed activities such as preparing process flow diagrams (PFDs) for the topside of the FPSO as well as simulating the design on ASPEN HYSYS. It also involved weight sizing for the equipment and pipeline sizing.</p>
               <p>Development of PFDs, HYSYS simulation of the design, equipment sizing, weight sizing, process design and unit operation, process and instrumentation control, plant process and maintenance, process analysis, production planning, computer applications, HSE, and research &amp; development.</p>
               <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
               <p><strong>[CE 2.7]</strong></p>
               <p>I did the Project management of the system which involved the modularization of the project in to definite number of stages &amp; created a sequence of the activities to be performed. I planned the entire project in two stages.</p>
               <p><b>PHASE 1: </b>During this phase I completed the study of the conceptual as well as philosophical foundations of the proposed work &amp; drew up a strategy to achieve the desired goad.</p>
               <p><b>PHASE 2: </b>In this phase; implementation of the strategy was done. After implementation the testing of the whole system was carried out.</p>
               <p><strong>[CE 2.8]</strong></p>
               <p>I along with my team carried our vast research on the project and studied tons of material over Internet &amp; through libraray books &amp; journals. I went through all the latest developemnts in order to have an overview of the project requirements.</p>
               <p><strong>[CE 2.9]</strong></p>
               <p><b>My Roles &amp; Responsibilities were -</b></p>
               <ul>
                 <li>Developing the Process Flow Diagrams (PFDs) for the system of the topside. This was based on certain specifications prescribed by the client.</li>
                 <li>Meeting the client with my manager to discuss the feasibility of the several proposed PFDs and implementing the client’s recommendation into a final PFD and accordingly seeking their approval.</li>
                 <li>Conducting stage two simulations of the system using ASPEN HYSYS</li>
                 <li>Processing the simulation data such capacity, temperature, density and pressure in order to facilitate the sizing design procedures of the separator and scrubber.</li>
                 <li>Researching for vendors that can do the sizing of the heat exchanger, pumps and compressors within allocated deadlines and the client’s standards.</li>
                 <li>Conducting the line sizing using the company’s templated of Microsoft Excel Sheets that are designed based on API and PTS (Petronas Technical Standards).</li>
                 <li>Performing weight sizing via the provided templated by entering data such as the size of the vessel and the class of material to estimate the weight of the vessel needed.</li>
               </ul>
               <p><strong>[CE 2.10]</strong></p>
               <p>The first issue was during the first meeting with the client. The client declined the proposed designs commenting that the system was not meeting the weight requirement. This required me to change the line sizing. To accomplish that, I sought insights from my manager on how to achieve this within the designated deadline and requirements advised by the client. I had to run the sizing process a couple of time using predefined spreadsheets prepared by the company’s engineers. However, this required thorough and in-depth reading of materials related to sizing so as to achieve the desired pressure dropdown asked for by the client in accordance to their set technical standards.</p>
               <p><strong>[CE 2.11]</strong></p>
               <p>After thorough understanding of the components that crude separator system comprises of; I designed the Process Flow Diagram (PFD) which included a production header, slug catcher, test separator, crude stabilizer, crude dehydrator, produced water treatment, gas dehydration system, and high pressure gas compression system. The design served as an input for the ASPEN HYSYS simulation to determine the working conditions for each component in the system. The adopted method for designing the PFD was with the aid of previous PFD designs from the company’s database and I tested the feasibility of the design on the HYSYS simulation. The software used to come up with the PFD was AutoCAD.</p>
               <p><strong>[CE 2.12]</strong></p>
               <p>I worked on designing the length and diameter of the pipeline. This was achieved using existing templates by altering the class of material used and the lengths to achieve the desired pressure dropdown. The templates adopt Microsoft Excel Spreadsheets designed by the company’s engineers.</p>
               <p><strong>[CE 2.13]</strong></p>
               <p>I performed the ASPEN HYSYS simulation in order to test the proposed PFD design with the operating conditions provided by the client. I performed two simulations. The first was Max Oil Case which had a flow rate of 50,000 BOPD, 15,000 BWPD, and 70 MMSCFD of gas. The second was Max Water Case which had a flow rate of 20,000 BOPD, 45,000 BWBP, and 70 MMSCFD of gas.</p>
               <p><strong>[CE 2.14]</strong></p>
               <p>I arranged meeting every week discussing status of the upcoming works, remaining clearances and resources allocation. Taking an essence of the previous completed works and made prior documentation for approval from authorities in order to perform seamless project proceedings.</p>
               <p><strong>[CE 2.15]</strong></p>
               <p>I was also accountable for the groundwork of feasibility study which included technical &amp; economic consideration of this project. I calculated the whole project cost scrutiny and presented it to the authorities as well. My feasibility report was given weightage and it was used to finalize the approach towards project execution.</p>
               <p><strong>[CE 2.16]</strong></p>
               <h2 class="sample_heading">SUMMARY</h2>
               <p>This project was a good chance to develop my knowledge regarding Crude Separation System and deepen my expertise in the design of Separation Systems. Moreover, I have this chance to excel my planning, analysing and execution skills. This project has elevated my project management, planning, analysis and implementation skills. The crude separation system was successfully implemented without any delay in project envisioned deadline. My role in every aspect of project was highly admired by the executives of the company.</p> 
