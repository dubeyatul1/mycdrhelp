               <p><strong>[CE 2.1]</strong></p>
               <p>This career episode is about my project titled “Development of Crude Separation System for Wahoo Field FPSO’’. This project was carried out during my tenure at NGL Tech as an Internee Engineer from ……………to ……………… .</p>
               <h2 class="sample_heading">BACKGROUND</h2>
               <p><strong>[CE 2.2]</strong></p>
               <p>Nowadays most of the companies use FPSOs for their production facilities. One of the most important parts of the production facility is the inlet separation system. This system separates oil, water and gas from the crude. As soon as they are separated, the products are stored in a storage facility. To make sure that the carry over liquid in the gas is low and vice versa, an efficient inlet separation system is needed. This system should have the ability to get rid of the water from the crude. The Wahoo field was discovered in Campos Basin by APC (Anadarko Petroleum Corporation). In order to develop the field, the option of an FPSO with a wet tree is considered. Enough drilling has been done to follow the defining of this concept for the support development by the host facility. This local host facility needs to support several wet trees for the production wells in vicinity of the floater. It will also support risers for the satellite wells of the subsea at remote locations from the host. Produced oil will be stored in the FPSO then exported through the shuttle tankers by offloading. Depth of the water around the prospect ranges between 4430 and 5085 feet. For the floaters they will be located at the depth of 4650 feet.</p>
               <p><strong>[CE 2.3]</strong></p>
               <p>This field is expected to produce for at least seven years. Thus, the FPSO will be designed for 15 years without the dry docking as it will not be needed. The facilities such as piping systems and tanks will be also designed for the span of 15 years with a few routine inspections and maintenance.</p>
               <p><strong>[CE 2.4]</strong></p>
               <p>During my tenure at NGL Tech Sdn Bhd which lasted for 8 months from the period September 2014 to April 2015, I took part in several projects for numerous clients. However, I was required by Universiti Teknologi PETRONAS to produce a wide-ranging report on one major project for partial fulfillment of the Student Industrial Project course for my bachelor’s degree.</p>
               <p><strong>[CE 2.5]</strong></p>
               <p><b>The project hierarchy is given below -</b></p>
