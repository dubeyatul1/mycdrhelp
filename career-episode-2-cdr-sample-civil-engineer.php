                <p>This career episode relates to project titled “Ministry of Interior Branch Building’’. This project was carried out during my tenure at AL ALSHAL Contracting &amp; Engineering Works as Project Manager.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p>The ministry of interior branches over the Kingdom of Saudi Arabia has witnessed an increase in the number of employee with no enough places in the buildings available for them. Qassim branch was one of these branches faced that problem. Consequently, there was a need for building new facilities or adding offices for the existing buildings.</p>
                <p><strong>[CE 2.3]</strong></p>
                <p>There was an old building, it was used as a prison in the past and it was exactly beside the branch building of the ministry. The goal was to construct a 3-story building on 2800 m2 area of land and connect it with a resistance building of 5000 m2 and repair the old one and redesign it internally in order to transfer it from prison to offices for new employees and off course connecting these two parts with the main branch building architecturally and on other aspects i.e. power &amp; data etc.</p>
                <p><strong>[CE 2.4]</strong></p>
                <p><b>The project Hierarchy is given below -</b></p>
                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 2.5]</strong></p>
                <p>I was entirely responsible for the management of the project at each phase. In order to make this project easily executable and for being completed in stipulated time; I divided the project into multiple phases. In the first phase I studied all the basic scenarios of the project and drafted the phase completion sheet for proper project execution. In the final stage I employed professional strategies for effective completion of the project.</p>
                <p><strong>[CE 2.6]</strong></p>
                <p><b>My Roles &amp; Responsibilities -</b></p>
                <ul>
                  <li>Studying all project documents and preparing the final proposal for the Ministry of Interior in order to win the project.</li>
                  <li>Make price analysis and choose subcontractors for some items of the project.</li>
                  <li>Reading original drawings and check the designed items and make changes when needed.</li>
                  <li>Making structural designs for the new building (Concrete foundations, Columns and slab).</li>
                  <li>Applying the drawings and designs to reality</li>
