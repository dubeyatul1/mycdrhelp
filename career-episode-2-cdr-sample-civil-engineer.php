                <p><strong>[CE 2.7]</strong></p>
                <h2 class="sample_heading">PROBLEM FACED &amp; THEIR RECTIFUCATIONS</h2>
                <h2 class="sample_heading">Problem A</h2>
                <p><b>Problem Definition: </b>The new building and the old prison should be connected to the main branch building as one piece by an external pass where they all have access. The problem was that by depending on that path as a connector we had to deal with an existing reinforced concrete wall on the edge between the branch building and the prison going through the pass and there was no agreement for removing the same.</p>
                <p><b>Effect of problem: </b>Apart from the delay in the project waiting for the ministry to agree eliminating the wall, using this pass as a connector we would have to install our pipe lines for electricity, data, and mechanical work through the wall all along the path which could have cost us over 50% more than the anticipated calculations.</p>
                <p><b>Root Cause of Problem: </b>Reinforced concrete wall found on the path and no acceptance to remove it or even any part of it.</p>
                <p><b>Final Solution: </b>Instead of using the proposed pass to connect the buildings, I suggested to connect them directly from the middle by some internal walkthroughs. This will make us avoid removing the wall in addition to a remarkable save in cost for the electromechanical work.</p>
                <h2 class="sample_heading">Problem B:</h2>
                <p>Applying the solution stated above created us another problem summarized in decreasing area due to the internal pass and redesigning the offices.</p>
                <p><b>Effect of problem: </b>A notable proportion of the area was lost in the new pass (exactly 180 m2). Because of this, the offices should either be decreased in area or in number.</p>
                <p><b>Root Cause of Problem: </b>Adding internal pass bonding buildings in the middle instead of the old outside one.</p>
                <p><b>Final Solution: </b>I insisted in Continuing with the new pass in the middle and combine the outer pass to the buildings area by installing steel columns on reinforced concrete foundations and connect the columns with buildings by fixed main girders connects steel columns with the reinforced concrete columns of the building.</p>
                <p><strong>[CE 2.8]</strong></p>
                <p>I designed reinforced concrete elements of the new building. Reinforced concrete foundations, columns, Grade beams, Slabs. The objective was to obtain safe design with the least cost as possible and I was able to fulfill that. I used the original drawings from the Ministry and made some changes using AutoCAD to add the new path and other changes related to actual dimensions in the site. After that I loaded the drawing to SAP2000 and defined items, sections, materials then applied loads and ran the program then obtaining analysis results which I used in design the items sections using a prepared excel chat for designing theses concrete elements using the WORKING loads for design.</p>
