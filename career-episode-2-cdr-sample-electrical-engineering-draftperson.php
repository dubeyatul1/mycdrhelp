                      <p><strong>[CE 2.1]</strong></p>
                      <p>This career episode relates to project titled <strong>“EGA Cloud Transformation’’.</strong> The project was done during my tenure with <strong>Emirates Global Aluminum.</strong></p>
                      <h2>Background</h2>
                      <p><strong>[CE 2.2]</strong></p>
                      <p>EGA is an Aluminum Smelter company and has been investing heavily in technology. Their core business system is state-of-the-art smelter technology which manages the end-to-end process. They have SAP on backend and many peripheral systems supporting the proc=duction and supply. The client wanted to move their backend and non-core IT onto a cost-effective environment. My company was invited to support them on this initiative by conducting a comprehensive consultative assessment of AS IS and build a blue-print for TO-BE hybrid involving cloud platform.</p>
                      <p><strong>[CE 2.3]</strong></p>
                      <p>The key objectives of this project were studying and mapping complete existing IT architecture and developing transformation readiness toolkit by building roadmaps and cloud migration templates.</p>
                      <p><b>The complete process included: </b></p>
                      <ul>
                        <li>Comprehensive analysis of their existing software and hardware, thereby mapping each application and databases across the organization</li>
                        <li>Developing the business plan for future state cloud model with right checks and balances</li>
                        <li>Envision the migration and develop gap and impact both to business and applications with regards to the infrastructure change process.</li>
                      </ul>
                      <p><strong>[CE 2.4]</strong></p>
                      <p>The ICT project awarded to my company was turnkey and encompassed both infrastructure setup and system integration.</p>
                      <p><b>The project Hierarchy is given below -</b></p>
                      <img src="images/PE-2-4.png" alt="Professional Engineer CDR report" />
                      <h2>Personal Engineering Activity</h2>
                      <p><strong>[CE 2.5]</strong></p>
                      <p><b>My Roles &amp; Responsibilities</b></p>
                      <ul>
                        <li>My main responsibility was leading the complete assessment as required to achieve the final conclusive outcome for client. This included preparing the high-level plan and approach inclusive of templates, activities and information gathering techniques</li>
                        <li>I was responsible for conducting the high-level feasibility study, preparing the organization readiness, setting up the approach for analysis; gap analysis and impact analysis with risk mitigation plans associated with the infrastructure.</li>
                        <li>Complete ownership of the stake-holder discussions, auditing the BA work being done and documenting key adoption framework for EGA</li>
                        <li>Individual responsibility was eliciting business need for cloud adoption and developing PoC plans for client next steps</li>
                        <li>Monitoring the BA work being done by team and ownership of complete documentation of the project</li>
                        <li>Leading the team of 2 in the complete project; monitoring, reviewing, supporting team in requirement capturing and mapping business with technology.</li>
                      </ul>
                      <p><strong>[CE 2.6]</strong></p>
                      <p>The primary problem faced was client’s education and awareness of Cloud Infrastructure – the actual benefits and redundancies they will face when working the current systems on the cloud.</p>
                      <p>My key objective was to encourage the client to understand and map their compute need, how many servers, databases they will need on the non-core IT and what the technology shift will mean to them. I had to conduct several workshops along the way to justify what our requirement gathering yielded. Being a cloud enthusiast and certified on Azure cloud also helped in simplifying the use-case and dealing with the myths the teams had. E.g. how will the home-grown logistics system work if moved from on-premise to cloud, will they face connectivity and what redundancies can they expect etc.</p>
                      <p>The second problem was the requirement gathering process itself. The client was ambiguous on how many products and services will go onto the cloud and what is their test plan for successful outcome.</p>
                      <p>I had to spend significant time setting clear expectation within the team and stakeholders on the process and outcome each one is expecting out of this project. The steps I took were, (a) first was to elicit the expectations and map them to technology needs for each stake-holder; thus get a buy-in for the tasks we will be performing (b) Modify the standard framework to suit EGA, thus creating a ownership among take-holders to move forward. (c) conduct workshops within the teams to develop appropriate use-case to the transformation itself. I also developed the cloud matrix during the initial phase became the base for all requirement gathering sessions.</p>
                      <p>The third problem was the typical one I have faced, namely lack of documentation, there were a number of in-house applications and business users were scattered; thus, due to the tight timelines, my team was not able to capture and map appropriately.</p>
                      <p>The solution I adopted was to revisit the individual documentation early on and ensure the team spent time capturing required information details. In any issues they would face, I as to be immediately approached to resolve the issues and get necessary support from diverse client’s team. I also took measures to put in assumptions and got sign-off from the SPOC from the client’s end.</p>
                      <p><strong>[CE 2.7]</strong></p>
                      <p>I designed the migration chart (detailed) along with risks and impacts for the client when they start the next steps. Most documents were as per standard and small modifications were required.</p>
                      <p><strong>[CE 2.8]</strong></p>
                      <p>I worked closely with EGA project manager in calculating the cost for moving to the cloud infrastructure with multiple scenarios. While the tools are easy to use, the ownership lied on us to showcase the actual cost effectiveness of moving to cloud.</p>
                      <p>I used MS Office, Project and Visio mainly for developing the functional mapping, documentation, design, and communication.</p>
                      <p>As I was leading this project, all presentations to stake-holders were given by me. I also gave presentations on our findings, gap and impact analysis. I had multiple reports to deliver on daily basis, weekly basis and monthly. The key reports were on the project timelines and observations on impacting the transformation. As part of this project we had weekly review meetings on project status, monthly reviews and I was part of executive meetings as well. All these are standard part of governance procedure in such projects.</p>
                      <p><strong>[CE 2.9]</strong></p>
                      <p>This project involved moving some components of SAP to the cloud. While the current system was on-premise, we had to create a futuristic plan for scalability and elasticity if HANA was adopted.</p>
                      <p>SAP HANA requires high compute power from the infrastructure, which the client was keen to adopt. I had to spend considerable time with the SAP product team to map the same. This was an excellent learning for me as well.</p>
                      <p>I introduced a comprehensive mind-map for the client’s roadmap after the requirement gathering session was concluded; This had complete infrastructure components and flags at various levels of work breakdown. This gave all of us a complete picture of the architecture to be moved from a simplified view-point. The same was showcased to the management.</p>
                      <p><strong>[CE 2.10]</strong></p>
                      <h2>Summary</h2>
                      <p>The project was successfully completed on time. It met all necessary goals set internally as well as from the client.</p>
                      <p>I have had the best opportunity with this project to demonstrate my skills as BA while also exhibiting my leadership in successfully delivering the project. I led the project from the start and owned every stage starting from scope sign-off to conclusive presentation of our assessment. The significance of my role came into effect post the project. The roadmap which I had built was successfully adopted by the client and post the project, my advise was sought many times for evaluating the cloud partners and impact analysis which I had shared earlier with them. Today the client has witnessed 20% cost reduction in their 1st year of cloud journey.</p>
