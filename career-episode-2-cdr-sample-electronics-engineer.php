                <p>The project of “IR based Underwater Communication System” was one of the projects I had worked on during my final year. This was also a group project, and our team comprised of three students in total. We used 8051 microcontroller in this project as well. This project helped me to understand communication engineering in more depth. My team and I were guided by our professors during each stage of this project.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p>The success of “Door Locking System” was a major boost to my morale, and I gained a lot of confidence in my technical skills. Coincidentally, I had to prepare a group project during my final year and so I and my team members decided to work on this project. Another reason to choose this particular project was that I had developed interest in the field of communication engineering in the last few years. Therefore, this project was a good opportunity for me to explore the field. We discussed the feasibility of the project with our teacher and started off with it.</p>
                <p><strong>[CE 2.3]</strong></p>
                <p>We got a positive feedback from our professors, after our meeting. They were willing to help us during the project, if required. By that time, we had enough clarity of what we had to do. Next step was to plan of how to do it. We divided the tasks amongst oursel.<br/> <b>My major tasks during this project were: </b></p>
                <ul>
                  <li>Identifying the various milestones to be achieved during project.</li>
                  <li>To carry out industrial survey and internet research.</li>
                  <li>To arrange the hardware to be used in the project and to have a rough estimate of the total cost of the project.</li>
                  <li>Hardware assembly of the project.</li>
                  <li>Programming the hardware for desired working.</li>
                  <li>Testing the project (both hardware and software) for any errors.</li>
                  <li>Preparing reports and final draft to be presented before the panel and our instructor.</li>
                </ul>
                <h2 class="sample_heading">PERSONAL ACTIVITY AREA</h2>
                <p><strong>[CE 2.4]</strong></p>
                <p>In the initial phase, I called a group meeting and discussed my ideas with my team members, which were accepted by everyone. I made a plan of how to approach this project; I created multiple milestones to be accomplished and divided the tasks amongst ourselves such that everyone got to do what they were comfortable in doing. Since the idea of update reports had been useful during our previous projects, I decided to do the same in this project as well.</p>
                <p><strong>[CE 2.5]</strong></p>
                <p>Internet search and industrial survey was my first task in the project for which I browsed various websites, read numerous books, visited local industries and vendors, and consulted my professor and seniors. Each day of research proved effective and I soon finalized the hardware that was to be used in the project.</p>
                <p>[CE 2.6]
                <p>Through my research, I chose the hardware that was both economical and efficient; performance wise. The main component used was 8051 microcontroller along with capacitors, diodes, and other passive components. <br/><b>The major components of the project were:</b></p>
                <ul>
                  <li>8051 microcontroller IC (AT89C51) (Fig. 6)</li>
                  <li>Diodes and transformer for rectifier circuit</li>
                  <li>Regulator IC (IC 7805)</li>
                  <li>IR Led (Sensor) (Fig. 7)</li>
                  <li>LCD display</li>
                  <li>Computer Keyboard</li>
                  <li>Passive Components like resistors, capacitors, etc. of varied values</li>
                  <li>Connecting wires</li>
               
                </ul>
                <p>The total cost of the project was estimated to be Rs. 3000. I compiled together all the research done by my team, the components, estimated the cost of the project and drafted it into the first update report for my project supervisor.</p>
                <p><strong>[CE 2.7]</strong></p>
                <p>Before the hardware assembly of the circuit, it was important to understand it’s working. For better understanding, I divided the circuit into various parts that are visible in the block diagram in the fig. 8 below.</p>
                <p><b>The major parts of the circuit were: </b></p>
                <ul>
                  <li><b>Power Supply: </b>The Power Supply contained of 220V AC power from the mains, the bridge rectifier circuit, and the regulator IC 7805. The AC supply was stepped down from 220V to 12V via the transformer, then changed to DC via the bridge rectifier circuit, and then further changed to 5V DC power supply using capacitor filter and IC 7805 circuit. It is then used to power the various parts of the circuit as seen in the diagram.</li>
                  <li><b>IR sensor: </b>These were used to send and receive the IR signals and played an important role in demonstrating underwater communication.</li>
                  <li><b>Keyboard: </b>It was used to enter the message to be transmitted. This project used normal computer keyboards.</li>
                  <li><b>Display: </b>The LCD was used to display the messages to be sent and all types of acknowledgements and error messages. This project used 16x2 LCD.</li>
                  <li><b>Control Section: </b>8051 microcontroller was used to control diverse parts of the circuit.</li>
                </ul>
                <p>The working was as follows: The transmitter keyboard was used to enter the messages to be sent. This message was sent to transmitter side 8051 that transmitted it via IR transmitter. The IR receiver of the receiver circuit received the signals and sent it to receiver’s 8051. The receiver side then displayed the message on it’s LCD along with it’s acknowledgement. In case the message was not sent error messages were displayed on LCD.</p>
                <p><strong>[CE 2.8]</strong></p>
                <p>The next step included the hardware assembly of the circuit. For this I referred to block diagram (fig. 8) while making connections. I connected the LCD in a 4-bit mode with 8051. The lower three pins of the Port1 of 8051 were connected with RS, RW and EN pin of the LCD whereas the upper four pins were used for sending data (D4-D7). The IR sensors were interfaced via Port2 of 8051 and the keyboard was connected via USB controller that was interfaced via Port0 of 8051.</p>
                <p>I completed the hardware connections. I took care of all due precautions during the assembly, for example, correct orientation of the component, grounding all the components, checking for loose connections, etc.</p>
                <p>Once the hardware was ready, I prepared a second update report that summarized all the work done till then and was submitted to our professor. He was happy with the progress and further encouraged us to work on the project. Then I started working on the program code of the hardware using Kiel Uvision software (fig. 9).</p>
                <p><strong>[CE 2.9]</strong></p>
                <p>I used C programming language for programming the hardware. I created many functions according to the component requirements and called them in function void main as per the requirements. Example, void delay was used for adding delay in the code, void cmd and void data were used for command and data register settings of 8051, void initialize was used to initialize the LCD, etc. All these functions were compiled together into a single C program and then converted into Hex file. This Hex file was then burned into 8051 via appropriate software and programming board and cable.</p>
