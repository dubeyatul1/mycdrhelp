                <p><strong>[CE 2.10]</strong></p>
                <p>For testing, I arranged the two circuits in a way that they were lined together with two water beakers in between. Now, if the message typed was successfully received, an acknowledgement signal was sent by the receiving side and displayed on the LCD, else, “message failure” was displayed on the sender side. So, I first kept the two water beakers in between such that they obstructed the Line of Sight (LoS) of IR communication (explained in fig. 10). In this case, no message was received. When the error was rectified, i.e. the circuits and the beakers were correctly placed, messages were successfully sent and displayed. Thus, the project successfully demonstrated the concept of underwater communication.</p>
                <p><strong>[CE 2.11]</strong></p>
                <p>As a part of the project, I prepared one final draft summarizing the various milestones achieved by us during this project, their pros, cons, future scope, improvements, and what I had learned during the project. My leadership qualities were highly praised by my project supervisor. My efforts were highly appreciated and my team was able to secure good marks in this project.</p>
