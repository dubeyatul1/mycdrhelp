                <p>The SAP Plant produces Super Absorbent Polymer. This polymer has the unique capability of retaining water and has wide area of applications. The Polymer produced at SAP Plant was the main component in the manufacture of baby diapers and had the capability to retain water 70 times its own of volume.</p>
