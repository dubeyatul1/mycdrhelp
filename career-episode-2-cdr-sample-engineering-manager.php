                <p>During the execution, I had identified the level risk for the project work and updated to the stakeholders about the project work. As the Job Site was a congested space, an 8 story high closed building with process piping and equipment from the ground floor to the top floor, the impact of the risk would be a Safety Concern and due to limited spaced of barging in outside equipment, the construction contractor may inflate the cost and length the project schedule as a part of his contingency plant. I came in conclusion that the risk of congested job site could be overcome by alignment with ISD (Industrial Security Department) along with the Plant owner, so that Permits would be issued in a timely manner as well I recommended engaging class A contractor, that had demonstrated executing technically challenging projects in the past. I also observed that lengthy EPC tendering and Award process increased the risk of the project by resulting in a delay. I decided to conduct close follow up with PMT organization so that the EPC package can be prepared early on, and start the selection and solicitation of EPC Contractors prior to the completion of BEP (Basic Engineering Package), this helped to mitigate the risk of lengthy EPC tendering process.</p>
