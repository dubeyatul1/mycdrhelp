                <p><strong>[CE 2.18]</strong></p>
                <p>During the implementation phase of the project, I had faced the challenge of increased particle size of the AGM7 project, which was exceeding the limit of 630 µm. It was assumed that the mesh sizes of the sieves defining the quality AGM7 are identical to those of AGM6, mainly 200 µm and 630 µm. This would result in less fines and higher amount in the target range, but this was not considered in the initial stage. From several points of the plants, mainly before and behind mills and sieves, samples were taken and the particle size distribution (PSD) was measured and the problem faced. I proposed to make use of the roller mills and sieves behind the overs dryer. The dryer itself should be operated at mild drying conditions to avoid condensation effects. The fraction bigger than 630 µm coming from the sieves of the second roller mill should be given to the overs dryer and been milled and sieved there, hence achieving the desired result.</p>
