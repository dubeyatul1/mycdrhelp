               <h2>Background</h2>
               <p><strong>[CE 2.2]</strong></p>
               <p>Since 2011, distributed storage became more and more popular in the storage market. Compared with classical storage products, it stores data on a multitude of standard machines, which behave as one storage machine although data is distributed between these machines. It provides a better cost-performance ratio, flexibility, and scalability than those of classic storage. Unfortunately, at that time Huawei did not offer any distributed storage solution. Huawei would not miss the huge business opportunities to access the market of distributed storage. Therefore, a project was set up to deliver a distributed storage solution for one very important customer.</p>
               <p><strong>[CE 2.3]</strong></p>
               <p>The overall aim of the project is to deliver a competitive distributed storage solution in the technical, environmental, commercial, and legal contexts. The detailed objectives of the project are provided as follows:</p>
               <ul>
                 <li>To identify requirements of distributed storage in the contemporary market.</li>
                 <li>To investigate competitors’ solutions to understand their pros and cons.</li>
                 <li>To define a competitive distributed storage solution.</li>
                 <li>To design, develop, build, testing, and publish a distributed storage solution to the global market.</li>
               </ul>
               <p>To make the solution commercially successful</p>
               <p><strong>[CE 2.4]</strong></p>
               <p>The project nature was the implementation of the design of distributed storage solution which was achieved with my engineering skills at Huawei Technologies Co. Ltd., China.</p>
               <p><strong>[CE 2.5]</strong></p>
               <p><strong>The following chart illustrates the organizational structure highlighting my position during the project.</strong></p>
