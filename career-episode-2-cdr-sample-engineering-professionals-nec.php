               <h2>Personal Engineering Activity</h2>
               <p><strong>[CE 2.7]</strong></p>
               <p>I initiated the work with the utilization of the UML to describe the requirements of the storage system in the process of business management. In detail, I drew the UML use case diagram to model the functionality of the coming storage solution using actors and use cases. Actors were the users, and use cases represented the functions. I used Jira as a tool to manage requirements. I created a Jira issue type specifically for requirements. From the customers' perspectives, I noted that there was a need for a storage solution which could offer better cost-performance ratio, flexibility, and scalability. From a technical point of view, a distributed storage solution was a great answer to them. Unlike classic storage techniques, distributed technologies are applied to storage industry for integrating computing and storage capacity of standard machines into one whole super machine instead of expensive and dedicated metal boxes made by some specific vendors. Customers needed to expand the capacity and performance as demanded. It was the core requirement of almost 85 percent of our potential customers.</p>
               <p><strong>[CE 2.8]</strong></p>
               <p>I kept abreast of distributed system development in the storage industry. I worked on the development of the multiple systems network sharing in different locations. I kept learning distributed technology and its practice in the storage industry for many years. The learning materials of the subject were not very rich, so I organized and performed the search process in a structured and pre-planned way. I determined the best and most relevant keywords to cover the different aspects of the distributed system. In detail, I considered all possible synonyms, close synonyms, relevant terms, narrower or broader terms, antonyms, and abbreviations, grammatical or linguistic variations of my keywords.</p>
               <p><strong>[CE 2.9]</strong></p>
               <p>I learned advanced knowledge and effective engineering methodology from professional colleagues across broad-ranging disciplines in the in-house seminars of Huawei. The topics of the seminars were related to industrial design, telecommunication, environmental engineering, methodology, business management, design pattern, legal compliance, etc. I gradually understood the storage industry should not exist in isolation. It must be integrated with other disciplines appropriately. I especially listened to technology lectures of distributed system fundamentals given by outside professionals and experts who were invited to the technical meeting of Huawei. I was responsible for the requirements on flexibility and scalability of the planned storage solution. Based on my understanding of distributed consensus algorithm, I proposed a cluster monitoring system (i.e. monitor) to solve the issues. With the support of the monitor, I expanded or shrunk the cluster by easily importing or exporting servers.</p>
               <p><strong>[CE 2.10]</strong></p>
               <p>Problem: The core problem was that all servers in a cluster must reach a consensus on the expansion or shrinkage.</p>
               <p>Solution: I introduced a consensus algorithm named Paxos to the design solution of the monitor. When a server leaves the cluster, the leading server of the cluster initiated a process of agreeing on the quit among a group of servers in the cluster. This was referred to a series of complex interactions between servers as shown below.</p>
