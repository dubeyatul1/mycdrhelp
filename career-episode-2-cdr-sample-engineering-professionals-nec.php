               <p><strong>[CE 2.6]</strong></p>
               <p>I led a project team with three engineers to deliver the necessary systems. Those systems would be integrated with other systems developed by other teams into the whole distributed storage solution. My duties in the project were presented as follows:</p>
               <ul>
                 <li>I clarified the requirements with commissioners.</li>
                 <li>I defined necessary systems which can make the new generation distributed storage solution competitive in the technical, environmental, and commercial contexts.</li>
                 <li>I managed the short-term development team to deliver the necessary systems.</li>
                 <li>I worked on drawing the UML use case diagram for completing the functionality model of the storage solution.</li>
                 <li>I directed the technical activities to estimate, plan, design, develop, test, and migrate solutions into production.</li>
