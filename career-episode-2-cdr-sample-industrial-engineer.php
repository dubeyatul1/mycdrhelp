                <p><strong>[CE 2.13]</strong></p>
                <p>I defined a list of control actions with the team and implemented the same to guarantee that the problem will not occur again:</p>
                <ul>
                  <li>Ensure the implementation of the Ford Production System tools (Lean manufacturing Methodology) do identify deviations in the process related to the Vaccum Cap Assembly: (Process Confirmation e Process Job Observation at AE 1240.1 / AE 1260.1)</li>
                  <li>Ensure Quality Process Audit to identify issues for vacuum cap packaging (Contamination)</li>
                  <li>Ensure Capability for Vaccum Cap Torque/ Assembly ( 1,33 for Cpk)</li>
                  <li>Ensure Quality Process for Reused Parts - Disassembly/Reuse Standards</li>
                  <li>Measure Vaccum Cap Flatness when required – Part when even changing the gasket, keep rejecting at AE 1540.1</li>
                </ul>
