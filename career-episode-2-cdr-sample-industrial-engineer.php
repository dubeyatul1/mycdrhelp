                <p><strong>[CE 2.1]</strong></p>
                <p>This career episode links to project titled <strong>“Oil Cavity Leak Test – AE 1540.1 (Assembly Test Station)’’. </strong>This project was carried out during my tenure at <strong>Ford Motor Company</strong> as <strong>Project Lead Engineer.</strong></p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p>Production Team recognized an inordinate number of rejections @ AE 1540.1 of Oil Cavity Leak Test impacting the FTT Indicator of engine assembly line. Current FTT 97.2% - Target 98.8%. (FTT= First Time Through)</p>
                <p><strong>[CE 2.3]</strong></p>
                <p>The scope of the project was operation AE 1540.1.The goal was to fairly reduce 60% of the rejections and accomplish the target of 98.8 %.</p>
                <p><strong>[CE 2.4]</strong></p>
                <p>The Hierarchy of the project is shown below:</p>
