                <p><strong>[CE 2.11]</strong></p>
                <p>A Benchmark study with Ford Cologne Engine Plant (where the same engine was manufactured) found the same problem with leakage through vacuum cap during oil cavity leak test. The root cause found was the flatness on machining surface due to non compliance with the specification.</p>
                <p>I conducted a capability study for vacuum cap flatness at Camaçari Engine Plant. The study was rejected showing a low value for CPK (0.93). According to the ford procedure, a process must have CPK result above 1.33 ( (Short Term).</p>
                <p>I suggested the corrective action of improving the machining process at supplier plant. I validated the effectiveness with a new capability study for flatness, that was approved showing values of 21,46 for CPK.</p>
                <p><strong>[CE 2.12]</strong></p>
                <p>Regarding the part contamination; the first idea was to change the package, since it has a cardboard package, influencing on its contamination. However, as the part was an imported one so it was not possible to change it, otherwise not only the cost would increase significantly, but also the discarding process would become too difficult.</p>
                <p>The proposed action was to implement a visual inspection at the Vacuum Cap assembly operation to check if there was any contamination on the Cylinder head or the Vacuum Cap before its assemble. I trained the operators of all shifts as well as the team leader to perform the check professionally.</p>
                <p>After executing all the 3 actions the reduction of the leakage within this region decreased by 80%.</p>
