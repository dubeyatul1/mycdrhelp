                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 2.5]</strong></p>
                <p>I was responsible for the management of the whole scenario. I drafted all the required documentation of the project &amp; made it possible to have a seamless synchronization amid departments. I also took care of all the project documentation.</p>
                <p><strong>[CE 2.6]</strong></p>
                <h2>My Roles &amp; Responsibilities</h2>
                <ul>
                  <li>Analysing the quality rejections.</li>
                  <li>Analysing the Cause &amp; Effect of the Quality Issue and identifying the root cause for Oil Cavity Leak Test.</li>
                  <li>Profoundly explore the root cause of the quality issue.</li>
                  <li>Conduct a Benchmarking study with others Ford Plants to look for opportunities.</li>
                  <li>Define and implement an Interim Corrective Action to immediately reduce the quantity of quality rejections.</li>
                  <li>Define and Implement Corrective Actions and control the data to ensure that the performance is maintained.</li>
                  <li>Replicate the study results and actions to other plants with the lessons learned</li>
                </ul>
                <p><strong>[CE 2.7]</strong></p>
                <p>The main challenge for me was identifying and measuring the specific region of the leak test. The oil cavity leak test validated the leakage of the engine in the oil galleries (low and high pressure) thus ensuring the quality of the related motor oil leak by galleries and joints installed in the engine. However it didn’t showed where the leakage was but instead kept on rejecting the engine.</p>
                <p>The test methodology consisted of mechanically sealing the motor with the aid of rubber seals and exerting a pressure of approximately 24KPa for 23 seconds and then analyzing the flow for 12 seconds through a flow meter installed in the equipment.</p>
                <p><strong>[CE 2.8]</strong></p>
                <p>leakage is present. It was essential to implement a measurement process to check where the most recurrent leakage is present in order to adopt proper corrective analysis.</p>
                <p><strong>[CE 2.9]</strong></p>
                <p>The offline test done by me showed that 46% of the leakages were on the Vacuum Cap (181 engines from 394 rejections). The other rejections were from many diverse regions having the second most impact from Camcover (17% - 67 rejections).</p>
                <p>Considering that the main leakage region was the top of the vacuum cap, the Interim Containment Action was implemented in order to apply Loctite prior to assembly at station AE1210.1.</p>
                <p><strong>[CE 2.10]</strong></p>
                <p>The second challenge was to identify the cause of the leakage around the vacuum cap region. I made an analysis of Cause &amp; Effect based on cumulaive team know-how and experience. The analysis considered the 6 main points, as below:</p>
                <ul>
                  <li>Measurement: Cylinder Head Roughness, Vacuum Cap Roughness, Vacuum Cap Flatness</li>
                  <li>Material: Vacuum Cap, Vacuum Cap Gasket, Cylinder Head</li>
                  <li>Personnel: Training</li>
                  <li>Environment: Cylinder Head Contamination, Vacuum Cap Contamination</li>
                  <li>Methods: Torque Strategy, Vacuum Cap Gasket Assembly, Vacuum Cap Assembly</li>
                  <li>Machines: Torque Tool, Oil Cavity Leak Test False rejects</li>
                </ul>
                <p>All elements were investigated and 3 were found as not capable:</p>
                <ul>
                  <li>Vacuum Cap Flatness</li>
                  <li>Cylinder Head Contamination</li>
                  <li>Vacuum Cap Contamination</li>
                </ul>
				<picture>
				  <source type="image/webp" data-srcset="<?php echo $siteUrl;?>images/PLE-2-10.webp">
				  <source type="image/jpeg" data-srcset="<?php echo $siteUrl;?>images/PLE-2-10.png">
