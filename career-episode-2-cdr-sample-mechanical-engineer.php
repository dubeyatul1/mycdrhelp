                <p><strong>[CE 2.1]</strong></p>
                <p class="sample_para">I joined GE in a Services Leadership Program which was designed to train experienced new hires about GE’s design, operation and financial processes on fast track basis. I had the opportunity to learn and get hands-on experience of maintenance and troubleshooting of several Industrial Gas Turbine ranging from 50MW to 180MW. After completing my one year on job training, I was assigned as Lead Contract Performance Manager to a multi-million dollar long-term maintenance contract with one of prestigious customer i.e. ENGIE. After my hiring I was responsible for preparing computerized maintenance plans, budgeting, coordinating to resolve all technical and contractual issues, executing planned &amp; unplanned outages of assigned power plants. I was also responsible for availability and reliability guarantees of the plants.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p>My assigned Power Plant is having 9 x GE Frame 7FA.03 &amp; 2 x 7EA Gas Turbines with the total capacity of 1800 MW. GT2 installed in an oil refinery of Saudi Arabia. The machine train consists of a GE 7E turbine driving a generator running at a design speed of 3600 RPM. Proximity probes are permanently installed at all fluid film bearings of the train to monitor radial vibration levels. Two proximity probes are installed axially on turbine outboard to monitor rotor axial position. The unit underwent a major inspection (MI) where generator rotor was rewound. Comparison of the vibration data before and after MI outage revealed an increase in vibration amplitudes in most of the locations (specifically on the generator). As vibration was on increasing trend so customer was worried. Customer requested me to perform vibration analysis &amp; evaluation of gas turbine along with generator train balancing.</p>
                <p>The unit underwent a major inspection (MI) where generator rotor was rewound. Comparison of the vibration data before and after MI outage revealed an increase in vibration amplitudes in most of the locations (specifically on the generator) but they are still well below the alert limit. I decided to start the analyses by ADRE data collection. Onsite data collection was done because seismic vibration data was not available in System 1 Turbine mid-span probe 39VS-21 gap voltage was outside OK limits since May 2017. Data collection from Mark VI was conducted on 31/12/17 and 01/01/18 to capture steady-state, shutdown and cold start-up events.</p>
