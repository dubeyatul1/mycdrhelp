                <p><strong>[CE 2.3]</strong></p>
                <p>Main objective of this project was to reduce the vibration values below 15 mm/sec by insitu balancing of generator rotor. This includes 4 phases:</p>
                <ul class="">
                  <li>Data collection</li>
                  <li>Analyses of data</li>
                  <li>Calculation of balance weight/location</li>
                  <li>Installation of balance weights</li>
                  <li>Trial run to see affect and repeat above</li>
                </ul>
                <p>As this was hit &amp; trail method so we anticipated that multiple attempts will be made to solve the issue. In this case it took 7 trials to reach up to required vibration levels.</p>
