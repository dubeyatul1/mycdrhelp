                <h2 class="sample_heading">SUMMARY</h2>
                <p>I recommended and executed following actions after my analyses:</p>
                <ul>
                  <li>Balancing generator rotor onsite is recommended</li>
                  <li>Turbine and generator rotors run out needs to be checked in next opportunity as preventive measure as surface roughness can also lead to vibration spikes/increase.</li>
                  <li>Loop-check turbine mid-span probe 39VS-21</li>
