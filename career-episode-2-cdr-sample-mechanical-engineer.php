                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 2.5]</strong></p>
                <h2 class="sample_sub_heading">My Roles &amp; Responsibilities</h2>
                <p>I worked as Site Manager on this project. Some of tasks I performed were:</p>
                <ul>
                  <li>I discussed in detail with customer to understand their Critical to Quality (CTQs) about this job.</li>
                  <li>I clarified the scope, duration and challenges of the project to customer. In this case I explained them that they must shut down &amp; start the machine multiple times since it was hit and trial method.</li>
                  <li>I explained the support required from customer during the project execution like logistics, site accesses, work permits, lighting etc.</li>
                  <li>I arranged meetings within my company (GE) with multiple linked stakeholders to explain the required resources, customer CTQs and expertise. In this case arrangement of ADRE equipment, qualified technician and craft were required.</li>
                  <li>I coordinated with the parts team and provided them the part number of possible balance weigh which will be used.</li>
                  <li>While unit was running, I mobilized the technician and asked him to hook up ADRE so that I can capture steady state and shutdown data to perform baseline assessment. On 28/12/17, vibration data was captured for steady-state and shutdown events. On 01/01/18 data was captured for cold startup and steady-state events to identify machine thermal behavior and compare slow roll vectors in hot and cold states.</li>
                  <li>With the baseline data I examined different plots like 1X-filtered slow roll orbits, direct orbits, polar plots and average shaft centerline plots. With this examination I ruled out the other unbalance reasons like misalignment, thermal effects or bowed/bent, seal / bearing wear or looseness.</li>
                  <li>Then I asked the customer to shut down the machine. After cool-down and making access to the rotor I marked /identified timing mark (key phasor in this case).</li>
                  <li>Finally, I made drawing which showed rotor blades, probes placement and angles. This served as starting point of calculating trial weight amount and angle which I performed afterwards.</li>
                </ul>
                <p><strong>[CE 2.6]</strong></p>
                <h2 class="sample_heading">PROBLEMS FACED &amp; THEIR RECTIFICATIONS</h2>
                <h2 class="sample_sub_heading">Problem definition:</h2><p>During balance correction, data gathered while trial runs I used two different ADRE units.The X-Y readings indicated swapped probes which meant that ADRE units were hooked up in wrong orientation.</p>
                  <h2 class="sample_sub_heading">Effect of problem:</h2><p>Resulted in incorrect assessment and 1 day of delay</p>
                  <h2 class="sample_sub_heading">Root Cause of Problem:</h2>
                  <p>The proper labeling cable was not made for ADRE data cables. Technician didn’t install correctly.</p>
                  <h2 class="sample_sub_heading">Final Solution:</h2><p>I performed 3 actions to resolve this issue:</p>
                <ol>
                  <li>Contacted the GE vibration team and arranged a configuration drawing of ADRE &amp; BN 3500 circuit.</li>
                  <li>I asked the technician to perform loop check.</li>
                  <li>I made firm Document about configuration of Probe orientation for all GT units and Labeled in PLC Panel where the BN-3500 was installed.</li>
                </ol>
                 <h2 class="sample_sub_heading">Problem definition:</h2>
                 <p>While installing balance weights I found cracked balance weight stake marks.</p>
                 <h2 class="sample_sub_heading">Effect of problem:</h2>
                 <p>These cracks have the potential to propagate due to continuous cyclic loading at high temperature. Crack propagation increases the risk of material liberation during operation which can lead to hardware damage downstream in the flow path.</p>
                 <h2 class="sample_sub_heading">Root Cause of Problem:</h2>
                 <p>Crack initiation in stake marks were due to tensile overload during the staking process residual stresses &amp; stress concentrations.</p>
                <h2 class="sample_sub_heading">Final Solution:</h2><p>All cracked stake marks were removed by the following steps.</p>
                <ul>
                  <li>I removed the balance weight adjacent to cracked stake mark.</li>
                  <li>Recorded the total weight in step 1.</li>
                  <li>Carefully blended to remove crack &amp; crack area to achieve a smooth contour &amp; transition into the undamaged surface. I advised the technician to maintain a 4:1 aspect ratio of the blended surface leaving a minimum radius of 0.125’’ on part. The blending was supposed to completely remove the displaced ligament of the stake mark. It provided a continuous smooth surface to the balance groove edge.</li>
                </ul>
                <p><strong>[CE 2.7]</strong></p>
                <h2 class="sample_heading">ENGINEERING DESIGN</h2>
                <p>I analyzed slow roll data at hot and cold conditions and found that there were insignificant differences. However, turbine and generator rotors both had high direct runout values. Slow roll 1X-filtered values were negligible when compared to direct ones which were mainly caused by rotor scratches and surface roughness. The scratches readings were superimposed onto rotor vibrations at the entire speed range of the machine. I decided to polish the rotor journal to remove this run out. For this surface polishing there was no tool available. I designed a small lathe machine and a fixture. I brought portable lathe machine and attached it with circular fixture. I made a travel track of the machine. In this way I could polish the rotor journal area without rotating the rotor. This helped me in getting actual unbalance weight. Rotor journal area polishing was done to meet API standard 670.</p>
                <p><strong>[CE 2.8]</strong></p>
                <h2 class="sample_heading">ENGINEERING CALCULATIONS</h2>
                <p>I performed the calculation to get trial weight. I used the formulae: Cf = m r 2 Here: Cf is centrifugal force in LBf (which basically is the output we want to achieve using balance weight) m = Mass of the rotor = Weight (G)/Gravity(G) = Lb/386.4 in/sec2 r = Weight add radius in inches  = Rotor speed in radians 1st I calculated trial weight to generate a force equal to 10% of the rotor weight. Secondly I added weight at a 8” radius Later I calculated the speed of the rotor in radians With all above values I could get mass of the rotor. Now, m = W/G, W = m * G In similar way I calculated the weight in every attempt and keep repeating the same till I got required vibration levels. Please refer to below Appendix-2 for detail off each attempt, graphical representation and conclusion.</p>
