                <p>A major crude oil-fired power plant of 500 MW capacities was installed at kosti, Sudan. So, Metering Skid project was established nearby to grant safe, steady and well-handled supply of crude oil as a cost-effective fuel for running thermal power plant. The project aim was to provide means for expenditure reduction, stabilize the electrical outputs, and minimalize the industrial pollution &amp; environmental damages resulting from using traditional methods of fuel transportation.</p>
                <p>I was assigned as Commissioning Engineer and my role was to join project team as an embedded member from PDOC with the task of following up commissioning and start-up activities until successful handing over for operational launch. The project experience helped me to establish the necessary skills and knowledge base for best practices in engineering segment as well as in project management.</p>
