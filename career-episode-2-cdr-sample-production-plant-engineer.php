                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 2.2]</strong></p>
                <p>Kosti Metering Skid project was designed, manufactured, supplied and executed on E.P.C basis, including associated civil works. Metering Skid takes off, filtrates, quantifies and supplies the crude oil transmitted through the PDOC export pipeline to the largest thermal power plant of Sudan which is comprised of 4 units of 125 MW; the project distance from the Thermal Power Plant was 9 Km. Metering Skid project key stakeholder was Sudanese thermal power generating company (STPG). The construction contract was awarded to Asawer Oil and Gas &amp; the consulting services were carried out by PDOC technical services department. Approximated value of the Metering Skid Project was 4.4 Million USD.</p>
                <p>The project was owned by a joint venture between Sudanese thermal power generating company (STPG) &amp; Petro-Dar Petroleum Operating Company (PDOC). STPG is a young company specialized in thermal power production; it was established as a result of a Republic decision 2010 thus dividing National Electricity Corporation into four state-owned companies in the business of electricity Production, transmission and distribution. STPG is the owner of the Electric Thermal Power Plants in Sudan; carrying out the operation and maintenance of all Power Plants in Sudan including Heavy Fuel, Boilers, diesel engines and gas turbines.</p>
