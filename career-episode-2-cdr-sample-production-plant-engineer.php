                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 2.6]</strong></p>
                <p>In accordance with project management practice standards that I have studied in PMP course &amp; as a representative of an essential stake holder I was responsible for performing close oversight at various project activities along with metering skid equipment testing and commissioning to meet the pre-set operative condition i.e. operating pressure 90 bar, operating temperature 70 C, Pour point 39, Flash point 50, Wax content 14.6 respectively.</p>
                <p>Moreover, I employed the principles of risk response planning in performing risk identification and analysis for the execution and operation start-up of drain and surge relief system; it was designed for personnel safety and for preventing equipment damage. Pressure and thermal relief valve was placed and should open if certain value of pressure was triggered to relief fluid into sump tank through several points across metering skid.</p>
                <p><strong>[CE 2.7]</strong></p>
                <p>As a PMI certified individual, I enhanced the advantages of employing quality assurance in operating a dependable flow control computer connected to SIEMENS PLC CPU system and integrated to UPS system and ESD System. The system was capable of initializing and terminating all processes and isolated the station to maintain safe state of process when abnormal conditions were observed; such as presence of high pressure, unnecessary flow rate, blockage and fire signal.</p>
                <p>I was tasked for exercising cost control basics for selecting, fitting and testing a set of other metering system components and updating cost baseline for Firefighting system, Electrical Heaters, Automatic sampling system, volume and flow limit switch, density meter, protecting filters and control cabinet.</p>
                <p><strong>[CE 2.8]</strong></p>
                <p>During the multiple phases of the project I took charge of authenticating project scope which included operative commissioning of crude oil metering skid with two 12" ANSI 600 tap off streams; each stream had dedicated incessant operation and control system with handling capacity of 20000 bbl. Metering system values of volumes were corrected using ASTM factors. I have applied scope management guidance to define work breakdown structures in the installation of bidirectional sphere prover unit.</p>
                <p>I also brought my schedule control considerate into practical action in determining and sequencing tasks and estimating duration and resources for the execution of line up test of Motorized flow control valves with allied pressure and temperature indicators which were installed to control and line up meter runs.</p>
                <p><strong>[CE 2.9]</strong></p>
                <ol>
                  <li><p><b>Problem faced</b></p>
                      <p>Kosti Thermal Power Plant since its inception consumed oil fuel at a snowballing rate as all the 4 units were running simultaneously to meet the growing local demand of energy.</p>
                    <p>Slightly after start-up tests, Thermal Power Plant operation department requested Metering Skid to upsurge oil flow rates to the maximum capacity. A problem occurred when Thermal Power Plant informed that oil flow rate was still the same even though Metering Skid signposted that flow was maximized.</p>
                    <p>I checked flow meters, outlet valves, pressure and temperature transmitters, drainage system and system alarms to safeguard these were working properly. All Metering Skid components were performing as normal with a noteworthy high pressure on outlet side. That was a clear sign of potential line blockage. I stopped Metering Skid operation immediately and contacted Thermal Power Plant control room asking for careful checking on their side. They sent a hot tapping team for on-site scrutiny.</p>
                    <p>Along the supply pipe there were no visible defects so they started to measure pressure differential at distributed points of the line. After outlining the entire segment and at the inlet point of storage tanks in Thermal Power Plant I determined the line blockage point which was located near an installed reduction flange joint.</p>
                    <h2>Adequate Problem Resolution</h2>
                    <p>Investigation were conducted to figure out the root cause of such situation and it concluded that due to the thermal expansion caused by the high rate and temperature of oil flow a significant damage befell to the sealing rubber inside the sequenced reduction joints. The damaged parts sustained to move with oil flow to the point that any more passing was not possible due to pipe diameter. As the diameter substantially reduced; this resulted in the accumulation of large pieces of sealing rubber thus causing the line to be blocked.</p>
                    <p>Proposals for typical solution were debated by operation team and hot-tapping team. The designated suggestion was about declining the number of reduction joints and to replace the sealing rubber with a higher quality type. Implementing this solution helped to maintain the operational assets in the safest and effective conditions of piping networks and storage facilities.</p>
                  </li>
                  <li><p><b>Problem faced</b></p>
                      <p>Metering skid is located in a region of Sudan that has a very rainy season and muddy soil which was definitely taken into consideration with regard to the structural design, but some hypothetical scenarios related to water trenches network started to appeared as serious technical obstacles when rain down poured. That was because the rain water sump pit was excavated and constructed on low level section of the facility ground due to functional design issues for helping fluids to flow accumulatively. Although project area was assiduously chosen and cropped &amp; despite the rain water sump pit is was equipped with screw pump for adequate water discharging process. But it was not designed to accommodate the abundant rate of rain which ultimately lead to pump failure and subsequently the site was water flooded.</p>
                    <h2>Adequate Problem Resolution</h2>
                    <p>After meeting with all parties concerned including stakeholders, contractors, consultant and project team, an extensive discussion took place and all agreed upon suggestion that was to expand the sump pit, install new high-pressure submersible pump and construct a sloping pipe by which water can naturally flow to the lowest area where an evaporation pond was drilled. The decision was put into effect few months after project was completed and considered as remarkable punch list.</p>
                  </li>
                </ol>
