               <p><b>[CE 2.1]</b></p>
               <p>My second episode is about my job in Accenture, and the project I did was for Vodafone UK. Vodafone UK has more than 18 million subscribers and is the third largest Mobile operator in UK and Globally Vodafone group is the second largest mobile company. Vodafone UK also acquired CWW and has entered in Fixed Line domain provide customer with Broadband and IPTV services through cable.</p>
               <h2>BACKGROUND</h2>
               <p><b>[CE 2.2]</b></p>
               <p>I worked on this project as Back Office Engineer and was responsible for all the activities related to Core Network. I was responsible to perform various operations tasks on different nodes of core network such as Media gateway, HLR and MSC. All the Nodes perform different function in GSM Network but work together in order to provide voice and data services to end user. I had to ensure the redundancy of all the core nodes and maintain healthy network. During this project I have implemented four redundant HLR Nodes along with deployment of 8 Media gateways. And for this I did daily and weekly meeting with Vodafone. Joint testing was conducted and led by me where node performance was measured and tested. A detailed Summary of all the meetings and testing reports was submitted to my NOC supervisor and NMS Manager</p>
               <p>Other than that my daily task was to ensure and close all the CSR raised by MS team within timeline, and for this I did daily meeting with supervisor and presented him detailed report on issues reported by MS team and the solution implemented for its rectification.</p>
               <p><b>[CE 2.3]</b></p>
               <p>I was directly reporting to my NOC Supervisor and under my supervision was a team of NOC Engineer (6 Engineers). Teams were divided into 3 shifts having 8 hours’ time for each shift. Two NOC Engineers were present 24x7 at NOC for network management related activities. I was responsible for all the critical activities and for this I had to take approval from my supervisor in which I had to present the detailed execution plan and also the roll back plan.</p>
               <p>Every day I did meeting with operator and discuss plan for implementation of redundant HLR and deployment of Media Gateways. Various things like IP configuration, Policy routing, Database preparation and management were discussed and finalized. After meeting with Operator teams I did meeting with my supervisor and take its approval for execution, also I present him with the daily progress report which include the detailed synopsis of closure of all the CSR raised by MS team. Nobody in team was allowed to leave NOC without completing task and sharing report, proper procedure was followed to handover pending tasks to next shift engineer.</p>
               <p>In order to achieve desired network KPI’s I had to ensure strict compliance and no outage was expected because the redundant nodes were being deployed and in case of outages there will be no backup and there will be complete failure in network.</p>
               <p>Weekly Meeting were held with NOC Supervisor and NMS Manager in which all the major activities performed were presented to them, and further next week action for the deployment of HLR and MGW was discussed. Also I present them progress report of all my team members and discussed about their weakness and strengths and prepare an action plan for the improvement.</p>
               <p><b>[CE 2.4]</b></p>
               <p><b>The project Hierarchy is given below -</b><br/> I was answerable to the NOC Supervisor &amp; NMS Manager.</p>
