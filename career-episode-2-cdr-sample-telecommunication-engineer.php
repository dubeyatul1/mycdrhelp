               <p><b>[CE 2.5]</b></p>
               <p>Daily and weekly reports were prepared by me including the plan to measure and maintain network KPIs and every team member performance was monitored.</p>
               <p>A separate report was prepared by me containing all the data related to possible outages and how to avoid them along with implementation plan for HLR and MGW. It was shared with NOC Supervisor, NMS Manager and Operator</p>
               <p>Core Network is the backbone of any mobile operator and every operator ensure that they have redundant plan for entire core network and it is check frequently in order to ensure that it will work fine when needed. In this project, I got the chance to know every node in core network and it boosted my technical skills.</p>
               <h2>PERSONAL ENGINEERING ACTIVITY</h2>
               <p><b>[CE 2.6]</b></p>
               <p>Integrating redundant HLR was major task as Vodafone UK cannot afford any outage in their network for even a second. So I have to be very carefully while performing any activity on HLR. In my first meeting with customer where I got to know complete Network of Vodafone UK and it shocked me that GSM services are being provided consumer and there is no Backup for HLR and for future upgradation their there is not enough capacity on existing Media gateways for traffic routing. After the meeting my Supervisor assigned me the task of preparing complete network high level and Low level diagram which should meet future expansion and redundancy requirements.</p>
               <p><b>[CE 2.7]</b></p>
               <p>I started doing health checkups of all NSS elements and measured the capacity of all the trunks Groups and their links along with signaling and media load for voice and data services. Also I have to check the software version and note if any upgradation was required.</p>
               <p>After collecting required information, I prepared detailed report along with existing network HLD &amp; LLD and presented to my Supervisor and Manage to review. Further, I started to prepare new HLD and LLD in which all the redundancy requirement was included with future expansion requirements. As per my LLD, Vodafone UK decided to implement 4 Backup HLR and 8 new media gateways.</p>
               <p><b>[CE 2.8]</b></p>
               <p>BOQ was prepared by myself for all the network expansion and got its approved from my supervisor and Vodafone UK. All the required nodes were installed by technician and hardware installation was monitored by me, completed quality audit report was presented which include detailed report and pictures of installed equipment were shared with Vodafone UK and my supervisor. As the equipment is very sensitive was even room temperature and humidity was controlled at specific ratings.</p>
               <p><b>[CE 2.9]</b></p>
               <p>For commissioning of HLR and MGW IP plan was prepared by me and was approved by my supervisor and operator, after the approval it was implemented on nodes. Further all the optical links of HLR and MGW with different NSS Nodes were tested for any packet loss or jitter.</p>
               <p>As HLR is central database which contains complete detail of each Subscriber or SIM registered in GSM network, every SIM has a distinctive IMSI number which is recorded in HLR. Whereas MGW are used to connect different types of networks as it converts between different transmission protocol (ATM and IP) and coding schemes used in network.</p>
               <p><b>[CE 2.10]</b></p>
               <p>All the database of active HLR was collected in approved downtime and was then loaded on to newly installed standby HLR. Joint testing was conducted by me in which operator and my supervisor tested all the software parameter and functions of newly installed HLR and at the end redundancy test was performed in order to verify full functionality of newly installed HLR.</p>
               <p>Newly installed MGW was commissioned by me and all the media and signaling Links between media gateway and Soft switch were tested. Test calls to different various operator and transit exchange were done and further checked using tracker in soft switch in order to proper function of media gateways.</p>
               <p>Weekly Meeting were held with NOC Supervisor and NMS Manager in which all the major activities performed were presented to them, and further next week action for the deployment of HLR and MGW was discussed. Also I present them progress report of all my team members and discussed about their weakness and strengths and prepare an action plan for the improvement</p>
               <p><b>[CE 2.11]</b></p>
               <h2>KPIs for network optimization and expansion</h2>
               <p>After commissioning HLR &amp; MGW were kept under observation and thoroughly checked for full functionality. For this, I used network tracker to check complete functionality. <b>During commissioning, the major problems I faced were: </b></p><ul>
                 <li><p><b>Call Routing</b></p>
                     <p>Media gateway function is to connect different type of network, one problem I faced is when test call was made to other mobile operator in UK it could not be established, for this call tracker function was used in Soft switch and it was found that RTP packets got lost between media gateways and BSC.</p>
                   <p>The first step was to check physical link between BSC and MGW using Ethernet tester and found working fine. After checking link I did call to the mobile operator using existing deployed MGW and used soft switch to trace the call, and in comparison found the UDP port defined in newly installed MGW is mismatched with BSC. Upon changing the UDP port the issue was resolved and call could be routed and established to other mobile operator using newly installed MGW.</p>
                 </li>
                 <li><p><b>H.248 Link</b></p>
                     <p>During deployment of one of the MGW one issue I faced was that the H.248 Link between MGW &amp; MSC could not be established. My first step was to check the Physical link between MGW and MSC and found ok. After checking physical link I started to check the IP and subnet mask defined on MGW and found it was in range with MSC. Then I checked the configuration of link from both nodes and found that the MGC ID is mismatch. And upon changing the MGC ID the link was established.</p>
                 </li>
               </ul>
               <p><b>[CE 2.12]</b></p>
               <h2>Summary</h2>
               <p>It was very important engineering project of my career as I have operate various NSS Nodes for routine task and have to deploy HLR and MGW in existing network. During this project my technically capabilities were boosted and got me better understanding of core network. The project also raised my project forecasting, scheduling, steering &amp; management skills. I also learned about using outside resources when the team was not able to figure out the complicated issues. I also learned a lot about team management skill.</p>
