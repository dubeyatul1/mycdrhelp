                <p>The primary objective of designing earthquake resistant buildings and structures is to reduce or minimize the loss of life that may occur in areas that are prone to earthquake. Although is not possible to fully control the terrible outcomes of earthquake but still efforts can be made to minimize the loss as much as possible that may occur as a result of earthquake. This project revolves around the design and seismic response of multistory RC structure in various seismic zones of Pakistan</p>
