                <p><strong>[CE 3.17]</strong></p>
                <p>The provision of shear wall provides resistance to lateral translation thus assisting in providing resistance to lateral loading. This results in reduction of reinforcement in the columns and decreasing their size resulting in economical design. A shear wall is a structural system composed of braced panels (also known as shear panels) to counter the effects of lateral load acting on a structure. Wind and seismic loads are the main loads resisted by shear wall.</p>
                <p>The building should be symmetrical in shape to avoid torsion moments in the frame of structure. The twisting action of building is prevented as much as possible. Similarly the mass on the structure (dead and live) should also be equally distributed.</p>
                <p>Instead of providing a single thick shear wall, it is recommended to provide more shear walls having less thickness to serve the purpose.</p>
                <p><strong>[CE 3.18]</strong></p>
                <h2>Summary</h2>
                <p>The main objective was to study &amp; identify the response of building (R.C structure) against different type of earthquake loadings regarding linear and non-linear analysis on E-tabs. Time period of the structure came out to be 0.8 seconds for 8 story buildings. Studies show that the time period of a building should be almost equal to no. of stories divided by 10. So the time period came out to be exactly right. Time period is the time taken for a building to oscillate and return to its original position. It is vital for structural stability and moreover for public safety.</p>
