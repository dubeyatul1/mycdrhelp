                <p>In the last I carried out the foundation design of the building using SAFE software. I provided spring to the foundation slab so that it cannot take the moment &amp; behave like the original foundation. I designed the foundation as a mat/raft footing as total area of footing covered by each column footing was more than 50% of the building area.</p>
