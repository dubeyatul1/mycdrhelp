                <p><strong>[CE 3.2]</strong></p>
                <p>A great challenge faced even today after years of Kashmir Hazara earthquake is that of providing earthquake resistant construction .the earthquake of October 2005 resulted in great loss of life and property. This project work is mainly focused on developing an economical design for RC structure. Earthquake resistance is ensured by analyzing the structure on ETABS for seismic activity of zone 4. The behavior of structure was found satisfactory for all zones.</p>
