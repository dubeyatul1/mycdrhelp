                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.3]</strong></p>
                <p>Siemens, a well-known Technology Powerhouse that globally stands for its Engineering expertise, actively providing good quality, innovations, reliability and worldwide services for more than 165 years in more than 200 countries. Along with one of the largest producers of energy efficient, resource saving technologies, it is a leading supplier of efficient power generation and power transmission solutions. Siemens is also a pioneer of ground work solutions and software solution provider to the industry. Its regional office in Dubai (in power and gas division) is responsible for preparing tender projects for power plants (Turnkey and Power Island Supply) in MENA region and it supports ongoing projects mainly for the onshore commodities.</p>
                <p><strong>[CE 3.4]</strong></p>
                <p>The main objective of my project is to prepare a bid for constructing a power plant based on IPP model (Build, Own and Operate the Plant) to efficiently replace the old power plants in Northern Emirates in the UAE and to meet the power demands. Siemens has signed a MoU with the end client (FEWA), and now it is preparing a final bid with the best tariff which must be submitted to the developer of the Power Plant. To construct the power plant based on IPP model (Build, Own and Operate the Plant). The main scope of the project was to prepare such a bid to design, procure and construct the FEWA Independent Power Plant (IPP) which is consisting of two power blocks 2 x (2+1) as follows:</p>
                <ul>
                  <li>Four Gas Turbines SGT5-8000H</li>
                  <li>Two Steam Turbines SST5-5000</li>
                  <li>Six Generators SGen5-2000P</li>
                  <li>Four Heat Recovery Steam Generators HRSGs</li>
                  <li>Balance of Plant equipment (Civil, Mechanical, Electrical and Process)</li>
                </ul>
                <p><strong>[CE 3.5]</strong></p>
                <p>During this project, my key responsibilities as Senior Procurement Engineer- Electrical included:</p>
                <ul>
                  <li>Prepare and issue the Request for Quotation (RFQ) for the electrical equipment that is required in the project.</li>
                  <li>Follow up the vendors and suppliers to get best offers.</li>
                  <li>Properly study the received offers commercially and seek the technical evaluation from the Project Engineering Manager.</li>
                  <li>Conduct negotiations for all the purchasing terms and coordinate the technical classifications with the vendors.</li>
                  <li>To potentially saving cost, work on the value Engineering measures.</li>
                  <li>Prepare Contract drafts and send it to the selected vendors or suppliers to get their initials.</li>
                  <li>Estimate the project purchasing cost and hours.</li>
                </ul>
                <p><strong>[CE 3.6]</strong></p>
