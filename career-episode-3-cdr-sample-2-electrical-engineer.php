                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 3.7]</strong></p>
                <p>This project is big enough to have many technical issues, but I tried my level best to face each issue with my engineering skills and vigilance. One of the foremost issues that must be solved was that the project technical specifications call for supply of a Black Start Diesel Generator with Electrical Power output of about 11600KW.</p>
                <p><strong>[CE 3.8]</strong></p>
                <p>After evaluation, the best offer received was the one which includes 8 gensets; I found that the size of the gensets will not fit into the allocated space based on the project layout. I tried to optimize the number of generators and reached to 5 gensets which were offering the same required power from a different vendor, but the problem was that the same issue still existed.</p>
                <p><strong>[CE 3.9]</strong></p>
                <p>Finally, I thought of considering a combination of Black Start and Emergency diesel gensets and synchronize them, by this combination, we got successful to secure the production of electrical power required which consumed less space complying with the project requirements.</p>
                <p><strong>[CE 3.10]</strong></p>
                <p>During this project, I was continuously consulting with the Electrical Designer for LV Cables and Communication System and performed the required Cables cross section Calculations. The whole project is based on the ISO, IEC, NFPA &amp; DIN engineering codes.</p>
                <p><strong>[CE 3.11]</strong></p>
                <p>While working on this project, for the successful fulfillment, I attended the following workshops and meetings:</p>
                <ul>
                  <li>Internal Kickoff Meeting, November 2016</li>
                  <li>Weekly Team Meetings</li>
                  <li>Management Team Meetings</li>
                  <li>Various clarification Meetings with the Suppliers/vendors</li>
                  <li>Negotiation Excellence at Siemens, Media Rotana Hotel, Dubai in November 2017</li>
                </ul>
                <p><strong>[CE 3.12]</strong></p>
                <p>For the accomplishment of this project, research work and knowledge about recent facts &amp; figures is must. For that, I worked on the Basic Design for Low Voltage (LV) equipment, clarification about technical issues with the vendors, design to cost measures, cost value engineering, e-bidding and e-auctions.</p>
                <p><strong>[CE 3.13]</strong></p>
                <p>While working on such a huge project, one should have engineering knowledge and skills to come over any problem. I worked very closely with the Engineering department to overcome any technical issue such as support vendors to comply with the project technical specifications and to solve the technical obstacles with the customer and also optimize design for saving cost.</p>
                <p><strong>[CE 3.14]</strong></p>
                <p>During this project, the software that has been used are Sisypro for electrical equipment tracking, PTT-PMA for project risks and opportunities, ACOM for commenting on customer technical specifications, and New Costing Tool For uploading calculated costs</p>
                <p><strong>[CE 3.15]</strong></p>
                <p>While working on this project, I was working under the project procurement manager who is reporting to the general bid manager. This requires from me to be in continuous contact and cooperation with the procurement management, general and proposal project management and engineering project management.</p>
                <p><strong>[CE 3.16]</strong></p>
                <p>During this project, it was necessary to arrange regular meetings with the seniors and supervisors to discuss issues and reach to the target on time. We used to have meetings with the project team on daily basis where we discuss the progress of the project, meetings with the internal management were arranged every month where the project status were shared and meetings with suppliers were held for expediting and technical and commercial clarifications.</p>
                <p><strong>[CE 3.17]</strong></p>
                <p>While working on this project, my engineering expertise supported me a lot to identify the problem, analyze the cause of the problem, propose ideas and solutions, select the best idea and its implementation was all my responsibilities.</p>
                <p><strong>[CE 3.18]</strong></p>
                <p>I didn’t lead the team directly but indirectly my role required management of contracts where I have to guide the vendors/ suppliers for handling and negotiating the offers of electrical materials as per the project specifications. Moreover, I was in touch with the regional and global electrical commodity managers for the project sourcing and supplier development requirements.</p>
                <p><strong>[CE 3.19]</strong></p>
                <p>Along with the material management, I also focused to maintain track records related to strategies used, cost consumption and project scheduling. I prepared the purchasing manual, purchasing schedule, project sourcing strategy manual and project status sheet (Electrical commodity).</p>
                <p><strong>[CE 3.20]</strong></p>
                <p>This project proved to be a new learning point of my engineering career. I developed many interpersonal skills such time management, cost value engineering, supplier relationship management, communication skills and advanced negotiation skills.</p>
                <p><strong>[CE 3.21]</strong></p>
                <p>As this project is a plant site project, it strictly required a lot of environmental, health and safety measures by the customer and the project team. Forthe safe &amp; successful completion of this project and to meet the customer’s expectations, I followed the below safety precautions:</p>
                <ul>
                  <li>Hazardous Energy Control</li>
                  <li>Electrical Safety</li>
                  <li>Working at Height</li>
                  <li>Driving vehicles safely</li>
                  <li>Potentially Explosive Gas and vapours</li>
                </ul>
                <p><strong>[CE 3.22]</strong></p>
                <p>From the kickoff of the project till today, me and project’s team performed very well and achieved the goals of the project. Along with completion of the project, I have also prepared a new proposal in line with the project roadmap.</p>
                <h2 class="sample_heading">Summary</h2>
                <p><strong>CE 3.23</strong></p>
                <p>This project proved to be a turning point in my professional career. The level of project completion I reached till the moment is a great achievement for me. I learned a lot of new things while working on this project such as leading a team, managing daily tasks, communicating with vendors, negotiation skills and managing and preparing daily records. In short, this project boosted my self-confidence and enhanced my interpersonal skills.</p>
