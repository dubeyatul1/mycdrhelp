                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 3.5]</strong></p>
                <h2>My Roles &amp; Responsibilities were as following -</h2>
                <ul>
                  <li>Ensure compliance of site standard operational procedure (SOP) and occupational, health and safety standards.</li>
                  <li>Develop the complete execution plan of the project.</li>
                  <li>Development and assistance of maintenance projects with the engineer team leader.</li>
                  <li>Prepare all technical details.</li>
                  <li>Testing the network at every stage to ensure proper optimization.</li>
                  <li>Delegate tasks among the technical staffs and maximize the manpower.</li>
                  <li>Finally, submit the details report to the control section to ensure workplace safety and to prevent environmental pollution.</li>
                </ul>
                <p><strong>[CE 3.6]</strong></p>
                <p>Before starting the project, first of all, I read different engineering books and journals. I did research in GSM RF optimization giving greater attention towards Antenna systems, handover, sectioning, interference &amp; GPRS performance. I also carried our research to find diverse techniques for improving cellular network &amp; how they can be implemented in FastWeb Network.</p>
                <p><strong>[CE 3.7]</strong></p>
                <p>First step I took was the extraction of necessary information for the analysis of data. The performance statistics extracted from BSC’s and Sites were</p>
                <ul>
                  <li>Dropped Calls</li>
                  <li>Non-Working sites/sectors or TRX’s</li>
                  <li>Capacity issues</li>
                  <li>Drops due to handovers</li>
                </ul>
                <p>I also analyzed previous drive test data which were</p>
                <ul>
                  <li>High Interference Areas</li>
                  <li>Coverage Holes</li>
                  <li>Other Interference Sources</li>
                </ul>
                <p><strong>[CE 3.8]</strong></p>
                <p>Once the data was acquired for the analysis I</p>
                <ul>
                  <li>Noticed that there were a extraordinary number of dropped calls due to unsuitable Handover parameter (The Handover is when you move from one cell coverture to another, for example when you are driving, the cell phone goes from one cell to another, the call can fail if the cell network do not have an appropriate parameter configured)</li>
                  <li>Found Sites/Sectors, which were not operational and caused a decline in the ability to handle traffic. I blocked sections (Blocked TRX’s) which are a transmitter/receiver that handle the traffic of calls. A cell is usually divided into 3 sectors of 120 degrees pointing in 3 directions to complete 360 degrees of coverage. Each sector has a certain amount of TRX that translates into ability to handle calls. If a TRX is blocked or an entire sector is blocked it means that no calls will passed through there and users may face problems in communicating.</li>
                  <li>Find capacity problems, sites where there were not enough TRX to handle a high traffic calls.</li>
                  <li>Found areas where there were coverage holes that did not allow users to enjoy sufficient coverage for making the calls</li>
                </ul>
                <p><strong>[CE 3.9]</strong></p>
                <h3>Call Setup Success Rate Issue Resolution (Challenges faced)</h3>
                <ul>
                  <li>If C.S.S.R is stumpy after optimization; I checked T.R.X quality statistics from back office, if quality statistics of T.R.X were underneath the standard; I managed site visit and requested the team to change the defective T.R.X. For this case I also requested team to change B.C.C.H T.R.X with leaping T.R.X rationally.</li>
                  <li>For T.C.H stalling, I requested RF. team to check traffic allocation between G.S.M and D.C.S layer and it is operated &amp; controlled by handover-parameters or by hard-optimization technique; low T.C.H was also observed if any neighbor site was down, so I also checked daily-network handiness report, if such is the case I reported the matter to O &amp; M teams.</li>
                  <li>If problem was not resolved I entreated R.F team for joint visit to site, and if interference is found due to over shooting cells antenna tilts were altered after having with discussion with the R.F team.</li>
                </ul>
                <p><strong>[CE 3.10]</strong></p>
                <p>I optimized the handover parameters in the cells having high incidence of dropped calls &amp; in addition a re-tune of neighboring factions were done in order to lower the interference. I loaded all these latest parameters in the BDC’s corresponding to the sites which presented these issues.</p>
                <p>I also proposed Antenna Azimuth, Antenna Tilt and Antenna Type parameters in order to improve coverage in the areas where coverage holes were found. In some sector, I proposed new Sites/Sectors in order to overcome these issues.</p>
                <p><strong>[CE 3.11]</strong></p>
                <p>I designed a corrective maintenance plan to restart the sectors/TRX that were blocked. First, Remote Reset was applied via MML Commands and in cases where they were not recovered, the operation and maintenance team was requested to go to the site and perform the appropriate troubleshooting.</p>
                <p>I designed a TRX growth plan which was to be implemented by the Operations and Maintenance team in order to increase the capacity of some sites which presented capacity congestion issues.</p>
                <p><strong>[CE 3.12]</strong></p>
                <p>I employed the changes by working together with the operation and maintenance team who were basically in charge of implementing the proposed changes at the BSC (Base station controller) level. The BSC is the cell controller and all this information is changed in the BSC and the personnel in charge of the operation of these teams, I provided all the information needed to make these changes. Here I have passed all the necessary information including MML commands that were executed in the BSC during the maintenance window that is from 11pm to 5am (time during which there is less traffic and less service is affected) this applied for changes in Handover parameters.</p>
                <p>The corrective maintenance plan worked in the way that I found some TRX, which needed to be replaced along with BB2 cards (sector controller cards). After performing this problem solving activity, the sectors/ blocked TRX became operative. For changes in antennas I also directed field personnel to perform Azimuth and Tilt readjustments (angle of elevation and orientation of the antenna) to improve the coverage. When these changes were made, I did a drive test to check if the coverage has improved. Prior to Operation and maintenance being executed, I did the drive test, collected data of calls to verify the previous and later operation of the network.</p>
                <p>The promising improvements of this project resulted in upsurge coverage and quality of service, minimized consumer complaints, minimized the dropped call rate of sectors / trx blocked were operational, increased availability of voice channels and consequently a reduction of congestion.</p>
                <p><strong>[CE 3.13]</strong></p>
                <p>Whenever I faced issues regarding BTS, BSC or PCU I first communicated with local level-1 technical person, if they were unable to resolve an issue or were unable to respond I then raised a ticket for remote support from the hardware vendor in China where the Research and Development team were available round the clock.</p>
                <p>After Service testing site was offered for Acceptance Testing Procedure. In this, all the acceptance protocols were achieved and shown to the Client. It included corroboration of the Bill of Quantity and service and steadfastness testing protocols. After completion, sites were handed over to Customer for commercial usage.</p>
                <p><strong>[CE 3.14]</strong></p>
                <p>I was answerable for the management of subcontractor work force resources available for this project, timely equipment transportation and to guide teams in order to avoid problems in installation and powering up the equipment respectively.</p>
                <p>The flow of work was that I had to tell subcontractor representative two days in advance for the requirement of a team for a BSC site. In the case of failing to send team, the subcontractor was made-up to give explanation and send the standby team for the corresponding task.</p>
                <p>Reducing vendor an extra visit to sites, otherwise we had to compensate and issue a PO for an extra visit. This procedure guaranteed us avoiding any problem concerning resource administration.</p>
                <p><strong>[CE 3.15]</strong></p>
                <p>I encountered many difficulties with several unforeseen snags during my work in the field, occasionally due to equipment or scarcity of resources in the subcontractor team. We were unable to accurately predict when equipment faults would occur, but I got assurance from subcontractor that a redundant member is there on standby to solve any future issue. Due to the execution of this policy progress improved and the project was completed within the timeline.</p>
                <p><strong>[CE 3.16]</strong></p>
                <h2 class="sample_heading">Summary</h2>
                <p>I worked on this project with great zeal &amp; zest. I did a lot of research both online &amp; offline regarding GSM RF optimization. My hard work did not go in vain, as this project refined my technical and managerial skills and broadened my vision. The project elevated my project planning, scheduling, conducting &amp; management. I also acquired know how of using outside resources when the team was not able to figure out the complicated issues. I also learned a lot about team management skill. Overall this project played a role of building block in my professional experience.</p>
