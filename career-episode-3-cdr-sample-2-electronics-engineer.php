                <p><strong>[CE 3.1]</strong></p>
                <p>This career episode is about my project titled “GSM RF Optimization of Fast web’s Telecommunication Network’’. This project was carried out during my tenure at Trade Up Parma, Italy as an Electronic Engineering.</p>
                <p>Trade Up is a company focused in telecommunication services for small/large businesses and corporations in Italy since its inception in 2004. Trade Up provides services ranging from planning, optimisation, operation and maintenance of telecommunication networks and is among the leading companies throughout Italy. In 2006, Trade Up joined hands with FastWeb, which is an Italian telecom company, which provides mobile communication services, broadband internet and digital television services.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p>It’s a normal practice in a telecom network that the periodic addition of new customers and the inadequate growth or development of plans creates several disputes that impact the performance of the network. Hence, the quality delivered is grounded on service quality indicators; meant that FastWeb required the execution of a plan in order to optimize the network and upsurge the reliability and quality of services.</p>
                <p>The GSM RF Optimization encompassed of two stages, pre-optimization and post-optimization. Pre-optimization process comprised of the analysis of network in order to extract raw counters in statistical form to analyze the KPI’s. In this particular case, different tools were used to obtain major KPI’s such as CSSR (Call setup success rate), DCR (Dropped-call rate), HoSR (Handover Success Rate), IASR (Immediate Assignment Success Rate) and others. The post-optimization process refers to the task of providing solutions for any issue found on the network after analysis and the follow-up of the corrective measure being taken..</p>
                <p><strong>[CE 3.3]</strong></p>
                <p>The prime objectives of the project were enlisted as following with the current implementation context.</p>
                <ul>
                  <li>To determine the requirement of the project securely</li>
                  <li>To identify the efficacy of the new system</li>
                  <li>To carry out analysis of KPIs for Pre-Optimization process</li>
                  <li>To carry out network analysis for Post-Optimization</li>
                </ul>
                <p>Being a project member in this specific project, I took the task of executing primary optimization, examining and identifying the problematic sites/cells, prioritizing of the issues, identifying solutions and implementing them properly and professionally.</p>
                <p><strong>[CE 3.4]</strong></p>
                <p><b>The project Hierarchy is given below -</b><br/>I was answerable to the project engineer.</p>
