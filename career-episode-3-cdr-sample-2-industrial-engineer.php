                <p><strong>[CE 3.1]</strong></p>
                <p>This career episode links to project titled <strong>“Engine Assembly Line Rebalance’’.</strong> This project was carried out during my tenure at <strong>Ford Motor Company</strong> as a<strong> Manufacturing Engineer.</strong></p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p>Ford Camaçari plant was presenting the inferior hours per unit indicator as compared to all Engine plants of South America (According to the reported results on The Harbor Report i.e. annual productivity report).</p>
                <p>HPU = Total of hours worked by the total of the assembly line employees divided by the Total number of engine manufactured</p>
                <p>2015 Camaçari Engine Plant = 8.9 HPU (Volume of 58 540 units)</p>
                <p>2015 Benchmarking: Ford Taubate Engine Plant = 4.9 HPU (Volume of 284 000 units)</p>
                <p>The volume of production depends entirely on the marketing conditions. It was needed to work reducing the number of employee at the assembly line to make it more competitive. So it was required by the South America Manufacturing director that I work with the Camaçari’s team for defining a plan to improve this indicator because I was the responsible for leading the Harbor Report in the South America region.</p>
                <p><strong>[CE 3.3]</strong></p>
                <p>The scope of the project was to identify opportunities to reduce the number of employees at the assembly line. The aim was to reduce at least 15% of the total employee currently working at the assembly Line.</p>
                <p><strong>[CE 3.4]</strong></p>
                <p>I was responsible for work with the Industrial Engineers from South America plants to reduce the total of Hours per Unit manufactured. The project Hierarchy is given below.</p>
