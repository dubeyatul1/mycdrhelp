                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 3.5]</strong></p>
                <p>I was responsible for the management of the whole scenario. I drafted all the required documentation of the project &amp; made it possible to have a seamless synchronization amid departments. I also took care of all the project documentation.</p>
                <p><strong>[CE 3.6]</strong></p>
                <h2>My Roles &amp; Responsibilities</h2>
                <ul>
                  <li>Support the team to analyse the current data and its impact on the Hours per Unit indicator</li>
                  <li>Support the Camaçari Engine Team to identify productivity upsurge opportunities</li>
                  <li>Analyse with the Camaçari Engine team about the viability of the opportunities identified</li>
                  <li>Define a work plan to achieve the target of 15% reduction in manpower</li>
                  <li>Conduct weekly meetings with the team to discuss the plan and its execution</li>
                  <li>Provide the team with information about competitors data using the Harbour Report</li>
                  <li>Present status of the project to the South America Manufacturing Director</li>
                </ul>
                <p><strong>[CE 3.7]</strong></p>
                <p>The most problematic situation was to identify how to lessen the quantity of employees once the plant was already working with the minimum required manpower for operation with just one shift. The issue was that the plant was designed to produce around 206,000 engines per year in two shifts with maximum efficiency. Due to economic crises in Brazil plant required volume was pretty low: 56 000 for 2015, 86.000 for 2016, just 42% of its capacity, and the forecast for the year 2017 were 70.000 i.e. 34% of the plant capacity.</p>
                <p>After numerous meetings with the team and analysis of the line balance, an opportunity was identified by me to eliminate some manual stations by adding their activity to other stations. This will result in the increase of the line speed while attending the new volume required.</p>
                <p>All the stations were analysed by the team of the engineers working at the plant under my supervision &amp; discussed their views during meeting. There I defined a line rebalance plan implying MODAPTS methodology to calculate each station cycle line on the GSPAS System.</p>
                <p><strong>[CE 3.8]</strong></p>
                <p>After rebalancing the stations I reached to a final plan of 15 manual stations elimination from the total of 71 analyzed. Additionally, the other operations were rebalanced increasing the cycle time to a maximum of 82 seconds. This was enough to attend the volume being forecasted. Current Cycle time was 66 seconds.</p>
                <p><strong>[CE 3.9]</strong></p>
                <p>Plant Capacity in 2 shifts (designed when of its implementation):</p>
                <p>235 work days per year * 16 working hours per day * 3600 to transform hours in seconds) / 66 seconds per engine = 205 090 engines per year</p>
                <p>Plant Capacity in 1 shifts (current):</p>
                <p>235 work days per year * 8 working hours per day * 3600 to transform hours in seconds) / 66 seconds per engine = 102 545engines per year</p>
                <p>Plant Capacity in 1 shifts (target):</p>
                <p>235 work days per year * 8 working hours per day * 3600 to transform hours in seconds) / 82 seconds per engine = 102 545engines per year = 82 536 engines per year</p>
                <p>The Line Rebalance consisted of the elimination of 15 stations – Activities were transferred to all the other stations. Stations being either eliminated or automated:</p>
                <p>AE 190.1 – Load 4 MBC with liners &amp; zero torque them with spindle fixture</p>
                <p>AE 250.1 – Oil Pan Dowells, Knock sensor</p>
                <p>AE 360.1A – Oil Bores &amp; Piston stuffing #2 ( become Automated )</p>
                <p>AE 370.1A – Piston Stuffing (PRA) bore #1 &amp; 3</p>
                <p>AE 480.1 – Install sprocket to crank, pump belt, oil pump assy, oil pump cover and R/D 3 oil pump bolts</p>
                <p>AE 580.1 – Place cam cap/cam fixture to the assy pallet. B/O vacuum cap and place it to pallet fixture Z/T 2 head bolts</p>
                <p>AE 690.1 – Back out cam cap 16 bolts. Remove intake/exhaust cam caps 6 bolts and place to tray. Remove caps bolts 2 &amp; place to tray ( become Automated )</p>
                <p>AE 990.1 – Front Cover Bolts</p>
                <p>AE 1080.1 – Install Crank Position Sensor &amp; Damper Pulley</p>
                <p>AE 1230.1 – Install Blanking Plate</p>
                <p>AE 1360.LH – Install Oil Pan Bolts, studs and rotate pallet</p>
                <p>AE 1830.1A – Wiring harness Map Sensor Station 2</p>
                <p>AH 490.1 – Cylinder Head unload</p>
                <p>AH 270.1 – Install Steam Seals</p>
                <p>AH 340.1 - Install Valve Springs</p>
                <p><strong>[CE 3.10]</strong></p>
                <p>To implement the changes designed it was necessary to schedule a collective vacation during December 2016 so that the stations were enabled to be deactivated and modified.</p>
                <p><b>I defined the execution plan as below: </b></p>
                <ol>
                  <li><p><b>Responsible – Assembly Process Coach &amp; Camaçari Industrial Engineer:</b></p>
                      <p>Define the layout changes required to implement the line rebalance designed during the meetings</p>
                    <p>Update the station’s documents (QPS, DCP)</p>
                  </li>
                  <li><p><b>Responsible – Maintenance team under the supervision of the Camaçari Industrial Engineer:</b></p>
                      <p>Update all the electronic system of control/ programs: OMS, PLC, RBMs</p>
                    <p>Relocate the Layout of the stations, including racks, platforms and lift table</p>
                    <p>Relocate the HMI and pressured airdrops</p>
                  </li>
                  <li><p><b>Responsible – Camaçaro Industrial Engineer, Assembly Process Coach &amp; Manufacturing Business Office Engineer (myself)</b></p>
                      <p>Train the operators on the new process designed</p>
                    <p>Measure the new cycle time to guarantee the reach of the cycle time calculated by MOPATS</p>
                  </li>
                  <li><p><b>Responsible – Manufacturing Business Office Engineer (myself)</b></p>
                      <p>Prepare the final presentation, align it with the work team involved in the project and present it to the South America Manufacturing Director</p>
                  </li>
                </ol>
