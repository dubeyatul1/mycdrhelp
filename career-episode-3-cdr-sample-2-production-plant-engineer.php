                <p><strong>[CE 3.6]</strong></p>
                <h2>Cement Slurry Design</h2>
                <p>This challenging job required a design based on limited time and resources available. A good slurry design is one which considers all the job consideration including well condition and is optimized according to the desired results. This case required fit-for-purpose slurry which could deliver the expected results. The challenge was to design lightweight gastight slurry, which would also prevent the gas influx from the shallow sands. This required low-density slurry that is capable to develop rapid static gel strength during slurry hydration phase. I selected to design 1.3sg LiteFIL slurry with combination of GASBLOK LT and DeepCEM additives. D500 is a GASBLOK additive for low temperature application. I used CemCADE temperature simulator to determine the bottom-hole circulating temperature with data inputs of actual sea current velocity and current profile.</p>
                <ul>
                  <li>This gas tight slurry is defined to have below requirements.</li>
                  <li>Fluid loss value lower than 30 mL/30min.</li>
                  <li>Zero free water.</li>
                  <li>Short static gel strength transition time (from 100 lbf/100ft2 – 500 lbf/100ft2).</li>
                </ul>
                <h2>Stop Gas Channeling</h2>
                <p>Gas channeling through a column of cement is most prevalent during its transition from a liquid to a solid. During placement and while the cement is in a liquid state, the main factor prohibiting the gas from entering the cement is the hydrostatic pressure of the cement and mud column above it. Another characteristic that affect hydrostatic pressure of the annular column is the slurry’s gel strength development. The reason for this is the fact that the ability of the slurry to transmit its hydrostatic pressure is a function of its gel strength.</p>
                <h2>Mud Removal</h2>
                <p>Poor Quality WBM posed the biggest challenge for effective mud removal and cement coverage around the casing. No matter how good the slurry design is, the overall success of the job is dependent of the removal of mud.</p>
                <p><b>Following challenges can be attributed by the presence of this Mud -</b></p>
                <ul>
                  <li>Thick filter cake across formation</li>
                  <li>Casing surface wet ability</li>
                  <li>15% oil in Mud to decrease friction</li>
                  <li>Formation of emulsions</li>
                  <li>Compatibility issues with spacer and slurry</li>
                  <li>Poor cement bond with casing and formation</li>
                </ul>
                <h2>Lost circulation (Low Frac Gradient Formation)</h2>
                <p>On top of stop gas migration, we needed to have a cement system which will not induce losses due to narrow pore Frac window. That is why need of light weight cement comes in place. Light weight cement is difficult to blend, design and transport.</p>
                <p><strong>[CE 3.7]</strong></p>
                <p><b>I designed three things in this project -</b></p>
                <p>I design Fluids selection based on the formation characteristics by considering the well security. To improve mud removal efficiency, I selected Spacer. It was designed taking account all mud removal criteria defined by WELLCLEAN II criteria. Spacer density and volume selection was mainly based on CemCADE simulation results.</p>
