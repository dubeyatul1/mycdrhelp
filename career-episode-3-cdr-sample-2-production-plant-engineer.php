                <p><strong>[CE 3.1]</strong></p>
                <p>This career episode relates to project titled <strong>“Low Cost Solution for Shallow Gas Risks in MPCL Operation’’. </strong>The project was carried out during my tenure at Schlumberger Pakistan.</p>
                <p>Schlumberger is the world's leading provider of technology for reservoir characterization, drilling, production, and processing to the oil and gas industry. Working in more than 85 countries and employing approximately 100,000 people who represent over 140 nationalities, Schlumberger supplies the industry's most comprehensive range of products and services, from exploration through production and integrated pore-to-pipeline solutions for hydrocarbon recovery that optimize reservoir performance.</p>
                <p>I am working in Well Integrity services of Schlumberger where we provide cementing services to the E&amp;P companies to provide zonal isolation to the well either by successful primary cementing or remedial cementing. Services includes from designing, testing and executing a cement slurry design to have zonal isolation in the well.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p>Block 17 in Pakistan is an onshore location, located in Baluchistan Province and operated by Mari Petroleum Company Limited.</p>
                <p>Some of the wells on Block 17, with depths varying from 600 m to 1350 m, must be drilled and cemented, where top whole sections were defined to have a potential shallow gas risks.</p>
                <p>Gas migration is a complex problem, involving fluid density control, mud removal, cement slurry properties, cement hydration, and between cement, casing and formation interactions. Therefore, Gas migration is one of critical problems in cementing.</p>
                <p>Cementing job for wells crossing through gas zone can be or are hazardous. Cementing gas zone is a challenging operation if the risk is not well evaluated and an appropriate solution used. Gas migration will become an issue, and if zonal isolation is not achieved, gas might migrate through the annulus to the surface or other reservoirs. This gas can cause numerous problems and this issue can become hazardous or even cause the loss of the well. The primary cause of annular gas migration during well cementing in gas zone is the underbalanced hydrostatic fluid pressure around of the gas-influenced zone.</p>
                <p>This project focused on achieving, low cost-effective solution for potential shallow gas hazards, throughout designing blend with reasonable, fit for purpose solid volume fraction, proper additive selection, and lab testing, following all steps of design, execute and evaluate processes for the top holes section cementing Jabo-1 well.</p>
                <p><strong>[CE 3.3]</strong></p>
                <p>The <strong>main objective </strong>of this project was to come up with a solution with which we can achieve complete Zonal Isolation through successful primary cementing job and hence reduce remedial costs. This project focused on achieving, low cost-effective solution for potential shallow gas hazards, throughout designing blend with reasonable, fit for purpose solid volume fraction, proper additive selection, and lab testing, following all steps of design, execute and evaluate processes for the top holes section cementing Jabo-1 well.</p>
