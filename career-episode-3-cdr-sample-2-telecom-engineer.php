                <p><strong>[CE 3.1]</strong></p>
                <p class="para">My Third episode is about my project in LCC International. LCC was established in 1983 and remains a leading pioneer in providing fixed network, wireless voice and data engineering services to the telecommunications industry. LCC is working in more than 50 countries worldwide and providing service in network management, business consulting, tools-based solutions, and training. Since November, 2014 it’s been acquired by Tech Mahindra Ltd.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p class="para">I worked on this project as Project Manager. In this project I was responsible for complete Implementation. I held meetings with customer and finalized scope of work for each site; I discussed all technical details relating to BSS (base station subsystem), Optical &amp; MW (Microwave) Transmission, Core, Power &amp; Civil to finalize the scope of work for each site with customer. I resolved the Issues which teams faced during Implementation by highlighting appropriate department &amp; concerns for meeting the Project timelines.</p>
                <p class="para">I had 9 BackOffice Engineers and 52 Filed Teams consisting of1 engineer &amp; 1 technician working directly with me. These worked in all region of Pakistan (North, South &amp; Center). Initially Project Coordinator and I did meetings with Customer for finalization of Scope and locked the Project Implementation plan with Customer.</p>
                <p class="para">Once Scope was finalized then I started hiring Back Office engineer and Filed Teams for Project as Existing Resources were busy on other projects. Once Resource was finalized then I arranged Electrical Work &amp; First AID training along with certification for all Field Teams.</p>
                <p class="para">Held in-house &amp; Field Training in Presences of customer to ensure that team knew what they will be doing on site &amp; if any problem arises then how to resolve it immediately.</p>
                <p class="para">I prepared daily progress reports of all the teams and kept KPIs of their work. Each team performance was monitored and was shared with customer on next morning before 09:30 AM.</p>
                <p class="para">Network Audit was very important for Mobilink as it is the Largest Mobile Operator in Pakistan. So it was very crucial that All the Equipment on each site must go through Audit Procedure and each equipment must be tagged through “Field Force” app and should be accessible on its Portal. The Purpose of this Audit was to get particular pictures of what is installed in their Network and to remove Junk and Extra Material to Warehouse.</p>
