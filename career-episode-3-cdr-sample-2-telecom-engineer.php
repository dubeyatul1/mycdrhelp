                <p><strong>[CE 3.3]</strong></p>
                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p class="para">In this Project, task was to do Audit of 9782 BTS Sites along with 32 MSC &amp; BSC Core Locations. It was a challenging task to do a detailed Survey of every Site along with Layout. Teams have to Scan each and every Asset Tag along with Serial Number of Item and if any Asset tag was missing then they had to paste Asset tag and then scan it.</p>
                <p class="para">On Daily basis Plan was discussed with Customer and then Shared with team for Execution. Same Plan was communicated to Mobilink Security Firm &amp; Operation Team. The NOC for site access and work Order was issued as a result of this communication.</p>
                <p class="para">Teams were mobilized according to plan and they started doing Survey and Audit Work. On Outdoor Sites team did survey without the presence of Mobilink POC. Teams used Mobilink’s Filed Force Mobile Tools to scan all the Asset tag along with Equipment Serial Number. Once completed teams prepared Survey Layout and then filled survey form on Filed Force tool.</p>
                <p><strong>[CE 3.4]</strong></p>
                <h2>These forms consist of three Portions, i.e. Civil, Power &amp; TE (Telecom).</h2>
                <p class="para">On Civil Portion I ensured that team check and fill overall site building, boundary wall/fence, main gate, Pads for BTS, Generator and other Equipment, cable tray and Tower Conditions, which clearly shows that sites is being maintained properly. Inspection of tower and cable tray was done very thoroughly as to check there is no rust present on them and it is properly coated with paint. If Light on tower was not working then it was installed and ensured that it’s in working condition. Pictures of all tower legs are taken from top and bottom to show installed Equipment. Mobilink used four types of tower on their sites SST, Tripole, monopole, guymast. Tower Height and type is also checked. Team checked the presences and connectivity of End-to-End Earthing and it’s values was checked using Earth Meter. Equipment shelter/shade was checked for any damage or rust.</p>
                <p class="para">For Power portion I made sure that teams check the condition of Genset. Load on Genset was measured along with number of running hours. Condition of fuel tanks was checked. Genset Alarms were checked and confirmed from NOC. ATS Panel was checked for working. Rectifiers, battery Banks &amp; Distribution Boards were checked for total capacity and Operation Load. Telecom Equipment load on Rectifier was measured. Number for free and used breakers were noted according to their capacity. Rectifier alarms were checked and verified from NOC. Air Conditioning system was checked for any faults, and also calculated whether sufficient cooling system is installed on site and whether it is reporting temperature alarms to NOC. Lastly Fire Fighting System was checked for working with alarms generation and reporting. Coverage of smoke sensor was checked as these should cover complete site and also Fire Extinguishers were installed. Audit of other Mobile Operator Power Equipment was done as sometimes site are being shared by Mobilink or some other Operator is sharing their sites.</p>
                
                <p class="para">For telecom portion I asked team to check BTS, Radio Module, RF antennas, Height (HBA), E-tilt, antenna assembly, azimuth, LOS, RF Cable and connector brand and type of combiner, CPRI Cable length, MW racks and Dishes along with Link Info. Same was done for OMO (Other mobile Operator). For Core Location survey team was accompanied by Customer operation Person who ensures that everything is done as per approved work order and no outage is occurred. On these locations audit of SDH &amp; DWDM nodes along with core Equipment such as Softswitch Media Gateways, HLR, PCRF, VAS etc, were performed and I check that all nodes audit was done within approved Downtime. Each cards and racks were physically inspected and Serial Number along with asset tags was scanned using Filed force tool. Alarms of all the NE were verified from NOC. NE’s were check for redundant Power, Earthing &amp; Dust. If found any issue its was escalated to operation team for resolution and then again Audit as performed to verify.</p>
                <p class="para">After completion of Audit I collected all the data from Filed team and provided to Back Office Engineer to Prepare Layouts on AutoCAD and Enter survey report on Filed Force Portal and verify for errors. Sometime filed team skips tagging and I make sure that back office engineer identify the problem and get it rectified.</p>
                <p class="para">Sometimes filed force portal is showing duplicate tags for this I took the issue with Operation teams and got it resolved.</p>
                <p class="para">I had to resolve Site Access Issue by coordinating with Mobilink Security Firm. If it is not escalated and resolved on time it may have affected the PIP of Project. I took decisions on spot whether team will stay and wait for access issues to be resolved or they move on and do the next site.</p>
                <p><strong>[CE 3.6]</strong></p>
                <p class="para">I had to ensure that all 52 teams do survey of 2 sites on daily basics to deliver the project before closure of PIP. I became successful and completed the project with 01 month remaining in the PIP.</p>
                <p class="para">I had to implement zero tolerance for EHS and thus everyone has to follow this. Nobody was allowed to climb on tower without wearing proper safety gear i.e. Safety Helmet, gloves, shoes, high Visibility jackets and full body harness with double lanyard. I did spot check on various sites to check teams are complying EHS standards.</p>
                <p class="para">Sometimes Filed Force Tool malfunctioned and it was not syncing data with Portal so I took this issue with IT Department of Mobilink for resolution as this tool was provided by Mobilink.</p>
                <p class="para">Extra Visit and cost record was maintained by me and Purchase orders were issued by Mobilink against these.</p>
                <p class="para">On monthly Basics Invoices were raised to Mobilink on Completed Sites and I kept track of entire project invoices.</p>
                <p class="para">The best part of project was that I and my company have earned multiple resources from open market and in future it was being suggested by me to My Account Manager.</p>
                <p class="para">I went all across my Account Manager and asked him to place some bonuses and Incentives on monthly basis for the field teams and in 05 months nobody was repeated in doing so because everybody was trying so hard to earn their respect and extra money.</p>
                <p class="para">In order to control cost of project I communicated with National HR Manager and Administration Manager to keep the DA fixed with all guys for this particular project and clearly communicate everybody while having HR interview session. This way we have saved almost 0.5M in this project life cycle.</p>
                <p><strong>[CE 3.7]</strong></p>
                <h2 class="sample_heading">Summary</h2>
                <p class="para">It was very important Project of my career as I have to deal with different department, i.e. Civil, O&amp;M, BSS, Transmission, Core, Power, IT &amp; security. I was involved in planning, hiring, management, evaluation and closure of this project. This Project polished my managerial skills a lot and taught me the importance of time management. The operations I carried out met all the safety requirements and complied with environmental norms as per Mobilink. During this period I managed to calculate, develop and select suitable processes so that they completely fulfil all the engineering tasks.</p>
