                <h2 class="">Concrete floors</h2>
                <p>The concrete within a car park is particularly susceptible to deterioration, so grade 50 concrete should usually be specified. Pre-cast units are generally more durable than in-situ concrete due to factory controlled production conditions. To minimize the percolation of corrosive fluids through cracks floors should be sloped at 1:60 falls to aid drainage, and the top surface of the concrete slab should be protected with an appropriate proprietary waterproofing system. Manufacturer’s guidance should be followed for the correct choice of product and application.</p>
                <h2 class="">Metal decking</h2>
                <p>G275 galvanizing (275g/m2 of zinc) is the standard protective coating for composite steel decking. This level of corrosion protection to the upper surface of the decking will be sufficient, provided adequate provision has been made to prevent the ingress of water (using reinforcement to control cracking, and waterproofing the top surface of the concrete). The underside of the decking should be given additional protection in the form of a pre-applied coating or epoxy paint applied insitu. Such an additional layer of protection has the advantage that it can be regularly inspected and remedial work undertaken if required. However, it is recommended that Corus be consulted on durability and future maintenance issues at an early stage if this solution is to be adopted.</p>
