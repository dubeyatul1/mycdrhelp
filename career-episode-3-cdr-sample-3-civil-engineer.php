                  <li>The presence and location of gas pipes, electric cables, drains, sewers that might be present at the site and must be moved or built with or without special precautions.</li>
                  <li>The maximum number of parking levels and height of building required by the client or allowed by design constraints or local authority.</li>
                  <li>The proposed use of the car park, whether it I long, medium or short stay, together with a anticipated vehicle entry and exit traffic flow figures.</li>
                  <li>The proposed method of payment to be adopted if any, payment of exit, payment on foot or pay and display or no payment at all.</li>
                  <li>Type of construction</li>
                  <li>The accommodation required for staff and public (offices, rest rooms, toilets)</li>

                  <li>The capacity and location of lifts/escalators.</li>
                  <li>The requirement of water protection.</li>
                  <li>The need for providing heating</li>
