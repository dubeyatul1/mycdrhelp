                <p>The client and design team must establish the design brief mutually. The aim of the design brief is to understand the technical aspects and constraints affecting the design. It is important that the clients wish list is reviewed by the design team, so in this respect we must follow the client’s requirements closely with feasibility with sketches as closely as possible.<br/> <b>Information we require were as follows -</b></p>           <ul>
