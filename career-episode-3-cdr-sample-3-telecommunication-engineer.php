                  <h2>Personal Engineering Activity</h2>
                  <p><strong>[CE 3.5]</strong></p>
                  <h2>Roles &amp; Responsibilities</h2>
                  <p>My Roles &amp; Responsibilities were</p>
                  <ul>
                    <li>Network planning and installation.</li>
                    <li>IT to Home services.</li>
                    <li>Software &amp; Hardware installation and diagnostic.</li>
                    <li>Installation of internet AP and WISP client</li>
                    <li>Customer complains check.</li>
                    <li>CCTV Installation.</li>
                    <li>Network maintenance</li>
                  </ul>
                  <p><strong>[CE 3.6]</strong></p>
                  <h2>Problems &amp; Rectification</h2>
                  <h2>Low Speed Transmission</h2>
                  <p>As working in the equator, I faced the problem of high rain intensity and high trees presence. The solution was to use high power system to provide a core network for the company inter-site connections.</p>
                  <h2>New Software</h2>
                  As part of providing a solution, window server 2012 needed to be installed. So, I used online resources and went through forums to gain knowledge of all its aspects and as a result I was able to install system with no major concern.
                  <h2>No Previous Plan</h2>
                  <p>Company network was an old network. It was installed without prior planning. It was needed to design and gather the acceptance from the management to install and change previous product and it was a bit hard to chase them due to the time constraints and their fulltime busy schedule.</p>
                  <p><strong>[CE 3.7]</strong></p>
                  <p>For the office network, I provided the design and location of all equipment as well as the flow of data between them. For CCTV system a new design for the camera to provide safe location as well as higher coverage. For Internet service testing I used ping and traceroute to guarantee that all equipment’s were connected and functioning successfully.</p>
                  <p>I did computation for the estimation of the project duration and budgeting of the project. I calculated all required equipment and workers and also the detailed schedule of the project to provide a clear view on how the project will work and if it is worth to take or not.</p>
                  <p><strong>[CE 3.8]</strong></p>
                  <p><b>I used following software -</b></p>
                  <ul>
                    <li>Window server 2012</li>
                    <li>Radius manager</li>
                  </ul>
                  <p><b>During the drive test I used -</b></p>
                  <ul>
                    <li>Laptop</li>
                    <li>Switch router and networking cable</li>
                    <li>CCTV camera, recorder and storage item</li>
                    <li>Rigger tools and tower climbing safety tools</li>
                    <li>Company car</li>
                  </ul>
                  <p><strong>[CE 3.9]</strong></p>
                  <p>Working with Electron I had a better understanding on how project proposal works from scratch and how it is evident that one should include all details in any project because details count every time. The importance of task and time management were a key to succeed in this project In this project hit and trial was used to guarantee that the best performing link was installed. With the change in the weather and nature obstacle, network performance was variable, hit and trial was the best to solve this issue by testing diverse configuration and choosing the best.</p>
                  <p><strong>[CE 3.10]</strong></p>
                  <p>Working with changing weather made it sometime difficult to stuck on the planned objectives. When weather was inconvenient I was working with the internal network and tried my level best to finish as soon as possible. This led to finish project 14 days before the deadline.</p>
                  <p><strong>[CE 3.11]</strong></p>
                  <p>The main person responsible was my manager who required a monthly review of all work and how we are going with the budget of the project. I was the responsible for the project. I contacted the higher management to sign the contract when obligatory. I also provided presentations to the prime stake holders. I used HP projector to do the job and printed the slides using color laser printer. I also kept the stake holders well informed on the progress of the project. For this purpose, I gave reports to the company management on monthly basis.</p>
                  <p>I also resolved all the issues, snags and glitches that may have arisen during project commencement. I used my skills to perform seamless operations. Mt strategy of operating the project was highly endorsed both by client and my company executives.</p>
                  <p>I also actively took care of the safety aspects of the project. I made sure that Personal Protective Equipment is used always during work at site. I followed all necessary rules and regulation for maintaining the compliance on site.</p>
                  <p>I was the leader so I used my time and task management to make sure project ends within time and budget.</p>
                  <p><strong>[CE 3.12]</strong></p>
                  <h2>Summary</h2>
                  <p>The project was successful and the company was able to deliver it before 2 weeks of the agreed deadline. There was an extra 2% to the initial budgeting which was compensated by project owner as they asked for extra work to be done.</p>
                  <p>The engineering knowledge was best described when performing tasks. I successfully prioritized tasks and adjusted time in a way to secure high performance working topology.</p>
                  <p>I was the prime focal person in the project; so I was responsible to reply to customer request and perform all design, maintenance as well as project budgeting.</p>
                  <p><b>Learnings from the project are as below -</b></p>
                  <ul>
                    <li>Task management and prioritization</li>
                    <li>Working with window server</li>
                    <li>Working in a changing environment situation</li>
                    <li>Working with multi-cultural company</li>
                    <li>How to communicate with other companies</li>
                    <li>How bidding works</li>
