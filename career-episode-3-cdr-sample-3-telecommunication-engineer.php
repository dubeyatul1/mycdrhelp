                  <p><strong>[CE 3.1]</strong></p>
                  <p>This career episode narrates my project <strong>“Ventage SL Network’’. </strong>This project was carried out during my tenure at <strong>Electron SL</strong> as a <strong>IT &amp; Network Engineer.</strong></p>
                  <p>Electron SL was incepted in 2014 by three entrepreneurs aiming to provide high level telecom services to the clients in Guinea Equatorial. Electron SL provided IT solution, network implementation, internet service, CCTV installation, maintenance and customer support.</p>
                  <h2>Background</h2>
                  <p><strong>[CE 3.2]</strong></p>
                  <p>Ventage SL is one of the leading trading companies in Guinea Equatorial. With the continuous grow of their network as well as their product; management was suffering from a low performance on their network and thus there was a need to change and maintain the network. Also the old CCTV system needed change.</p>
                  <p><strong>[CE 3.3]</strong></p>
                  <h2>Objective</h2>
                  <p><b>The project objectives were -</b></p>
                  <ul>
                    <li>The continuous improvement of the network</li>
                    <li>Provide solution for the slow network transmission</li>
                    <li>Maintain and solve CCTV issue</li>
                    <li>Provide seamless connection amid departments</li>
                    <li>Link all company locations together</li>
                  </ul>
                  <p><strong>[CE 3.4]</strong></p>
                  <p><b>The project Hierarchy is given below -</b></p>
