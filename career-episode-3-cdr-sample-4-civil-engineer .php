                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 3.5]</strong></p>
                <h2>My Roles &amp; Responsibilities</h2>
                <ul>
                  <li>Liaising with the client to inspect the site (building under consideration) and identify the damaged areas.</li>
                  <li>Propose that the tests should be conducted at certain areas, supervise the tests and review the results.</li>
                  <li>Propose the suitable cure and repair for the damaged areas based on the test results.</li>
                  <li>Discuss the methodology and test results with the authority to obtain the approvals and maintenance permit.</li>
                  <li>Supervise the work at the site and resolve any issues.</li>
                  <li>Design the jacks required to be installed while repairing the damaged columns</li>
                </ul>
                <p><strong>[CE 3.6]</strong></p>
                <p>During the repair work at the 1st basement level and as a part of the work was to remove the slab on grade near the retaining wall for 80cm horizontally and vertically 1.2m of the plaster on the retaining wall. However, during the exposing of the raft and removing the backfilling sand, I noticed that the raft itself is affected by the water table which raised and penetrated thru the construction joint in the retaining or thru any defect part while concrete casting to the raft. I observed that almost 75mm cover was wrapped off and the top steel at some locations was exposed. This diverted our thoughts to conduct certain tests on the raft at certain locations to identify the compressive strength of the concrete, chloride level, steel corrosion status and the humidity level.</p>
                <p>Therefore, being the civil engineer in charge in this project, I instructed to develop the exposure part of the raft to 1.0m inside, and to divide the work in to eight parts. I communicated with the lab department to conduct the required tests and provide us the results at the earliest. Based on the results, I requested for a meeting with my line manger to disclose him the situation at site and my proposal to repair the affected parts of the raft. I concluded the meeting to amend our method of statement to include the raft in our repair work at the certain locations required. However, it was required to communicate with the client to inform him about the revised method of statement and additional work required as per the site condition, and therefore the claim for extension of time and cost.</p>
                <p>I handled the proper forms and corresponded to claim the additional work and reserve my company’s rights.</p>
                <p>Also, another challenge faced in this project was the underground water tank leakage. After exposing the raft around the underground water tank, we observed that the water stopper installed in the tank’s walls were damaged and leakage was coming thru the construction joint and affecting the raft.</p>
                <p>I recommended to free up the water tank and take the proper repair and water proofing laying to the inner side of the water tank. The method of statement been revised &amp; included the repair of damaged parts and cracks by polystyrene and crystalline paint and injections, as well as the micro concrete casting for severe damage in the raft. The challenge faced while this part of repair was to provide additional water source to the building added to the roof water tanks. The tank was tested before commencing the work to ensure that no leakage water proofing mechanism is assured.</p>
                <p><strong>[CE 3.7]</strong></p>
                <p>In this project it was required to design the jacks which should be erected around the damaged columns to carry the load while column repair. With the absence of the design files done for this project, I decided to model the affected columns and apply the actual loads carried, to insure the capacity of the columns is adequate for the demand firstly and secondly to design the jacks.</p>
                <p><strong>[CE 3.8]</strong></p>
                <p>To determine the physical and mechanical properties of concrete, a comprehensive in situ and laboratory test program was prepared, samples from repetitive structural elements were collected to conduct the following tests:</p>
                <ul>
                  <li>Schmidt hammer test to get the compressive strength of the concrete.</li>
                  <li>Cover meter survey to determine the concrete cover to reinforcement.</li>
                  <li>Half-cell potential test to provide the probability of steel corrosion.</li>
                  <li>Depth of carbonation, to ensure that carbonation limit didn’t reach the reinforcement within the design life of the element.</li>
                  <li>Ultrasonic Pulse velocity to identify the concrete quality grading at different locations.</li>
                  <li>Electrical Resistivity measurements of concrete surface to identify the corrosion of steel and the state of the concrete structure, and determine if cathodic protection system may be required at any element.</li>
                </ul>
                <p><strong>[CE 3.9]</strong></p>
                <p>In this project the safety of the residents and workers was our main intention. Therefore, health and safety procedures been followed strictly to avoid any accident may happen. And for the columns repair, the proper jacks been provided as per the approved design, to ensure that the load path is continued and not distracting.</p>
                <p>That’s in addition to the health and safety procedures where I provided a ventilation and vacuum machine was placed at the basement during the grinding work to ensure that dust generated has been suctioned.</p>
                <p><strong>[CE 3.10]</strong></p>
                <p>After covering all the project aspects and issues, I developed an excel sheet reflecting the daily productivity at site at each zone, manpower, machines, materials, and losses. This daily report, been gathered to combine the weekly report should presented to my line manger to highlight any excess or delay may occur.</p>
                <p>During this project I was in daily communication with the project supervisor to check the daily productivity, the work done, and balanced. I was insisting to carry over all the site issues may appear and discuss them with my team to be resolved immediately. In the meantime, work plan and material delivery were discussed on weekly basis to avoid any shortage or delay may appear.</p>
                <p>I was leading a team which encompassed site engineer, supervisor, draughtsman and 8 labors, overall 11 personnel. I utilized my personal leadership skills and my knowledge in project management, to drive the project to be successful &amp; free of any gabs or fails.</p>
                <p>I prepared Progress daily report, weekly report and cash flow chart were designed for this project. I developed an excel sheet to present the information and figures reflects the site condition and status through the project. The reports were supported with charts and photos reflect the site at all stages.</p>
                <p><strong>[CE 3.11]</strong></p>
                <h2 class="sample_heading">Summary</h2>
                <p>The project was successfully delivered and completed. The scope of work was achieved and we were successful to get an extension of time and cost to repair the infected parts in the raft.<br/><b>Learnings from the project are as below -</b></p>
                <ul>
                  <li>Different types of the concrete repair techniques.</li>
                  <li>The load path thru the structure and load amount carried by each and every element.</li>
                  <li>Liaising and discuss the design calculations with the authorities for the approvals</li>
                  <li>Correspondence required to claim for variations of cost and time.</li>
                  <li>Conduct the physical and chemical tests and study the results.</li>
                  <li>Lead the team at site and conduct the proper work to meet the project goals.</li>
                </ul>
                <p>I learnt the Tests methodology and objectives in this project thru the lab engineer, to originally decide which test to be conducted at what location was really a valuable knowledge. That’s in addition to the knowledge taken from using different chemical repair materials and the fiber carbon technology for the shear and compression strengthen were an updated techniques and technology gained.</p>
