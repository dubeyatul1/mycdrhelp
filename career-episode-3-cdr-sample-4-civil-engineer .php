                <p><strong>[CE 3.1]</strong></p>
                <p>Nour Building Systems was established in the year 1986 in Cairo, and established their branch in UAE on 2007, having about 30 years of experience in Building systems Industry, maintenance and specialized in steel fabrication and erection, with the full time working staff of 80 and an operative work force of 120. The Company is spread into various divisions such as construction, maintenance, property services, facilities management, steel fabrication and erection.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p>Nour Building Systems is an engineering consultancy firm, which provides the complete engineering services to the real state companies and developers. It comprises of a group of well-trained engineers in all engineering disciplines. The company provides the architectural concept, design of the residential, hospitality and public buildings, to meet the client requirements and expectation as well as the local authority regulation and international standards. That’s in addition to the complete structural, mechanical and electrical design and approvals which required prior to the construction.</p>
                <p><strong>[CE 3.3]</strong></p>
                <p>Damages in the residential building were identified at the ramp slab, retaining walls, some columns at the basement in addition to the water leakage observed at the sum pit, lift pit, where these damages were due to leakage at the retaining walls and raft due to some week points in the water proofing and the construction joint. Leakage caused some cracks and some corrosion in the steel, shrinkage cracks due to restraint walls of the basements slabs specialty at the ramp slab, and the leakage thru the expansion joint and from the drainage system occurred above the raft and below the slab on grade. Therefore, our main objective was to cure all the visual damages at the structural elements, strengthen the structural elements affected and resolve the water leakage source. The drawings showing the test locations and damages observed along with the proposed repair methodology been prepared and obtained the concerned authority approval.</p>
