                  <p>Shallow foundations are also called spread footings or open footings. The 'open' refers to the fact that the foundations are made by first excavating all the earth till the bottom of the footing, and then constructing the footing. During the early stages of work, the entire footing is visible to the eye, and is therefore called an open foundation. The idea is that each footing takes the concentrated load of the column and spreads it out over a large area, so that the actual weight on the soil does not exceed the save bearing capacity of the soil. There are several kinds of shallow footings: individual footings, strip footings and raft foundations. In cold climates, shallow foundations must be protected from freezing. This is because water in the soil around the foundation can freeze and expand, thereby damaging the foundation. These foundations should be built below the frost line, which is the level in the ground above which freezing occurs. If they cannot be built below the frost line, they should be protected by insulation: normally a little heat from the building will permeate into the soil and prevent freezing.</p>
