                  <h2>I drafted the factors affecting the selection of foundation</h2>
                  <p><b>On the basis of ground/soil condition -</b></p>
                  <ul>
                    <li>Shallow foundations are preferred where soil close to the surface is capable of supporting structure loads.</li>
                    <li>Where the ground close to the surface is not capable of supporting structural loads, hard strata is searched for and deep foundation is required.</li>
                    <li>Uniform stable ground requires relatively shallow foundation whereas filled up ground has low bearing capacity thus requires deep foundation.</li>
                    <li>Based on Loads from Building:</li>
                    <li>In the case of low-rise building in a larger area, the extent of loading is relatively low, so shallow foundation can resist the load from the structures.</li>
                    <li>In the case of the high-rise building built within less area have high loads. Therefore, the deep foundation is required as shallow foundation may not be able to resist such loads of greater intensity.</li>
                  </ul>
