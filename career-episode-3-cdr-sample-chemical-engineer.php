               <p><strong>[CE 3.5]</strong></p>
               <p>The main objective is to recommend the best possible design for VCM production and process. Besides, the objectives are to design green, sustainable and environment friendly process. It is decided that this plant will have a production capacity of 328 k-ton/year. Assuming the plant operates for 344 days, which means 21 days for maintenance work or unplanned shutdown occasion, it is calculated that the plant is to produce 636 mole of VCM per hour. The feedstocks are generally ethylene, chlorine gas, hydrochloric acid, and oxygen gas.</p>
               <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
               <p><strong>[CE 3.6]</strong></p>
               <p>I did the Project management of the system which invloved the modularization of the project in to definite number of stages &amp; created a classification of the activities to be accomplished. I prearranged the entire project in two stages.</p>
               <p><b>PHASE 1: </b>During this phase I finalized the study of the conceptual as well as philosophical nitty-gritties of the proposed work &amp; drew up an approach to achieve the desired goad.</p>
               <p><b>PHASE 2: </b>In this phase; I have employed strategies for successful execution. After execution I carried out the testing of the entire system.</p>
               <p><strong>[CE 3.7]</strong></p>
               <p><b>My Roles &amp; Responsibilities were -</b></p>
               <ul>
                 <li>Designing the heat integration system to minimize the energy consumption in the plant. It was done with the help of Hint Software which was designed to act as a tool to assist the minimization of energy. This task requires deep understanding of the Pinch technology.</li>
                 <li>Designing the oxy-chlorination reactor in order to maintain the temperature and pressure required for the reaction to occur. More attention was given to the temperature controller as the reaction is an exothermic reaction.</li>
                 <li>Designing the flash tank which is used to separate the products into two phases. It then purges out the unwanted gaseous products such as carbon dioxide, acetylene, unreacted ethylene, oxygen and nitrogen gas.</li>
                 <li>Deigning the waste treatment plant which is carefully designed so that the waste disposal classifications be in accordance with the laws and regulations set by the government. The treatment plant includes thermal incinerator, scrubber and chimney.</li>
                 <li>Process route screening to ensure the selection of the best process route for the production of VCM which is prior to the HYSYS simulation.</li>
                 <li>Simulating the process flow-sheeting using ASPEN HYSYS simulation. HYSYS modelling is used to simulate the real working process of a VCM plant. The simulation was done with the help of team members.</li>
                 <li>Assisting the team members in formulating the VCM plant layout. Basically, the site layout can be divided into two parts: Process area and Non-Process Area.</li>
               </ul>
               <p><strong>[CE 3.8]</strong></p>
               <p><b>Problem A</b></p>
               <p>The first main issue I faced was on how to integrate the cold and hot streams in the plant in order to minimize the energy consumption significantly. Before adopting the heat integration process, the plant consumes 42.96 MW. Intensive research and follow-up with the supervisor were done in order to find the optimal solution to the issue.</p>
               <p><b>Solution</b></p>
               <p>Long after, Pinch technology was found to be the best way to optimize the energy consumption. The pinch technology requires understanding of the cold and hot areas where the integration could be matched. Six hot and cold streams identified as a whole. After that, composite curves, table algorithm and grand composite curve were constructed to analyze the heat recovery and to fully optimize the energy. Maximum Energy Recovery (MER) method is used to pair two or more streams which results in minimum heat utilization. Energy consumption after integration was 22.2 MW. The energy saving adopting the Pinch design method was up to 50 %.</p>
               <p><strong>[CE 3.9]</strong></p>
               <p><b>Problem B</b></p>
               <p>The second issue was on how to simulate the process flow-sheeting as the simulation requires intensive knowledge of Aspen HYSYS simulation and how it functions. The team discussed the possible and practical approaches tackling the issue. The faculty has got many experts in HYSYS simulations so we sought assistance from the experts. The experts directed us to some references to read.</p>
               <p><b>Solution</b></p>
               <p>Intensive research was made so as to fully understand and apply the concepts. After two months, we gave a huge shot to run the simulation and interestingly it ran smoothly with no apparent issues. To confirm the simulation, we performed a manual calculation on mass and energy balance. The results from the manual mass and energy balance and the simulation were nearly even.</p>
               <p><strong>[CE 3.10]</strong></p>
               <h2 class="sample_heading">ENGINEERING DESIGN</h2>
               <p>Each member of the team was required to design one major equipment and one minor equipment. Oxy-chlorination reactor was set to be the major equipment to design. To produce VCM feedstock which is EDC, there are two ways either by direct chlorination or by oxy-chlorination process. The reactor that will be used to accumulate the oxy-chlorination process is packed bed reactors with shell and multiple contact tubes. The first step was to determine the operating temperature and pressure of the process which are 220 0C and 450 kPa respectively. The second step was to identify the appropriate volume and height. To determine the actual volume, manual calculations were involved. Moreover, Levenspiel plot (A graph of rate law versus conversion) was also adopted to find the volume which found to be 22.3 m3. The step after that was to calculate the number of tubes (2720 tubes) required by applying the equation (Nt = Actual Volume / Volume of one tube). Then, Shell inside diameter and Baffle diameter (5.007 m and 5.003 m respectively) were calculated using the below equations: Ds (Shell inside diameter) = 0. M + Bundle diameter D (Baffle Diameter) = Ds – 4.8 mm Since the reaction is exothermic, heat should be removed. The exothermic reaction can reach up to 500 oC. Heat Removal System was grounded.</p>
               <p><strong>[CE 3.11]</strong></p>
               <h2 class="sample_heading">ENGINEERING CALCULATIONS</h2>
               <p>I performed the design calculation for oxy-chlorination reactor. The design calculations include finding the optimum volume to satisfy the desired output. The calculations were done manually and explained in detail in the report. Besides that, some data obtained from Aspen HYSYS simulation were used to calculate the reactor volume.</p>
               <p><strong>[CE 3.12]</strong></p>
               <p>The simulator I used in this project was Aspen HYSYS which used to simulate the VCM flow-sheeting process. Moreover, I used Hint software as a tool to minimize the energy consumption in the plant</p>
