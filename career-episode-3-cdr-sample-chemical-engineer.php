               <p>This career episode is about my project titled “Design of Vinyl Chloride Monomer Production Plant”. This project was carried out and submitted for Bachelor degree requirement in Petroleum Engineering in ________________________________________________ from ……………..to ………………</p>
               <p><strong>[CE 3.1]</strong></p>
               <p>The project was done during the graduate course in Petroleum Engineering in ____________________. The project was executed in congregation of 5 candidates under the supervision and advantageous guidance of Dr. Pradip Chnadra Mandal.</p>
               <h2 class="sample_heading">BACKGROUND</h2>
               <p><strong>[CE 3.2]</strong></p>
               <p>Vinyl chloride is an organochloride with the formula H2C=CHCl that is also called vinyl chloride monomer, VCM. With an annual total production of 13 billion kilograms. VCM is among the top twenty largest petrochemicals (petroleum-derived chemicals) in world production. Normally, the need to find the process alternative that has the lowest cost is preferred, but it must be ensured that the process is safe, will satisfy environmental constraints, is easy to start up and operate, etc. The heuristics approach is applied, to eliminate certain process alternatives from further consideration, but it is necessary to design various alternatives and then to compare their cost. So, the goal of this project is to design an environmentally friendly, safe, and economically profitable vinyl chloride production plant.</p>
               <p><strong>[CE 3.3]</strong></p>
               <p>The purpose of this project was to fulfill the aptitude I have for Chemical Engineering. I joined the project as an additional member for the Academic Semester January 2016. The project supervisor was Dr. Pradip Chandra Mandal, whom I worked with for the two previous academic sessions. He welcomed the idea of me joining the group of Chemical Engineering Students, who are under his direct supervision, on their Plant Design Project course. However, I did not sit for any assessment throughout the project. I contributed where I could to my maximum capacity and learned a grasp of Chemical Engineering.</p>
               <p><strong>[CE 3.4]</strong></p>
               <p>This project work was completed in a flock of 5 students. I lead the team of engineers.<br/><b> The Hierarchy of the project is displayed below:</b></p>
