                      <h2>CE 3.1</h2>
                      <p>This career episode relates to project titled <strong>“Gawler East Link Road’’. </strong>The project was done as a Civil Drafter at Tonkin Consulting Engineering Adelaide South Australia.</p>
                      <p>Tonkin Consulting engineering was established in the year 1955 having about 63 years of experience in engineering industry. Tonkin Consulting Engineering started its operation from Woodville West in South Australia in only double garage and now expanded one of the leading engineering consultant in South Australia as well as in entire Australia. The company has diversified and spread into various divisions such as asset management, automotive engineering, building structures, construction administration, contaminated land, environmental consulting, Geographical information systems, infrastructure planning, land development, mine infrastructure, pavement design and management, storm water management, transport planning, landfill engineering, water resource management.</p>
                      <h2>BACKGROUND</h2>
                      <h2>CE 3.2</h2>
                      <p>The state government of South Australia announced $55 million for this project in 2015-2016 budgeting plan. The major construction of this project is expected to complete by end of the year 2019 and the post construction such as operation and maintenance will start after the year 2019. This project is the 30-year plan for greater Adelaide and to develop the town Gawler. It provides opportunity to traffic user direct access to Main North Road without passing through Gawler town center and reduces the travel time as well as fuel emission. This project is expected to provide economic benefits and will support jobs for local people over the life cycle of the project.</p>
                      <p><b>Objective</b></p>
                      <p>The objective is simply completing the project through designing and construction stage. But my role was drawing drainage and making cross section of the abutment of the bridge. The bridge need to make on the way to this road. I was also responsible to check the final bridge CAD design drawings and validate their effectiveness. It was necessary to include all trades in these CAD drawings.</p>
