                      <h2>CE 3.5</h2>
                      <p>I used AutoCAD and Auto TURN software in this project. As I was junior trainee level, I was asking some drawing related questions to my leader. My leader used to make meetings every two weeks, talking how the work is going on to track the work process.</p>
                      <p>I did some drawing of making contour line, making drainage using AutoCAD software and drawn road using AUTOTURN for vehicle specification.</p>
                      <p>Gantt chart was used to track the work to accomplish on time which showed the percentage of work done, percentage of work left. How time left to finish task before hitting the target date.</p>
