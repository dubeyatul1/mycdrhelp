                <p><strong>[CE 3.7]</strong></p>
                <h2>Problem Faced &amp; their rectifications</h2>
                <p><b>Problem A</b></p>
                <p><b>Problem Definition: </b>While working on the sewage pipelines I faced a problem of applying the designed slopes along the pipeline network.</p>
                <p><b>Effect of problem: </b>Connecting the site pipeline network to the main network line outside the factory was impossible since the level of the exit point of the inner network is a 40 cm lower than the main manhole we are supposed to connect with.</p>
                <p><b>Root Cause of Problem: </b>When preparing the land for the installation of the foundations there was a mistake in some buildings base levels up to 20 cm</p>
                <p><b>Final Solution: </b>I decided to ignore the original slope design and redistributed the slope according to the data I obtained from the current situation. With a very small range of slope flexibility, I succeeded in connecting the networks proficiently.</p>
                <p><b>Problem B:</b></p>
                <p>During our work compacting the area for the industrial workshop, I noticed that there was a specific points experienced settlement compared to the rest of the area even after adding another two layers of soil they continued to level down by the next day. We tried adding soil for three times in 5 days and no change was observed.</p>
                <p><b>Effect of problem: </b>We are not able to continue with the plane concrete step in order to fix the steel and construct the reinforced concrete floor.</p>
                <p><b>Root Cause of Problem: </b>After finding no explanation to this; I suggested that we should dig these two points to understand the situation by finding out to what depth we can reach and get a sample of the soil to examine. My suggestion was approved and I led the process, we went down about 2 m to find out circular holes almost horizontally prolonged, one to the second hole and the second towards the mountain behind the site, the soil was expanding within the holes.</p>
                <p><b>Final Solution: </b>We cut all the surrounding area inside the workshop to a level which is beneath the level of the holes and refilled it with layers of soil of 25 cm each and compacted them one by one to reach our base level and we followed the cave to outside the site, uncovered it and destroyed it.</p>
