                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p>I worked as a Senior Research Student / Team Lead to assist my junior batch in completion of their Final Year Project, I was accompanied by two of my project fellows Engr. Mahfoz &amp; Engr. Farhan during year 2010 – 2011, our focus was to design an embedded system based project, so after extensive research and brain storming we have chosen” Wireless Control Of Single Phase AC Motor Speed With High Precision” from the available list of projects which were available to the final year students under various Phd Qualified Faculty Members. The project was supervised by Senior Faculty Member of CASE Dr. Saeed ur Rehman.</p>
                <p>A technology commonly known as wireless control is being implemented to control various devices and home appliances such as TV, AC etc. Taking this basic concept, I implemented the wireless control technology on” SINGLE PHASE AC MOTORS”. Controlling the motor speed through remote control, have many applications like we can control the speed of fan or desert cooler or the water pump motor of the fountain and so on.</p>
                <p><strong>[CE 3.3]</strong></p>
                <p>The project aims to assists domestic &amp; industrial consumers to control SINGLE PHASE AC MOTOR equipment such as (Fans, Air Coolers and Water Pumps) with a speed variation ranging from (1 – 99) since the appliances available in market have been offering only three modes of speed operations (Low, Medium and High) and usually implemented in DC Systems. Our project facilitated the consumer to adjust the speed control as per their requirement. Similarly, same system is appropriate for governing lighting intensity.</p>
                <p>In this project I have controlled the speed of single phase AC motor with the aid of remote control with high precision. High precision means that speed will upsurge or decrease not only in one or two steps but in quantity of steps (100 steps).</p>
                <p>As I controlled the speed through remote control I used microcontroller to decode the pulses produced by the remote control so that action was taken accordingly. Our project mainly comprises of three parts Power Electronics, Embedded Systems and Wireless Control.</p>
