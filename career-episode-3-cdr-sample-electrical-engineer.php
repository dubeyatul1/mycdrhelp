                <p>Center for Advanced Studies in Engineering (CASE) was established in 2001 with the main campus situated in the heart of Islamabad in G-5 / 1 sector Pakistan. CASE provides its students state of the art educational facilities with highly qualified faculty from USA and rest of the world. CASE has established its own research center to enable engineering students to work along with researchers, and scientists to attain practical knowledge in research &amp; development sector, which is known as CARE (Center for advanced Research in Engineering).</p>
