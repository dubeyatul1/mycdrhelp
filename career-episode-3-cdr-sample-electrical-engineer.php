                <h2>Hardware Damage</h2>
                <p>During the hardware design and testing, the electronic components got damaged as reversed voltage was applied to the circuitry by one of the team member. As per my previous design experience I emphasized the students to design the built-in power supply for all the electronic circuitry and designs. The external power supply can be used during initial stages for the result verifications however it has always been the best practice amongst the professionals to design &amp; execute power supply within the circuitry for easy trouble shooting, all the electronic components can be effortlessly checked. I implemented Reverse voltage protection in the power supply by adding a diode in the negative Terminal of the power supply so that none of the IC could be demerged if the reveres voltage is applied accidently.</p>
