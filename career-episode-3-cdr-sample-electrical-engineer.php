                <h2>IR Transmitter: Phillips Remote Control:</h2>
                <p>My job here was to decode the receiving waveforms at the demodulator output. I observed the direction of the changing at the middle of the bit, if going down, means bit 0, going up, means bit 1. But it was easy to sample the middle of the first half of the bit, so it directly tells us what the bit state is. I was decoding the pulses through Manchester scheme because Phillips remote control was using RC5 protocol and Manchester was the decoding scheme.</p>
                <p>In the Philips remotes, the bit #3 is the CHECK bit, every time you press a key at the remote, even pressing repeatedly the same key, this bit flips state.</p>
                <p><strong>[CE 3.7]</strong></p>
                <p>Decoding the IR transmitter waveform:</p>
                <p>I used SONY remote control and PHILLIPS remote control to change the speed of the single-phase AC motor speed. By using two different remote controls, added diversity in our code.</p>
                <p>Decoding Sony remote control waveform:</p>
                <ul>
                  <li>I used Pin 3 of port 3 of 89s52 microcontroller to receive the remote-control waveforms and by using external interrupt I counted the high to low transitions of input remote signals.</li>
                  <li>The first two pulse transitions in every waveform were the same (the time for the first two transitions is same). After these two pulses the waveforms changed their pattern. Based on this observation I have applied the logic that after these two transitions I applied a check on the waveform transitions and acted accordingly i.e. either to increase the speed or decrease the speed or stop the motor or run the motor in normal mode.</li>
                  <li>After two falling edges of received remote control signal, I started the timer for 500µs. After every overflow of timer i.e. after every 500µs we get one value (0 or 1). I saved the results in an array. As we are using the timer interrupt auto-reload whose maximum value is 276 µs which is less than 500 µs, so we run the timer two times for 250 µs to get value of 0 or 1 after 500µs.</li>
                  <li>I used PROTEUS software on which we produced waveforms just like those emitted from remote control and those waveforms were shown on the oscilloscope available on PROTEUS software and compared those waveforms with actual remote waveforms and results showed they were the same. The waveforms shown on PROTEUS oscilloscope are shown below.</li>
                </ul>
