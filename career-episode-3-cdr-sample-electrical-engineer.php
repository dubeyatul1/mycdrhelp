                <h2>Wireless Control Design</h2>
                <p>Main problem we faced while using various type of remote control existing in the market, most of the waveform I checked using receiver circuit gave us distorted waveform which was very firm to detect. Therefore, I kept the focus over using only 2 kinds of remote control offered in the marketplace.</p>
                <p>The soul of our project was wireless control of single phase A.C motor speed. Phillips and Sony remote control was used as transmitter and simple IR receiver was used on the receiver side. Four buttons (1, 2, 3 and 4) of Phillips remote control and Sony remote control were used to execute four commands namely Speed Up, Speed Down, Off and On/Normal Speed.</p>
