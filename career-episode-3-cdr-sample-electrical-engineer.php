                <h2>Cut off Frequency Setup for TRIAC</h2>
                <p>TRIAC was connected to MOC3023 on one side and the inductive load on the supplementary side. We were implementing the dimmer circuit, but the variance is that in dimmer circuits DIAC was used to trigger the TRIAC but here we are implying microcontroller to trigger the TRIAC. Our project goal to variate the speed from 1 to 100 and this task is attained by creating pulses of different widths which are fed into pin 2 of MOC3023. The TRIAC is synchronized to the input AC power signal through zero crossing circuit. The task to increase speed of single phase AC motor by one step through 1 to 100 was achieved by following steps. f= 50 Hz, T=1/50=20ms 20ms/2=10 ms %for half wave 10ms/99=101microsec %dividing into 99 parts This means that variance amid two steps is 101 micro seconds.</p>
