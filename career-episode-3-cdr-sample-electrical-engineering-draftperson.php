                      <h2>Personal Engineering Activity</h2>
                      <p><strong>[CE 3.5]</strong></p>
                      <h2>My Roles &amp; Responsibilities</h2>
                      <ul>
                        <li>Leading digital transformation study; creating the technology roadmap for cloud migration and operations.</li>
                        <li>Requirement gathering with focus on back-office operations; conduct a data-map and systems map across the organization for identifying the applications that can move to the cloud</li>
                        <li>Management of the stake-holder discussions, requirement gathering sessions, auditing the BA work being done and utilizing best practices to documentation</li>
                        <li>Modelling the digital plans and aligning with company strategy and in turn mapping the functional specifications to the systems.</li>
                        <li>Monitoring the BA work being done by team and ownership of complete documentation of the project</li>
                        <li>Leading the team of 2 in the complete project; monitoring, reviewing, supporting team in requirement capturing and mapping business with technology.</li>
                      </ul>
                      <p><strong>[CE 3.6]</strong></p>
                
                      <p>This project was a pioneer initiative for the client. As part of digital transformation process, the biggest issue I encountered was lack of overall organization digital strategy. For us to conduct a complete requirement elicit session, it became challenge when the client’s team was not ready.</p>
                      <p>The solution I adopted was to conduct a complete digital awareness session and develop a detailed project charter with RACI MATRIX.<br/> <b>This primarily enabled the client to -</b></p>
                      <ul>
                        <li>Involve appropriate stakeholders into the project</li>
                        <li>Have a transparent view of what is the problem statement and the goal to be achieved</li>
                        <li>The journey itself was clearer, since now client had more clarity and directive on their investment and outcome</li>
                      </ul>
                      <p>The second problem I faced was the projection of TO-BE related costs while redefining the SaaS and infrastructure platforms; which client saw as a cost impact rather than as an investment.</p>
                      <p>Since this was phase 1 and initiated from the CIO’s desk, it was a top-down approach inside the organization.<br/><b> The following steps helped resolve a clear path for success of the project -</b></p>
                      <ul>
                        <li>I along with the team had to create multiple scenarios to depict the TO-BE state suitable for the organization and its subsidiaries.</li>
                        <li>The security feature required client’s central security team to be fully involved and CISO to be part of the decision making.</li>
                        <li>I conducted interviews with the product teams to analyze the impacts application-wise as well as on operations.</li>
                      </ul>
                      <p>The third issue faced was moving the client’s systems to the Alibaba Cloud. The challenge here was that in a brown-field project, it requires more detailed analysis of the existing environment before moving to the cloud. The applications were running on different versions and related infrastructure was on inflexible technology stack in most cases.</p>
                      <p><strong>Solution:</strong></p>
                      <p>The client’s enterprise architecture team was fully engaged to plan for refactoring the existing architecture. Additional technical resources were employed to develop new API and tests plans were developed. The second activity was to schedule a phase-wise application migration to the cloud in a spiral model. This ensured that each application was isolated and tested on the cloud for data flow and lesser impact of the cloud migration.</p>
                      <p><strong>[CE 3.7]</strong></p>
                      <p>I designed the migration chart (detailed) along with risks and impacts for the client when they start the next steps. While the high-level design in place, it was imperative to define the applications that needed to be moved on Cloud immediately, which required a cloud migration plan.</p>
                      <p>The other solution I designed was the TO-BE business case, which had all the risk assessment and impact analysis done. While there were multiple scenarios for client to adopt, the core business case needed to be sound and become part of the critical documentation for digital transformation within the timelines and budgets of the CIO.</p>
                      <p><strong>[CE 3.8]</strong></p>
                      <p>I used MS Office, Project and Visio mainly for developing the functional mapping, documentation, design, and communication.</p>
                      <p>This project was critical for the client’s business. I took help from Alibaba technical team client’s enterprise architecture team to ensure success of the project in terms of moving to the Cloud and understanding the underlying portions of technology which were not documented. I also got help from the existing team on the systems architecture designs which were needed fresh in this case to ensure the future state can be defined</p>
                      <p>I introduced a comprehensive mind-map for the client’s architecture; which had complete infrastructure components and flags at various levels to work breakdown structure.</p>
                      <p><strong>[CE 3.9]</strong></p>
                      <p>As part of this project we had weekly review meetings on project status, monthly reviews and I was part of executive meetings as well. All these are standard part of governance procedure in such projects.</p>
                      <p>I had daily meeting with the client project manager and regular meetings with my team on the project progress. I also had weekly update meetings with the CIO for this project. Signoff meetings were held every week to ensure we have sign-off from client on the work done.</p>
                      <p>As I was leading this project, all presentations to stake-holders were given by me. I also gave presentations on our findings, gap and impact analysis.</p>
                      <p><strong>[CE 3.10]</strong></p>
                      <h2>Summary</h2>
                      <p>The project was successfully completed on time. I utilized project management procedure for managing the successful completion of this engagement. Project met all necessary goals set internally as well as from the client. I got greater exposure to the Cloud Transformation process and now got templates for assessments which can be done faster and more accurately in future.</p>
                      <p>This was an important project for MEERAS, and I owned the solution for future state which involved digital initiatives across the organization. My main contribution was completing this project with no delays and ensuring customer satisfaction. This project involved more than business analysis and I was responsible for defining the complete study, build the TO-BE models, &amp; ensure a complete guideline for the roadmap.</p>
