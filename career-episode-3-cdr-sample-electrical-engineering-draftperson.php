                      <p><strong>[CE 3.1]</strong></p>
                      <p>This career episode relates to project titled <strong>“Digital Transformation – Phase 1’’.</strong> The project was done during my tenure with <strong>MEERAS.</strong></p>
                      <h2>Background</h2>
                      <p><strong>[CE 3.2]</strong></p>
                      <p>YVOLV, is part of Alibaba Cloud Services locally hosted in Dubai. The company offers state-of-the-art, customized solutions in cloud computing and Digital Transformation to private companies and Government institutions in the MENA region. The list of offering includes IaaS, PaaS, Cloud computing, Big Data, Digital Transformation and Hosted Managed Services.</p>
                      <p><strong>[CE 3.3]</strong></p>
                      <p>The digital transformation project was for MEERAS, which is a semi government company into Retail, Leisure, Entertainment, Healthcare and Hospitality. The client required a complete move from traditional to digital by 2019. Overall technology shift included shifting operational processes, enhancing customer experience through Omni-channels for social communication, and in return increase in efficiency and productivity. The project was Phase 1 of the 3 phased approach towards digitization. The scope of this project was to develop a roadmap, move applications on to Alibaba Cloud and automating some operations as part of phase 1.</p>
                      <p><strong>[CE 3.4]</strong></p>
                      <p><b>The main objectives of this project were:</b></p>
                      <ul>
                        <li>Conducting complete study of the existing environment and traditional processes which were employed across the organization and its subsidiaries; and developing a road-map for Cloud shift, analytics and AI driven processes in future.</li>
                        <li>Move applications to Alibaba Cloud, utilizing the cloud infrastructure for backend ERP and CRM; in parallel creating platform for mobile application development</li>
                        <li>Automation of some identified processes in the organization, partly to initiate paperless environment; introduce intelligent chatbots for business support</li>
                      </ul>
                      <p><strong>The project Hierarchy is given below -</strong></p>
