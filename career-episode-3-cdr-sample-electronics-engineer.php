                <p class="para">The project was the most recent one, and assigned to me as a part of my course as a Masters Project (Minor). This project carried 100% load of total subject assessment. In this project, I had proposed a voltage and current controlled circuit for the visible light communication, otherwise known as Li-Fi. I had to carry out in-depth research regarding the types of communications in this project and thereby, after a complete analysis discuss my proposed model for the pros and cons of this technology, also discussing the future scope and improvements required in my project. Unlike my previous projects, I used Arduino platform, whereby I used the microcontrollers of AVR family. I had around four months’ time to accomplish this project and I had to submit report by the end of the project as a part of the assessment.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p class="para">As a part of my course requirement during masters’ degree at the University of South Australia, I was given a project on “Software Defined Visible Light Communication” in which I had to propose circuits for communication using visible light as source, unlike the conventional RF-based communication systems. I had been into the field of communications for quite some time and this project was a good learning opportunity for me. This project appeared to be quite promising to me as I had to do decent research for this particular project. Therefore, I was highly enthusiastic for the same and dedicated four months to do this project.</p>
                <p><strong>[CE 3.3]</strong></p>
                <p class="para">During recent time, the White Cool Light Emitting Diode has taken over the conventional RF systems due to its several advantages like large bandwidth, good life expectancy, etc. it is now viewed as the communication technology of the future and is quite promising for the future. Also, the limited amount of frequency in the RF band requires us to find a new alternative to the existing technology. Hence, through this project, I got a chance to study about the emerging technologies and made use of Software Defined Radio for the same.<br/><b> During this project, my objectives were:</b></p>
                <ul>
                  <li>Propose the proper led control circuit for the transmitter design for generating a different waveform.</li>
                  <li>Design a current control circuit to provide the constant current.</li>
                  <li>Make a prototype of this transmitter circuit and demonstrate its efficiency by using LEDs.</li>
                  <li>Detailed explanation of the experimental work carried out on the prototypes and discuss the performance.</li>
                  <li>Suggest a guideline for the design and implementation of the future development of the prototypes.</li>
                </ul>
                <p><strong>[CE 3.4]</strong></p>
                <p class="para">Though it was an individual project, I had the luxury to consult my mentors and the Industry personnel from time to time. I took help of my instructor/Professor for completing my thesis in the given timeframe.</p>
                <h2 class="sample_heading">PERSONAL ACTIVITY AREA</h2>
                <p><strong>[CE 3.5]</strong></p>
                <p class="para">Since this project required quite a lot of research in the field of communications, I began researching on the topic. I visited various websites, read research papers and articles on the internet, and went through few books on communications as well in order to understand it. I started my research from the history of communications, it’s importance, the type of technology existing currently, it’s pros, cons and step by step getting into the Visible Light Communication (VLC). Next, I compared the VLC with RF-based Communication systems, and outlined some of the key differences between the two. <br/><b>I observed that:</b></p>
                <ul>
                  <li>VLC based systems tend to have much larger bandwidth compared to the existing RF systems.</li>
                  <li>VLC systems are energy efficient and require minimum maintenance cost.</li>
                  <li>VLC systems use LEDs as transmitting source. Their easy availability makes VLC systems low cost systems. Also, RF communication systems require power for operation as compared to their VLC counterparts, hence adding to the low cost advantage of VLC systems.</li>
                  <li>Unlike the RF systems, VLC systems don’t suffer from problems like attenuation, diffraction, Electromagnetic Interference, etc. and are less obstructed. They also tend to be more secure than RF systems.</li>
                </ul>
                <p class="para">I also analyzed the pros and cons of VLC systems. Next, I researched on Software Defined Radio (SDR) that plays a key role in VLC systems. I browsed through many articles by IEEE and many more prestigious organizations. I analyzed SDR, it’s schematic structure, it’s benefits, and some common terminologies related to it like Adaptive Radio, Cognitive Radio. Below is a schematic diagram of SDR (fig. 11).</p>
                <p class="para">I further continued my research on Software Defined Visible Light Communication (SDVLC), in which I analyzed all the previous works on the same.</p>
                <p><strong>[CE 3.6]</strong></p>
                <p class="para">After having analyzed my topic in great depth, I started to work on it. I used Proteus software for my research and simulated the circuit using Arduino platform, ATmega8 microcontroller boards. I programmed the microcontroller as per my requirements. Fig 12 shows the simulated circuit diagram using Proteus.</p>
                <p class="para">As one can see, the MOSFET is used to drive the LED such that the LED is switched ON when MOSFET is given Logic “1” at the gate terminal, and LED switches OFF when given Logic “0”. The Input train “1” or “0” is given by the controller ATmega 8 that has been programmed likewise. I also simulated 5V DC power supply to drive the circuit by using appropriate components (step down transformer, rectifier circuit, capacitor filter, and IC 7805).</p>
                <p class="para">The green LED was initially ON, implying that it was receiving some input, while the ON state of yellow LED signified presence of 5V DC as output from IC 7805. While analyzing the circuit I found that some output displayed at the oscilloscope when the green LED blinked on debugging. I also encountered some problems which led to a Modified Voltage Control Circuit (fig. 13).</p>
                <p><strong>[CE 3.7]</strong></p>
                <p class="para">Through the analysis of fig. 12, I found two areas of improvement. First, I needed 5V power supply for Arduino board as we can use our laptop for the same purpose. Second is the danger of the LED burning out because of absence of resistance between the LED and the ground. One way to solve this would be using a resistance between LED and ground or to make use of current control circuit (fig. 14) that draws out the excess current that might burn the LED.</p>
                <p class="para">The working of Current Control Circuit is simple. On application of power supply, the MOSFET is turned ON by the gate resistor RG. This allows the current to travel through the LED, MOSFET and sensing resistor RS. In accordance with Ohm’s law, the current across RSSS increases the voltage drop which switches ON the transistor. When the base emitter voltage is below 0.7V, the MOSFET is switched OFF, hence, controlling the current run through the LED.</p>
                <p><strong>[CE 3.8]</strong></p>
                <p class="para">All of my analysis gave me the final transmitter circuit that is capable of generating waveforms (fig. 15)</p>
                <p><strong>[CE 3.9]</strong></p>
                <p class="para">All the analysis previously mentioned helped me propose the final circuit. Next, I researched on the various components of the circuit (diodes, LEDs, MOSFET, etc.) to help choose the parts (model and type) accordingly. I narrowed down to the following components:</p>
                <p class="para">BXRA-56C5300-H-00 (LED): This LED is manufactured by Bridgelux family and delivers compact and cost-effective solid-state lighting solutions and the high performance to attend the universal illumination or lighting marketplace. It has a decent lifetime, higher efficiency and reliability with the light output levels of many conventional lighting sources. It overtakes the existing light sources like High Wattage Compact Fluorescent Light (CFL), High Intensity Discharge (HID) conventional light sources. It delivers 3,000-10,000 lumens under neutral, warm and cool white color temperature conditions and provides high quality illumination. Hence, I chose this LED. It is shown in fig. 16.</p>
                <p class="para">IRFZ44N (MOSFET): It is an N-Channel MOSFET that uses “trench” technology. It is shown in fig. 17.</p>
                <p class="para">ATmega 8: It is an 8-bit controller of AVR family that uses Arduino platform for its programming. It was one of the major components of the proposed circuit and controls the whole circuit.</p>
                <p class="para">BC547 (Transistor): It is N-P-N transistor with maximum current gain of 800. It is shown in fig. 19.</p>
                <p><strong>[CE 3.10]</strong></p>
                <p class="para">My thorough research and deep understanding helped me conclude VLC as the technology of the future era. I was able to design the circuit and learn a lot about VLC communication systems. In the end, I documented my work in a report and was able to complete the work in the given timeframe.</p>
