                <h2>HCIS – Integrated Security Systems</h2>
                <ol type="A">
                  <h2 class="sample_heading">INTRODUCTION</h2>
                  <p><strong>[CE 3.1]</strong></p>
                  <p>I worked on this project as Engineering Manager in National Industrialization Company (TASNEE).</p>
                  <p>Project Name: HCIS- Integrated Security Systems</p>
                  <p>Duration: 30th Nov 2014 - 5th May 2018</p>
                  <p>Project Location: Jubail Industrial City, Jubail, Saudi Arabia</p>
                  <p>Name of the Organization: National Industrialization Company (TASNEE)</p>
                  <p>Position: Engineering Manager</p>
                  <p>Supervisor: Head of Department, Project Execution Organization</p>
                  <h2 class="sample_heading">BACKGROUND</h2>
                  <p><strong>[CE 3.2]</strong></p>
                  <p>Tasnee was established in 1985 as the first Saudi private sector's fully owned joint stock industrial company with the aim of advancing the economic diversification in Saudi Arabia. TASNEE’s Petrochemical complex in Jubail Industrial City Saudi Arabia's second largest Petrochemical producer, after SABIC. Due to the importance of petroleum, industrial and services facilities, the Saudi Arabia’s leadership urged the establishment of a High Commission for Industrial Security and set the policies and plans to protect such facilities overseeing the areas of security, safety and fire protection, to ensure on-going process of work and production under all circumstances. Thus, a royal decree number 8/439, dated 27/2/1397 H., was issued approving the formation of the High Commission for Industrial Security with H.R.H. Crown Prince, Deputy Premier and Interior Minister as its Chairmen, and membership of a number of heir royal highnesses and their excellences.</p>
                  <p><strong>[CE 3.3]</strong></p>
                  <p>The High Commission for Industrial Security (HCIS) had made it mandatory for all Petrochemical Companies to upgrade their Security Systems for compliance to the HCIS Directives. Hence, the project charter was compliance to HCIS 2010 Directives and implement all necessary changes to the TASNEE Security Infrastructure.</p>
                  <p><strong>[CE 3.4]</strong></p>
                  <p><b>My duties and responsibilities in the project were: </b></p>
                  <ul>
                    <li>Coordinating Engineering activities and support Technical Operations</li>
                    <li>Oversee Engineering requirements for Plant Operation and Maintenance</li>
                    <li>Determine and implementing Management of Change (MOCs), review engineering strategies and monitoring implementations as per company policy and procedures.</li>
                    <li>Develop process procedures for carrying out various engineering phases</li>
                    <li>Present Performance for Projects Costs and Schedules</li>
                    <li>Ensuring compliance to applicable Engineering Standards and Codes</li>
                    <li>Support Research Center for R&amp;D related projects</li>
                    <li>Coordinate with Business Planning and Marketing team for modifications, revamp and update of Plant as per Market requirements.</li>
                    <li>Selection and Recruitment of Discipline Engineers</li>
                    <li>Lead team of discipline engineers</li>
                    <li>Prepare Scope of Work for suppliers/contractors to execute jobs at the plant</li>
                  </ul>
                  <p><strong>[CE 3.5]</strong></p>
                  <p><b>The organizational chart below showed my position in the project: </b></p>
