                  <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                  <p><strong>[CE 3.6]</strong></p>
                  <p>After reviewing the HCIS 2010 Directives, I had prepared a Project Scope and assigned resources. I had put together my team with the following discipline engineers and assigned them the followings tasks:</p>
                  <ul>
                    <li>Manager QA/QC to ensure compliance to specification and carrying out necessary inspection test plants</li>
                    <li>Manager SHE to ensure compliance to all aspects of safety, both for internal client (i.e. maintaining TASNEE internal safety standards) and for external client (i.e. compliance with HCIS directives)</li>
                    <li>Electronics Engineer to review the equipment specifications like X-Ray machines, Cameras, Turnstiles and barrier systems</li>
                    <li>Electrical Engineer to conduct Load studies and ensure adequate power is made available, including cable selection and junction boxes</li>
                    <li>Mechanical Engineer to review all mechanical related items including mechanical works for the Search Facility Building.</li>
                    <li>IT Engineer to review all communication between access points and cameras to the main integrated server system.</li>
                    <li>Civil Engineer to review all static bollards, fencing and excavation works.</li>
                  </ul>
