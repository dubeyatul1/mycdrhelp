                  <p>The project started off with the FEED phase and I worked with the HCIS Consulting firm to review the 30% and 60% Models. After completion of FEED, I used the MTO (Material Take Off) and assigned each of the discipline engineer assisted in coming costing data. I prepared Cost Estimate to be presented to the management for budget. <b>Below is the precis of my cost estimate results:</b></p>
