                  <p>As part of the project progress reporting, I had directed my team to give me periodic input regarding the project progress. I had to conduct high level monthly internal and external customer project progress meeting Internal clients included staff from Operations and external clients included personnel from Industrial Security. One of the mandate from Industrial Security was that during the execution of the project, the perimeter security shall not be compromised at any given time.</p>
