                  <p>I was involved in managing my team staffs and I had the safety officer to put up safety signs. I had arranged the regular awareness program among all the team staff about the security system. Then I was involved in conducting the morning toolbox and a weekly management safety walk through. After that, I conducted the regular meeting for assigning the safety signs in each block of the buildings. Additionally, I researched various safety tools and arranged them in the procurement order for assigning each safety sign. While selecting the safety tools, the power consuming and cost effective safety tools were given priority. Furthermore, I arranged a meeting with the project coordinator for the arrangement of the best safety tools that suit the designed security system. Finally, I arranged the training of the project coordinators with the high-class family of the safety signs managing team.</p>
                  <p><strong>[CE 3.13]</strong></p>
                  <p><b>The following were the main difficulties that I faced during the project time -</b></p>
                  <ul>
                    <li>HCIS (high commission for industrial security) noted that critical assets such as storage and refrigerant tank, substation were located within the 60 meters from the facility perimeter fence. However, the location of these assets did not comply with SEC-02 section 4.9 which required a 60-meter clear zone from the critical assets to the perimeter fence. Further COD proposed a “video fence” on the dike wall but that did not mitigate the potential risks associated with the limited distance of the assets from the perimeter fence. So for this, I added the additional measures of using dedicated fixed CCTV cameras with video analytics that were included in 30% design to extend the 60-meter clear zone requirements beyond the facility perimeter. Hence the addition of CCTV helped in locating the assets within the required distance.</li>
                    <li>HCIS noted on drawing KDOCS-062PF-COD-0001 that the security control center and support facilities indicated near the main gate was inside the perimeter clear zone and the walls formed part of the perimeter fence. The location of the building violated SEC-02 section 4.5 and 4.9. HCIS SEC directives did not provide for building walls to replace the perimeter fence as stipulated in SEC-02 section 4.5. In order to solve this problem, I advised that the security control center was to be relocated near to truck unloading area.</li>
                    <li>One of the directives of HCIS was to conduct and then develop a worst-case scenarios report of blast consequences in case of vapor cloud explosion due to the presence of hydrocarbons within the petrochemical plant. Two scenarios were developed to facilitate the blast consequence analysis. One was “worst case” scenario for a release due to hydrocarbon tank/drum leakage and other was “Most Likely” scenario for a release due to pipe rupture. The VCE could cause the complete destruction of certain parts of the building and serious injuries to humans in certain areas. Overpressure impacts of 1psi or greater caused by VCEs in the worst case scenarios could extend to more than 840m from the centre of VCEs and most likely scenarios could extend to more than 440m from the center of VCEs. I proposed the breeze modelling software for doing explosion damage assessment modelling and incident analysis. The software implied the theory of distance/damage correlations, pressure/impulse/damage correlation, blast effect curves, and blast resistance of the building and human injury resistance of blasts to each designed scenario. The images below show the scenarios.</li>
                  </ul>
