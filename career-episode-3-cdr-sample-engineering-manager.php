                  <p>I identified risks, maintained a risk register and updated risk which helped the project a lot to keep on track and avoid cost overruns and schedule slippages. I did a market survey and prequalified contractors to execute this project. Based on the FEED, I prepared the SOW and RRP package for the construction companies specializing in implementing safety projects. I reviewed the bids received form a technical standpoint and later once the commercial bids were opened, I was assigned to go back to the BOM (Bill of Material) and do value engineering to see where quantities can be optimized. These iterations in value engineering resulting in much lower final bid price.</p>
