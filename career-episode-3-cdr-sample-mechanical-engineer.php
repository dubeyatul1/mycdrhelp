                <p><strong>[CE 3.1]</strong></p>
                <p align="justify">I joined GE in a Services Leadership Program which was designed to train experienced new hires about GE’s design, operation and financial processes on fast track basis. I had the opportunity to learn and get hands-on experience of maintenance and troubleshooting of several Industrial Gas Turbine ranging from 50MW to 180MW. After completing my one year on job training, I was assigned as Lead Contract Performance Manager to a multi-million dollar long-term maintenance contract with one of prestigious customer i.e. ENGIE. After my hiring I was responsible for preparing computerized maintenance plans, budgeting, coordinating to resolve all technical and contractual issues, executing planned &amp; unplanned outages of assigned power plants. I was also responsible for availability and reliability guarantees of the plants.</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p align="justify">My assigned Power Plant was having 9 x GE Frame 7FA.03 &amp; 2 x 7EA Gas Turbines with the total capacity of 1800 MW. On January 5 2016 my M&amp;D informed me that there wa sudden increase in vibration and decrease in compressor discharge pressure (CPD) in one of the 7FA.03 unit installed at Juaymah site. I immediately arranged borescope inspection and found that unit was completed wrecked. It was catastrophic failure which damaged complete compressor rotor, stators, casing and some of turbine components. As contract &amp; site owner I was responsible for execution of this major outage.</p>
                <p><strong>[CE 3.3]</strong></p>
                <p align="justify">My most critical tasks in this job were:</p>
                <ul>
                  <li>Arrangement of parts and special resources in short time</li>
                  <li>Installation of new rotor</li>
                  <li>Installation and alignment of casing</li>
                  <li>Installation of enhanced compressor package-3 component</li>
                </ul>
                <p><strong>[CE 3.4]</strong></p>
                <p align="justify">The project Hierarchy is given below. I was responsible to identify scope of work, identify and arrange required resources and parts, execute the job and resolve all technical and administrative hurdles till completion of project.</p>
                <img src="images/mechanical-engineer-CE-03-4.png">
                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 3.5]</strong></p>
                <p align="justify">Damaged Assessment: After disassembly I started the project by conducting detailed inspection for making damage assessment of all the compressor rotor &amp; stator vanes. The Mid Compressor Casing (MCC) was also damaged. Pictures of damaged rotor and stator vanes are given below.</p>
                <img src="images/mechanical-engineer-CE-03-5.png">
                <p><strong>[CE 3.6]</strong></p>
                <p align="justify">In addition to rotor/stator, I performed damaged mapping on MCC as well. Following were my comments after damage assessment and structural analyses of casing:</p>
                <ul>
                  <li>Extraction ports flow path – R9 was critical life limiting location and plug repair could not to be applied due to thin casing ligament.</li>
                  <li>The gouges will act as stress concentration resulting in crack while in operation over a period.</li>
                  <li>Gouges &gt;130 mils on stage 9 would require approximately 10-14 plugs to repair, which was beyond GE fleet experience for a single stage scattered in all the quadrants, could lead to potential casing deformation/operational issues.</li>
                  <li>Too many plugs if applied could have a debit in casing life, as the stresses were locally increased with each plug.</li>
                  <li>Local Stress increase may lead to Casing out of Roundness beyond re-usable limits or may have operational issues.</li>
                </ul>
                <p align="justify">On above grounds, I recommended to the customer that complete casing was also required to be replaced in addition to rotor and stator vanes.</p>
                <p><strong>[CE 3.7]</strong></p>
                <p align="justify">As part of GE’s field quality management procedure I developed Quality control plan (QCP) for this project. Emphasis was given on critical hold &amp; witness’s points. I identified all the check points and incorporated into the QCP. I mentioned the relevant drawing, clearance diagram and applicable field procedure reference as verification document for each inspection points. The QC\P was submitted to customer for review and approval, which was approved later.</p>
                <p><strong>[CE 3.8]</strong></p>
                <p align="justify">All the upper halves of casings (inlet, CDC, MCC, turbine &amp; exhaust) were removed. After taking opening clearances, rotor was required to be rigged and removed. It was complex and important rigging having rotor weight of more than 50Ton; I designed a lifting plan keeping in consideration the center of gravity. Crane position, boom length, slings and lifting beam were key factors of lifting plan. The lifting plan was approved and rotor was safely removed and placed on rotor stands.</p>
                <img src="images/mechanical-engineer-CE-03-8.png">
                <p><strong>[CE 3.9]</strong></p>
                <p align="justify">After rotor removal, MCC casing, lower half compressor vanes and lower half turbine hot gas components were removed including Stage-1, 2, 3 Buckets, nozzles and shrouds. Combustion parts (Caps, lines, Transition pieces &amp; fuel nozzles) were also removed. Detailed inspections of all the components were carried out including visual inspections, measurements, NDT and borescope inspections (where required). After thorough cleaning of lower halves of the casings, assembly of the components was started.</p>
                <p><strong>[CE 3.10]</strong></p>
                <p align="justify">IGVs blades and gears were replaced with new ones. The existing Y&amp;F type IGV actuator was replaced with Woodward type. The new actuator was provided to accommodate any future changes to opening and closing angles of IGVs. With the new actuator the IGV operating angle was 21 degrees (fully closed) to 92.5 degrees (fully open).</p>
                <p><strong>[CE 3.11]</strong></p>
                <p align="justify">Compressor / Turbine Rotor was replaced with refurbished rotor on back buy deal with damaged rotor, the refurbishment was performed in GE Manufacturing facility Dammam Saudi Arabia (GEMTEC). The rotor had R0 blades with enhanced features of airfoil tuning, laser shock peening and dovetail undercuts.</p>
                <img src="images/mechanical-engineer-CE-03-11.png">
                <p><strong>[CE 3.12]</strong></p>
                <p align="justify">Because of the compressor event the compressor blades and vanes of all the stages from S1 were severely damaged and broken. Stator vanes S14 to 17 and EGVs were removed. Compressor vanes S0 to S13 were not removed as the Mid Compressor Casing was replaced. All the stator vanes were replaced. S0 to S5 stator vanes were replaced as part of Enhanced Compressor Package-3 under FMI # F6135G18. The vane drops were adjusted to specs by machining the vanes base. Shim pinning was performed on two vanes of stage 14 to maintain vane drop limits. Since the allowable limit on individual vane base was only 10 mils, many vanes (391) had to be machined to set vane drops. Machining was done in a local workshop (Al Najim Saudi Int'l Co Ltd) in Dammam.</p>
                <p><strong>[CE 3.13]</strong></p>
                <p align="justify">As mentioned above Mid Compressor casing (MCC) suffered heavy dents and damages which were not repairable. MCC was replaced with new one. Prior to installing new MCC, bushings were installed on the dowel holes locations on inlet casing vertical joint (9 places). The purpose was to use the standard size dowel pins after casings alignment.</p>
                <img src="images/mechanical-engineer-CE-03-13.png">
                <p><strong>[CE 3.14]</strong></p>
                <p align="justify">Dents and rubs were found on Compressor Discharge Casing (CDC). High spots were removed and grinding / polishing was performed. Damaged mapping was done and the dents were found in acceptable limits. NDT (Fluorescent Penetrant Inspection) was performed on hook fit areas, no crack indication was found. CDC was returned to service.</p>
                <p><strong>[CE 3.15]</strong></p>
                <img src="images/mechanical-engineer-CE-03-15.png">
                <p><strong>[CE 3.16]</strong></p>
                <p align="justify">The combustion liners, liner caps and flow sleeves were found in good condition and were reused. The Transition Pieces and fuel nozzles were replaced.</p>
                <p align="justify">Some of the 1st stage buckets were found with missing material. 2nd stage buckets were found with cooling holes blocked. No significant damage was observed on 3rd stage buckets. Buckets of all the 3 stages were replaced. 1st, 2nd and 3rd stage nozzles were found with metal splatter. 1st stage shrouds found with FOD and rubbing. 2nd stage shrouds found with honeycomb seals worn and damaged. Metal splatter was also found. Minor rubbing was found on 3rd stage shrouds. All nozzles and shrouds were replaced for next operation cycle. Removed nozzles and shrouds were sent to GEMTEC for repair.</p>
                <p align="justify">No damage was found on Exhaust frame casing. The struts were found in good condition with no cracks.</p>
                <p><strong>[CE 3.17]</strong></p>
                <p align="justify">Moderate scoring was found on turbine bearings and thrust pads. The bearings were replaced with new ones. Thrust bearing cage was also replaced, new active and inactive thrust pads were used. All as-removed bearings were sent to GMETE for re-babbiting. Bearing instrumentation including bearing thermocouples and vibration probes (except bearing-1 axial probes) were replaced. Bearings seals of all three bearings were replaced.</p>
                <p align="justify">Turbine axial clearances measurement requires the rotor to be set at A-Set Dim with rotor pushed towards active side (exhaust end). While attempting to set the rotor at A-Set Dim by machining of active side thrust shim, it was found varying with temperature. The A-Set Dim was measured in the morning time and active thrust shim thickness was recalculated. Finally A-Set design dimension was achieved and clearances were measured. Compressor and turbine clearances were measured &amp; recorded per new clearance drawing 104T9941 in FMI, all found within specs. Clearances were sent to design team for review and were approved. After installing all the upper half casings, the rotor 6-point check was performed for rotor position and all clearances were found within specs. Inactive thrust shim was machined and Rotor thrust float was set at 10 mils.</p>
                <img src="images/mechanical-engineer-CE-03-17.png">
                <p><strong>[CE 3.18]</strong></p>
                <p align="justify">During the last MI new alignment specs 334A1553 were followed for Generator to Turbine alignment. Customer reported that new specs caused increase in bearing-1 and 2 temperatures by 10-12 deg C. Therefore, optimized alignment specs between the new and old (363A5495) specs were used during this outage. After generator to turbine alignment, turning gear to Generator alignment was also performed.</p>
                <p align="justify">Since turbine rotor was replaced, Green Rotor Procedure was followed for unit start up. Unit was cranked on 12 March 2016 and monitored, then reached FSNL. Over speed Trip was also performed. When unit was shut down to restart, the Inlet bleed Heat (IBH) valve was found stuck. The valve was removed by customer and some debris was found stuck in the valve strainer. The valve was replaced with new one by customer.</p>
                <p align="justify">Unit was successfully started on 14 March 2016 and reached base load. Unit was also tuned. All operating parameters were found normal. Unit was handed over to customer on 14 March 2016.</p>
                <p><strong>[CE 3.19]</strong></p>
                <h2 class="sample_heading">SUMMARY</h2>
                <p align="justify">As this job was having quite big scope of work so there are many other details available however due to space limitation I tried to brief the work I performed. Certainly, this job enhanced my engineering and managerial skills. Starting from getting prepared for this emergent and unplanned job, damage analyses and decision making to execution of the job within quite stretched timeline following complex engineering standards and procedure was amazing opportunity to learn. In addition to technical aspects; this project fine-tuned my project management skills as well.</p>
