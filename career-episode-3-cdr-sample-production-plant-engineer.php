                <p><strong>[CE 3.5]</strong></p>
                <h2>My Roles &amp; Responsibilities</h2>
                <ul>
                  <li>I participated in providing production technology inputs that account for the long¬ term production optimization targets to augment the eventual capabilities of the production field.</li>
                  <li>I undertook the DFL and SFL shooting and submerge depth determination for vertical producing wells using ECHOMETER.</li>
                  <li>I performed integrated technical data gathering process for the application of artificial lift design, re-perforation/ re-completion, choke sizing and well bore clean out.</li>
                  <li>I monitored daily surface production operations of a variety of heavy/volatile and Water/ Oil wells.</li>
                  <li>I appraised the productivity behavior and the employment of well interventions proposals using Nodal and network analysis software (Pipe-Sim).</li>
                  <li>I contributed in delivering functional study of well heads integrity and X-mass tree.</li>
                  <li>I participated in classifying technical constraints related to surface processing facilities.</li>
                  <li>I formulated proper actions for rectifying and dealing with existing or potential problems.</li>
                  <li>I guaranteed that statistical records are passably kept to maintain familiarity with all activities and status.</li>
                  <li>I scrutinized material consumption and equipment inventories of wellhead parts and pumps components.</li>
                  <li>I drafted and presented numerous periodic and technical activity reports.</li>
                </ul>
                <h2 class="sample_heading">PERSONAL ENGINEERING ACTIVITY</h2>
                <p><strong>[CE 3.6]</strong></p>
                <p>On daily basis, I used to perform field surveillance to wide range of production operations for vertical and deviated wells, light and viscous oil wells. In addition, I exercised the fundamentals of formation evaluation in the assessment of well deliverability and well data interpretation analysis Furthermore; I was tasked with the partaking in discussing the proper delivery of wellheads and X mass tree parts. Employing the concepts of engineering management, I contributed in problem solving decisions and controlling the consumable materials.</p>
                <p><strong>[CE 3.7]</strong></p>
                <p>I followed the principles of oil production engineering &amp; correspondingly directed the daily activities of collecting and analyzing the production data of about 160 wells, including oil &amp; gas wells, producer &amp; injector wells. Also, it was my foremost task to be involved in the process of technical inputs gathering for the wells that were proposed for the application of PCPs, PCMs, BPUs, ESPs and Gas lift. I used my specialized engineering knowledge of well completion design and well logging and testing when I attended and followed up the operations of wells intervention, resizing process of the choke, and bottom hole cleaning.</p>
                <p><strong>[CE 3.8]</strong></p>
                <p>I prepared and executed detailed schedule of DFL and SFL for over 160 oil producing wells. I made sure that production optimization plans were implemented to accomplish the ultimate targets by close monitoring of surface facilities parameters, fluids gathering manifolds, operations reports, work-over reports and shutdown reports. I was accountable to undertake other related administrative task such as presenting daily production report of all field wells, deficiencies report, analyzing statistical data and signifying technical advice for the bidding process and maintenance contact options. I synchronized with operation and drilling personnel and head office in the departmental meeting for setting priorities, updating targets and identifying deviations from baselines.</p>
                <p><strong>[CE 3.9]</strong></p>
                <h2>Problem faced</h2>
                <p>Fula oil field project had a variety of production optimization approaches, oil enhancement and recovery mechanisms, from the application of traditional lifting with different types of downhole pumps to the implantation of gas lift technologies and cyclic steam operations.</p>
                <p>In April 2006, during my working shift in the field, I was in a team consisting of a piping and corrosion senior engineer, SFS operations supervisor and two production engineering personnel. We were performing regular patrol around OGM No 13, in which a group of light oil wells are gathered and pumped to the field processing facility. As a production team, we were accountable for checking surface parameters; recording drive speed and fluid level shooting to determine the pump submerge depth for number of wells in Moga oil field. Well FN 38 was put recently into production with ESP lifting methodology. I picked to check the parameters &amp; found the flow indicators normal but with a notable increase in the wellhead pressure of 13 M Pascal.</p>
                <p>Fluid level shooting typically carried out according to predetermined timetable for every production well that on artificial lift of PCP, PCM, BPU. Wells that are operated with ESP are scheduled for drive speed reduction before level shooting is carried out. Well FN 38 wasn't planned for level shooting until the end of April since it was recently activated.</p>
                <p>While I was recording the OGM flow lines parameters not so far from FN 38, my colleague was misguided by another well schedule and proceeded to FN 38 well head and began to open the casing plug to insert the ECHOMETER; assuming that pump drive speed is low enough to just circulate the well fluid which wasn't the case. Before he could open the casing plug completely, the crude oil started impulsively bursting all over his body and into ground.</p>
                <p>I was close enough to see what happened and I rushed to evacuate him and I immediately headed to the speed drive cabinet and shut it down. The well fluid gradually stopped flowing and finally we inserted the plug back and closed all wellhead valves.</p>
                <p>The oil spill was enormous and reported to be hundreds of barrels, the environmental damage was also severe. An extensive investigation was conducted to figure out the root cause &amp; a committee was formed to determine an effective and practical approach for conducting level shooting of different type of producing wells.</p>
                <p>Since FN 38 accident, a list of strictly requirements of level shooting procedure are applied for wells that are under ESP operation, such as, Field manager permission, presence of all concerned parties of operation, production, maintenance, civil defense and rush repair team. Schedule preparation and presentation is updated twice a week and distributed daily with production report. Moreover, top management call for meeting with technical consultant and venders to discuss the improvement of the design and technologies of wellhead interface.</p>
                <h2>Adequate Problem Resolution</h2>
                <p>After FN 38 accident, I attended the implementation of new design related to level shooting procedure. The design was prepared by the technical consultant with my consent and executed by local contractor. The scheme was aiming to link the casing drain valve, to a draining pit located nearby, using 2" steel pipe in order to empty the well head before the placing of the ECHOMETER and starting the level shooting.</p>
                <p>The application of the suggested design was limited to few numbers of well because only wells under high ESP pressure were subjected to design test. Although it worked as planned, the implementation of the new scheme had accompanied with the lack of possibility to be applied to all field wells since that required large area of the field is needed to be shutdown. Therefore, it was employed for 11 producing wells.</p>
                <p><strong>[CE 3.10]</strong></p>
                <h2>Engineering Calculations &amp; Simulations executed</h2>
                <p>Well production behavior is well simulated with Pipe-Sem software. I used Pipe-Sem for nodal analysis, flow network optimization and completion modelling I carried out all necessary calculations of well flow, pressure loss and daily production computations. I used to perform the required interpretation of workover report, well downhole clean out reports, initial production tests and performance evaluation of well intervention.</p>
                <p><strong>[CE 3.11]</strong></p>
                <h2 class="sample_heading">Summary</h2>
                <p>Fula oil field project was the first step of my journey towards professional career. The experience was my gate to practice my formal study and technical education in the practical field. I gained a valuable knowledge in handling daily field production operations, learned to skillfully use several engineering software, assimilated many skills in addressing critical situations and I'm glad to be given the opportunity to engage with people from all over the world and exchange the ideas and expertise.</p>
                <p>Fula oil field project has developed and expanded so much since 2003. Many wells and field have joined the operation, some field facilities have upgraded and extended, some new facilities were added. The total production rate reached 60000 bbl. Day. Fresh engineers continue to build their capacity by working in the field and the project continues to positively contribute in the overall economic improvement.</p>
