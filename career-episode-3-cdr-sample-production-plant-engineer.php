                <p><strong>[CE 3.1]</strong></p>
                <p>This career episode is about my project titled <strong>titled “Fula Oi Field Development Project, Upstream oil production’s field facilities’’. </strong>This project was carried out during my tenure at <strong>Petro-Energy E &amp; P</strong> as a <strong>Production Technologist</strong> from ……………..to ………………</p>
                <h2 class="sample_heading">BACKGROUND</h2>
                <p><strong>[CE 3.2]</strong></p>
                <p>Western area of Sudan is geologically positioned within Al-Muglad Basin; the biggest Sedimentary Basin in the country, which protracted Southward and linked with Southern Sudan. Fula oil field project had a promising production potential that drove all parties of government and other stakeholders to invest in developing the project competences. Since the production start-up, the national economy has been revived, industry practices and standards have been refined and enhanced; utilization of national energy resources have been augmented and national engineers found good opportunities to develop their skills.</p>
                <p>The project owner, operator was Petro-Energy E&amp;P; an oil &amp; gas operating company with foremost objective to explore, develop and produce oil &amp; gas in block six. Petro Energy E&amp;P was officially established by the signing of the Production Sharing Agreement (PSA) amid the Ministry of Energy and Mining of Sudan and China National Petroleum Corporation CNPC. Petro-Energy E&amp;P has a registered branch under the laws of Sudan with registered office at Al Almogran area, Khartoum, Sudan.</p>
                <p><strong>[CE 3.3]</strong></p>
                <p>Fula oil field project is operated by Petro-Energy E&amp;P. The oil fields area is about 17,875 km2; and in production since 2003. The project consists of several oil fields which include Fula, Moga, Keyi, Jake, Naha, Shoka, Hadida and Sufyan. Each field has its own production facilities i.e. processing facility, surface treatment skids; these are connected with oil gathering manifolds. In addition to control center and accommodation camps; power generation is carried out by using the produced gas in gas turbines which thus generate the required electricity for operations. Transportation from and to the field is conducted through small airport. Fula oil field project production capacity is 40000 bbl. per day. The produced oil is to be transferred to Khartoum Refinery for treatment and distribution as fuel products in local &amp; international market.</p>
                <p>SThe oil reservoir and paying zones are varying among the field, containing light and heavy crude, water and gas wells, along with different types of production mechanisms such as artificial lift and gas injection.</p>
                <p>I was assigned as Production Technologist in Development &amp; Production department. Before taking this project I did a lot of online research to gain some ideas related to the project. I was also open to any suggestion from my immediate boss at the site. Moreover, during the project lifecycle I had consistent meetings with the client &amp; my field production superintendent in order to discuss about the problems &amp; difficulties. I have prepared relevant papers &amp; reports along with flow charts for unfolding the whole process.</p>
                <p><strong>[CE 3.4]</strong></p>
                <p>The project Hierarchy is given below. I was answerable to the Field Production Superintendent.</p>
