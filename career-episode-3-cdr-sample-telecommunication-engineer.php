               <p><b>[CE 3.5]</b></p>
               <p>Core network is the backbone of any GSM network and every operator is ensuring the redundancy and Nodes health. One-minute outage on core can affect millions of user at same time. So Network Safety is Number one priority at Core Operations.</p>
               <p>I check daily and weekly progress of all team and gave them new task according to their capabilities. Complete Network health check and network performance was monitored on weekly basics.</p>
               <p>A separate report was prepared by me containing all the data related to possible outages and how to avoid them along with implementation plan was discussed with team the same was discussed with vendor</p>
               <h2>PERSONAL ENGINEERING ACTIVITY</h2>
               <p><b>[CE 3.6]</b></p>
               <p>As Vodafone UK is the third largest mobile operator in country and due to this Network is being expanded continuously. In my weekly meeting with my manager I raised an alarm on STP capacity and offered a solution to deploy three more STP in core network to cater future Business Needs. After detailed discussion with Vendors, business and Finance department it was agreed to add three STPs in network. STP is very critical part Network as it provides signaling and routing information to MSC and HLR basically STP provides information of MSC and HLR for all the Mobile number who had been Port IN, Port OUT or Cross Port. STP have to be implemented in MESH topology and it should also be connected to other mobile operator STPs so that it can update Mobile user accordingly and provide correct routing and signaling information of calls.</p>
               <p><b>[CE 3.7]</b></p>
               <p>For implementation of STP in core network, I started compiling data of existing network, which includes the collection of database from existing nodes. Then high level diagram (HLD) and low level diagrams were prepared and presented to Project manager for review and approval.</p>
               <p>After finalization of STP implementation plan, Equipment was delivered to six core locations across UK for installation. STPs are deployed in active and standby mode. To deploy three STP in network it means that total of six STP will be deployed, as these are deployed in pairs. For this I mobilized site engineer to site for hardware installation monitoring according to standards. Daily progress report was checked by myself which was sent by on site engineers and instruction were given if any abnormity was found. Before energizing the equipment earth bar resistance was checked and equipment was connected to earth bar whose values were below 3ohms. After energizing the STP were connected to IP network using Gigabit Ethernet and for these optical cords were used.</p>
               <p><b>[CE 3.8]</b></p>
               <p>After hardware installation were completed QC engineer were deployed to check Hardware quality and to ensure proper standards were met. Then STP configuration scripts were prepared by myself and provided to on site engineer for loading on to STP using its terminal.</p>
               <p>After STP configuration testing begun and for this various test scenario were created to check requirement and full functionality. Multiple test calls were made to other mobile operator and each operator number was Ported/shifted to other operator to check whether STP is properly routing and providing signaling to MSC &amp; HLR. For example, Vodafone test number was ported out to O2 Network and then test calls were made individually to remaining mobile operator.</p>
               <p>Redundancy test were performed all three active STP to check Backup STP functionality. There should be zero minute outage in services while performing redundancy test.</p>
               <p>After successful testing all STPs database of existing network was loaded on to every STP in order to share load of already deployed STP.</p>
               <p><b>[CE 3.9]</b></p>
               <h2>KPIs for network optimization and expansion</h2>
               <p>During integration of STP in existing Vodafone network multiples issues were faced but the major issue were -</p>
               <ul>
                 <li>Configuration of protection Links (M2PA)</li>
                 <li>SS7 Link for signaling</li>
               </ul>
               <h2>Configuration of protection Links (M2PA)</h2>
               <p>As STP is deployed in pair so it is required to have links for both STP with each other for sharing purposes. It is done to protect the network from outages and in case of failure, the stand node will start working without interpreting services even for one seconds. For this M2PA link is configured on both STPs and for this purpose I was assigning one IP path to both the links. And by doing STP were not properly communicating resulting in failure of redundancy. The issue was resolved when two diverse IP paths were used to connect both M2PA links. Different IP paths are required to achieve complete protection.</p>
               <h2>SS7 Link for signaling</h2>
               <p>SS7 links are defined on STP Pair for communication/signaling purposes and this link was not operating properly and because of this, SRI packets were not properly sent to HLR. So No test number were receiving any SMS or calls. For this trace function was used on STP and then compared with configuration script and Network LLD and found that due to mismatch of MAP version at STP and HLR, the calls and SMS are not routing properly. Same MAP version was defined on both STP and HLR ends and successfully calls and SMS were received on test number.</p>
               <p><b>[CE 3.10]</b></p>
