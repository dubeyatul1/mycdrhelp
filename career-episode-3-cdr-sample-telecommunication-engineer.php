               <p><b>[CE 3.1]</b></p>
               <p>My third episode is about my job in Accenture, and during this time I did Project for Vodafone UK. Vodafone UK has more than 18 million subscribers and is the third largest Mobile operator in UK and Globally Vodafone group is the second largest mobile company. Vodafone UK had also acquired CWW and has entered in Fixed Line domain provide customer with Broadband and IPTV services through cable.</p>
               <h2>BACKGROUND</h2>
               <p><b>[CE 3.2]</b></p>
               <p>I worked on this project as Team Lead and was responsible for all the activities related to NSS/Core Network. I was responsible for creation of technical specification documents like HLD, LLD, test scenarios and deployment of solution patches on nodes. As team Lead I was responsible to give Technical training to teams and provide solution for the all technical issues. I had to ensure and resolve all the issues faced by vendor during testing or deployment of core nodes. Every day I collected Progress reports from site engineers, NOC engineers, QA and vendors and the issues were discussed in daily meeting. A detailed summary was prepared and sent to my Project Manager for review.</p>
               <p>Other than that my daily task was to ensure and close all the issues raised by MS team within timeline, and for this I did daily meeting with my team and presented them with solution and timelines for implementation.</p>
               <p>This Particular episode is about a STP Upgradation Project, which I did for Vodafone UK, because capacities of existing STP were not enough for future and need to be increased.</p>
               <p><b>[CE 3.3]</b></p>
               <p>I was directly reporting to Project Manager and under my supervision was a team on NOC engineers, QA/QC engineers and site engineers. NOC engineers were divided into shift to provide round the clock operation services. Whereas site engineer were mobilized according to requirement were either expansion/upgradation or addition of network elements was required. The same goes to QA/QC engineers as their job was to do spot check on sites in where nodes are being added or dismantled.</p>
               <p>Approval for all the activity was provided by me and for this it was necessary to mention complete configuration plan along with rollback scenario. Every day I did meeting with vendor and discuss implementation plan and various test scenarios. Issues created by vendor were also informed to them and rectification plan was discussed and finalized.</p>
               <p>On Monthly, basics arranged in house training for teams in order to groom them technically and to utilize the capabilities in maximum. Technical interviews were conducted with team in order to check their capabilities.</p>
               <p>I had to ensure that team close all the operation and maintenance issued raised by MS team. And for this I had to certify Network safety so that there will be no outages.</p>
               <p>In order to achieve desired network KPIâ€™s I had to ensure strict compliance and no outage was expected because the redundant nodes were being deployed and in case of outages there will be no backup and there will be complete failure in network.</p>
               <p>In case of Network expansion or implementation of new nodes I had to take approval from my manager and present him with current network scenario and business needs, after which approval was granted.</p>
               <p><b>[CE 3.4]</b></p>
               <p>The project Hierarchy is given below.I was answerable to the Project Manager,</p>
