                <h2>Background</h2>
                <p><strong>[CE 1.2]</strong></p>
                <p>I received many feedbacks and complaints from customers that they suffered many services interrupts during the process of business systems in which H3C’s networked storage product was employed. Such interrupts affect the stability of their business systems, so they expected that H3C was able to investigate what problem(s) caused such malfunction, and then solved the problem(s). A project was launched to deal with this situation.</p>
                <p><strong>[CE 1.3]</strong></p>
                <p>The overall aim of the project was to improve customer experience and to enhance stability when using the storage system based on the design solution &amp; synthesis strategy.<br/><b> The detailed objectives of the project were provided as follows: </b></p>
                <ul>
                  <li>To determine customer requirements and identify risks and issues in the existing software and hardware systems.</li>
                  <li>To propose design solutions and synthesis solution strategies.</li>
                  <li></li>
                  <li>To implement the design solutions as subsystems to solve the problem(s).</li>
                </ul>
                <p><strong>[CE 1.4]</strong></p>
                <p>The nature of my work area in H3C’s storage product department is to design and develop the storage devices by applying theories of mathematical analysis and computer science to design, evaluate or develop storage applications and devices that are crucial for making storage products work. My job begins by analyzing the requirements of a customer and then designing, developing, and testing the system to determine whether those needs are met. Flowcharts and UML diagrams are drawn during the process as well as algorithms that inform the server what and how to do.</p>
