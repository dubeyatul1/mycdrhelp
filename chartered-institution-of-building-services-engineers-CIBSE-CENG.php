			<p>After you are accepted as an ACIBSE, you might seek Incorporated Engineer (IEng) registration with the Engineering Council of UK. However, if you do not meet the requirements set by the council, you would have to write a technical report. This report should be separate from that of the Engineering Practice Report which is also required for CIBSE membership. This should be submitted after gaining approval for ACIBSE. The exemplifying qualifications for IEng is an accredited Bachelor's Degree or it may be BTEC HNC/D  with other associated further learning qualifications. </p>
