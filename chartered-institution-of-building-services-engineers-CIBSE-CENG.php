			<p>Purpose of this technical report is to prove your competence and show that you have the same level of competency as that of a person with exemplifying qualifications. For registration at IEng level, you need to show that have knowledge equivalent knowledge to that of BEng (Hons)- Bachelor of Engineering. Your technical report should be much like your portrayal of decision making and problem solving that appear to be building characteristics of engineering fundamentals. Your report should normally be based on your project and what you have done during the project. This should also be able to mention your contribution towards the projects and how you have been able to handle the situation. It must be kept in mind that the report should be able to compensate for the lack of academic qualifications. However, this report should not be about your skills and knowledge as that would be covered by the Engineering Practice Report (see fact sheets A20 and A21). </p>
