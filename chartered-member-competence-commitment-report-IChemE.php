                <h3>What are the possible levels of accreditation?</h3>
                <p>IChemE works with different universities to benchmark their degree programs with respect to international recognized standards. One of the following levels of accreditation would be required:</p>
                <p><b>M-Standard</b></p>
                <p>Qualifications with the above accreditation would be able to meet fully fledged requirements for the Chartered Member (MIChemE) as well as a Chartered Engineer (CEng). </p>
                <p>M-standard degrees are equal with UK MEng degree due to different requirements of different countries. M-standard also considers integrated degrees which include a solid foundation of first cycle degree (B standard) or Advanced knowledge in engineering (F-standard). </p>
                <p><b>B-Standard</b></p>
                <p>First cycle degree, which is offered as per B-Standard are considered to meet the full academic requirements for Incorporated Engineer (IEng). This can be supplemented with F-standard if there is a need. </p>
                <p><b>D-Standard</b></p>
                <p>Higher education or short cycle programs, as for instance HND qualification, accredited with D-Standard can be considered as proper requirement for Technician Member (TIChemE). </p>
                <p><b>F-Standard</b></p>
                <p>Qualifications with a second cycle degree, such as a Masters in Science, accredited at F-Standard along with an IChemE accredited qualification at B-Standard is considered as complete requirement. </p>
