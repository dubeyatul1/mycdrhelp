				<h3>What is Work Experience Reporting – APEGS for Members in Training, Supervisors and Mentors</h3>
				
				<p>Experience reporting is mainly for the registered members-in-training and is also applicable for member-in-training applicants like international graduates. However, these applicants must have been assigned with confirmatory exams and is bestowed with the option of submitting work experience to waive confirmatory exams. </p>
				
				
				<h4 class="kao">Here are some basic guidelines for completing the work experience reports:</h4>
				
				<p>The work experience report is helpful for developing written communication skills for member-in-trainings. <br/><b>Follow the below guidelines for writing the report:</b></p>
				
				<ul class="wewre">
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>A professional level quality of the reports should be maintained, which must be the same as that of the engineering or Geo-science report done for clients and supervisors. Experience reporting is considered as a peer review process which effectively means that a number of peer groups would be reading the report.</li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>Make sure to provide clear and concise report, which means you should avoid all the unnecessary information</li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>Make use of your professional judgment and provide examples from work, which will attract the attention of assessors</li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>It is supremely important to state what are the projects done and how they have been done. Do not use up more space than the given ones in the form of text boxes, but the supervisors can do so for stating extra information</li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>Be specific with your information, especially while stating project experience. A typical Resume style content would not be suitable in this case. </li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>Your supervisor would have more knowledge on your working stance in projects and thus their contribution is empirical. However, specific information and suitable information must be there such that it helps the Experience Review Committee to review your information and make a judgment. </li>
				</ul>
				
				<p><b>The information should include:</b></p>
				
				<ul class="wewre">
					<li><i class="fas fa-caret-right" aria-hidden="true"></i><b>What you did in the project exactly:</b> for example, the background of specific engineering or Geo-science project, your role in the project, circumstances faced and others. </li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i><b>Why did you do that specific project:</b> reasons for  engaging on the project, your role and how it satisfies your needs, any specific contribution</li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i><b>The outcome:</b> what was the outcome of the project, any comments regarding that</li>
				</ul>
				
				<p class="cenb">Specifically, your report should cover WHAT you did, HOW you did it, WHY did you do it and WHAT were the outcomes of the project. </p>
				
				<ul class="wewre">
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>Further, your roles in the project must be stated and for that background information of the project is necessary. Each point should consist of a statement with first person singular tense. Avoid using terms like “assisted”, “involved” or “participated” and any similar references to your work because these words sound generic and does not adds value for your personal contribution to the project. </li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>State your progress from the previous role (if any). Here, work is an experience can range from 5- 55 and hence your report must state how your role changed over the experience, rise of complexity and your response towards complex procedures. </li>
					<li><i class="fas fa-caret-right" aria-hidden="true"></i>You can also state a single project example in different areas of the report. For example, one of your projects may demonstrate application of a theory like assisting in design of a component, management activities like (monitoring costs and supervising technicians) and awareness of social implications. However, it is always better to state different experiences in the report for portraying your capabilities of working in different fields and circumstances.</li>
				</ul>
				
				<p>One of the main reasons why reports fail and need resubmission is because of generic information, vague statements and irrelevant explanations. For example, if someone asks about your project experience, you should state the experience, field of experience and each te of experience. If you are somehow concerned with the confidentiality of information, you may use surrogate names like project X, projypect Y or any other names. However, let your supervisor know, which one is which project beforehand. </p>

