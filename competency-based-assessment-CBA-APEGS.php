				<h4 class="kao">Life Member</h4>
				
                <p>Life member refers to the members who are above the age of 65 and retired from work. Annual license fee or membership fee payment is free for life members. In order to apply for life membership, send the below-stated statement to the APEGS office via letter, email or fax: “I want to apply for life membership as I am eligible and meet all requirements of life membership”. </p>
				
