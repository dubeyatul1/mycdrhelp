                <h3>APEGS Membership Classifications </h3>
				
                <p>There are four kinds of membership for geoscientists or engineers in APEGS. Membership benefits, voting rights, volunteer participation can be gained by a candidate after becoming a member of APEGS.</p>
				
                <h4 class="kao">Member-In-Training </h4>
				
                <p>This category refers to the engineers who have not acquired the work experience, delivered references or attained the Professional Practice Exam. </p>
				
				<h4 class="kao">Professional Member </h4>
				
                <p>This category is for the candidates who have achieved and provided all the required credentials for geosciences work or engineering practices. </p>
				
				<h4 class="kao">Engineering Licensee / Geoscience Licensee </h4>
				
                <p>This category is for the members who have not achieved the academic credentials for P.Geo or P.Eng. </p>
