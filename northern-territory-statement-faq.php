                <p>For any queries regarding study requirements please refer to this site- Department of Home Affairs website</p>
                
                <h3>26. Once the Visa is granted, is there any chance to work outside NT and transfer my nomination to another state?</h3>
                <p>No, you have to work at NT for at least 3 years as the Government does not provide release letters under that limit. </p>
                
                <h3>27. If my nomination is accepted by MigrationNT and DHA, what would be my obligations?</h3>
                <p>There is a need to sign a declaration that you will stay and work in the NT for at least 3 years. This is a serious commitment and as mentioned above no relaxation would be given here under any circumstances, so please be careful with that. Go through the basic obligations again. </p>
                
