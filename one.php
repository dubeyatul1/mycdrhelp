								<h4>2. Business Opportunity or Problem</h4>

								<h5>2.1.	Describe the business opportunity or problem(s) this project addressed.</h5>

								<p>Historically, at icare for the Nominal Insurer Scheme had multiple Scheme Agents (Insurers), each agent managed claims on their own systems.  icare has brought all of those claims (except legacy) onto a single claims platform, with a single Provider. This created lesser customer satisfaction as workers compensation claims processing time were increasing. In order to improve customer choice and satisfaction, the decision was taken to allow additional Authorised Providers to manage claims for some large employers.  The Authorised Provider model project extends icare claims system and multiple Authorized providers can manage claims on the same icare claims platform, using icare’s operating model, while maintaining privacy and data security.</p>
								<p>Another problem addressed in this project is Guidewire Claimcenter invoice handling, for the claim benefits/services were provided by service providers via through mail/email/FAX. These invoices will be processed manually that creates a sub-optimal experience for the service providers and increased administrative cost and effort on service providers and in claim handling process. A new payments capability called Digital Payments feature objective enables digital invoices coming from Medipass  to icare ClaimCenter. Invoice payments could the be handled with real-time claim benefit adjudication using Straight Through Invoice Processing rules. This project reduces effort and costs associated with data entry and processing of invoices and enable staff to focus on more complex and higher value activities in the claim process.</p>														
																														
