<h2 style="font-size: 18px;">SECTION 2 - RPL PROJECT REPORTS</h2>

<p>A project report is a clear written description of a project or engagement that provides you with the opportunity to show how you perform as an ICT Professional.</p> 

<p>Each report is to relate to a significant project or work episode undertaken by you during your professional ICT career.</p> 

<p>The purpose of these reports is to enable you to demonstrate your command and implementation of the Areas of Knowledge described in Section 1 of this application. </p>

<p><b>Please Note: You are required to provide two project reports.</b></p>

<p>Of the two reports, one must apply to a project undertaken within the last three years, and the other for a project within the last five years.</p>
<p><b>Projects over two years long may be used for both reports under either of the following conditions:</b></p>
<ul class="list10">
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>The project has clearly-defined work efforts which took place in parallel, each with their own solution development and design activities and their own deliverables.</li>
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>The project had clearly-defined phases that were executed in succession, each with its own solution development and design activities and deliverables. Note that a second project phase that constructs and implements the solution developed by the first phase does not meet this requirement.</li>
</ul>
<p>Depending on the nature of your role in each project, the Project Report should cover an appropriate selection of factors. </p>

<p><b>Appropriate factors will be determined based on the type of ICT project selected.  Possible factors include:</b></p>
<ul class="list10">
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>System Analysis and Design and Software Engineering methodologies used.</li>
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>Contribution to the processes involved in the design and implementation of enterprise-wide computing systems.</li>
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>Programming languages, design paradigms and implementation procedures adopted.</li>
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>Database and/or file design and management techniques employed</li>
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>Network topologies, including size, distribution and security facilities installed</li>
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>Project Management and quality assurance techniques followed</li>
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>Internet application design, including database interactivity and security measures implemented</li>
<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i>ICT managerial activities, demonstrating the nature and extent of responsibilities</li>
</ul>
