<h2>Instructions</h2>

<p><b>The following pages provide a template for your reports.</b></p>

<p>When writing your reports please provide your own thoughts - do not just copy project documentation.</p>  

<p>Please use the first person in your discussion, so it is clear to the assessor what you did versus what others did - say "I did X" rather than "X was done".</p>  

<p>Diagrams from the project documentation may be helpful, but the text should be in your own words. Please ensure that diagrams are relevant, readable, and help the assessor to understand what you did as a member of the project team.</p>

<p>If sections of the Project Report template (see below) are not relevant to your participation in the project, then leave the section blank.</p>

<p>Focus on quality rather than quantity. <b>Each Project Report should be no more than four or five pages in length.</b></p>

