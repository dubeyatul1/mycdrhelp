								<ul class="list10">
								<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i><b>P address: </b>I can say that one of the most logical components of an IP address network is actually an IP address to communicate between hosts in the network, or better to say, every device in the network is used with another device with a 32-bit string which is defined in different classes that by using VLSM smaller subnets can be created.</li>
								<li class="df1"><i class="fas fa-caret-right" aria-hidden="true"></i><b>DHCP server: </b>I can honestly say no large or even medium-sized network can be imagined without a DHCP server, which manages to allocate IP address to all hosts and network servers play a huge role in reducing network admins problems such as duplicate addresses.</li>
