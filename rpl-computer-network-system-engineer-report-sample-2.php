							<p>participated in the project of connecting 320 branches of the Social Security Organization with the data center located in the central building at various stages. In the first phase of the project, we designed the preliminary communications design with other members of the design team with the help of Microsoft Visio software. During the configuration phase of the equipment, I helped the project team with the large number of routers and network switches in the configuration of the equipment. As a result of segmentation, I set up the final setup and fixed the connection between the central branch and the branches. I also helped the project manager with the final report.</p>
							<p><b>3.2. Describe any design or problem solving methods you used on this project.</b></p>
							<p>Our first step in launching the project was to create a lab environment using GNS3 software in which one router was configured as a Headquarter router and three as a Branch router:</p>
							<p>After this step we started designing a new IP addressing network. The first point in designing IP addressing was designing the IP address for WAN communications and the second was designing the IP address of each branch's internal network. Perhaps the most important point in designing IP addressing was the proper use of VLSM. In fact, the use of / 30 ranges for Point-To-Point WAN communications and side tunnel addresses and designing appropriate subnets for each branch was based on the number of hosts and predicting a possible increase in the number of hosts in the future. For example, for a branch with 20 hosts we used a range of /27 with 30 usable IP addresses. In the following graph there is an example of an IP address project design:</p>
							<p>After designing IP addressing, it was time to launch DMVPN between the headquarter and the branches. Before deploying operation, we first tested this solution with a DMVPN tunnel between the headquarter and one of the branches and after successful testing we started configuring other offices:</p>
							<p>The reason for choosing Routing protocol EIGRP was firstly it's very proper convergence time, secondly its easiness of configuration and troubleshooting over other routing protocols. But the reason for using the DMVPN approach is to create a tunnel for each branch with headquarter rather than a client / server approach. We do this by creating only one tunnel on the Headquarter router, and the Hub router establishes an NHRP table where the address information of all the branches is easily communicated between the branches (if necessary) and the headquarter with the branches. In the displayed configuration IP address hub: 172.16.0.1 and also the IP address hub: 172.16.0.1 branch 172.16.0.2 has been configured,the split-horizon feature was disabled using the no up split-horizon eigrp 1 command. IP MTU 1400 packet size of MTU 1400 configured. For authentication we used the "ip nhrp" authentication command and set the key to "dmvpn". It should be noted that the key in both hub and spoke must be the same. In the DMVPN configuration, using the bandwidth and delay commands which these two items are used in the EIGRP metric calculation we set them manually, and also set the nhrp server address in the branches using the "ip nhrp nhs" command. We used the "ip nhrp holdtome" command in the headquarter and in the branches to adjust when NHRP dynamics inputs expire. We can use the "show ip nhrp" command on the Hub router to monitor the established tunnels and we can clear the NHRP table contents with the clear "ip nhrp" command.</p>
