                                  <h2>Teamwork Concepts and Issues</h2>
                                  <p>History shows us that humanity has through the ages attained much more by working together as a collective, achieving more than what one person would be able to do on his/her own. This is also replicated in nature where members of the same species will stand together when facing a threat or danger, proving that team work has better results.</p>
                                  <p>The working environment has grown in the last 20 to 30 years to become an environment where one person alone cannot create a real noticeable impact. People who know this have achieved unbelievable feats, examples like the late Steve Jobs, Elon Musk and sir Richard Branston to name a few, were/are great team leaders, being able to motivate members of a team to accomplish so much more working together as one unit.</p>
                                  <p>I have been fortunate enough to be part of many different types of project teams, even different areas outside of IT during the past 19 years. With each team member bringing with them a very different set of skills and beliefs, also a diverse way of looking and thinking about a given task or project requirement. This sometimes create conflict, either due to personal ego and the unwillingness to see others point of view, or through a total misunderstanding due to the lack of proper and clear communication.</p>
                                  <p>There were cases where we had to reallocate team member responsibilities, and in one extreme case, move a member to a different department to allow the remaining team to connect and become a unit.</p>
                                  <p>I was fortunate enough to receive personal coaching in a team setup and in my personal capacity throughout the years, and one of the most interesting topic I learned about was the 5 stages of team formation. Something we were really struggling with at the time after merging two very different performing teams into one. The five stages, Forming, Storming, Norming, Performing and then ultimately Adjourning, assisted me and other team leaders in identifying where a team needs to address the issues during these phases.</p>
                                  <p>I participated where simple ideas were being thrown around, turning into brainstorming and finally delivering a solution to requests that seemed impossible to begin with. Where team members motivated each other to push through tough times when they thought they reached the limit of their abilities and setting a new personal benchmark in what they are capable of doing.</p>
                                  <p>With every project team, whether the project itself was successful or not, I tried to learn a bit more from the team’s diversity and way of thinking. A way to acquire a new skill and perspective. Learning something new, allowing me to grow and become a little bit better than before, either as a person or in what and how I do things, sometimes, in both.</p>
                                  <h2>Communication</h2>
                                  <p>Communication is one of the first starting points of mankind I believe, with pictures being drawn to first record and later rely as to what is required, changing into letters, numbers and language. So we have evolved into beings communicating with each other on consistent basis, relaying information amid each other.</p>
                                  <p>Within IT, technology is constantly changing, evolving and becoming more complex, day after day. Without proper communication between the team of people developing the technology, the product would not be able to complete it development. So, when the team is deploying and supporting the technology, if there is no proper communication between them and the consumers of that technology, especially when issues/problems arise with the product, the product would not be used and ultimately fail.</p>
                                  <p>IT technologies have grown into a presence currently in our lives as never seen before. This would not have been possible if IT technology companies did not properly communicate with each other and their consumers.</p>
                                  <p>In my current field, with different IT technologies implemented to serve, most of them are dependent on each other, and with various support personnel administering these systems. If there were no clear communication between our team members on each system and its requirements, these systems would stop working at a certain point in time due to failure in the eco system. Either due to network congestion or no connection at all. A lack in storage space or IO required, or the limitation of performance due to systems competing for resources.</p>
                                  <p>When I started, systems were mostly standalone, performing a set task and if it failed or performed badly, that service alone would be affected, with one person taking the responsibility. Virtualisation has changed the scope of things and has tied so many different systems onto one that there is a great dependence on each other. That is why we know it is so important for system owners to constantly communicate with each other and to keep each other in the loop of planned system changes.</p>
                                  <p>Communication among various teams in IT, including ours, needs to be clear, and all requirements correctly stipulated, with the priority of these requirements noted. It has happened where I was involved in a project where the full scope of requirements where not fully defined, and we ended up doing work that was never requested. On other project the priority was never stated, and due dates were already missed in the planning phase alone.</p>
                                  <p>Awareness and constant feedback as a means of communication can be an ally. A big internal project we did ran behind schedule due to circumstances out of our control, but due to constant communication with all stake holders involved, including the userbase, we were able to complete the project behind schedule, with no losses. Due to the awareness/communication all parties involved took necessary steps to absorb or work around the delay.</p>
