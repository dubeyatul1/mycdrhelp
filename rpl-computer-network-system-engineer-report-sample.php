                                  <h2>IT Project Management</h2>
                                  <p>Project Management is also something that developed and changed a lot over the past few years and especially in IT. Unfortunately, one of the biggest things I believe that make IT Project Management difficult is the uncertainty of timelines and risk when taking on something that has no baseline to be measured on.</p>
                                  <p>I and some of my colleagues, still run into scenarios were something needs to be done or implemented that was not done before and time lines need to be established. The problem is systems do not always play nice and could either work 100% the first time or make you work every minute to the end.</p>
                                  <p>Where possible I would first request we run a proof of concept or small pilot project to determine the total effort required for the actual project. This helps in better planning of the project and allow more accurate times and costs for the actual project. We have had external parties give through timelines on projects that they implemented and then missing the deadlines due to unforeseen or untested events.</p>
                                  <p>Every member of our IT team gets a chance to run with his/her project and use the rest of the team as resources. This helps all in understanding the importance and requirements when working with projects.</p>
                                  
                                  <h2>Security Management</h2>
                                  <p>My philosophy was and still is to only allow the bare minimum. This is not always the easy road and sometimes takes a lot more planning, testing and getting frustrated but worth it in the end. Even if you must stand your ground forcing 3rd parties developing software to make their solutions work the way you know is most secure.</p>
                                  <p>I headed the IT section of the group company’s governance guideline enforcement for a year, giving training and awareness on what was required for us and affiliate companies.</p>
                                  <p>From there I moved on as team leader in our Microsoft environment, assisting with system security planning and enforcement. I also headed and was responsible for security changes on our Active Directory and Group Policies. Finding ways to get systems to work when locking them down and reducing user support calls due to limited rights on desktop/mobile systems. Security discussions has become a natural part of every topic discussion or project that we undertake.</p>
